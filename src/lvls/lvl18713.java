//从Level 1算起，这是琥珀营地的第四个基地。
//该组织通常会帮助新人，并教导他们后室的知识。
//路过的流浪者若有需求，可以在该哨点领取一份有限的补给。
//这个前哨还会指导他人应当前往那些层级。
//该组织并无固定基地，他们四处游荡、帮助他人。
