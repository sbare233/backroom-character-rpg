//入口与出口
//枢纽有一种稳定的进入方法。从Level 1的入口出发，沿走廊直走，穿过一扇门，走到走廊的尽头。接着，原路返回Level 1的入口，就能发现一个新的岔路。向左走，走到下一个岔路向右，再下一个向左，再下一个向右。在那之后，能看到两扇分别标有A与B的门，进入B门。穿过那扇门之后，你会发现自己来到了一个完全相同的房间，这一次选择A门1。进入A门后，你就进入了枢纽。如果你在其中任何一步走错了，那么终点将会完全随机。
//
//以上的方法是唯一一种久经考验、百试百灵的进入方法。但由于枢纽连通着所有层级，流浪者也很有可能从其他层级进入这里。当某人意外切出时便有可能抵达枢纽，有时其他层级内部也会短暂地出现一扇通往枢纽的门。
//
//想要离开，只需要穿过一扇开启的门，便能抵达相应层级。一旦离开枢纽，就不可能再度返回，因此身处其中时请谨慎考虑你的选择。
//
//但是，如果你能找到一面装饰着可爱气球的窗户，你就离派对不远了！=)
