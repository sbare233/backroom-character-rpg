//枢纽
//枢纽是后室中的一个隐秘层级，也是很多流浪者安全的通行/定居之所。枢纽似乎是一个隧道，其两侧分布有上锁的、以及未上锁的门。
//
//描述
//枢纽表现为一条无限延伸的昏暗地下隧道，似乎没有尽头。隧道内的地面为干涩的沥青材质，通过三条黄线划分为四条车道。其墙壁则是巨大的混凝土板，每两块之间都存在缝隙。墙上还配备有成排的亮黄色工业照明灯，使枢纽呈现出了橙黄的色调。枢纽通常会扭曲上下左右的空间，导致部分区域的布局不够稳定。然而由于其内部为线性路径，穿越枢纽依旧非常轻松。
//
//枢纽内似乎完全不存在声响，使得整个层级使人感到尤为空旷。在大部分区域，枢纽的两侧都存在巨大的混凝土门成对排列，每一扇门上皆刻有一个奇怪的符号。这些符号均无法辨认，形状类似于麦田怪圈。除此之外，门上既无编号、也无其他标记，且门排列的顺序也与M.E.G.数据库中层级的编号不符。大部分门都是上锁的，只能用相应的层级密钥打开。
//
//每一扇门的右侧都有一个多边形钥匙孔，与门上刻印的符文相符。所有钥匙孔内部皆有复杂的锁定机构，且会时刻变形，因此无论是使用撬锁器、还是错误的层级密钥，都不可能强行打开这些门。只有通往Level 1、11、178的门永远都处于未锁状态。通往Level 1的门位于第7排，通往Level 11的门位于第10排，而通往Level 178的门位于第5排。此后的门似乎均被永久性锁住，只能使用对应的层级密钥打开。
//
//如果你通过一把层级密钥进入了枢纽，那么你可以遵循其振动找到通往相应层级的门，直接将之打开。然而，所有先前被手动开启的门对于下一位进入枢纽的人而言，都将重新变为上锁状态。
//
//过去曾有大量实体被目击到进入枢纽，但没有一个成功出来，甚至在枢纽内部也无法找到它们的踪迹。许多实体与失落一族等古老组织都将枢纽称作“十字路口”，但其最初的真名却无从得知。
//
//基地、前哨与社区
//对于许多流浪者/探险者而言，枢纽都是一个热门层级。因此，枢纽内成立了大量前哨，用途各不相同，从临时据点到贸易路线不一而足。在走入下一个层级之前，你可以在这里造访许多不同的社区。
//
//M.E.G.门前阶哨站：
//M.E.G.在枢纽中的哨站。
//大量不同的层级密钥在消失前会被储存于此。
//所有流浪者都可以在此询问枢纽内有哪些密钥可用。
//开锁器兵团主要的密钥存放点。
