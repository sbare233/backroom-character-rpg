//你还记得几年前我说的重现的梦吗？嗯，觉得你的建议还不错，就一直把那些你有可能感兴趣的梦写成了日记。之所以到现在我也没给你看哪怕一篇，是因为它们好像都是很隐私，每个都出于某种原因。就像我自己的秘密一样。但是我现在就是需要不把它当成秘密了，把这些梦将给某个人。也许它们会浮现地更真实一点吧。下面的这些附件是记录，而且是所有。 或者至少我自己是这么认为；我也不想要更多了。
