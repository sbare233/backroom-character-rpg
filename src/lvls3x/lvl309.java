//描述：
//Level 309 是后室的第310层，外观表现为一森林，植被生物覆盖密集。森林中最常见的树木为白桦木，但也可以发现其他的品种。 Level 309 内的土壤肥沃，可供大部分花草生长，也可以种植作物。 有许多流浪者报告称他们在层级内为绕圈而非直线行走，因此 Level 309 被认定为非欧几里得空间。层级内地势平坦，但偶尔也会出现小山。天空无云，无论何时其颜色及亮度均相同，推测该层级并不存在昼夜变化。
//
//Level 309 内空气过于湿润，导致整个层级都蒙上了一层雾。在雾最浓的地区，可见度少至只有几英尺。在此层级内栖息的一些敌对实体已经演化出了忽略雾所带来的影响的能力并利用此能力进行攻击。
//
//Level 309 中存在许多种真菌，主要为该层级特有的闪着微光的蘑菇。即使 M.E.G. 已经在进行调查，对于这些蘑菇的信息仍知之甚少，但层级内大部分蘑菇均可食用，但味较苦较难下咽。但不建议准备长期生活在此的流浪者囤积这些菌种，因为它们会在被拔根后2~3天之内腐坏1，所以建议流浪者先行准备物资。腐坏的蘑菇可能会让流浪者的肚子痛上几天。
//
//Mushroom
//Level 309 内发现的一只发光蘑菇。
//
//Level 309 目前发现的最有名的菌种是狂孢菌。狂孢菌与层级内其他菌种不同，它无法发光，且不可食用。狂孢菌颜色暗黄，高三英尺。切成小块煮熟的狂孢菌即成为一种毒品，称为狂孢毒。吸食过狂孢毒的流浪者将其描述为“味咸，吃下去后感觉呼吸困难。”
//
//狂孢毒会自然散发烟气，吸入烟气的流浪者会感到强烈的躁动感，促使大脑分泌多巴胺。其初步症状为：缺少理性思考；有轻飘飘的感觉；以及不正常的大量精力，吸入者一般会以跑步或其他方式消耗这种精力。后室中许多组织均会收集这种物质以作贸易或娱乐用。一磅狂孢毒的平均售价为一升杏仁水左右。
//
//行途：
//行途 为一条笔直的一码宽道路，由木板建成，木板已经老化磨损。在行途上行走时流浪者有几率发现有毒腐烂的木头，这一般是死亡飞蛾所致。行途估计约有10英里长，会对流浪者产生特殊效果：流浪者离行途越近，其周围雾的厚度越低，能见度越高。
//
//大多数流浪者来到 Level 309 时会处于行途的起点。想尽快安全地离开层级的流浪者应尽量保持走在行途上。否则他们会来到层级内其他更为危险的地区。
//
//在行途的中点之后，整条行途变得不稳定，走在其上的流浪者随时都有突然切出的可能。流浪者传送到的层级取决于其在行途中点之后行走的路程。2然而，在行途旁边而非在其上行走便能防止突然切出。
//
//实体
//死亡飞蛾在 Level 309 较为常见，一般位于行途旁边以看清楚周围。进入大雾中的死亡飞蛾可能会成为潜伏其中的实体易得的猎物或棘手的目标，具体取决于死亡飞蛾的性别。对于流浪者和捕食者的好消息是，雌性死亡飞蛾相对较少，而且数量也在减少。流浪者可使用 Level 309 内的蘑菇驯服雄性死亡飞蛾，但这么做大多只是浪费资源。
//
//另一种在层级内找到的实体是猎犬。 Level 309 内的猎犬已经演化出了使用红外线以相对容易地透过大雾看周围的能力。 此外，层级内的猎犬也与普通猎犬极为不同。它们以2~4只为一组伏击没有防备的猎物，通常是流浪者或走失的死亡飞蛾。已经有大量流浪者被猎犬伏击时过度惊吓无法及时作出反应而受伤甚至死亡。
//
//为了避免伏击，建议流浪者手中持武器以表现出自己很健壮，这通常能恐吓猎犬，减少它们发起攻击的可能性。此外，在伏击前看见一只猎犬就足以让一整队猎犬撤退。若流浪者已经遭受了猎犬的攻击且没有逃生措施，建议使用钝器攻击猎犬脆弱的皮肤。
//
//菌体人：
//在 Level 309 中也发现了菌体人，为外形像蘑菇的类人生物，在层级内形成了最初的文明。
//
//菌体人与 Level 309 内的发光蘑菇以及狂孢菌外形（可能生理也）相同。与发光蘑菇一样，目前对这种生物的生物过程几乎不了解。菌体人会发出微弱的光，且能随自己的意愿改变光的颜色。调查表明这是菌体人向同伴交流的一种方式。菌体人的语言很难翻译，目前只翻译出一些词语。例如，在菌体人安全健康时会闪绿色的光，而在生气或感觉到有危险靠近时会闪红色的光。总体上来说，菌体人除了在受到攻击或捕猎猎犬外均不会表现出敌意。
//
//菌体人 身高约为2英尺，重约30~40磅。菌体人是一种杂食动物，主要以猎犬肉以及 Level 309 内的发光蘑菇为食。菌体人相对脆弱而寿命较短，因此它们团队行动，制造工具以捕猎或保护自身。
//
//菌体人已经会利用它们与狂孢菌相似的生物学特征。有报告称见到菌体人使用木制刀片割下自身的一部分并用火烤制。得到的狂孢毒被卷成一个球，外部裹叶片，与矛相接。在进行反击时，1~2只菌体人会使用矛攻击敌人，使得敌人在接下来的几分钟内发生摄入狂孢毒的一系列反应，以让其他同伴更快速的杀死并处理对手。
//
