//菌体文明
//菌体人在 Level 309 中雾最浓的地方构建社区，流浪者距离行途越远，可能发现的社区就越多。 一菌体文明通常为许多小村落以及一首都的组合，村落分布于首都方圆两英里以内，但也发现了非常文明，例如拥有多个首都或只有一个村落。
//
//村落中一般居住8~12只菌体人，每两只共用一座房子。其他的房子一般为工作用或学校。每一个居民都有不同的工作以确保社区能维持。居民的工作有：捕猎，收集材料，以及制造武器和工具。每个村落也有一外交官，定期将村落的一部分资源送往首都。
//
//菌体人无性生殖，且大约每几个月交配一次。新生儿在一岁之前一直待在房子里并被喂食。一岁后，这些新生儿可能会随着物资送往首都，或者如果村落维生困难会直接加入村落工作。
//
//村落居民一般对流浪者无感。若流浪者相对富裕，居民可能会给予少量食物。然而，流浪者与菌体人同住的时间越长，菌体人就越对流浪者感到厌恶。在大约一周后它们会对流浪者表现出极大的敌意，直到他们离开为止。
//
//首都有约25~50只居民居住，有巨大的石制建筑，一个建筑可以容纳许多居民居住。由于诸多村落都会送予食物，住在首都的菌体人并不需要工作很久，过着更为轻松愉快的生活。它们的自负使得首都的建筑以及装饰更加华丽。
//
//在建筑中能找到菌体人所谓的信仰之‘神’或‘王’的画作。这些画作都描述了一只高大的菌体人，外观类似一猴头菇3。王手持一木制长毛，在画中经常为举矛刺杀黑色而无法辨认的类似植物的生物。
//
//城市内也有为王所建造的神庙，神庙内有一生长异常高的蘑菇，周围有一圈小鹅卵石。在此的菌体人有时会坐下并快速改变颜色，推测为表现出对王的祭拜。
//
