//实体
//Level 370中似乎完全缺乏生命迹象。在该层级中，没有任何人报告称遇到了实体或其他流浪者；这有可能是因为Level 370中存在着一种未知的隔离属性，也有可能是因为该层级庞大得难以想象，导致两个生物在其内几乎不可能相遇。直到取得更多发现之前，该层级都将被划分为生命绝迹的区域。
//
//基地、前哨与社区
//该层级缺乏建立哨点的必要、存在可能的隔离属性，且其地形也难以建造长期的聚居点，因此Level 370中并不存在任何前哨站。且该层级的规模过于巨大，又难以维持生命，导致这里没有愿意搭设永久性居所的居民。此外，该层级内部的声音异常也会使建立社区十分困难。
