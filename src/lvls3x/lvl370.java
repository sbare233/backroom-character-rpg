//描述
//Level 370由无数广阔的房间与走廊相连而成，整个层级中均流淌着微波起伏的温水。该层级内的每个区域都有着截然不同的尺寸与结构：一些较小的房间构造简单，设计平滑整洁；而其他房间则呈现出了怪诞的结构，要么缺乏明确的设计理念，要么宽阔无比。然而，在“异常”这一点上，每一个房间都是相同的。该层级内的墙壁、天花板与地面皆铺有相同的白色瓷砖，除此之外再没有任何其他颜色；唯一的例外就是水体呈现出的蓝绿色调。所有瓷砖都诡异地光洁如新，每一块都毫无区别，闪亮的瓷面上没有哪怕一条裂纹。所有破坏这些瓷砖的尝试最终都证明，它们几乎坚不可摧；因此，以现有的技术，不可能通过钻开墙面来快捷地穿过该层级。
//
//Level 370的建筑结构多变，但却自有章法，使得其在混乱之中又表现出某种秩序。该层级内的不同区域之间以平滑的过渡相连，缺乏显著的可识别性。尽管不同区域的作用难以确认，但显然，它们远大于标准尺寸的建筑规格绝非是普通泳池所需要的。Level 370内部异常的几何构造使得它难以拥有任何所谓的“作用”：在某些区域，会有柱子从水下伸出，其数量之多难以统计；而在更开阔的区域，则缺乏供人驻足的平台。光线从不规律的角度射入，随意地切割着空间，导致某些区域完全处于黑暗之中。有一些阶梯径直通向水底，似乎暗示着本层级曾一度未被淹没。通往水底深处的入口通常表现为螺旋阶梯、或是水下深井。强烈不建议任何流浪者尝试探索深水，微弱的照明条件与尚不清晰的环境性质都将带来难以避免的风险。倘若未佩戴相应装备，流浪者在该层级的深水中很容易迷失、甚至溺死。有许多理论和猜想试图解释这些古怪结构存在的原因，但没有一个能够自圆其说。目前为止，没有人能够说明为何该层级内的建筑风格如此混乱，也无人清楚这些房间为何会被这样设计。
//
//Poolrooms-3.jpg
//Level 370的另一张照片。
//
//研究表明，Level 370的水体中存在泻盐（硫酸镁）的痕迹。当人体长期暴露于这种化合物时，会感到自然的放松感，包括肌肉的舒张与身体疼痛的减缓。除此以外，水体内还发现了数种未知的化合物。直到本档案撰写之时，研究者们依旧未能查明这些物质的来源与特性，但鉴于它们与上述的泻盐一同出现，可以推测这些物质皆可通过某种异常方法增强水体的放松效果。
//
//有趣的是，整个Level 370内的液体表面均荡漾着轻微而永不停歇的涟漪，但却无法找到它们出现的原因。这些涟漪可能是从该层级内的某一点发出的，也可能是水体自身的特性——现有的理论提出，由于层级的规模过于庞大，其内部很可能存在着自己的潮汐力。这是在后室的其他层级中从未出现过的现象。值得注意的是，水体的温度恒定在不冷不热的程度，即使在光线无法触及的黑暗角落也是如此。
//
//Level 370中的声音会受到严重的干扰。例如，层级内部的环境噪音突然减小，水波不再发出应有的荡漾声，且人声也受到了“消音”，变得“十分沉闷”。据推测，这一异常很有可能是水声导致的。该层级内部的水声表现得极不寻常。在大部分探索中，流浪者都报告称这里的水波安静祥和，几乎能使人感到镇静；而在少数报告中，水声会突然变大，并淹没其他声音。出现这一效应的原因目前仍在调查中，但是至今为止都未取得可靠的结论。
//
