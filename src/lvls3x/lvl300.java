//这是唯一一段流浪者可以离开Level 300的时间; 那些曾离开过该层级的流浪者表示，在瀑布后面有一段长达一公里的洞穴通道，它会逐渐通往Level 173。洞穴内几乎没有光线，流浪者需要小心，以免绊倒或撞到岩壁。山洞也很窄，随着深入你可能会发现，山洞内的空间开始缩小，并不得不匍匐前进，挤进狭小的空间内。离开该层级的过程非常困难。由于水流十分湍急，想要穿过瀑布几乎是不可能的——试图穿过瀑布的人则会造成骨折。似乎在瀑布下停留三秒钟就足以对身体造成永久性的伤害。
//
//离彩虹瀑布越近，木板路的细节就会稍微改变。木板通常会变薄，木板路的栏杆会明显变厚。尽管如此，目前还没有确切通向彩虹瀑布的路径。
