//Level 317，通常被称为“霓虹沙壶球”，是后室的第318层。
//
//描述
//Level 317是一个怪异的、像是实验室的设施。该层级内的所有家具和建筑物均呈现以黑色为主色调，外框为亮蓝色霓虹灯的样式。该层级被分为两部分——控制室和大厅。
//
//Level 317的控制室像是一个在窗边有着长柜台的空旷的巡洋舰控制甲板，它拥有一个附属于中心的D-pad。房间的后方是一个未被观察到载重限制的电梯。除了被贴上标签“L”的按钮通向大厅之外，该电梯的按键会通向任何有电梯的层级而非楼层。控制室的窗外是一个奇怪的游戏，该层级的居民称之为“霓虹沙壶球”。看上去就像是多个控制室，因此可供多人同时游玩不同游戏。此外，若多个人能全部呆在大厅电梯中，则这些人可以进入同一控制室。
//
//大厅形似自助餐厅，并总拥有各式各样的汤、三明治和薯片。食物本身完全正常，但是若食物来自于不透明包装，其包装均呈现为全黑及霓虹灯外框的样式。一台有一个按钮的自动售货机坐落于大厅最左侧的角落，该机器能够出售任何使用者能在脑海里描绘出的液体。任何尝试获取危险物质的行为均不会成功，而是获得该机器生产的有独特风味的苏打水。这些液体将被装在使用者描绘出的容器里，而当使用者没有想象容器时，液体会被盛装于一个黑色的、有着“霓虹沙壶球”商标的易拉罐里。
//
//电视组成了大厅的墙，其均展现各种不同的、由该层级中将自身称为沙壶球队的居民们创造的“沙壶球”。大厅中有若干个能显示沙壶球及清除沙壶球所能获得奖励的触摸屏亭。值得注意的是，这些触摸屏亭均有一个usb端口。电梯的左右两侧是两个巨大、潮湿、无标号且拥有不断起雾的镜子的浴室。
//
//首次进入Level 317的流浪者将总是出现于控制室末端。当进入时，一个低沉、平静的，自称为“指挥者”的声音将会欢迎流浪者来到该层级，并亲切地将这里称为“霓虹沙壶球”。它随后将在允许流浪者再次进入电梯前，令流浪者完成十个“教程关”。这个游戏需要用柜台上的D-Pad在“瓷砖”做的13×8网格上移动一粉一绿两个方块，使它们到达它们各自的坑里。每个输入都由两个方块共享。这些沙壶球游戏会逐渐增加难度，特别是瓷砖的数量。可以在此找到一份来自沙壶球队全面的、关于这些瓷砖的清单。完成这些等级将会产生以道具为形式的奖励：从杏仁水到滋水枪。这里也有五种Level 317独有的道具，它们均在“沙壶球队”的官方网站ShuffleSquad.org中被一一列出。
//
