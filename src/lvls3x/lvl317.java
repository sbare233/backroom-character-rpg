//值得注意的是，在完成十个教程关后，流浪者将获得创造自己的沙壶球游戏的机会从而和他人分享，这种促成了一种社区的形成。“指挥者”似乎和这个社区有些关联，如果关卡作者递交了一些独特的或是有挑战性的的关卡并给它留下了印象，它将允许这些人自行改变他们游戏中霓虹灯外线的颜色。此外，沙壶球队成员“REDBEACHZONE”和一些其他人应用纹理包修改了他们的游戏。具体方法未知，可能是通过前文所述层级内大厅触控屏亭中似乎能让私人上传自己关卡的USB接口实现的。“指挥者”似乎不会干扰他们上传这些纹理包，有时甚至会鼓励他们这样做。
