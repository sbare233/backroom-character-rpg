//1998年9月25日，bHZsMzUy被发现了。它是一系列随机生成的储存间，没有确定的大小。bHZsMzUy整个层级就是由走廊和两边的储存间所组成，而且储存间被编上了伪随机的序号。除了一些丁字路口对面的门和在储存间门边的小垃圾箱外，bHZsMzUy中完全没有可以定向的标识。 bHZsMzUy为一列列存储单元旁的荧光灯所照亮，或者也有些荧光灯位于层级的墙上。
//
//在bHZsMzUy的储存间中，塞满各种纸箱和塑料箱。箱子里，装着一些引起回忆的物件，比如一些破旧掉毛的毛绒玩具、各种被撕毁的照片，照片上的内容是访问者的家人。但是，据报告，在这一堆堆旧物件中，肢团可能会塞在物件的下面，来伏击那些正将物件一件件拿开的人，也就是肢团的猎物。
//
//bHZsMzUy产生的最大危险，是它的独特侵入性性质：某一个人说出或者记录下这个层级的编号时，周围91米为半径的区域都会被这个层级所取代。语言触发的效果不会持续，这个层级会消失；然而，从记录文本所触发性质后，直到这个文本被摧毁或删除前，这个侵入效果都会存在。所有拉丁字母表文字，即使有一定变体，均会触发这个性质，如法、西、波、英语，甚至越南语。bHZsMzUy也会识别一些其他语言，如西里尔、希腊、阿拉伯文字。
//
//异常的是，bHZsMzUy不能识别写成64进制、16进制数等的层级编号，虽然它们也是用了拉丁字母来描述。目前，我们正在想尽办法宣传，使流浪者用bHZsMzUy1来代替它原本的层级编号，由此控制bHZsMzUy。
//
//如前所述，bHZsMzUy只存在一种原生实体：肢团。它们藏在储存间的箱子下面。但当bHZsMzUy对任何一个层级开始侵入，所有原层级的流浪者、实体、物品都会转移到bHZsMzUy内，因此这将决定bHZsMzUy的生存难度和实体数目。Level 0、Level 1、Level 4、Level 11与Level 61易受bHZsMzUy影响，但其他层级的实体也存在于bHZsMzUy内。
//
