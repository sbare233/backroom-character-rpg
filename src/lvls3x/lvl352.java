//当bHZsMzUy的数字被说出时，层级立即侵入，在两位流浪者周围展开了它的走廊与储存间，也使流浪者被突然惊吓。记录显示，一个储存间就正好落在一位流浪者前方，于是这位流浪者挤坏了储存间门的结构，他脖子、左臂和右腿的位置留下了门上一个个大洞。在bHZsMzUy被发现时，层级只有简单的一个区域，所以离开层级易如反掌。
