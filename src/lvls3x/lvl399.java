//入口
//尽管未被证实，但现有的理论表明，只有当流浪者单独行动，且在层级之间做着长距离的旅行时才能来到这个层级。该层级只能够通过睡眠的方式进入。在进入Level 399后，流浪者的身体以及所带的物品都会被传送到这里。
//
//出口
//流浪者在Level 399中再次进入睡眠就可以离开那里，并在他们的目的地醒来。也可以从客车的车门切出至Level 50、Level 69和Level 80。但这种方式是非常不推荐的。
