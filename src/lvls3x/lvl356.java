//启程门厅：
//启程门厅如名所指，为 Level 356 的首个门厅。于其内，右墙挂着一块板，大体布置有许多不干胶贴，每张贴纸都带有以下三条通信之一：BANTU KAMI 或 SMILERS MENGEJARKU 或 KITA TAK AKAN LEPAS DARI INI1。翻译过来则是关于一个流浪者被笑魇追赶的事情。
//
//扭曲作伴
//“扭曲作伴”是 Level 356 中，一个高天花板、右墙带电闸、左墙堆木料的走廊之外号。在“扭曲作伴”的末尾，有个出口标记，内含一间小室，小室右侧有扇门。勿信彼门，因其将送你至终点。
//
//切-出？对：
//切出？对，是 Level 356 内的一个空走廊。看似空荡荡，然并非如此。其地面露有一便签，上书“我们全赖杰瑞而生，莫要否定它。欲睹其容貌于他荣光之中，前行至走廊尽头后打开门。你将很快遇见他。”这便签显示了此处为通往杰瑞之门室的入口。若于 Level 356 中以切入的方式进此走廊，通常会转至其他层级的“负层”。无有此门厅的照片。
//
//出口门厅：
//出口门厅是 Level 356 的另一种走廊。该厅内有一扇双开门，门顶上有一个出口标记。此门将通向某一随机层级，而折回时则会重返 Level 356。
//
//出入口：
//如进入，必于 Level 21 中，经105号门至356号门后，终到此地。如离开，则去寻找“出口门厅”进入双开门，之后就能自动传送至某一随机层级。
