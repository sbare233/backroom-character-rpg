//该层级建设在多雾的永夜环境中，这座主题公园如同一座代表着欢愉与亢奋的信标，矗立于这座无穷愁楚的森林正中。游客在最初只能只身进入森林，但随着他们的探索，其他旅者的踪迹会在眨眼间若隐若现，他们好像几乎就在你身旁，但也不尽然，嘉年华狂欢的躁动、呼喊、音乐，这些被缄默了的声音没有方向地漂浮在空气中。阈限森林中那些乐于助人的工作人员，他们的形象多是由访问者们记忆中那些活着或已经去世的人们组成的。这提醒着这些游览公园的旅客们，这些工作人员的外表或许只是欺骗性的皮囊，公园的管理员对他们的外貌如何而来也都闭口不谈。
