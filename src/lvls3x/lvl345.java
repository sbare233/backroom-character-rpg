//这座公园采用现收现付的收费机制。在这里，乘坐游乐设施、购买食物饮品、体验该公园众多集市风格的游戏或浏览景点，这些项目均需花费每人一张票券。票券需要花费游客的记忆购买，且它以十张为单位起售。（抽取的记忆由公园管理员酌情决定，且所有交易不可退款。）票券也可以通过多种方式赢得或挣取。
