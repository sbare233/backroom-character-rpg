//阈限森林，也被称作怀旧公园与享乐地，是后室中一个存在于人类意识阈限之上的主题公园。它是一个方位，但并非空间，它可以坐落此处，也能位在他方，这座乐园于昨日与明天的间隙保持开放，想要入场是免费的。
