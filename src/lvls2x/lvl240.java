//描述
//Level 240，也称洛特卡湖，是一片被迷雾树林围绕的湖泊。该层级并没有明确的界限，越是深入森林，雾气就会变得越重。在湖泊的中央，存在着一个小巧的、略高于水面的石台。石台上雕刻着不同的符号，据推测应都表示“孩子”或“夜晚”。湖泊下有着许多睡莲，距离湖面仅有一英尺（约30cm）。湖底是粘土和沙砾的混合物，访客有时可以在其上看到稀有的玻璃蛙。
//
//在某些区域，湖的深度会大到不可见的地步，但是据推测湖底的材质应与其他部分相同。而在湖底之下，则栖息着Entity 114，或用它对自己的称呼，洛特卡。Entity 114似乎对整个层级都拥有着掌控权，可以随意创造食物、树木、并重塑整片土地的形状。同时，Entity 114也使得Level 240内有了抗衰老效果，据信为除树林深处那个巨大的污水坑以外Entity 114的另一创举。因此，大部分人相信是Entity 114创造了Level 240，或者，它至少参与了这一过程。
