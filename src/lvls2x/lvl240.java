//基地、前哨和社区
//洛特卡的子孙们
//他们是一群生活在Level 240内的孩子。他们在湖泊周围建造了树屋，并将Entity 114视为亲近的同伴。除去他们的“组织名”以外，这些孩子和Entity 114没有任何联系，无论是从血缘上还是从精神上。
//他们的“首领”是卡米拉·诺斯克特，一位来自英格兰的家庭女教师，1797年掉入后室，传言是第一位进入Level 240的人。她负责教授所有其他的孩子，并且是整个Level 240内第二年长的人，17岁，已经是能够进入者的上限了。年龄最大的人是一个名叫伊利亚·“酒桶”·米勒的男孩，同样是17岁，但是比诺斯克特年长数月。他和他的弟弟艾利·“小桶”·米勒都是2023年进入的，负责为诺斯克特打下手。
