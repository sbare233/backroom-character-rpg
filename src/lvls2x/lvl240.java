//入口与出口
//入口
//Level 240可通过一扇绘有儿童画的白色大门进入。一旦有超过18岁的人触碰到门，门把手就会自动消失。这样的门可在大部分存在墙壁的层级内找到，但是根据报告，在性质上偏向“孩子”的层级或地区，入口出现的几率会更大，例如Level 33内的一家玩具店。同样地，在Level 52、Level 117与Level 283都可以找到大量这样的白色大门。
//
//出口
//孩子可以自由地出入Level 240，只要找到门就行。想要离开，他们只需要告诉Entity 114，后者将会为他们创造一扇离开的门。这扇门将通往这名孩子进来时的层级。
