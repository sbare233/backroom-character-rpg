//管道
//Level 283内存在着大量的管道，它们通常有着圆形的入口，流浪者可以选择爬行进入。管道的入口通常可以在堡垒与休息区找到。管道的直径从3英尺（约91.4厘米）到5英尺（约152.4厘米）不等，并且经常会出现交汇的十字路口或是引导流浪者垂直移动的攀爬段落。管道内的窗口皆模糊不清，并且散发微光，是管道内的自然光源。有传闻说Level 283内有窗户出没，但迄今为止还未被证实。管道的尽头是螺旋状的管道滑梯，通往休息区、迷宫和海洋球池。虽然Level 283内仅存在少量实体，但介于管道内狭小的空间，一旦在这里遭遇攻击将很难逃脱。笑魇是Level 283的管道区内最常见的实体，这里漫长而缺乏足够照明的管道对他们有着莫大的吸引力。
