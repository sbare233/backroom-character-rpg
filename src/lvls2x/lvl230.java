//描述
//Level 230，也通常被称为“疑云港”，是一个破旧、腐败的港口。
//
//如今，该层级已成为后室中各个团体交易的热门地点之一。这里出现的资源包括一些旧水手服、饼干、风干的肉、曲奇饼干与杏仁水，他们会出现在一些板条箱里，并不定期地得到补充。以目前探索的极限推断，该层级的面积是无限大的。流浪者在Level 230内从集聚点出发探索过的最远距离大约是100英里（约161千米）。在他们继续远离后，虽然仍能与M.E.G.保持联系，但是集聚点与他们的无线电交流却会受到严重干扰。
//
//整个Level 230都被不变的夜幕笼罩。在这里出没的实体包括笑魇、兜帽商人、搬运工、无面灵，以及较少见的死亡飞蛾。安全区是交易贵重物品的地带，这里除了极少数的兜帽商人与无面灵以外不存在其他敌对的实体。不幸的是，Level 230也因其内部的“黑市”问题而臭名昭著。众所周知，F.T.B.等可疑组织会将该层级作为运营的主要地点，违背M.E.G./B.N.T.G.制定的交易守则，在此提供某些商品与服务。
//
//区域
//码头
//该区域处于一片水域的边界，向远方无限延伸。这片水域的深度与其中的液体究竟为何都仍未知。由于组成这片水域的液体成分未知，其会一直被记录为“不可饮用”，直到有研究队能对其进行考察并验证液体的具体成分。
//
//该区域有无限多个船道整齐排列，从海岸出发，一直延伸进水域上方。船道的材质大概率是磨损的木头，但具体材料还有待分析。这些船道在绝大部分时间里都是闲置的，但偶尔也会迎来一些货运船。这些船虽然来历不明，但已知其上存在着搬运工，以及一些尚未确定的其他实体。货运船上有一些电灯柱，提供了昏暗的照明。
//
//由于有大量的实体在此出没，码头被视为禁区。M.E.G.以及其他官方团体严令禁止任何人非法进入码头。
//
//交易区
//这里是Level 230内唯一确定安全的区域，也是整个Level 230最热门的地点。M.E.G.、B.N.T.G.、F.T.B.和F.S.P.等大型组织都在此扎营，家园酒店也坐落于此，提供大量居住区域和交易场所。
//
//作为该层级人流量最大的区域，交易区的面积大约为28平方英里（约72.5平方千米）。该区域的街道宽大但是曲折，因此很容易在此迷路；不过任何一个神志清醒的流浪者都可以从友善势力那里打听到该区域的路，甚至购买到交易区的地图。
//
//作为一处商业中心，交易区在居住区内外都设立了大量的零售店和餐饮馆。这里的商店大小不一，从街边小摊到多层的大商店都有，通常都是交易者公会的成员。这些商店出售的物品包括食物、饮料等必需品，大部分都为从“零食室”（Level 6.1）中所得的，数量与质量皆有保障。在这里可以买到绝大多数饮料、各种各样的农产品和保质期较长的食品，众多的餐厅也提供了不同风格的美食。流浪者同样可以在家园酒店等设施登记入住。
//
//Level 230的交易区也以其提供的高品质探险装备著称。后室补给公司1等专卖店里出售大量专业装备，或者在潘西与艾博百货商店也可以找到一些。
//
//陋滩
//陋滩与码头接壤，由狭窄、蜿蜒的街道联通到交易区。除了B.N.T.G.的230号资源提取站坐落于此，没有其他的B.N.T.G.或M.E.G.人员在此把守。该区域不宜居住，任何试图在其中探索的流浪者都会被淹没在无穷无尽的实体之中。该区域内仅存在一次记录在案的探索，探索者最终与M.E.G.断开了联系，并从此下落不明。
//
//机构
//购物
//潘西与艾博
//潘西与艾博由托马斯·潘西与罗伯特·艾博创办，是一家在Level 230内备受好评的百货商店。我们是不结盟商贸集团（B.N.T.G.）的一员。我们位于一栋大楼的一层，出售杂货、饰品、服装、装备等商品。作为Level 230内首批优秀零售机构之一，我们是一家信誉良好的零售商，能为您满足一切交易需求，并有自由运输场（F.S.P.）背书。同时，潘西与艾博的供应商也乐于收购珠宝与其他奇物。
//
//后室补给公司
//后室补给公司对很多流浪者而言，都是一家户外用品商店。其中出售外套、绳索、罐装食品、灯具、武器、驱笑剂2等众多设备。众所周知，无论是应对恶意实体还是环境，在后室中的探索都会消耗大量流浪者的装备。在Level 230中的交易区中重新补充库存，开启下一程深入后室的冒险。
//现已加盟F.T.B.。
