//餐饮
//托尼的每日精品
//托尼的餐馆就在Level 230，那是一个有趣（或者是古怪）的地方。那里每天供应带有异域特色（当然了，仍然是本地风味）的不同料理，这主要取决于当天有哪些食材可用。与在前厅中的餐馆不同，那儿使用的肉类不局限于牲畜的肉；事实上，它们绝大多数都是某些实体的肉。在任何一个夜晚，你都可能在那儿品尝到窃皮者的肋排或是油炸死亡飞蛾（通常都是无毒的雄性）。笑魇炖肉在托尼的餐馆附近大有名气，但一旦问起它们是否真的是用笑魇做的，往往都会引起激烈的争论。
//
//飞行死蛾酒吧
//欢迎，旅行者。这里是飞行死蛾酒吧，我们听说你在后室中四处探索一整天之后饿坏了。如果你想找几个志同道合的朋友喝一杯，那你就来对地方了。你应该感到感到幸运，至少我们的店里没有寄生虫了——只有几只死亡飞蛾！哈哈，放轻松，这只是个玩笑。应该是吧。
//
//守望者之灯
//守望者之灯是一所位于交易区边缘的小酒馆。这里的常客主要是精疲力竭的下班M.E.G.特工与附近B.N.T.G.资源提取站里的工人们。虽然这它并没有什么独特的产品，但却为疲惫的工人们提供了热腾腾的食物、可口的饮品与温暖的壁炉。
//
//基地，前哨和社区:
//交易者公会
//这一由B.N.T.G.设立的前哨站会为附近地区的流浪者提供食物。常驻人员大约40人。
