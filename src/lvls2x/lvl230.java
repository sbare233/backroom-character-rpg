//后室自由贸易联盟（F.T.B.）
//这个松散的可疑组织由一群肆无忌惮的商人成立，其主要运作基地位于Level 230。他们没有可信的身份证明。流浪者应警惕那些冒充其成员的兜帽商人。F.T.B.具体的商户数量尚不清楚。
//
//M.E.G.Delta基地
//在M.E.G.入驻之前，Level 230因充满了敌对实体而臭名昭著，并被公认为不宜居住的层级。任何一个误入该层级的流浪者都会祈祷能找到尽快离开的办法。
//
//然而，随着M.E.G.的到来，常驻约100人的Delta基地拔地而起。M.E.G.在保卫基地周边地区的同时修复了附近老旧的基础设施，并肃清了这一区域的实体。
//
//B.N.T.G.与F.T.B.等团体在那之后接连而至，开启了交易的热潮。一个名为自由运输场（F.S.P.）的社团也在这里兴起，最终形成了如今的交易区。
//
//自由运输场（F.S.P.）
//这个集聚点介于交易区与码头之间，是B.N.T.G.与F.T.B.的商人们所居住的区域。有趣的是，F.S.P.没有自己的巡逻队伍，完全依赖M.E.G.的保护免受实体侵扰。这里的居民主要居住在街道两侧的膝板房里，常驻人口大约有400人。
//
//家园酒店
//这个团体在交易区经营着一家小旅社，为该区域的流浪者提供住宿服务。他们原本的酒店位于Level 5，而他们的第二家酒店则在Level 11。
//
//B.N.T.G.230号资源提取站
//这一由B.N.T.G.建设，并已在此开采了许多资源的基地位于高度危险的陋滩，不得不需要他们使用武力保证一条从交易区通向该基地的走道。该基地开采到的资源主要包括从地下萃取的杏仁水、从房屋上拆卸下来的建筑材料，以及从鹅卵石中提取到的石膏。该基地内有20名常驻的安保人员与50名工人。
