//Level -5
//在 Level -5 时，一般只会发生在冬季时期的事件将会出现。事件发生期间，天空中将会出现阈限极光并转变成黄昏的状态，气温也会逐渐降低并在 2°C 左右徘徊。此类事件发生期间，若在户外度过了足够的时间，人将会进入 Level 260。
//Level 93 - “顶峰”
//从 Level 93 进入 Level 260 需要在天气处于第一和第二阶段期间于一棵树旁扎帐篷。一旦所有条件达成，人、帐篷和里面的物品将会被传送至 Level 260。
//Level 118 - “露营地”
//从 Level 118 的起点开始，向东北偏北方向走 60 分钟。45 分钟后，地面将出现小块积雪，同时云杉数目增加。50 分钟后，气温将在几秒内急剧下降至大约 2°C，若此时伤口暴露，人可能因急剧变化的气温而休克。55 分钟后，阈限极光变得完全可见，之前出现的实体将不会来到此区域附近。60 分钟后，人将正式进入 Level 260，此时在人的视角中 Level 118 将会分裂成碎片。
//Level 198 - “坠落”
//从 Level 198 进入 Level 260 是在 Level 198 时几乎完全随机发生的事件。若层级里除了阈限极光的出现以外大部分是黑暗的，则即将到来的进入 Level 260 的入口会非常明显。
//乳白天空
//从乳白天空进入 Level 260 需要利用一扇破窗。和它们那些完好无损的同类比，破窗并不展现相同的属性，它们周围总是有积雪而非大量光照。挖走积雪将通向 Level 260。无法以同样的方式原路返回。
//« 出口 »
//目前已知的 Level 260 的出口仅存在于 Level 198，Level 93 和 Level 135。需要注意 Level 198 和 Level 93 共享同一个出口。
//
//Level 93 & Level 198
//进入 Level 93 或 Level 198 需要从高原边缘跳下去。出口所指向的层级似乎是接近随机的。环境的气温和时间段会根据层级的基础设置来调整。需要注意，若已退出 Level 260，Level 198 将无法再次直接通向 Level 260。
//Level 135 - “斯卡兹卡”
//Level 260 可以从一片密林通向 Level 135。和 Level 118 的入口一样，通往 Level 135 的出口直到一个特定的点前都是由高原铺成的路。从人所在的原点起步行 30 分钟至一片密林区。20 分钟后，Level 260 的雪开始消失，同时树上的叶子转变成适应秋天的模样，并失去抗寒能力。25 分钟后，阈限极光从视野中消失，天空模拟成黎明状态。当 30 分钟已过后，天空已完全变成正午状态，且人将正式进入 Level 135。
