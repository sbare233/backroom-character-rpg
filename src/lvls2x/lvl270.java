//据报道，实体会频繁出现，并且通常是在Level 270死亡的主要原因。已确定存在于Level 270的窃皮者、钝人、猎犬、肢团以及极少数情况下的死亡飞蛾。对一些人来说，这些实体经常出现在更黑暗和更潮湿的地方。由于Level 270星球的缓慢旋转，当在该层级中徘徊足够长的时间后，可能会经历夜间阶段。到了夜间，景观往往会变得非常黑暗，这些条件被认为允许新实体产生。由于Level 270的缓慢旋转，夜间估计持续 24 小时2 ，并且温度通常会下降到 -15°C (5°F)。
