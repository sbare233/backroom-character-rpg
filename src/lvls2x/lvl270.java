//Level 270的风景
//
//在Level 270中发现的池塘和河流中含有高浓度的钾和磷等对肾脏有害的矿物质。经常饮用这种液体通常会导致血尿和几个小时后轻度至重度的肾痛，并且经常被报道会增强该层级的视觉幻觉。进一步的研究发现了一种额外的无机元素，被认为是导致这些症状的主要原因。已观察到该元素在暴露于红色环境光时会生长，而在远离阳光时会收缩。这种未命名矿物的大小被认为与这颗红色太阳的活动大致成正比。这进一步表明它们彼此之间存在着密切的相关性。然而，由于研究成员之间反复的猜想冲突和分歧，它经常被忽视。虽然这种化合物主要存在于液体区域，但任何设法进入 Level 270的流浪者都会在他们的枕叶中发现这种矿物质的痕迹。在一篇论文发表后，出于好奇心而进行的大量科学研究证明了这一点，这些研究表明这些化合物可以快速出现在生物密封的大脑中。
//
//Level 270的整体大小目前未知，但肯定是有限的。许多流浪者报告说，影响他们的重力与位置无关。1除了其自然形状外，Level 270还有一个强大的磁场。它被认为是由行星核心中的铁凝固以及行星赤道的旋转引起的旋转电流（电磁）产生的，使指南针导航更容易一些。
