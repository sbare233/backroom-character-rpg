//入口与出口
//入口
//一般来说，在其它带有黑板的层级（如Level 117和Level 321）中向着黑板切出，会导致从Level 202的墙壁中掉出。有趣的是，能作为该层级入口的黑板上常有被擦去一部分的文字和图画。虽然这一条件似乎不是必需的，但传闻从这种黑板进入的成功率更高。
//
//出口
//在极少的情况下，你会在墙壁上找到木框，它们几乎都被粉笔灰覆盖着。通过木框所围出的黑板切出，会使你随机出现在另一个有黑板的层级中。然而，由于木框上总覆盖着粉笔灰，进行切出会很危险。
//
//在该层级中，离开是首要任务。切记携带小型铲子等物品来帮助你挖掘寻找出口。使用木桩、绳索等物品保护自己在不慎陷入粉笔灰时不被掩埋。时刻注意周边是否有木框。
//
