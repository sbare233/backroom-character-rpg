//描述
//Level 202，或称粉笔长廊，内部是一堵堵黑板形成的墙壁，墙壁构成了一座可能无限大的迷宫。其主要结构为走廊，约3米宽（合约10英尺），无大型房间存在。该层级整体被浓雾笼罩，能见度仅有约10至15米（合约30至50英尺）。空气中的粉笔灰可能会造成窒息，因此建议携带口罩。站在该层级中仰头看时，只能看到墙壁延伸并消失在黑暗中。未知层级上方是天花板还是天空，至今还未找到墙壁的尽头。
//
//Level 202的地面被白色粉笔的细灰所覆盖，粉笔灰底部材质未知。层级中，大部分区域的粉笔灰都被夯实，可支撑流浪者在其上行走；但有部分区域的粉笔灰极为细小松散，进入这些区域会立即陷入其中。如若不慎陷入松散的粉笔灰中，很容易最终因为窒息而死亡，因为周围总是缺少可供人攀附的物件。至今，未在该层级发现任何流浪者或实体的尸体。因此，建议流浪者时刻注意前方的灰尘是否可供行走，最好配备一支手杖或类似物品。
//
//黑板组成的墙壁上偶尔能发现古怪的文字或记号，其来源不详，且一般不认为是由被拆散的流浪者们（该层级的这一特性会在下文说明）书写。一般认为，书写者是潜伏在该层级雾中的人形生物（详见下文实体一项），但是这一设想未能被有效证实。也有可能这些文字和记号本身就是层级的一部分。
//
