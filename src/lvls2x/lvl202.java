//各式各样的箭头。
//此外，与Level 0的特性相似的是，流浪者在该层级似乎无法与其它人类相遇。未知这一现象的原因是流浪者被送到了距彼此极远的地方，还是每个流浪者都会进入一个该层级的“副本”。即便流浪者们尝试集体进入该层级，也还是会在进入时立即同彼此分开。这一特性会使进入其中的流浪者长时间孤身一人，很容易引起孤立、分离的感受，对本就处在危险环境中的流浪者造成精神伤害。请牢记，只要找到出口，就能和其他人相见。
//
//实体
//有流浪者曾记录该层级存在干瘪而模糊的人形实体，但并无接近或与之互动的记录，实体相关信息也较少。因此，没有记录显示该层实体对流浪者进行了任何形式的攻击。这些实体似乎只会在远处观察流浪者，试图靠近的流浪者则称这些实体总是会转过拐角并消失。
//
//基地、前哨和社区
//由于流浪者们总是会被拆散，因此几乎无法在该层级建立基地、前哨和社区。除了这个原因，该层级潜在的危险和走廊狭小的空间也导致建立这些设施毫无意义。
//
