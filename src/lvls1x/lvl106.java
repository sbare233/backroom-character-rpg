//Level 106是后室的第107层。这里被发现的日期不得而知，只有通过帮助者1和在他们帮助下逃跑的人才能知道里面的情况。
//
//Level 106似乎是一系列永无止境的走廊，所有这些走廊都通向各种家庭中的房间，例如卧室、客厅、储藏室和厨房。偶尔有例外，例如发电机房。
