//Level 106分为三个主要区域，每个区域似乎都是无限长，四层楼深。虽然每个区域都有相同的主题房间，但它们都逐渐变得更加腐朽。
//
//区域A：Level 106的区域A被认为是三个部分中最安全的，这是因为缺乏实体和维持群体的能力。这里的房间如下：厨房、储藏室、卧室，在极少的情况下，还有发电机房。该区域中的所有计算机或类似物品都处于正常工作状态，但是它们都被一个似乎不存在的密码或一个不断变化的密码锁定，因为没有多少黑客能够成功地将它们启动到引导窗口，所以得出了目前的结论。电视也是如此，但它们都被永久地调到了电影《马克·吐温历险记》片段的循环中。在那里，一个人物，实际上是魔鬼，对一群有知觉的粘土生物造成了巨大的痛苦。痛苦地杀死他们。在关闭之前，新闻总是以引用“假若我们有了他们的帮助，我们能做成更多”结尾。当附近没有人时，所有的电视似乎都关掉了。因此，如果你发现一个打开的，通常意味着“移居者”2在附近，或者曾经在附近。厨房里有各种各样的食物和水，电器都正常工作。尽管炊具似乎会自行启动和关闭，即使设定了时间，但它还会改变炉架的位置和加热温度；微波炉会设置一个随机的时间，等等。这个层级有灯，但不是所有的灯都能亮。虽然这个迷宫里没有一个房间是完全黑暗的，但也会有一些阴暗的地方。然而，谢天谢地，这里并没有隐藏任何秘密。至少在这里。
//
//区域B：Level 106的区域B略显危险。这里的房间与区域A的房间一模一样，但都比较破旧。墙纸从墙上脱落，水管里的水从天花板上滴下来，一些家具甚至被翻过来、毁坏或搬走，在木头或地毯上只留下模糊的轮廓。这些房间里的机器很容易出错，有些能启动，有些则不能。但是尝试启动它们在不止一个方面是一场豪赌，因为在Level 106的这个部分打开任何东西通常都会吸引实体。例如，实体有猎犬、笑魇，有时还有肢团。
//
//区域C：区域C是Level 106最危险的区域。这里的房间比其他区域更破旧，也被植被覆盖，从杂草到发芽的树木和丰满的灌木。在这个区域中没有光，实体无处不在。所有的房间都只有一些基本的家具，就连这些家具也都被毁了。这里有床架、书桌、空书架、柜台等等，但没有枕头、书籍、食物等更小、更独特的装饰品。这里的任何机器在退出之前都会关闭，这意味着照亮这一层的唯一方法是通过油灯、火、煤气灯等。穿越这里可能很有挑战性，因为当你试图逃离实体或只是正常穿越时，植被会挡住你的路，减慢你的速度或绊倒你。
//
//此层级有一种奇怪的效果，就是你感觉自己被人注视着，尤其是当电视打开和关闭时和使用其他机器时，或者当你到达区域C时。一份找到的文件将其描述为“好像有什么东西一直在你背后，你看不到甚至不知道。但它总是在那里。”这究竟是对一个人心灵的影响，还是更险恶的东西，目前尚不清楚。一些人认为它是一个生活在所有楼层的实体，其他人只是认为它是偏执狂或恐惧。
//
//基地、前哨和社区
//“移居者”
//移居者是一个由大约8-10人组成的群体，他们完全依靠Level 106区域A的资源为生，因为他们都缺乏深入B区和C区逃跑的勇气。他们用床单和沙发在大约1-3个房间里露营。在离开之前，他们对周围几个房间里找到的食物进行定量配给。如果你遇到他们，你可以随时加入和离开。他们接受的人数不超过10人，如果他们的人数少于8人，他们会更加急切地希望你加入。
//
//“帮助者”
//帮助者是一群帮助其他人的人，他们最终在这里逃跑。幸运的是，他们在一个楼梯和另一个楼梯附近找到了大量的楼梯，这给了他们一条通往较低楼层并最终逃生的捷径。从少数目击者的叙述和故事来看，帮助者和移居者并不知道彼此的存在。他们手头没有太多战斗力，反而在你逃跑的路上堵住了不必要的门口和房间。他们也很友善，只有在有理由的情况下才会咄咄逼人。他们只会给一个非常需要的人食物和水，因为剩下的是留给志愿者的。
//
