//卧室
//卧室是一种与21世纪早期美国郊区住宅的卧室类似的房间平台。其次墙总是被漆成浅棕色、蛋壳白色或亮蓝色再者贴上墙纸。卧室里的油漆或墙纸（连同Level 162的其他平台房间）也可以被破坏，会露出下面的灰色混凝土。卧室总为多边形，被描述为矩形合并在一起。卧室里的家具包括床、书桌、梳妆台、边桌和椅子，还有电视、照明设备和通风设备等电器。所有家具和电器都是用其前厅对应物的资源制造的。而墙壁装饰，如描绘前厅文化特征的绘画和海报（自然场景、乐队、电影广告等）十分常见。
//
//所有的家具和电器都是运作的。虽然卧室里没有可食的食物和水，但于卧室居住和睡觉均十分舒适。 卧室是居住在Level 162团体的休息区，也是迷路的流浪者和探险者的接待场所。通常卧室里有一张或两张床，这意味着床通常由多个人共用。
//
//abandoned%20kitchen
//一个厨房平台。除了水槽以外的所有器具均缺失，水槽无法运作。砖墙实际为贴上砖墙纸的混凝土墙。
//
//厨房
//厨房平台为类似于21世纪初美国郊区房屋中常见厨房的房间。厨房墙壁总是漆上白色或贴上不同的印刷墙纸，通常模拟砖块或木板。厨房通常为矩形或三角形，少概率为方形。与其他平台的门不同，厨房门由可滑动玻璃制成。玻璃几乎为半透明，只在另一面映射出模糊的形状。冰箱、水槽、灶台、烤箱和厨具是厨房里常见的物件，尽管它们从来都无法运作。然而，冰箱和烤箱中会出现可食、易腐烂的食物，包括各种新鲜水果和蔬菜。有时能够在水槽中找到饮用水，尽管水龙头永远不会运作。
//
//由于Level 162居民经常会有坏血症，因此人们希望厨房里有富含维生素C的水果和蔬菜。冰箱里可以找到柑橘、柠檬和酸橙等柑橘类水果，Level 162的居民会定量供应，这使它们成为最有价值的交易货物之一。厨房平台除了补充食物和水外，无其他用途。如厨房平台几乎无资源，通常会被层级的居民忽视。
//
//仓库
//仓库平台类似于大型仓库建筑的横断面。仓库平台始终为矩形，是唯一无次墙的平台。上方的天花板虽然为与主墙相连，但不会因缺乏支撑而倒塌，且保持于直立。仓库里唯一能找到的器具为长长的金属货架，货架上可能摆放着21世纪初风靡于世界各地的非功能性家用电器品牌。除了电器，还有一盒盒易腐食品，这些食品可能是新鲜的，也可能是陈腐的。这些盒子里装着许多常见的零食，如薯条、椒盐卷饼、牛奶巧克力和口香糖。仓库里均有瓶装的杏仁水，这使得其成为饮用物资的主要来源。
//
//仓库是食品与杏仁水的高频生成点。虽然食物无丰富的营养，但其确实令喜欢这些零食的人精神振奋。Level 162的主要群体则向无附属的流浪者售卖零食和杏仁水。
//
//店面
//店面平台类似于前厅的小商铺，主要为餐厅、熟食店和便利店。墙壁与地板通常漆上许多不同的颜色或覆盖着瓷砖，而平台形状通常为矩形、三角形或六角形。店面内有柜台、小桌子、椅子和21世纪早期典型设计的空自动售货机。与其他平台不同，店面平台中的可食用食物为正餐，通常为汤、三明治与熟食。也可以找到烘焙食品，如蛋糕和饼干。这些烘焙食品味道鲜美，如同刚从烤箱中取出一般。
//
//虽然店面平台上无饮用水或杏仁水，但许多流浪者也愿意饱餐一顿，他们想要的不仅仅是仓库里的口粮。由于这些食物如厨房般罕见，Level 162的居民极其关注店面，通常在搜刮一阵后只存留下器具。
//
//办公间
//办公间是类似于Level 0中的办公室的平台。其总体设计——办公间是唯一在平台区域有次墙的平台——与Level 0极其相似，但墙纸和地毯会有多种不同的颜色。办公间总会被次墙分隔，次墙无门或窗，尽管其所连接的主混凝土墙能够容纳门。这些平台从来没有天花板，而这是进出办空间的唯一途径，要么爬墙，要么从更高的平台跳进去，要么穿过主混凝土墙上的入口。
//
//办公间总是呈矩形，是饮用水的不错来源。办公间内器具丰富，在桌子或椅子旁边，极可能有一个饮水机。水壶可取出，能够由流浪者携带。办公用品，如纸、笔、剪刀和胶水，在办公间很容易找到，一些组织用其来制作标识服装。除水与办公用品外，Level 162内的办公空间无其他用途。
//
//浴室
//浴室平台类似于21世纪早期建筑中的公共厕所。浴室总是矩形或多边形的形状，并于墙壁和地板上贴有陶瓷瓷砖。厕所、水槽、镜子、间隔板与地面潮湿的标识是浴室中仅有的器具。在每个水槽和厕所都可以找到饮用水，这使得浴室平台成为收集水方面最重要的方式。
//
//由于浴室平台相对较小且易于寻找方位，并几乎总是能找到水，因此浴室成为Level 162较大组织进行清洁运动的常用对象。
//
