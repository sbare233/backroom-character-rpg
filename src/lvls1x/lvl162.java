//壁牧者
//壁牧者是一群居住在Level 162的人，由104名成员组成。他们利用登山装备、绳索和滑轮在平台之间穿行，自称是在Level 162的流浪者的守护者。他们为那些对环境缺乏经验的人进行救助，与其他较小的群体和流浪者进行交易，并带领那些不希望留在层级的人到较为安全的出口。
//
//壁牧者可以通过他们的徒步装备和绳索来识别。除了装备外，每个壁牧者还必须佩戴灰色臂章，上面用白色蜡笔写着“壁牧者”的符号——一条笔直的垂直线和三条横线贯穿其中，象征着主墙及其众多的平台。这些臂章是用在办公室里找到的纸和胶带粗略制作的。壁牧者会在他们能找到的任何平台上定居，不过出于宜居性的考虑，他们通常会选择卧室、仓库或店面平台。
//
//他们靠各种平台提供的食物为生，壁牧者用他们的登山设备与绳索，五人一组，从一个平台远行至另一个平台。
//
//组织者
//组织者是一个由49人构成的组织，为出入Level 162的人员提供会计系统。他们在纸上记录大量日志，跟踪进入该层级的人员，确保退出的人员只到达最安全的层级——或者，如果他们是某个组织的官方探险家，则确保他们安全到达更危险的层级。虽然组织者经常与壁牧者同行，但他们是各自独立的团队。
