//描述：
//Level 162是一堵极大极厚的混凝土墙，墙的侧面会伸展出很多平台，这些平台上又有另外的墙体和天花板，内部还附有家具。根据目前的观测结果，无论是高度还是宽度，其墙面均向各个方向无限延伸。其墙下无任何支撑，因此与许多层级不同，Level 162实际上没有地面。墙及其平台前方为一个无限的浅灰色空间。这一层级虽然温和，但无任何天气因素，如雨或风，并且始终保持在79°F（29°C）的温度。 Level 162无昼夜循环，尽管光线与前厅白昼时段相当。观测表明，这种光直接从墙的对面照射到墙上。这束光线来自浅灰色的虚空，虽然目前还不知道这束光究竟来自虚空中的何处，如果在任何具体的方位。假定该种光源的大小可能是无限的。
//
//由于其众多的出入口，以及该层级本身的宜居性，Level 162已经成为层级间的交通枢纽。从某一层级进入会到达特定的平台，从某些平台离开会到达相应的层级。作为层级整体宜居性的一部分，Level 162物理属性的一个独特之处在于，从任何高度坠落都不会导致在撞击另一个平台时受伤。尽管如此，坠落时仍然会产生惯性。
//
//平台通过未知的方式从墙的侧面随机出现，并无缝地被吸回所述墙中。所有平台在任何方位都保持可见和稳定，从10分钟到几个24小时不等。通常地，当一个平台即将移动时，平台在移动之前会晃动大约30秒。接着晃动将停止，平台将移回墙内。这些平台可以通过其间生成的墙1的门和窗进出。如果在平台后退时仍有人在平台上，随着地板移动，他们最终会跌落到下面的另一个平台上，若在平台有四堵墙的情况下，他们会被动地切出。
//
//粗略的计算表明，平台的移动速度为每小时4英里，即人类的平均行走速度。平台的大小可能在250到3000平方英尺(76到914平方米)之间，尽管这些平台尺寸可能有极大的差异。次墙的高度通常在8到15英尺(2.4到4.5米)之间。大多数平台为方形、矩形或多个四边形并合在一起的组合。虽然偶尔会发现三角形和六角形的平台，但还无所记录平台偏离上述形状的情况。这些平台的次墙和主混凝土墙本身都有具有正常功能的门，尽管主混凝土墙上的门总是通向其他层级。次墙中出现的门则为Level 162内平台的入口与出口。平台的次墙上会出现窗户，但无证实窗户出现在主混凝土墙上的案例。
//
//broken%20wall
//一个普通平台上破损的次墙的案例。虽内部钢筋弯曲，但仍极其稳固。
//
//试验表明，混凝土墙可能会碎裂与损坏，但不会完全破裂。当从主墙上凿下小块混凝土时，新的混凝土将在30秒或更短的时间内填平孔洞，就像墙体在自我修复一般。由于墙的坚固性，这种混凝土的样本通常极其小，证明了其为由普通强度的混凝土制成的，即混凝土由未经加工的混凝土、沙子和砾石及沙子的骨料制成，每克的比例为1:2:4。邻近平台上的次墙也表现出同样的结构，尽管它们为可破坏的。当这些墙被拆开时，其似乎包含有支撑的钢筋。平台的地板由同样的混凝土混合物制成，尽管上面可能覆盖着由合成纤维制成的地毯，这些合成纤维有各种各样的颜色。钢筋也出现在地板比平台墙的数量更多。
//
//平台的地板由相同的材料制成。大约有一半的平台大部分是空的，没有重要的特征，而另一半类似于前厅中的房间。一些空房间可能有小型的家具，如餐桌和椅子，尽管这些家具往往比前厅的家具更易碎。对比之下，前厅样式的平台通常载满了各种家具。
//
//有些房间可能有电器，但唯独已知运作的电器是墙上的插座和电灯。管道系统与Wi-Fi等自然存在于后室的网络在Level 162上无法使用，但400-500 MHz或以上的无线电信号可以正常工作。这使得在超过3000英尺(914米)的距离上接收无线电传输变得困难。
//
