//铸造厂是一座占地约 47 平方千米的建筑，外形如同一所破旧的工厂，不少区域已是锈迹斑斑，而且充斥着金属废料和各种垃圾。铸造厂内部发现了 Level 150 产生怪异效应的可能原因。这栋建筑内部完全由一种装置占据，该装置被“新人”们称作“机器”，它可能就是造成记忆替换现象的源头。M.E.G. 派来的探索小队发现，“机器”不仅占据了建筑的大部分区域，还向下延伸至数个地下室，延伸深度暂时未知，但估计有 1 千米以上。
//
//“机器”似乎总是处于运行状态。在进入铸造厂时，能听到机械运转的轰鸣声，但尚不知其来源。经尝试，暂无法破坏、拆解“机器”或进入其内部，因此尚不知晓它的运作原理。由于该层级较为危险，所以相关测试进行得较少；但已经可以确认流浪者一旦进入 Level 150 就会受到精神影响。尚不明确“机器”是如何检测到有人在层级内的，关于这个问题有两种假说。
//
//- 地下侦察假说
//在铸造厂下方发现的地下室使得我们怀疑“机器”可能不是单单向下延伸，而是朝各个方向延伸，并在 Level 150 内组成一张网络。这也能解释该层级地表出现的轻微震动。
//
//- 注视假说
//不能排除天空中名为“眼”的红色球状物对该层级的影响。“新人”们认为该物体会注视他们，甚至会把它赞颂为神明。因此，可能该物体与“机器”有所连接，会提醒它有新的目标进入了层级内。
