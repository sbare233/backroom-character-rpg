//Level 150 是后室的第 151 层。该层级于 2019 年 5 月 11 日被“Reed”小队发现。
//
//描述
//Level 150 也被称为记忆铸造厂。该层级是一片翠绿的草地，其内还有一些树木生长。整个层级都被淡蓝色的光笼罩，仿佛一直处于日落时分。天空中有一个类似月球的红色球状物，该层级居民（也被称作“新人”）称这个物体为“眼”，红色球状物来历不明，可能是天体、实体或其他异常物体。
//
//Level 150 中有一条老旧、狭窄的柏油路，路的缝隙间已经长出了一些植物；这条路连同整个层级似乎都是无限延伸的；“Reed”小队在探索过程中发现，如果朝这条路的垂直方向前进，一段时间后会回到起点。这种现象会突然发生，被影响者常常会很惊讶，而且会随之丢失现象发生前几分钟内的记忆。推测该层级本身会以某种方式改变流浪者的记忆，并将他们引回柏油路。
//
//位于 Level 150 时，能隐约听到一种嗡鸣声。一开始这种声音还比较小，但随着人在该层级中停留的时间增加，这种声音的强度也会提升，最终，声音会升高至“仿佛有钻头直冲你的脑袋钻去”的程度。这种声音似乎与该层级对人造成的精神影响有关。在 Level 150 中停留的时间过长时，会有一种古怪的现象发生：受害者的记忆会被逐步替换，最终成为全新的个体。似乎正是这种现象导致该层级实体绝迹，因为它们都“忘记”了自己的存在。
//
//精神影响现象
//这一现象也叫“铸造”，它会使层级中流浪者的记忆被逐步替换。在层级中停留的时间越长，或离“铸造厂”越近，这一过程就会发生得越快。虽然记忆被篡改的速度快慢不同，但这个过程总是会按以下几个阶段进行：
//
//阶段一：在这个阶段，所有与受害者主体性无关的记忆都会被替换，虽然不会影响到受害者的自我认知，但会引起极大的混乱。受害者将难以决定前进方向，无法编排行动计划，给自己在 Level 150 中的探索造成不便。
//阶段二：在第二阶段，一些根植于受害者头脑深处的记忆开始被替换，这些记忆称作“遥远记忆”，它们非常微小，虽然被头脑记录，却不会影响到受害者的人格、生活经验。这一阶段开始后，受害者对自己过去的认知会改变，但程度还不足以影响其心智完整性。
//阶段三： 在第三阶段，受害者在 Level 150 的影响下会产生难以治愈的自我认知损伤。这一阶段，受害者过往学习的所有技能都会被遗忘、替换。这会使他们产生一种不协调的感受，因为他们会发现自己的知识与行动能力无法匹配了。
//阶段四： 在这一阶段，受害者所拥有的各种知识会被一并抹除、替换。在少数情况下，替换出的新记忆还有助于增强受害者产生的新能力。这一阶段结束时，受害者的核心记忆仍然保留着，但是他们对自己生命经历的认识已经完全不同。
//阶段五： 最后一阶段，受害者的剩余记忆也会被替换，丢失这些记忆后，他们原本的自我认知会随之消失，成为一个完全不同的个体。不过，至今还没有人因为这一效应获得全新的姓名。这可能是受到此影响后残留的唯一记忆。
//铸造厂
