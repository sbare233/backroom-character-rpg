//描述
//Level 180，或称其为“旅者之海”，似乎是一片无边无际的海洋，因海水受其不同区域变化的重力的影响，部分海平面呈现出像山坡或山地的地形。普通海洋应有的各种"洋流" — 没有更合适的词语以形容1— 似乎并不存在于岛屿周围与深海的海域中；然而根据先前的航行经验，这些不可见的洋流可以像真正的水一样影响航行。借助这些洋流航行时，看起来就像是在稀薄的空气中航行，此时下方的海床不再可见。这些洋流在《“旅者之海”海图及参考地图》中被用于将碧蓝的海洋划分为不同的区域，并作为这些被分割的区域之间的边界线。
