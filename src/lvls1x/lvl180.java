//这些洋流一段可长达约一公里，是相当危险且不稳定的，同时它也会产生重力扭曲现象。同样危险的还有一种粘稠的灰色海水，与海水相比更不透明。“摆渡人”和海军通常都只会避开灰海，因为许多人都认为灰海中含有一种神秘的物质，并称其为 海洋异形
