//实体
//鱼类确实栖息于“旅者之海”，但目前尚不清楚它们本身就存在与此层级，还是通过其他海洋切入的。此实体放在这里是为了作为样板。
//
//较为常见的实体之一是荷兰人，一艘没有船员并缺少维护的船。在极少数情况下，在整个层级都可以看到荷兰人。船体上有较大的缺口，但不会被洪水淹没。然而，炮火可以对它造成伤害，并且可以使其下沉。水手们都认为，荷兰人是古老的幽灵船，它们独自航行，在光荣的战斗中死去，而不是像死亡或失踪的船员一样在岩石或海岸上死去。然而，这些传说没有任何证据支撑。
//
//一种很少遇到的罕见的实体，被称为海洋异形或后室海妖。报告称，它寄身于淤泥中，偶尔通过“洋流”到达另一个淤泥中。据目击者推测，这是一个球状实体，其多边形触手状长达十几米。虽然没有船只遭到袭击的报告，但经常有船只失踪。一些偏执的水手声称是海洋异形袭击了他们。
//
//已建立的港口与社区
//莫提港
//莫提港是Level 180中建立时间最长的社区和港口之一，港口内有一个小城镇，最初发现时只因人口过度增长而略显混乱。它靠近交通繁忙的出口，同时又是Level 180的主要入口，被称为商人中心。位于岛的另一端有一个干船坞，用于维修和定制作业，但价格非常昂贵。
//
//兰利港
//以后室卫队海军元帅的名字命名。这是海军的一个武装要塞，各处都是修复枪支和大炮的工厂，还有装满火药的储藏室，供远征使用。港口内有一个小镇，但它仅限于海军人员进入，守军会对未经许可接近的人发出警告并开火。
//
//达尔利港
//达尔利港是一个以聚会和啤酒闻名的小镇，当地有一家酿酒厂，由一家于Level 180创立公司管理，是最受海盗、海军和平民欢迎的地点之一。值得注意的是，所有停靠的船只都必须相互停火，任何人开火的行为都会受到岛上一些或小或大的堡垒的联合攻击，大型堡垒配备了更强大的火力和更多的人力。
//
//鲍尔港
//鲍尔港的位置未知，这个地点的位置只被一些海盗船长所知。因此，许多人认为这只是一个传说——然而；由于它声名狼藉，被认为是“海盗交流中心”，因而它被列在这个名单上。若有流浪者知晓相关信息，请向M.E.G.或后室海军卫队人员汇报。
