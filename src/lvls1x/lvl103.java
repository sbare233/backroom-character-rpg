//红色的管道会传输一种独特的红色物质，这种物质很难检测；因为它蒸发的速度很快，一旦把它从红管中取出，它就会迅速地消失，这导致了它无法被取走。顺着管道走有时会到达生物危害控制室，它最终会连接到某种发电机或离心机之中。
//灰色的管道会将氧气输送到整个走廊，因为走廊从任何起点延伸的越远，氧气的含量就越少。这些氧气会被连接到空调设备中，为流浪者的生存提供足够的氧气。
