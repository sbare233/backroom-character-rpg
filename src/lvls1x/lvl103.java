//
//描述
//
//尽管在无尽之海中航行，这艘航空母舰依然屹立不倒并保持坚韧：它为了存续并对抗一切危险而建造；它没有目标，没有目的地，只以自由和自己的意志为方向。
//据推测，Level 103是一片无限大小的水域，因此在口头上可以称它为Ocean 103；因为缺乏测量这片海洋确切面积的手段，因此无限只是一个近似值。
//
//NOorYCajsU5MPx4.jpg
//有时，这艘航空母舰会停靠在一个浮船坞旁边。在这里，流浪者可以取走各种补给品，但如果他们花了太多的时间，航空母舰就会自己开走而留下他们。
//
//也许它航行在一条看似偶然的路线上，但它会找到一个目的地；有时航母会在一个浮船坞上停上几分钟。
//停靠的船坞上有着许多装满贵重补给的供应箱，因为此时会形成一道临时的桥，它会在航母和船坞之间形成连接，所以流浪者可以把补给搬到船上。
//这些板条箱——尽管很大——但永远不会装满补给；与此相反，少量的补给会在箱子里集中摆放在一起，其中包括各种物品和工具，包括但不限于：杏仁水，它可能装在瓶子里或是别的容器里；傻瓜口香糖，他可能装在小袋子或是玻璃罐里；火盐，它会装在密封的金属箱里；以及寂静汁液，它会是粉末状的，装在另一个金属箱里。
//
//航空母舰会在短暂的停留后离开，所以要尽可能快速地收集补给。如果有人被抛弃且迷失在了这里，对于其他人来说，他们的命运会变成未知数，因为没有人再看到过他们，而且除了船以外的地方没有任何的出口。留在那里的所有流浪者都被认为是已经死了。
//
//与我们正常的现实无异，Level 103有着与前厅相似的日照周期，大约有8个小时的夜间和16个小时的白天。
//
//由于该层级没有任何的海底动物和植物，它的主要特征和细节都来自于航母本身。看似有限的外表，却有着非欧几里得的内在——一旦进入——所有的走廊都会相互重叠，形成无限的迷宫，房间和走廊排列错综复杂；不过，任何标记为出口的通道都会将流浪者带回外面。
//
//由于采用了坚固的材料建造，这艘航母非常地稳定和牢固，在任何时候都不会出现任何损坏或刮伤的迹象。对航母造成的任何损坏，只要通过浓雾或周围的水接触到受损区域，就会被立即修复。这些效果似乎只对航母有效，因为伤口和损坏的物体都不能用同样的方法愈合或修复。
//
//作为航空母舰的主体结构，舰岛也具有上述的非欧几里德性质。如果有人走近墙壁，控制室将无缝地向前延伸到无穷大。只要流浪者留在原地，这些变化就会一直存在；如果流浪者离开，房间就会开始收缩，直到它达到最初的形状。
//
//战斗机和直升机都在航空母舰上，但都无法使用。因为航母的内部已经被上文所提到的房间和走廊所填满，机库和任何甲板都在内部消失了。
//飞机上的燃料可以被取走，用在许多散落在航母里的发电机上。这些发电机通常被流浪者和社区用作他们自己的基地和前哨的能源供应，机器虽然不大，但它能为许多流浪者提供照顾。
//此外，如果航母上飞机在浓雾中穿行，那么燃料就会再生；航母在浓雾中停留的时间越长，燃料就再生得越多，从而产生无限的能源。然而，雾有时需要几个星期才能再次形成，所以必须找到其他能源，或者等待航母重定向至这些基地——尽管此时仍然会发生电涌现象。
//
//下层旅行
//走进楼下怪异的走廊，它们的外表没有什么特点。墙壁上覆盖着蓝色、红色，有时还有灰色的金属管；地板是用干净的、亮晶晶的米色金属制成的，屋顶也是用同样的材料制成的；偶尔，人们还会遇到电线、电路、门，甚至窗户。这些窗户没有任何的异常效果。
//
//好奇心会引导许多人进入这个走廊上的无数个门，大多数情况下，这将导致人们进入到一个独特的房间。房间之间有很大的不同，下文列出了有记录的房间类型及简要描述，但必须记住，实体会在这些走廊和房间中游荡，因此应该非常谨慎地进行探索。
