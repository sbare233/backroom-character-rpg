//
//描述
//Level 110是一片空间性质异常的口袋维度，位于Level 110-Alpha的奇点处。
//
//Level 110-Alpha是一片直径约为34.72米（13.91英尺）的视界，其质量约等同于12个木星的质量（比木星重12倍）。Level 110-Alpha类似于前厅中观测到的黑洞，但其并不具有引力，也不会散发霍金辐射。尺寸类似Level 110-Alpha的黑洞的寿命应为3.406x1061年，但由于后室内部特殊的时间膨胀效应，Level 110-Alpha的寿命将被延长至1010^100年。
//
//Level 110似乎是一片黑暗的虚空，其尺寸与Level 110-Alpha相同，由110-Alpha碾压形成的大量原子与亚原子微粒聚合而成。Level 110的废料呈现出液态的特征，但其温度却远远低于寻常液体的凝固点。与其余的材质不同，有机材质在该层级内似乎不会被碾碎为微粒（否则该层级就无法被观测），其原因目前未知。
//
//我们无从得知Level 110-Alpha是如何使得Level 110在其奇点处存在的，根据物理学法则，那里应当是一个“点”而不是“面”，也就意味着奇点处的体积理应是无限小。
//
//诞生
//Level 110最初的形态是一座粒子加速器设施。2034年3月，M.E.G.在原Level 110中召集了一批科学家，并创造出了现在的Level 110与Level 110-Alpha。科学家们让电子与正电子以0.5倍的光速（3.353x108米/时）对撞，借此制造出了一个球形闪电1。Level 110-Alpha吞噬了整个原层级，包括所有在内的科学家；而Level 110-Alpha内镶嵌的Level 110则占据了原Level 110的层级编号。
//
//2035年2月，M.E.G.研究员██████成功识别了Level 110。██████接收到了一条长达15分钟的音频信号记录，其中有一个声音低沉的男性正在说话。
//
//研究员██████的报告摘录
//我们没有预想到会有这种结果。我们本来以为这样只会创造出后室中的又一个物品——事实上，我们已经计划好在取得预期的成果之后将其编号为Object 96了。但当我们听到那个虚无的声音向我们描述他身处的环境之时，我们才瞬间反应过来，那真的是一个层级。一个物品，同时也是一个层级……简而言之，就是这样。
