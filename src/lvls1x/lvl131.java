//入口
//就像Level 104一样，Level 131可以通过电视进入。然而，Level 131的不同之处在于，任一层级内都能找到一台屏幕碎裂的电视，并可通过其屏幕进入。无法看到上述电视机的内部结构，取而代之的是一片黑色的虚空。可以移动这台电视，尽管它会在某个随机的时点上从某个位置消失，然后重新出现在另一个层级中。进入时要当心，因为屏幕的位置仍然存在碎玻璃，这可能会造成损伤。
//
//出口
//想要出去的话，人们必须在本层的任意区域探索400米到1600米的距离。在这个位置能找到一个出口，类似于一家商店的正门。外面是一片看起来异常明亮的白色光源——一旦出去的话，这片光线就会消失，而流浪者会出现在一个普通的层级中。如果是身处本层的较暗区域，则会有一个带有霓虹灯“出口”标志的金属门——它可能位于电视货架旁、被货架包围、甚至是被货架封住。出去以后，流浪者会出现在一个负数层级中。强烈建议你寻找前一道出口，但若是正在被追赶或者身处其他危险当中，那也可以使用这扇门，风险由流浪者自行承担。
