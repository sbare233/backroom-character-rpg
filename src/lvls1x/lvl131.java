//描述：
//Level 131总的来看像是一家电视机旧货店，通常处在一种近似阳光的照射中。然而，对此类阳光源头的搜寻没有任何值得重视的结果，因为光源似乎就在房间外面，在每个房间当中。这种阳光并非无处不在，它可能会变弱、变得完全陷入黑暗——在这些地方，光线的源头就转为电力光源，例如电灯，或者电视机本身。有时会有无光区域。进入Level 131时，流浪者通常出现在该层的一处阳光充足的区域中。实体可能出现于该层的任意位置，然而，它们最常出现在较暗的区域中，因此强烈建议你去找一处阳光充足的地方。
//
//有时，区域中的阳光会尽数熄灭，如同按下电灯开关一般，该层的每一台电视都发出了信号受扰的噪声——你只能听到这些声音。通常，这意味着徘徊电噪即将出现，尽管它也可以在没有此类情况的时候显现。徘徊电噪是一种有时会出现于Level 131层内的类人实体——尽管其出现的次数要少于其他常规实体。如果Level 131层内的多台电视机发生自行启动并开始播放静电噪声的情况，请立即离开。
//
//对Level 131进行探索时，建议尽可能不要触碰多台电视机，因为可能会有复数电视机在某台电视机受触摸时发生反应，这会导致响度足以吸引实体的噪声。许多电视机会在看似随机的情况下启动，但是经过它们也有可能会令其启动。这些电视被观察到以多样的语言播放多样的频道，但是都没有足够的时长可以了解其播放的内容。此外，也常会观测到电视屏幕中出现信号干扰，以及似乎是关于某人的过去或未来可能发生的事件的记录。然而，这些东西似乎只囊括了他们在后室中的生活，因为没有播放关于外部世界的片段的记录。在Level 131的较暗区域，这些电视机会变得更加活跃。这些电视机可能具有某种程度的知觉，因为一台电视机的被毁似乎会让其他电视陷入某种“恐慌”之中——它们会迅速调换频道，移动任何旋钮、按钮、或者是它们可能拥有的其它“附肢”。老式收音机也会出现在Level 131，尽管它们不那么活跃，也不那么常见，但它们似乎也有类似的行为方式。
//
//整个Level 131层内到处都能见到随机的物品，包括一辆自行车、一个闹钟、一个工具箱等等。发现了很多类型不一的、似乎属于店主的文件——像是高中毕业证书之类的——但是每个文件上面的名字都被划掉了。除此以外也没有发现其他关于所谓的店主的证据。
