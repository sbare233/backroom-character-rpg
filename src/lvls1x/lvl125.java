//在平原地区内有一名为“道路的尽头”的告示牌。由于该告示牌后方更远处的地带的空气中二氧化碳含量随与告示牌距离而增加，其后方地带逐渐变得不可居住。由此，该告示牌被认为标志 Level 125 的宜居地带的边界。尽管如此，二氧化碳似乎并没有渗入标志以内的区域和通往标志的大公路。此外，“道路的尽头”之后的建筑与前厅相比更不正常，例如加的油为 液态痛苦 的加油站以及售出不能食用且看起来恶心的食品的小吃店。距离道路25至30英里的地带之内的建筑变得扭曲而无法进入，甚至有时与之接触也极为危险。并且，20英里以外的建筑的标志文字以及产品名基本为无法理解的胡言乱语。
