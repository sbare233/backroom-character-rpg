//出于自身意愿停留在Level 177的人，数量从几人到几百人不等。
//虽然大多数留在Level 177的人员都是出于偶然或是为了援助被困在此处的亲人，但也有些厌倦了后室流浪生活的的人故意将自己困在Level 177，为了避免孤独感，他们通常是成群结队的。一些长生果馆的规模随时间推移而猛增，当人员经过时，他们会邀请流浪者加入。人员不应轻易加入长生果馆，因为这一决定不可逆，除非你之后要将自己献给Raubritter勋爵。
//
//M.E.G.经常派出援助和研究团队，向坐着的流浪者提供所需货物及服务，通常是为了换取有关表演和Raubritter勋爵的信息。研究表明，从#519开始的长生果馆与Raubritter勋爵的关系非常好，拥有最详尽的情报。
