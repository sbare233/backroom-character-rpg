//Level 177是后室的第178层，主要由不确定数量，可能是无限多的红色天鹅绒剧院座椅构成。
//
//描述：
//Level 177类似于一座装潢豪华的大型剧院内部。尽管身处Level 177的任何地方都可以看到剧院的舞台和侧墙，但剧院没有后墙，当看向或者走向舞台之外时，该层级似乎会无限地延伸下去。
//
//尽管无论人员向着或者背着舞台行进多远，舞台似乎都不会接近或是远离，但剧院的座位本身是有编号的，这被证明是一种在Level 177内测量距离的可靠手段。目前为止，流浪者所到达最远的一排座位的编号是#744, 400，估计距离#1排有一千多公里远。尽管如此，在那里，舞台依然清晰可见。
//
//剧院的宽度虽然有限，但行行不一。目前遇到最少的每行座位数是39，而最多的有429座。而这两种情况都是发生在#892。在不那么极端的类似情况下，每行的座位数也经常有变动。
//
//Level 177没有前哨站，因此除了人员自己携带的东西之外，没有其他补给。但这并不构成问题，因为该层级的出口每隔一千行座位就出现一处，像时钟一样精准。在Level 177，真正的威胁来源于剧院的座位本身。这一威胁来的极其隐秘，很容易让那些一无所知的人，甚至是疲惫或心不在焉的知情者受害。
//
//坐在Level 177的任何座位上的任何人员都会与座位永恒地融合在一起，再也不能离开。而座位本身已被证明是完全不可摧毁的。因此，坐着的人无法自救，站着的人也无法对他们施以援手。
//
//综上所述，当务之急是避免让流浪者坐在Level 177的座位上。但遗憾的是，这并不像说起来那么简单，对于那些在Level 177长期停留的探险队成员来说更是如此。身边有无数的舒适坐垫，自己却几天、几周甚至更长时间不能坐下，这是某种怪异的挑战，也是许多缺乏准备的流浪者无法抗拒的诱惑。任何带入Level 177的椅子凳子1都会在坐上去的那一刻变成剧院的座位。
//
//任何情况下，流浪者都不应试图坐在某个受害者的腿上，这会使两人以这种方式不可逆地融合在一起。
//
//虽然能够与之互动，但那些被困在Level-177座位上的人员都在经历某种高度扭曲的体感时间。他们身上所有基本的生物过程似乎都被冻结了，失去了对食物、水分、睡眠、排泄甚至卫生方面的任何需求。但是，如果有第三方提供食物和饮料，受害者依然可以食用，但没有观察到体重增加或是肌肉萎缩的情况。
//
//这种扭曲的体感时间体现的最明显的地方是，除非受害者与其他受害者或是站着的流浪者存在互动，否则他们不会感知到两个剧院剧目之间的间隔时间。
//
//实体：
//Level 177的实体只在剧院演出时，出现在舞台以及看台座位上，由于这两处的时空扭曲，流浪者无法进入。表演开始于零散的时间点，时长似乎也是随机的，从7分钟到14小时不等。值得一提的是，坐在地板上是很难看到舞台的，这加剧了在长时间表演时，去坐在座位上观看的诱惑。
//
//在舞台上表演的实体一般为人形，但他们总是身着全身紧身衣，头戴面具，没有皮肤裸露在外。因此他们可能不是人类。他们表演的节目通常是原创的音乐剧或是歌剧，里面伴有诠释性的舞美演出，语言通常是英语、法语或是两者的混合体，而演出的主题、表演者的对话和互动往往非常怪异，这很可能表明表演者和台下人类之间存在着明显的神经和心理差异。
//
//虽然表演者向观众致意的情况并不少见，但从未听过他们直接同特定观众讲话。这与一名演出前和演出中出现在看台座位上的实体形成了鲜明对比。该实体自称Raubritter勋爵，乐于和附近的人类高谈阔论，无论后者是否坐着。
//
//Raubritter勋爵看上去像个瘦削的高加索人，穿着双排扣燕尾服，带着阔领带、高帽和不透明的眼睛。他说话时有德国口音，但和表演者一样也能说流利的英语和法语。他经常在表演开始数小时前出现，与耳边的任何人员闲聊。
//
//在谈话中，他声称自己就是剧院的现任主人，但并非是它的创造者，还提到他在后院的某处有座大型工厂。这些谈话表明他是个很神经质的人类，但似乎也非常喜欢台上混乱的演出2，经常对其发表言论，并且起立鼓掌，把紫色玫瑰扔到舞台上。
//
//Raubritter勋爵是目前已知能够主动离开座位或者向舞台投掷物体的唯一个体。如果有任何他的人类聊天对象提及这些能力，他只会将其称作无关紧要的细节而推脱，并提出要帮助他们离开座位来换取一个人情。如果他们同意，就会即刻从座位上消失，出现在阳台上，Raubritter会向他们伸出手，把他们拉起来，并带着他们从看台的出口离开。
//
//这是迄今为止人员唯一的逃离Level 177座位的手段，但经Raubritter勋爵释放的受害者都了无音讯，其命运也是未知的。如果再被问及此事，Raubritter往往会避而不谈，但有时会声称自己把他们带到了某个未知的田园牧歌的层级，或是甚至说自己把他们从后室中完全释放了。M.E.G.目前正在寻找追踪被Raubritter释放的受害者的方法。在找到相反证据之前，我们认为，Raubritter的说辞令人生疑，而且很可能是谎言。
//
//基地、前哨和社区：
