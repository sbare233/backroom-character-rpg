//描述：
//Level 153以一间循环的书店的形式存在。每个进入该层的流浪者都会来到这间城市背景下的书店的门前。从外观上看，书店贴着浅奶油米色墙纸，上面装饰着一些复杂精巧的小设计，包括两个似乎穿着希腊式服装的女人形象。如同图中所示，没有任何指出书店名称的招牌。店外的空气中弥漫着淡淡的绿薄荷与香草的芬芳。同时，该层缺少昼夜循环且一直处于白昼的状态。
