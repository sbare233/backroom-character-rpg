//书店正前方的马路是无法通行的，因每尝试走上马路时，总有一股无形的力量会阻止人们通过。流浪者或许会沿着人行道探索——然而，书店旁边的每栋建筑都上了锁并且无法打开——而转过拐角总会回到同一条人行道上。
//
//正如前文提到的，该书店处于一种循环的状态。值得注意的是，此循环具有一种古怪的改变时间的效应，因此需要对其进行更深入的描述。请不要在意描述的长篇幅，因人们能否安全地离开该层取决于层中发生的变化。下面是关于流浪者们会遭遇的循环形态的描述，首先是起始形态。
//
//关闭虚实期描述
//当流浪者进入书店，他们将会发现自己身处于类似1950年代的装潢中。角落里典雅的留声机播放着轻柔的音乐。在店里，绿薄荷和香草的气味会变得浓郁很多。入口的右边是柜台，人们可以在那购书结账。店里零散地摆着几只皮椅；椅子旁有小桌子，桌上通常配有一套茶具和一个装满薄荷茶的茶壶。茶水是可被安全饮用的，并有着和杏仁水相似的治愈功效，也是层中绿薄荷与香草气味的来源1。天花板上长满了藤蔓和各种植物，一些情况下是百合花和紫菀。店内洋溢着明亮的自然光，因此所有照明设备都是关闭的状态。
//
