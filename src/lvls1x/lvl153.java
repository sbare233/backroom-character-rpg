//任何想要再次打开入口的尝试被证明是无效的，因其在人们进入书店后会被锁住无法开启。但同时，书店后部一扇通往储藏室的门会被解锁。储藏室内部有着许多箱子、摆满书籍的架子与一台小型的装订机。进入储藏室，关上门，重新开启，你会回到第一次进入书店的位置，但面对的是一个不同装潢的书店。这一新形态被称为欲障期。
//
//关闭欲障期描述
//如前文提到的，打开储藏室的门并离开会把人们带回第一次进入书店的位置，但有一点例外：书店变更为1970年代的样子。椅子上的皮垫现在变成了绳织的材质，且水壶中的绿薄荷茶被替换成木槿茶。喝下茶水某种程度上仍是安全的，但伴有微弱的晕眩和混乱症状。蔓越莓和巧克力的气味弥漫在空气中，天花板上的藤蔓被星星装饰物所替代，而一个收音机将会播放音乐。店外的光线似乎处于永恒的“黄金时光”状态，即最常见在日出后第一小时或日落前一个小时的景象。除这些之外，店里的几支蜡烛也被点燃且无法熄灭。书店的窗沿和柜台上常可以看到摆放着一些紫菀和多花月季。
