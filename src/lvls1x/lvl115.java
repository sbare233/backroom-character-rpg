//Level 115形似一个室内滑雪场。它的走廊蜿蜒曲折，一眼望不到头，冷空气从墙壁两边的通风口之中吹出来，空气的温度由你进入这里前的位置决定。有时它们会喷出杏仁水结成的雪花（不建议在未经过滤时饮用），并可能达到-10摄氏度以下的低温，这让这个层级非常难以定居。大量升降椅占据了本层的很多空间，其中大部分都是可以正常使用的。 无面灵是本层最主要的实体，其在未被惊扰的状态下可视为无害。他们能够被观察到穿着厚实的衣物，并尝试滑雪。大堂中开设着店铺，由无面灵运营，你可以向他们购买食物和杏仁水作为补给。罕见的情况下，可以观测到游荡的窃皮者和猎犬；虽然猎犬似乎并没有表现出敌意，但仍可能会在应激后产生攻击行为。
