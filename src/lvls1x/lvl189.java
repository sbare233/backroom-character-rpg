//危险增加理论：
//根据目前的信息，在此层级经历的前50至60个房间是完全安全的。一个公认的理论是，你在遇到出口前穿过的房间越多，到达出口的可能性就越小。以超过50~60个房间开始，探险者报告称感觉越来越不安全，并注意到各个房间，包括豪华房间在内，开始以各种方式进行着恶化。
