//实体
//在前50到60个房间内，根本没有实体存在。然而，当超过这个数字时，房间中实体存在的概率将会增加。这位考察了200个房间的探险家报告说，笑魇和猎犬的数量越来越多，同样也有非敌对状态的无面灵出现，他们只会站在隔间或镜子前，就像是在模仿厕所里的人类行为，不过我们无法理解它们这么做的目的是什么。
//
//前哨
//本层目前没有已知的前哨存在。然而，本层级却拥有作为大量杏仁水和普通水资源相对安全来源的可能，这点促使M.E.G.定期派出探险队于此搜刮，尽管任务中总伴随着队员的失踪。
//
//马克的卫生间
//根据那位在当时已探索该层级199个房间的探险家回忆，就在他失去希望的时候，他发现了一扇门，与其他门不同，这扇门的把手被拿掉了。当他走进去的时候，他发现了一个叫做“马克”的实体正向他打着招呼。
//
//据说，马克在当时告诉了那位探险家他考虑搬来该层级居住，他似乎正在堆砌垃圾，让他所在的这一房间看起来更像是他以前的居所。接着，探险家殷切地询问马克该层级的出口位于哪里，马克很干脆地打开了一扇隔壁通往Level 4一条走廊的门。马克似乎对探险者的这一询问感到十分疑惑，据探险家说，马克好像完全不理解为什么对方不在想走的时候直接离开。
//
//入口与出口
//入口
//据悉，当有人想使用Level 2的卫生间却又对其卫生状况感到厌恶恶心时，这个人便会在打开下一扇门的那一刻进入该层级。这种方式的更有效版本包括坐在该层级的厕所里，朝它尽可能多地喷洒空气清新剂，让气味侵入隔间，并闭上眼睛想着遇到一个“漂亮的卫生间”。当你睁开眼睛时，你将确定地出现在此层级。
//
//有一种理论认为，如果有人在Level 34中持续行走足够长的时间，就可以进入本层级。然而，这种方法并不可取，因为所有试图通过这种方法进入的探险家均还未返回。这表明，即使他们成功到达本层级，也无法找到该层级的出口。
//
//出口
//如前所述，唯一已知的离开该层级的方法是离开你现在所在的卫生间。以探险者记录为据的计算得出，80%的人会在经过1至12个卫生间后离开。大多数情况下，这种方法会让你回到Level 2中的卫生间。如果这种情况没有发生，那么你每经过一个房间后逃脱的机会都会变小很多。M.E.G.的许多成员仍能从后续的路程中得以返回。
//
//一些人推测，如果一个人通过足够多的房间，这个层级会将他带往Level 34，但此说法有待证明。
