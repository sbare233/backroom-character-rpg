//Level 178是一座环形的町屋，墙壁和地板都是用木头制成的。该层级的一个显著特点是没有实体，在某些区域悬挂着食物、杏仁水和衣服，作为许多人居住或探险者们补货、淋浴和进食的基地。可以使用各种方法进入它，包括在Level 11中找到一幢房子或在Level 7中睡觉。该层级有多个部分：一个中央有温泉的庭院，一个环绕式门廊，厨房，主区域，淋浴间和卫生间，以及卧室。
//
//主区域通常是大多数旅行者最终到达的地方。它是一片宽阔的平地，明显且温暖的竹木香气在该层级弥漫。大多数门都是滑动打开的，便于找路。有时可以在该层级的某个地方听到涓涓细流和笛乐的声音。地板通常铺有地毯或木质表面，在某些区域可以找到露天的工作室，武士刀和武器挂在柜子里，纸1灯笼照亮房间。壁橱里能找到挂起的和服和可穿的木屐。穿过中心区域会到达其他部分。
//
//厨房可以通过玻璃和木头制成的移门进入，这是一个狭小的房间，里面放着厨房电器，如水龙头、冰箱、橱柜和烤箱。在这里可以找到杏仁水和食物，所有这些都可安全食用，在冰箱里会找到准备好的食物和一张食物清单，可能是天界准备的。右边的一扇门通向用餐区，短木桌上方挂着一盏灯笼，下面是可以坐的垫子。另一扇门通向门廊。
