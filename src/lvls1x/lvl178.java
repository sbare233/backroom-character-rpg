//可以从该楼层的多个区域看到178的环绕式门廊，它是一个环绕庭院的正方形，面向中心的温泉。灯笼通常悬挂在弧形屋顶上。庭院和门廊被困在一个时间循环中，表现为清晨，露水覆盖在植物上，从各个角度都能听到轻柔的流水声。门廊下流出一缕通向温泉的小瀑布，使用和浸泡其中都很安全。通过Shoji风格的门可以从庭院区域进入该层的其余部分。有一扇门通向一个装满热毛巾的壁橱，这些毛巾可以用来擦干身体或在温泉中使用。在温泉中睡觉可以通往Level 66或Level 132。
