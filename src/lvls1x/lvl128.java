//描述:
//Level 128是一片平坦的草地，长约60厘米，似乎永远向各个方向延伸，持续的雾层使其无法看到1公里以外的东西。天空似乎永远是暗蓝色，没有明显的太阳；由于没有明显的日/夜周期，时间以BST为准1周期性的风扫过该层，尽管风速从未被报告为高于强风。探险家们注意到，整个层级上弥漫着一股淡淡的臭氧气味，随着风速的增加，气味越来越浓。
//
//该层级表现出一种重力异常，自由落体的物体在接触地面之前会迅速减速，大大减少了因快速下降地进入此层级而造成的伤害；这种重力异常的上限似乎是500公斤，在超过500公斤后它无法触发。
//
//本层最重要的属性是其中出现的结构：房间和柱子。
//
//房间
//房间是一个单一的房间或走廊，由一个看似随机的，悬挂在半空中的一个固定位置出现，海拔高度从25米到70米不等。它们的位置、高度或大小没有明显的模式，每个房间唯一不变的特点是它们都来自于后室的其他层级。每个房间的墙壁从外面看都是透明的，因此可以看到房间里的东西。
//
//这些房间並不是简单地模仿其他层级的房间；它们实际上与相应的层级相连，形成了Level 128的唯一入口和出口。从内部来看，几乎不可能分辨出房间是否连接到Level 128，唯一已知的方法是破坏不同房间的墙壁或地板，直到通过其中一个洞可以看到该层（不过这种方法成功的概率特别低），或者找到探险家之前留下的洞；这些洞是与Level 128的双向连接，也可以由探险家设法从Level 128爬上并突破这些房间。
//
//任何有洞或开口可以进入Level 128的房间都被称为 "破洞"；探险者应谨慎对待可能连接到危险层级的破洞房间，因为这些是实体进入Level 128的唯一已知途径。值得注意的是，从来没有实体表现出有能力突破房间，一些探险者报告说看到实体在没有突破的房间里行走，完全无视其观察者。
//
//与大多数这些房间的源头层不同，它们从未被观察到会自行移动或改变，即使在遭受了一定程度的结构性破坏后也是如此；因此，一旦一个房间被攻破，理论上任何探险家都有可能再次找到这个通道。然而，如果一个房间被损坏到几乎完全摧毁的地步，它可能会从它本来的层级上 "脱离"，切断该通道，导致房间倒在地上。一位在原头层目睹这一事件的探险家声称，房间的门本身就消失了，没有留下房间存在的痕迹。2
//
//支柱
//支柱是巨大的长方形结构，正好是20 x 20米宽和长，似乎无限地延伸到空中，也可能同样地深入到地下。虽然它们显然是以完美的90度角切割的，但其多孔的表面使它们具有非常粗糙的质地。所有的柱子都以完美的网格排列，每根柱子与直接相邻的柱子相距1公里；由于整个层级的雾气，这意味着在任何时候都只能看到一到两根柱子。
//
//支柱的材料是一种不明的多孔石，后来被命名为 "柱状岩"。实验证明，由于柱状石对任何试图划伤、碎裂、融化、腐蚀或以其他方式破坏它的行为都有明显的抵抗力，因此很难研究；更多信息可以在其物品条目中找到。
//
