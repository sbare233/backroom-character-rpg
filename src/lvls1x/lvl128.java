//M.E.G. 侦察队“Trailblazers”由队长马库斯·霍尔特和特工怀亚特·皮尔斯顿和泰勒·琼斯组成。选择这些代理商是基于他们在导航和制图方面的技能，以及他们之前在戶外类型层级导航的经验。
