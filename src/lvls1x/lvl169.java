//检查点 B½位于沙漠中心以北190公里处。这个检查点是一所家具齐全的房子，还有一张可以用于睡觉的床。
//检查点 C 位于沙漠中心以北 230 公里处，是你在此层级中可以找到的最后一个检查点。
