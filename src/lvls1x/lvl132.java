//梅利亚的人口较少，面积较小，都只大过波利斯薇尔。该地位处公国最南端，也因此拥有最温热的气候。梅利亚中的建筑风格多样，胜过其他村庄。此地也是发现 Level 132 时遇到的第一个村庄。当时村庄中仅有几座小小的连排房屋、一座广场，以及广场周边的商铺。但现在，梅利亚的面积已大大扩增，而且其中的每一座建筑都是由居民亲手打造。不少梅利亚的居民都有自己的豪华房屋，或在一条街道上有多处房产，这在此处非常常见。
//
//梅利亚的很多商铺都会贩售建筑工具、材料，以及园林工具，因此居民们对建筑都相当了解。很多人都具备设计师、建筑工、工程师等所需的能力，他们不是专业从事者就是业余爱好者。 梅利亚的房屋材料全部来源于当地商铺，建筑工们会根据自己的喜好选择房屋的样式。各种精心打造的房屋和豪华别墅点缀了梅利亚当地的风景，但有些房屋在结构上不如一般的安全。不少居民在盖房时会尽力造大造高，因为这里不存在像现代社会一样对房屋占地面积和高度的限制。这也使得梅利亚的街道往往蜿蜒细小，两旁布满了各种外形特异的实验性建筑。
//
//虽然梅利亚中也有少数人不参与房屋建造（一般是参与者的亲朋好友），但想要成为梅利亚居民并在其中盖房子的人，都需要证明自己能建造一座至少质量过关，不会伤及他人的房屋。这也导致梅利亚内部关系紧密，相对排外。不与他人一起居住的人必须拥有自己建造房屋的能力才会被允许在其中居住。
//
//梅利亚的居民通常都有其他居民无法建立的收藏：房屋。在梅利亚的各种节日庆典上，居民们会展开建筑比赛，或成单，或组队，在郊区附近修筑新房。由于类似的事件举办得很频繁，梅利亚内有很多房屋都处于未完工或废弃状态，居民们对待房屋的态度就与对待其他商品一样。最近，梅利亚中逐渐流行起可移动、可运输的房屋，因为这些建筑可以在跨村庆典上交换到别的优质商品。
//
//梅利亚崇尚简约烹饪，不同于别处，这里的食物往往清淡普通。居民们总是忙于修筑新房，因此更喜欢在工作时也能吃的简餐、零食。村庄内饭店稀少，但熟食店和烘焙店很常见，用于给居民提供便于携带的糕点、零食。不过，从某种程度来说，梅利亚的菜品十分古雅美味。
