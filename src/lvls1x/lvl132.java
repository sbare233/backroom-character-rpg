//莫里贡特公国
//
//
//介绍
//Level 132，也称“莫里贡特公国”。该层级空间十分广袤，且有多个人口聚居地。尽管被称作公国，莫里贡特公爵自身却并非人类，因此，Level 132 不由任何人类领导的组织监管。其中的村庄多会自行处理内部事务，不受后室中的众多组织干涉（包括 M.E.G.）。Level 132 中存在5个村庄，它们人文习俗不同，地理环境相异；但整体而言，该层级的自然环境相对温和。相比于后室中的其他层级，此处环境多样、远离冲突、适宜居住，数名流浪者已经在此定居。因此，M.E.G. 决定将该层级的生存难度标记为0级。
//
//Level 132 中的人口合约800名，居住在其中的各个村庄中。公国面积约50平方英里（130平方千米），其外围只见悬崖不断向下延伸，直至白色的虚空；并未发现其外存在其他土地。该层级的异常特性之一是其中存在大量集市、商铺，可为居民提供充足的食物；因此，虽然可耕种的土地众多，居民们只会根据个人喜好选择是否在田园、农场中居住。
//
//Level 132 中的商店并不像中世纪中期的村庄商铺，而像是由现实中各个时空的商店拼接而成。当今地球上已不复存在的商店在该层级中复现，这些商店会不定期补货，兜售古今千年间存在过的各种商品，品牌多样，款式齐全，希腊古瓮甚至能与智能手机并排展示，不必吃惊。
//
//该层级中存在大量古玩珍奇、也有很多科技产品。因此，在这里，购物是人们日常生活的中心，也是广为盛行的消遣，各个村庄中心的店铺都装潢得光彩夺目。所有村庄的经济都完全由贸易商谈支撑，因为该层级中的各个商品都有其独特价值。公国中物品的货币价值不复存在，因为无论商品贵贱，商铺都会以相同的频率补货，且不需要昂贵的采购过程。该层级中的商品价值完全由人们的个人收藏所需决定。比如，倘若某人需要一颗苹果才能完成自己的水果收藏，而另一人需要一枚钻戒才能完成自己的珠宝收藏，那么此时，一颗苹果的价值就与一枚钻戒相等。
//
//贸易是各个村庄中最重要的活动，因此该层级的人们内部关系极为紧密，十分排斥外来团体。村庄的街头巷尾，尤其是购物中心和活动广场常常被清理得美观整洁，一尘不染。鹅卵石铺就的路上还会点缀一些桌椅长凳、花坛草丛，这些物件并不会妨碍交通，因为人们大都步行前往各个地点。Level 132 中最常见的商店种类是饭店，各个村庄中都能看到别具特色的烹饪场景和各式各样的菜肴。节日庆典在这里也非常普遍，每个村庄都有自己的独特节日。这些节庆的内容往往有关特定主题的商品——如鲁米利亚的手机节，波利斯薇尔的单车庆典。节庆期间，居民们均会展示自己的相关收藏。
//
//这里的人们非常喜欢收集特定品类的物件。不论某人的收藏是在数量还是种类上高人一头，他都会受到各方尊重。居民们的收藏一般是通过谨慎的议价、交易积累而来。有时，这里会举办跨村庆典，各个村庄都会展示出自己的珍奇货品。居民们最常以这种方式交易获得其他村庄的商品，借此丰富自己的收藏。在各自的村庄中收藏最繁多齐全的五个人会比较彼此的成果，其中最优者会去拜访莫里贡特公爵，并和他比拼彼此的藏品。话虽如此，至今没有任何一人收集的藏品足以胜过公爵，他是 Level 132 中最积极的收藏者。
