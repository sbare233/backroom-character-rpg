//
//之所以不少居民在从罗希欧的学校毕业后会选择搬迁，是因为学校内为各年龄段的居民配备了不同课程，培养他们对其他村庄专长有关内容的兴趣。而选择留在此地的人们通常会成为教师、或去书店以及其他商店工作，而且不少商店都会供应学校所需物资。不同学校间的竞争十分激烈，人们也由此分裂为不同的团体。不同学校的学生间常有极大的分歧，因为跨校交流时泄露的信息会被用于反对、诋毁彼此的学校。
//
//还有件事值得去往罗希欧的人们留意，那就是吃饭时要付两次钱：第一次的费用用于得知食物的价格、名称、原料，第二次的费用则用于购买食物，这与其他提供菜单式服务的商铺一致；但在罗希欧，不管去什么饭店，都要用这样的方式购买食物。
//
//虽然罗希欧的人口主要是成年人和青少年，但也有部分老年人。罗希欧十分开放，欢迎各个年龄的流浪者来访、居住，也不会像其他村庄一样为他们设置特定的考验，而罗希欧的学校也会竞相争取流浪者们的加入，不论年龄大小，均可入学。在某些领域有专长的流浪者在这里会备受尊重；而且，如果他没在罗希欧的学校中读过书，或是来自其他村庄，则会更加受人青睐。
