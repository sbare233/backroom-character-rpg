//层级陷阱：
//层级陷阱是该层级在“警戒状态”期间对内部的振动做出的敌对反应。这些陷阱的设计意图是杀死层级内的所有人，但只要了解下文中说明的内容，就有机会逃脱。层级陷阱被触发时，可能出现的情况包括：环境变化、层级结构转变、有害气体发生、敌对实体出现、精神危害及生理影响。
//洪水陷阱
//flooded-hallway.jpeg
//拍照者死亡前拍摄的“事件＃1”。
//
//洪水陷阱被触发时，可能会出现两种情况，分别称为事件＃1、＃2。事件＃1发生时，层级内的走廊中会产生规模无比庞大的洪水，无论走廊的面积有多大，都会被完全淹没。据记录，水流的最高时速可达70英里（约113公里），其产生的巨浪足以将周围的一切物体卷入其中并摧毁殆尽。事件＃2发生时，层级内会出现与天花板材质相同的金属墙体，这些墙体会按照约10英尺（约3米）的距离封锁、分离层级内的空间。此时产生的洪水温度仅有10˚F（约-12˚C），但却不会结冰。这些洪水会不断上升直至到达天花板，水位会如此保持7分钟。7分钟后，金属墙会坍塌，但此时洪水产生的冲击会造成更大的噪音，从而诱发层级产生更多陷阱。不过，当此事件结束时，多数受害者已经死于低温或溺水。为应对事件＃1，可以利用层级内的家具和拐角处遮挡自己，以防被水流冲走。而如若发生了事件＃2，只能通过使用合适的潜水设备规避危险。也正因如此，事件＃2的致死率极高。
//
//挤压陷阱
//Squeeze
//挤压陷阱中拍摄的照片。
//
//当此陷阱被触发时，层级内的墙壁、地面和天花板都会迅速地向附近的流浪者和实体靠近，如果不能及时逃脱，就会被这些结构挤压致死。挤压发生时，墙面只要紧贴住彼此便不会再分开，这会使原有的通路被封死。因此，在逃离此陷阱时不应去往死路，以免因环境被封闭而无法离开。应对此陷阱的唯一办法便是在被挤压前逃离陷阱所在区域。
//
//旋转陷阱
//旋转陷阱被触发时，层级内会有小块的地面开始扭曲、转向，从而导致大部分区域无法抵达或难以通行。陷阱持续时，很多地面会断开与层级的连接并以最高55英里/小时（约89千米/小时）的速度旋转。每片旋转的区域上都有一个“固定点”，换言之，即一个在旋转时位置不变的轴心。如果脚下的地面开始旋转，请迅速找到这个点，因为它在陷阱持续时是最稳定的区域。旋转陷阱一旦启动，受到其危害的流浪者就会被猛烈地甩向墙壁或抛入虚空区域。由于其转速之高，所有曾被困于旋转区域内的流浪者全都不幸遇难了。不过，由于这类陷阱较少出现，且可通过合理规避旋转区域从而逃离或幸存，所以该陷阱的致死率并不高。
//
//心灵操控陷阱及身体/精神危害
//心灵操控陷阱被触发时，受害者首先会遭受一项生理损害或精神异常。这些危害事件包括但不限于：视力模糊、双目失明、器官衰竭、肌肉萎缩、癫痫发作、记忆消失、出现幻觉等等。同时，约半径0.5英里（约0.8千米）的范围内会出现各种诡异的物理现象。有流浪者报告称，陷阱触发时Level 148的地板会塌陷，暴露出下方的虚空；层级内的各种物品，乃至墙面和天花板都会飞起并急速冲向引发陷阱的实体或流浪者。此陷阱启动后，保护自己的最好方法就是用周围的物体遮挡自己，床垫一类的东西最为合适。即使没有类似的物品，也应尽力利用所有可阻挡伤害的物件，躲在一台打印机后也好过毫无防备。应对该陷阱的另一种方法则是远离可能飞来的大型物件，或进入宽阔的空间以便规避伤害。
//
//实体潮陷阱
//实体潮出现时，务必立即逃跑。该陷阱是目前记录中最危险的“警戒状态”事件。陷阱启动后，通常会有至多25个实体以未知方式出现在受害者视野外，它们会立刻杀死周围的一切其他生物。陷阱中产生的实体的敌意和智能都会高于平常状态。记录中，这些实体种类繁多，但最常出现的有：肢团、扼杀者、幼年无面灵以及咆哮者。实体潮出现时，一定不能陷入恐慌。想要逃离，最好先脱下鞋子，这样可以减小自己行动时的声音，降低被发现的几率；其次，还应抛下所有可能减缓移动速度的物品。完成这两件事后，应竭力跑向最容易到达的出口；如果逃跑途中遭遇实体，应当更换路线或尽力躲避实体，直到抵达出口。
//
//火灾陷阱
//该陷阱引起的事件正如其名。陷阱被触发时，层级内会出现一小撮火苗，虽然一开始很小，但其引起的火灾最终却可能波及层级的大部分区域。火灾陷阱诱发的火焰呈现黑色和蓝色，温度高达2192°F（1200°C），无法被熄灭。人的行进速度可以超越火焰蔓延速度，但因为火情扩散时的覆盖范围极大，所以很难从中脱出。流浪者们一般将这种火焰称为“无尽之火”（Infinifire）。如果附近看不到火焰，听不到燃烧的声音，闻不到物体燃烧的气味，说明你大致处于一个安全的位置。但即使如此，仍应继续远离火源。陷阱中产生的火焰似乎不会破坏该层级的结构，也不会留下燃烧的痕迹。但如果人被这种火焰烧伤，会感到极度疼痛，还会留下无法治愈的伤疤。如果火焰伤害到的身体面积过大，或已经穿透了流浪者的某个部位，则必须进行截肢手术，因为烧灼后的灰烬会诱发伤口产生一种致命的感染。如果不及时处理，感染就会从伤口侵入人的身体并毁坏人的重要脏器。
//
