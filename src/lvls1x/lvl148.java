//Level 148又被称为“活体层级”，这是因为该层级本身就是一个实体。当它感受到层级内部有物体运动时，就会开始改变自己的环境。即使是行走引起的微小声音也会造成该层级的敌意。研究表明，该层级可以与内部的流浪者交谈，不过这种现象很少发生。这种能力的发现使研究人员认为该层级很可能有活跃的智能，且对人的敌意严重，会有意操纵、摆布其内的流浪者。它能够在自己的墙上写出文字，也可以直接和流浪者对话。不少人形容它的声音低沉沙哑，有时会给人伪造虚假的安全感，有时也会逼人陷入绝望，但这都是为了更方便地杀死猎物。
//
//目前，已记录到该层级内有两种状态。第一种是相对安全的“一般状态”；第二种则是危险的“警戒状态”。在“一般状态”下，该层级的内部结构会随机排布，外形类似其他层级的房间、走廊。多位流浪者报告称在其中看到了巨大的开放式房间、各式各样的楼梯、甚至还有虚空。很多房间和走廊内都有貌似取之不尽的随机物品，其中最有用的东西包括：袋装薯片、垃圾食品、睡袋、例如弹簧刀的自卫武器、可以使用的手机、各种衣物等等。这些房间和走廊内还会出现各种家具，比如三角钢琴、双层床、折叠椅、纸板箱等等，每样家具都与其所在的房间风格相符。
//
//在“警戒状态”期间，流浪者一小时内可能就会遭遇2-4个实体；但在“一般状态”下，实体出现的频率会变低，所以在这时穿越该层级是比较理想的。不知为何，驱笑剂等物品不会对该层级的各种实体产生任何影响。在Level 148的“警戒状态”期间，层级内的实体会“苏醒”并变得极其敌对、恶意。此时应尽可能待在不容易被发现的封闭小空间内；此外，还应尽量减少移动，因为流浪者产生的振动可能会引起Level 148的各种反应（这些反应会在下文列出）。在“警戒状态”下，该层级内的照明设备和流浪者携带的移动光源都会熄灭；不过有时它们也只会变暗。随着原有光源的消失或变暗，整个层级会发出微弱的红光。此外，墙纸也会在未受外部破坏的情况下开始剥落。
//
//“一般状态”与“警戒状态”交替一次的时长并不固定。记录中，最短只需三小时，最长则有四天。食物和各种物品、工具在整个层级中相当常见，还有配备基本家居用品的房间。有人推测这些物品是其他流浪者留下的，但目前尚未证实这一假设的真伪。但总之，除非携带了适当的装备，否则不建议任何人进入该层级。
//
//活体层级
//如前文所述，该层级本身就是一个实体，能够进食、睡眠、狩猎，能感到痛苦，会和人交流。“警戒状态”就是该实体“清醒”的时候，这一状态下出现的所有层级陷阱都是由该实体制造的。严格来说，每个走廊、房间都是活体层级的一部分，但在“一般状态”期间，它们不受该实体控制。这个实体最奇怪的地方在于它的交流方式。即使在“睡眠”期间，它也能在墙上写字或用人类语言与流浪者们交谈。委派M.E.G.双语特工前去多次测试后，我们发现每个人听到的声音都是他们的母语。在一次测试中，2名母语不同的特工（一名是西班牙语，另一名是俄语）被派去与该实体交流，测试结果令人震惊，两名特工同时听到了相同的信息，但这些信息在他们耳中却分别以他们各自的母语呈现。进一步的测试又表明，文盲、无法使用语言者、聋哑人、盲人都会经历不同的奇特体验：不识字的人不会看到墙上出现文字，只会听到声音；聋哑人会遇到相反的情况；无法使用语言者不会收到层级的信息；盲人的体验则更为古怪，他们会感觉有6个手指接触他们的身体，这些手指会按照盲文轻敲流浪者，而且就算流浪者不懂盲文，这种现象也会发生。其他相关测试尚未进行。
//
//该层级会借助模仿者呈现虚假的出口，如果流浪者不清楚这一情况，会很容易因此受伤。这些假出口的外观类似于门，且上方标有“EXIT”的字样。流浪者进入其中后，门会关闭，而模仿者则会随即吞噬流浪者。可以与层级进行完整的对话，但这些对话通常很短暂。对人敌意方面，该层级会在感受到任何微小的振动时做出敌对反应。但是，据推测，层级本身没有视力，因为它布下的陷阱有时会触发其他陷阱，此外它发起的进攻有时也不会命中振动产生的确切位置。由于该层级的行为表现类似生物，所以研究人员推测可能有途径将其杀死。当破坏该层级的墙壁时，其内部会流出一些液体。经确认，这些液体是类似血液的有机组织。该层级在休眠时如果受到攻击，则会立即苏醒并进入“警戒状态”。由此引起的“警戒状态”持续时间通常会比平时短，但并不建议有意去触发该现象。经证实，活体层级会呼吸，也会排出废物，这些废物表现为强酸性的血肉和骨骼。层级内的墙壁极厚，但碍于危险的环境，并未组织研究Level 148“皮肤”下方的结构，因此无从得知更多该层级的生物学特征。
