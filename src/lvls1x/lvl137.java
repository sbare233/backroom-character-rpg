//她俯身，把触角顶在墙上，挎包放在一边。休息片刻后，她把手按在满是粉末的地板上，把自己往上撑，直到站起来。还有其他可用的，她只能继续工作。
//她把挎包举到桌案上方，慢慢地打开，掏出她左边展示柜的钥匙，盯着它看了一会儿，然后又埋头观察她的新材料。
