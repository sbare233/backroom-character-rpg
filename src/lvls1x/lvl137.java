//整个区域覆满了某种粉末，最初被认为是灰尘，但之后发现是褪下的飞蛾鳞片。多个探险者都声称自己听到了窸窣的响动、风铃声、和扇动的声音，但并未发现其来源。
//
//Level 137的首个房间内，有一张桃花心木桌，四扇门，靠墙有一个标本展示柜，里面有各种蛾类的标本，用翅膀下方的两个螺栓固定在柜子的后部。
//
//这些展示柜填的满满当当，被门闩锁住。
//
//房间正中的办公桌总体上干净，上面摆着各种海报、文件和笔记，字迹难以辨认。而且当这些材料被拿走研究后，办公桌上面也变得满是粉尘。
//
//从这里开始，后面的每个房间都是一样的。每面都有四扇门，每面都有展示柜。这些展柜大多没有上锁，有些的里面空空如也，但其中部分展柜里有死亡的蛾类标本。据称某个房间里有个展示柜被打碎了，但意义不明。
//
//对蛾类标本的研究显示，其腹部下方可见一条裂口，里面完全没有任何器官或是组织物。触摸这些标本的内部会使其短暂地摆动它们的翅膀和附肢，但没有找到令它们长时间复活的方法。M.E.G.所属的特工人员已带走其中一个标本，将其带回了阿尔法基地。
//
//但她的母亲很想她。
//她滑坐在膝盖上，折起的翅膀不自觉地压在身后的地板上。她缓慢地把双手遮到脸上，把手折叠在本应是嘴部的光滑位置周围——并无声地啜泣。
//她花费了多年时间去研究完善她的手法。即使是创造像待在自己箱子里的这些小生物，也是一项艰难的工作。而哪怕是失去其中的一个，也是毁灭性的损失。
