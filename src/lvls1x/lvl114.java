//压迫霉菌
//最近研究表明，Level 114有一种独特的霉菌，它生长在房间墙壁和天花板上的小块区域。这种霉菌似乎具有诱发焦虑的特性。分析表明，真菌的赘生物中含有一种目前尚未确定的蛋白质，这种蛋白质导致了霉菌强烈的“沉闷”气味。研究发现，这种化合物可以刺激人类大脑中管理压力反应的部分，增加肾上腺素水平，引发焦虑。
//
//当有人待在房间内时，压迫霉菌会迅速增长，导致流浪者的压力水平随着时间的推移逐渐升高。已知在一个人进入大约8小时后，该房间内的霉菌会超过阈值，对人体呼吸和神经系统健康构成威胁。因此建议流浪者不要在Level 114逗留太久。
//
//笑魇
//此层级的床底藏有很多笑魇，在黑暗的地方它们会更加密集地出现，比如橱柜或抽屉的内部。当灯亮着的时候，它们会待在黑暗的空间里，不会对流浪者造成威胁，但是关掉房间里的灯会使它们显现并攻击。
//
//窗户
//房间里的窗户偶尔会被实体替换掉。4童年时代的卧室有窗户的流浪者应当保持警惕。如果流浪者童年时代的卧室没有窗户，他们就没有什么可担心的。
