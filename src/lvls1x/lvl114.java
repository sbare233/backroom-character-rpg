//描述
//Level 114在空间上具有不同步性——除去一些个例，每个流浪者所见的该层级都是独特的，并无法察觉到其他流浪者。这一层级的外形就像每个流浪者在青春期前（约9-12岁）的童年时代的卧室。
//
//暂不知道当流浪者的童年时代有不止一间卧室时，Level 114的变化如何。如果一个流浪者的童年时代有不止一间卧室，那么Level 114将会呈现出类似于Level 38的混乱空间结构，通常被流浪者描述为“许多房间无规律的融合在一起”。此外，如果两个或以上曾共用同一个房间的流浪者同时进入Level 114，他们将出现在同一空间。除非满足这个条件，流浪者无法观察到同时存在于层级内的其他流浪者。
//
//当流浪者进入Level 114，他们通常会发现自己站在门道中，面向房间。他们身后的门微微半开，而门外除了一片漆黑的虚空之外别无他物。
//
//除去虚空，房间内的床也是在所有Level 114的表现中唯一不变的。在所有Level 114的表现中，流浪者童年时代的床将被完全相同的一张单人床替代。1这张床的床垫被紧密地安装在一个木制床框上，床单是蓝色的，底面绘有花卉图案。床单中有大量的床虱，躺在床上的流浪者经常会发现自己暴露的皮肤被叮咬并出现皮疹。
//
//此外，Level 114内的大多数电子设备以及电器无法使用。这种现象是非正常的——当检查时，这些设备的元件从未出现过可识别的错误。幸运的是，被流浪者带到Level 114内的设备不受这种现象的影响，可以正常工作。尽管如此，能用的电池或其他电源仍然无法为此层级内被影响的设备供电。
//
//此层级内唯一能正常工作的电器似乎是默认打开的电灯开关。尽管如此，房间内的灯光也不会像流浪者童年时代卧室中那样表现。灯光无一例外地呈现出黯淡，单调的白色，仅仅能够照亮周围的环境。建议流浪者无论在任何情况下都不要关掉房间里的灯，这会使隐藏在黑暗空间的笑魇出现并攻击流浪者。
//
//虚空
//虚空并不是该层级的“屏障”，而是该层级的一部分——通过门道可以随意出入虚空，并且包含可呼吸的空气。该空间中的重力比较低，产生了类似让流浪者可以漂浮于水中的效果。
//
//尽管如此，流浪者的运动无法抵消掉空间的引力。换句话说，在虚空中向上游泳是不可能的，而掉入虚空的流浪者们将永远沉溺于黑暗。从来没有尸体从虚空中被追回，目前推测那些坠入虚空的人最终会死于脱水。
//
//不像其他类似的空间，这里并没有潜伏在内的骇人恶兽，但你也绝不能在此放松神经。从房间内凝视无边无际的虚无，如同于海湾眺望张着血盆大口的汪洋，那口中充斥着如同墨水般黑暗的虚空，我敢肯定绝对没有人能深陷其中后安然无恙地返回。虽然暂时没有观察到什么生物从中显现并吞噬那些战栗的过路人，但也许确有什么恐怖的怪兽潜伏在这之中。
//
//仔细端详这个萦绕在心头不去的鸿沟使人想起死神那无情的咽喉。意识到这样的命运正等待着我们每一个人的灵魂实在是令人战栗——甚至是在这里，这个失落的世界——在我们平凡的旅途之终。那门后的房间也没有任何的舒适感，单调乏味，丝毫没有生灵的痕迹，仿佛虚空中那逐步逼近的恐怖存在已经爬了进来，遮蔽了空间，扼住了生灵的咽喉，直到那可怜的灵魂在这房间中成为死去的一个挥之不去的阴影，这就是大多数人的命运，对徒劳的生活感到绝望却无能为力，只能眼睁睁的被黑暗的深渊吞噬。
