//Tramvajáci，亦被称为“电车帮”，是Level 172中一个移动的团体，采用改造过的电车作为他们的基地。他们因驾车穿行该层并绘制地图，且为其他流浪者提供补给、庇护所的帮助而为人熟知。
//
//该团体拥有六名成员。
//他们很愿意与其他团体交易。
