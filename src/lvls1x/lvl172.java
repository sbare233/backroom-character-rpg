//描述：
//Level 172类似于一座建造于2000年前后的现代工业/办公区域，包括许多大厅、仓储空间和偶见的办公楼。该层的湿度很高，温度徘徊在10 ℃（50 ℉）左右。会使流浪者惊讶的是，该层拥有日夜循环，且如果准备不充分，可能会造成伤害。该层可被划分为三大区块：大厅与仓库（在功能上相关）、办公楼、街道。
//
//大厅与仓库：
//大厅与仓库是Level 172中两种最常见的建筑形式。它们通常在结构上彼此相邻，并有着正门与侧门二者作为入口。内部的电力可正常运作（尽管不是所有情况下），但房间里找到的设备/物品偶尔会是散落在地上的。仓库内常常储满了有用的补给，甚至有食物。叉车是其中一件可在仓库中找到的实用的物品，有时也可以在大厅里找到。它们可以作为一种更加迅速的交通方式且能运载大量的补给/材料。
//注意：若大厅与仓库内部的灯光熄灭或正闪烁，请尽量远离。
//
//办公楼：
//在Level 172中，办公楼属于罕见的建筑。它们在规模上比大厅与仓库更加小。与前者不相似的是，大多数的办公室有着许多窗户，大部分窗口是暗的。建议流浪者保持谨慎并与窗户保持一定距离，因许多办公楼已被Entity 2所侵染。当位于楼内时，人们能在办公室里找到仍可使用的电脑与放置在走廊四周的、装满了杏仁水的冷却器。
//
//街道：
//街道是Level 172中位于大厅、仓库和办公楼之间的户外区域。街道通常由大小不一的沥青路组成，也包括人行道、路灯以及不时能遇到的电车轨道。路上的交通并不繁忙，但流浪者或许会遭遇驶过的巴士、无轨电车、带盖/敞口卡车或有轨电车（通常每小时一到两班）。这些车辆均无驾驶者并貌似自主驶动。
//注意：当行驶着的车辆接近时，建议流浪者们请勿在街上穿行。这些车辆速度极快且不会因有行人而停下。被行驶的车辆撞击或将导致重伤甚至死亡。
//
//街道上值得注意的地点:
//
//停车场——可能停放着车辆。这些车辆无法使用，但它们能够在夜间提供庇护所且可能内含一些重要补给。
//车站——在路边有时能发现巴士/电车站。概率地，在等待一段时间后，巴士或电车将会出现并停靠。有可能登上它们并被载往其他层级。（有时等待车辆出现并停靠将需要24小时以上，因大多数驶过的车辆只是无视站台）
//停靠的巴士——可在路边发现停靠的巴士（有时甚至是电车），或发现它们在车站前等候。这些车辆不会自主驶动且有时可被手动操纵，提供了夜间的庇护所和在层中的快速交通方式。
