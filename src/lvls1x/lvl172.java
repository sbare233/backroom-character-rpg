//白昼通常是活跃的。其被认为是该层的默认状态。白昼期间，可在层中的街道上安全地漫游，遭遇实体攻击的威胁将会是最小的。白昼的持续时间不固定，但据测定在20-42小时之间。
//
//黑色黎明：
//由层中的流浪者命名，黑色黎明是层中的光线开始迅速消失的一段短暂时间。是夜晚即将到来之前会发生的事件。在此期间，流浪者们应立即在任何建筑内甚至是街道上的车辆中找到庇护之处。黑色黎明将会持续2-15分钟。
//
//夜晚：
//在夜晚事件中，环境光线亮度将急剧下降至接近漆黑一片。在夜晚外出漫游是极其危险的。进入黑暗中会导致心理压力增强、迷失方向，有时也会产生幻觉致使受害者更加深入危险的境地。受害者们或将遭受来自未知源头的，瞬间的、剧烈的撕扯。撕扯的力度会随着受害者进入黑暗的时长而增强。暴露在黑暗中超过数分钟以上即可导致受害者被撕裂或流血至死。夜晚的持续时间各不相同，但已被测定在1-16小时之间。
//
//实体：
//笑魇、猎犬和窃皮者偶见于大厅和仓库中（若建筑内的灯光熄灭它们的数量将更多）。亦发现有大量的尸鼠，它们的行为取决于灯光是否亮起（当灯光熄灭时它们将变得更具侵略性）。有时可在仓库内的板条箱中发现大量的雄性死亡飞蛾。
//
//经常可在办公楼内目击到窗户，亦有少数报告称目击到笑魇。
//
//街道上通常是实体绝迹的，但在夜晚结束后的短时间内例外。在该时间段，出现了猎犬 与窃皮者 的目击报告。
//
//基地、前哨站和社区：
