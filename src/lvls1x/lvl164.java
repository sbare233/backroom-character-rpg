//描述：
//Level 164是一个无穷无尽的博物馆，它的光线通常很昏暗。它显露出19世纪50年代至20世纪10年代的建筑风格，但具体的建筑风格取决于博物馆的不同区域。该层级由两种不同的空间构成: 走廊 和 展览厅。
//
//该层级向来杂乱无章。陈列柜和木箱中标有“易碎”或者是“珍贵”的文物被胡乱地散落在大厅和展览厅。即便如此，该层级也始终保持整洁，这归功于该层级唯一已知的物理实体无面灵。几乎总能看到成年的无面灵穿着碧绿的制服进行打扫，例如对物品进行除尘，或把箱子搬运到我们不得而知的地方。幼年无面灵也会出现，尽管他们的所作所为与在Level 11影响下的幼年无面灵一样，只会对那些毫无戒备的流浪者开些温和的恶作剧。
//
//该层级大部分区域都会播放音乐，并且这些音乐被形容为“抚慰人心的” 和 “超凡脱俗的”。但所有尝试录制这些音乐的行为均以失败告终。
