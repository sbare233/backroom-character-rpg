//走廊
//走廊长而宽，绵延数公里，目前记载最长的不中断走廊延伸超过5公里。每隔一段时间，一条走廊就会与另一条走廊相交，相交线总是90度。因此有人认为这些走廊已经组装成了网格状结构。
//
//这些走廊只含有寥寥无几的老旧装饰品和前面提到过的杂物。走廊的建筑构架很华丽。要么有高悬的吊顶和错综复杂的窗户，要么设计更封闭，但有更好的建筑雕塑品。这点与展览厅形成了鲜明的对比，展览厅只有十分简略的布局。
//
//gallery2.jpg
//另一张关于展览厅的图片。
