//展览厅
//展览厅是摆满了各种各样物品的大房间，以至于无法很好的浏览它的全貌。这些展览厅都拥有很大的入口，入口为木门，木门上都有一个牌子，牌子上有一个标签。这个标签决定了展览厅内展品的主题，例如，M.E.G.的特工发现了一个标签为‘通信’的展览厅，其展品内包含电话，电话线和断开连接的计算机服务器。这些标签几乎都与人类有关系，像是‘战争’和‘厨房用品’。这些展厅中有木制的基座，基座上有用英语刻写的段落，这些段落以第一人称描写，据称，这些段落是在‘胡言乱语’和‘自省’。目前不知道这些段落是由一个未知实体写的还是该层级自己产生的。M.E.G. 将这个假想的实体称作馆长。
//
//任何试图触碰展览厅中展品的行为都会对触碰者造成锐器伤，触碰者身体与展品触碰的部位会出现剧烈疼痛。已知唯一消除疼痛的方法是触碰者从被触碰物的位置往后退，退到大约一米远的位置，这会立刻止痛。
//
