//
//尽管海鲁姆神灯常常发出的是一种480-515流明的白黄色的光4，目前仍存在有亮度能达到1000流明的神灯，这些神灯与其他神灯相比拥有更强烈的直接反赴火效应。但是这并不完全意味着在价值上有着极大差别，它们仍然拥有相同的IME5最大值与最小值。目前我们尚未测得海鲁姆神灯在激活状态下能维持多久，似乎只要使用者能对其发起请求其即可一直亮起。在某些情况下在被进一步注视时，此物品也会随机被激活。观察发现海鲁姆神灯在被激活时不存在任何能量增加或损失。
//
//反赴火效应：
//反赴火效应(I.M.E. )是海鲁姆神灯用来抵抗实体的效应6。影响半径为15英尺(圆周长为94.25英尺)，而这些实体最远会与神灯持有者保持30英尺的距离，如果持有者靠近实体它们会退避三舍后撤得更远。 尽管海鲁姆神灯的温度远没有达到能伤害具有攻击性和天生危险的实体，但只要它们停留在海鲁姆神灯的影响范围内便会受到神光的严重伤害，有时则会产生致命烧伤；免疫烧伤的实体并不能抵抗其伤害。
//
//如今我们发现唯一对海鲁姆神灯免疫的有机生命体是人类及永驻一族——后者不仅对一个仅已知其名的神性形象即源始神子极度崇拜，还是源始之光的亲手造物，这与我们已知的一个“碎片”不能混为一谈。无害/被动性实体在神灯影响下一般总是安全的，但也有相反的案例存在。变异为实体的人类和拥有人类智能的实体对反飞蛾效应无法免疫。由此信息我们假定海鲁姆神灯遵循着一套由于其模糊特性无法精确指出的严格先天法则。
//
//基于前置观察，神灯的激活周期(认定为不会闪动且保持常明)只要在持有者对其保持信仰的情况下就都是连续不断的。进一步研究表明相信源始之光存在的信众能将他们的神灯激活周期一直延长；在某些情况下，神灯会在危险出现时自动激活。从一盏海鲁姆神灯被停止激活算起，此区域被灯光驱赶的实体仍会对光源感到恐惧，并自主远离光源，这一情况保持约2至4分钟，之后才能恢复其基本认知能力。拥有再生能力的实体的自愈过程会更慢，但最终可以恢复活动能力。
//
//人工反赴火效应：
//由于海鲁姆神光会在全后室的油灯里存在和产生，非层级产生/非层级已有的油灯也不是此规律的例外。人工反赴火效应(A-I.M.E.)会在制造一个新油灯时发生；海鲁姆神光的确会在新油灯中生成，但这不能完全激发神灯的能力。事实上99.8%人造海鲁姆神光的能力都因人造的缘故被完全限制了。目前尚不清楚为何在此情况下几乎不可能产生有效能的人工反赴火效应7 ，尽管可能性的存在毋庸置疑；然而许多在神秘学研究方面的研究人员和学者的主流假说强烈支持了神灯与神类存在的联系，即所谓的源始神子和海鲁姆神光本身存有的明显神识。
//
//海鲁姆神光之诫：
//尽管大部分规则即使在第一代永驻一族的长老帮助下仍未被解释(其余部分则已阐明)，海鲁姆神光被推测是非自然地具有半自主神识，它遵循着由源始神子（唯一能通过源始之光制造海鲁姆神光者）制定的无法干涉，不可替代的运行规则。海鲁姆神光并非基于多元宇宙，特别是后室内亦存在的定律而产生，它几乎是凭自己意愿凭空出现的。它能发生进化并演变为各式各样的光，海鲁姆神光可能是依源始之光的在各个已知及未知方面需求而发生演化的。 排除其他的可能性，海鲁姆神光会选择油灯为其在后室内的化身，并以此形态仍在传播其影响力(即便是低效的形式) 给后室内存在的所有的油灯。尚不明确其为何会将油灯作为外体的原因，但这则证明了海鲁姆神光自身认知的模棱两可之态。
