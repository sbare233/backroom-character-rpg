//前言：
//为了进一步了解海鲁姆神光，我们首先得知道海鲁姆神光的实质是什么：海鲁姆神光是源始之光破碎后掉落的碎片；是包容所有产生灯火的物质的起源，海鲁姆神光亦为灯具的源头。海鲁姆神光可以以任一自然发光体的形式呈现，如星体、烛焰，广而言之即非人造的或已存在于此宇宙的自然法则者，乃至超越常规空间者，比如在后室内发现的发光物体等皆可为其载体。海鲁姆神光在此条件下仅仅会在后室中以油灯的外观呈现。如果一个人试图通过人工制造一个油灯以获取海鲁姆神光并产生其光焰的话，其与原始形式的海鲁姆神光的效能将有天壤之别1。这一现象证明了海鲁姆神光总是能在不同特定物体中存在，并诞生于多元宇宙中。
