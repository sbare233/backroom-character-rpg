//发现
//有识血肉与人类建立社交关系1~2年后，一部分个体逐渐学习更多文化，开始尝试在外骨骼上添加装饰，后来添加此类装饰的有识血肉越来越多，于是学者氏族向伟大母亲上报此事。
//
//后伟大母亲主导开始研发“具有有机特色的服饰”，并于消息放出七年后成功研发出可量产使用叶绿素的净菜丝刷，后来经过一系列工艺和生长资源优化最终形成现有净菜丝刷，在迭代过程中进行光合作用的化学物质也转为视黄酫。
//
//获取
//净菜丝刷确实可以开花并产生种子进行繁衍，但目前认为其繁衍过程需要像菟丝子一样寄生于植物上，但寄主要属于某种水生植物。过程繁琐复杂，即使成功培育成本也远高于直接从有识血肉处进行购买，因此目前没有组织尝试对净菜丝刷进行培育，确有需要的探索者可以直接前往有识血肉贸易巢购买均码普通图案版，或是花费1~2个月时间等待专属的定做特殊尺码及图案版。
