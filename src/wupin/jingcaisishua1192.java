//物品编号：Object C-192
//净菜丝刷为有识血肉所制造有机构成体之一，用于制造覆盖有机建筑表层的伪装网或作为有识血肉个体的衣物寄生于身表，目前该物品也向流浪者出售，但穿戴过程较困难。
//描述
//净菜丝刷是一种经过大量改造的合成生物，样本提取基因组显示绝大部分基因来自植物，主要部分基因分别来自丝藻和菟丝子，少数基因来自大叶榕和一部分动物混合组。
//
//明显混杂的基因组也展现在外观上，净菜丝刷外观是由粗壮藤蔓缠绕成的藤条衣物，藤蔓近似菟丝子但表皮呈现紫黑色，推测是因为其中含有的大量视黄醛并且因纤维质含量过高而看上去明显不透明。
//
//单条藤蔓直径约10毫米呈紧密横向螺旋状，彼此间以及面向穿戴者方向长有大量细密尖锐的气生根系统连接，此类结构可在不增加纵层时保证藤蔓在垂直方向的结构稳定性，由于是单层无纺织结构也让整个物品外观看上去较为轻薄实用。
//
//关节处因为经常活动，由类似丝藻枝干交叉织成的活性多层织布进行连接，保证活动性和轻便性，外表面上观测到类似荷叶表层的微型绒毛状结构，可以产生极其可靠的防水防尘效果：多数落在表面的杂质会迅速掉落。
//
//尽管净菜丝刷看上去只是普通藤蔓和丝线构成，但是由于上述材料都处于水分充足的活体状态，纤维没有干燥断裂，因此韧性其实比起大多数化工纺织材料也不落下风。出色的防水性和防尘性以及受损后能自我修复的能力使其成为许多远离据点的流浪者的首选。
//
//持有者长期处于室内条件等比较温和的环境时，净菜丝刷会在积累足够养分后开花。开放的种类在购买时决定，目前可选的花香有桂花、玫瑰和槐花。花和结出的果实相比自然环境下的生物模板要小得多但香气更浓，新版产品中加入对花粉过敏症患者特制的无花果版本。净菜丝刷开放的花朵可以直接依靠人体行走时的抖动完成自体授粉，因此穿戴者无需专门找到蜜蜂或是其他生物完成这一行为。
//
//使用方式
//由于净菜丝刷穿戴时要与身体对接，过程可能会带来强烈不适感，因此穿戴时建议购买配套的感觉抑制剂服用。完毕后将净菜丝刷置于自己正上方缓缓套入，确定调整到合适位置后捏住胸口处作为保险装置的肉团使之缺氧坏死。
//
//随后穿戴者皮肤表情会感觉剧烈的瘙痒并且肩部和背部传来刺痛，此时气生根会生出尖锐末端刺入毛孔吸取其中分泌的汗液和油脂作为营养，脊柱两侧和肩部用于辅助共生的多孔刺会直接植入身体。
//
//和水煮骨髓所用技术类似：多孔刺会在刺入穿戴者体内后快速展开，依靠激素和溶解酶将刺入区域周边的血管溶解重排，分离出对应的血管并在末端生长透膜保证仅有水分、糖分和无机盐可以正常渗入。
//
//此时共生系统基本完成，植入者体内的大量无机盐和体表的渗出物都会被净菜丝刷作为养体吸收，身体表层的气味显著减少，加之体表植物可降低体温并降低周围的二氧化碳浓度，多数情况下穿戴者将不会被蚊虫识别为目标。
//
