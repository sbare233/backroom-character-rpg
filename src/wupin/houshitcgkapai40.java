//实体
//实体是这款游戏的灵魂。无论它们是普通的动物、植物、还是只想活下去的人类、亦或是强大如神一般的存在，实体都在游戏中扮演着核心的一环，和后室本身不相上下。每一实体在每一回合中可执行一次行动。这包括袭击一个敌对实体、一座前哨站、或激活一项强大的技能。此外，每一实体都有其费用（通常使用食物支付）、伤害值、生命值、以及承载值。下面我们来详细讲讲这些数值。
