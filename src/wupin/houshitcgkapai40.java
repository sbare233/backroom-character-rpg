//游戏准备：
//一局The Backrooms TCG开始时，每位玩家的牌堆放置于桌面上并将他们的G.O.I.牌正面朝上展示。一位玩家的牌堆中有40张卡牌，且相同的牌至多有3张，不过可能有一些特例。当所有G.O.I.卡牌被翻开后，每位玩家选择3张层级牌，将它们混在一起洗匀。这将作为整局游戏的层级牌堆放置在桌面中央。一枚特制的探险骰将会轮流自动投掷，随后玩家牌堆牌堆就会自动洗混！这就是超自然卡牌游戏的优势！随后每位玩家抽5张牌，除非G.O.I.牌另有说明。
//
//游戏区域
//两位玩家各有一个牌堆，一个弃牌堆，两只手以及一块区域。对于卡牌在玩家区域中的放置不作强制要求。玩家可以根据个人喜好自由在手中或者玩家区域中放置手牌。这些区域可以在玩家的左边，但我们建议您将牌堆放置在右侧，弃牌堆则居于其后方。
//
//层级牌堆以及所有中立实体应当被放置在游戏区域的中间位置。
