//入场/离场
//这很简单。当卡牌进入游戏时触发入场效果。当卡牌退出游戏时触发离场效果！
//
//切入/切出
//在游戏过程中，卡牌可能会由于某种效果而切出当前的激活层级。在这一情况下，将卡牌和所有附于其上的所有东西翻至背面。这些卡牌将被视为退出游戏。这将会触发任何离场效果。如果游戏返回到被切出卡牌所在的层级或某一效果通过其他方式使它们切入，则将它们重新翻至正面朝上。这将触发任何入场效果。
