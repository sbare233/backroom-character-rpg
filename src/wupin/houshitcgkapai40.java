//费用（黄色）：
//打出一张牌所需要的费用。除去层级牌和G.O.I.牌外，每一张牌都有相对应的费用。费用通常使用食物——游戏中的资源——支付。在第1回合，你将拥有1点食物，第2回合拥有2点，以此类推，最多只能拥有10点食物。
