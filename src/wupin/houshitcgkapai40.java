//探索
//每当你进行探索时，你需要掷出一个特殊的六面骰子，四面为空白，一面为切入/切出，还有一面为特殊遭遇！如果你掷出空白面，你额外抓一张牌。如果你掷出切入/切出面，你可以弃掉当前的激活层级并翻开层级牌堆顶端的卡牌。如果层级牌堆已空，则将先前弃掉的所有层级卡牌洗入新牌堆，然后再翻开新的激活层级。如果你掷出了遭遇面，则触发激活层级的遭遇技能！有各种各样的卡牌可供探索，这通常需要占用一次行动机会。
