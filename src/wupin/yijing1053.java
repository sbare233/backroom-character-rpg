//德库晶
//
//使用方法
//将身体的任意部位接触库德晶超过五分钟，使用者将会主观地感受到席卷全身的寒意。6接下来回忆任意一段记忆，随着回忆的进行，使用者的行动也会越发迟缓，直到几乎不能移动的时候，德库晶将迅速萎缩并直接消失。使用者恢复行动能力，这也表示着成功激活德库晶。
//
//成功激活后，流浪者在使用过程中回想的记忆将被“冻结”。被冻结的记忆具有以下表现：
//
//记忆变得非常牢固，使用者可以清楚地回忆起其细节
//与使用者的其他记忆和认知产生隔绝7。
