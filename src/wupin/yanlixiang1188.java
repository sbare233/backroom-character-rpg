//Object C-188 - “岩里香”
//评分: +18+–x
//物品编号：Object C-188
//岩里香是一种通常生成于各室内层级墙壁或屋顶等区域的可饮用液体，味道独特，目前被作为一种高价值奢侈品和流通物在层级之间进行交易和使用。
//
//描述
//部分情况下后室在生成层级或产生物资时，原本应该出现在室内的物资会被错误地生成在墙体内部，这种情况下墙体内部的食物会长期处于有一定空气流通但相对封闭且温度恒定的环境中。
//
//尽管大多数情况下食物会干枯碎裂成墙体的一部分，但当所属层级湿度较高或是错误生成的食物本身含有较高的水分时，这些食物可能会在墙体中长期发酵，最终形成一种可以饮用的特殊液体。
//
//和其他发酵产物相似，这些液体有20%~40%的酒精含量，除此之外有10%无法探明的杂质，探索者将这种产物称为岩里香。
//
//尽管岩里香通常出现在绝大多数层级的墙体或是地下。或屋顶之类的位置，但是这些物质却超乎预料的纯净，在其中几乎不会检测到固体杂质，少数能检测到的固体杂质也是原有食物的残余。
//
//特殊效应猜测
//目前认为岩里香的产生是由于后室错误生成“补给”和“场景”的相对位置，因此后室对这些物品进行某种“修复”，希望其能以“食物”的性质脱离出墙壁而不是继续保持“交互”状态，但固体在空间上无法脱离墙壁。转化为的液体保有这一属性，作为一种“补给”不再“场景”这一固体发生交互。
//
//实验结果证明这一观点，岩里香在渗透入尘埃或墙体时不会溶解其中物质，只会渗透在其中。如果对其进行密闭蒸馏或是区域内渗透的岩里香已经到极限，就会很快滴落，滴落的物质不会与滴入前发生任何成分上的改变。
//
//获取
//岩里香最常见的获取方式是使用一个容器对准正在渗出岩里香的墙壁和天花板进行接纳，如果探索者恰好处于可以对墙壁以及周边物品进行破坏的层级也可以直接将浸透岩里香的墙壁砸下带回聚集地进行蒸馏提取。但是需要注意的是由于岩里香具有挥发性，保存容器需要有较好的密封性，目前认为最适合的保存容器是杏仁水喝完后的空瓶。
//
//少部生成环境极为巧合的岩里香会恰好处于一个相应的容器中，对于这类岩里香的采集需要小心的将容器取出后携带至聚集地中。
//
//使用方式
//由于岩里香对于口感的掩盖性较强，不建议与其他食物共同食用。目前认为较好的处理方式是在睡前或是用餐后取用，少量岩里香摄入并置于舌尖上品尝其味道，等待其中味道已经品尝完毕后自然咽下，感受暖流流过身体的感觉。
//
//功效
//无，尽管诸多传说中岩里香有助于过滤身体内重金属离子、强化探索者切入切出能力、甚至是让探索者获得奇术能力等种种功效的传言。
//
//但是实际上根据对其内容进行的化验以及试验结果来看，饮用岩里香的效果约等于饮用一瓶等量的清香型白酒，但是可能有略长的醉酒时间，原因似乎是那些具有芳香性但是无法探明的杂质。
//
//文化内涵
//由于岩里香有独特的口感且极其稀少，产出方式也较为独特，这使得它成为一种被广泛认可的奢侈品，通常来说在聚落欢迎外来者或是庆功的宴会上会被作为压箱体的高级物资取出以表示对来宾的尊重或是对功绩的认可。
//
//除此之外异途道门极其看重岩里香，认为这种从后室中自然生成的物质蕴含着某种灵气，是有助于修行的珍贵天才地宝。因此异途道门往往会以相比普通探索者高的多的多的出价来收购各类岩里香并在冥想打坐前饮用作为辅助冥想的物质，因此通过岩里香异途道门能获取大量资源也被证明是可行的。
//
//最后岩里香中特殊的杂质目前尚未完全探明，这给予许多科研探索的可能。有识血肉似乎也在大量收集岩里香并提取其中的杂质进行研究，但即使是有识血肉这样生物技术较为出众的文明也没有找到能够人工生产岩里香的方式，将食物放入墙中发酵只能得到普通的酒精，并且会有大量建筑材料残余。
//
