//顶上芳
//这是最常见的一类岩里香，经常在各种室内层级的天花板上以液滴的方式缓慢滴落，晶莹透明，酒味香醇，入口绵，落口甜，饮后余香不绝，有一股淡淡的生石灰气味，如果上方有铁质电器的话会有一股独特的铁锈腥气，目前的市价是50倍等体积的杏仁水。
//
//壁中香
//同样较为常见，这类岩里香由于位于高度较低的墙壁中，通常味道更为浓郁厚重，酒体入口醇厚甘美，酒体柔和净爽，著香协调，余味悠长。如果所处区域有一些其他家具尤其是木质家具，则会带有一股淡淡的木头香味，否则只有酒香和淡淡的生石灰味，市价同样是50倍等体积的杏仁水。
//
//值得一提的是，如果生成区域靠近机械时，壁中香会有一股明显的石油味，严重破坏口感，这会导致其价格大打折扣通常只有原有价格的一半不到。
//
//锈花香
//这是指出现在有大量金属，尤其是机械墙壁之类区域的岩里香，其最明显的特征是周围的金属墙壁会出现轻微的锈蚀但并不会被锈穿，通常来说可以将周围的金属切割后直接将锈花香和金属容器一并取出。
//
//此种岩里香酒质清澈、色泽微红如锈似花、酱香醇厚、扣杯留香，故得名锈花香，市价可达百倍杏仁水，若是能带上容器则价值更高。
//
//琼浆
//这是最为稀有而独特的岩里香，目前可以确定的是，它会被错误生存在一些矿物质内部。这会使矿物质内层出现一个明显可见的空腔，而由于矿物质本身的完整性，这一空腔会连同其中的物质一同保存，最终形成类似水胆石的效果。
//
//由于本身有极高的艺术价值，该类型的岩里香几乎不会被饮用而将作为一种宝物永久收藏，其价值也极其惊人，其价值的判断方式近似于大多数宝石根据其成色和液体内容量来决定最终价值，但是可以明确的是这些价值已经超越了大多数探索者的承受上限。
//
//异化香
//这是出现的少数错乱和本身就有错误生成倾向的层级的特殊产物，其性质尚未探明，并且极其难以采集，目前已确认这样生成的产物不具有饮用可能性，但是有极高的科研价值，对于研究物质在后室错误生成的规律与利用提供了珍贵的研究素材。
//
//发现
//该物质最早的发现纪录是C层群民众自卫保护组织中队长之一的郭淼为改善营地中由于水资源匮乏而导致居民个人卫生状态较差的问题而组织进行的一次科研搜寻项目，目的是提取出一些较为容易制造并且气味较好的可喷洒香料。
//
//一部分探索者曾经在旅行中闻到浓烈的酒香味伴随着一股独特的香气，大多数探索者都反映这种气味能够舒缓心态并且让人略感放松，因此一只探索小队被派出试图寻找这些物质的源头，最终从墙壁中蒸馏出了最早的岩里香。
//
