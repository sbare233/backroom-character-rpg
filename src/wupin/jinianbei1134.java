//纪念碑表面往往镌刻有意义不明的姓名、生卒年月、生平事迹2所构成的「墓志铭」，无论目击者使用何种语言，「墓志铭」皆被描述为使用其母语撰写。经过有关人员对于其中数十份「墓志铭」的指认，纪念碑所记载的信息已被证实来自于确切存在且已经逝世的流浪者——除此之外，已知的数百份「墓志铭」的归属者似乎没有其他显著共性。据此，或有推测纪念碑的异常性质没有针对人群，实则囊括每一位于后室离世的人类，即可解释为，崭新的纪念碑个体将会伴随着后室中人员的死亡而诞生。此说法尚未得到证实，但已逐渐成为当前对于纪念碑成因的主流观点。
