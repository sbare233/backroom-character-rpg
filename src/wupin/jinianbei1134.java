//Object C-134，即“纪念碑”，被认为是一系列行踪不定的物体的统称，它们往往可以在户外层级被找到，例如Level 10、Level 11“郊野”1与Level 48等。其通常表现为形式不一的石质纪念碑，另有极少数明显由其他材料铸造的个体，它们皆无法以任何方式被损坏。M.E.G.推测，源自纪念碑的异常现象确切影响范围实则包含了碑体本身与其半径3～4米内的地表区域。
//
//纪念碑一般在停留1~4天后迁移至别处，此过程似乎完全随机，但其似乎会刻意地避开人口过分稠密或稀疏的区域。数个纪念碑常常生成于邻近处（直线距离不超过4米），形成“团簇”，形成团簇的各个体不会在迁移中被拆散。目前已知的最大团簇位于Level 11内的巴尔费茨（Balfetz）神父公墓，该团簇容纳着68块纪念碑个体，这个数字仍在缓慢增长。
