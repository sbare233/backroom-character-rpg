//以下为目前已知的杏仁制品的一部分：
//
//广泛名称：杏仁溶液
//子编号：#-1
//加工方式：溶解
//描述：大部分2物质溶解于杏仁水的物品，例如将火盐溶解获得加热溶液；将治愈水晶溶解获得治愈药剂等。
//
//广泛名称：杏仁蛋糕
//子编号：#-9
//加工方式：烘焙+修饰
//描述：将杏仁水替代制作蛋糕需要的水所制得的蛋糕胚。普通的杏仁蛋糕之具备与杏仁水形同的功效，但如果制作时或者增加点缀3，杏仁蛋糕的效果将改变，典例就是蓝莓杏仁蛋糕，吃下之后可以暂时缓解眼部疲劳并且加强使用者对于黑暗的适应度。
//
//广泛名称：杏仁曲奇
//子编号：#-15
//加工方式：烘焙
//描述：将杏仁水混进面粉制成曲奇，并不会因为加入其他食材而改变效果，杏仁曲奇在食用之后可以暂时提升食用者的记忆力，十分适合在学习知识时食用。
//
//广泛名称：杏仁柠檬水
//子编号：#-48
//加工方式：混合
//描述：将柠檬汁混合进杏仁水中，饮用与普通杏仁水效果无异，但这种混合杏仁水似乎受到任何非人类之外的生物与实体的厌恶。涂抹在身上可保证在杏仁柠檬水挥发前免受实体侵害，也可以当作喷雾喝退敌意实体。
//
//广泛名称：杏仁汽水
//子编号：#-67
//加工方式：加压气体
//描述：将二氧化碳打入杏仁水中，饮用后可以比普通杏仁水更加提神。近些年很受欢迎，尤其是年轻的一代。
//
//广泛名称：杏仁压缩饼干
//子编号：#-900
//加工方式：压制
//描述：可将大量杏仁水一次性压缩，但本制品丧失了后室杏仁水的神奇功效，性价比极低。目前只在后室网络吃播中出现。
//
//点击展开采访记录
//广泛名称：杏仁火锅
//子编号：#-61
//加工方式：涮煮
//描述：顾名思义，是用杏仁水做汤底的火锅，其味道被大多数流浪者评价为“又甜又涩”，但也不乏有出生于后室的人类喜爱。值得注意的是，杏仁水将大大延长火锅味留在身上的时间。
//
//广泛名称：杏仁边角料
//子编号：#-998
//加工方式：蒸制
//描述：便宜又实惠的一个杏仁产品，将杏仁水蒸干后的粉末作为佐料加入其余的食饮之中，虽然已丧失了全部作用，但仍有浓郁的杏仁香气与独特的甜味，使其成为后室厨师联盟的日用品之一
//
//广泛名称：杏仁豆腐
//子编号：#-512
//加工方式：煮沸+冷制
