//备好盲知
//收集作以食用的盲知是件耗时的工作，你大部分时间都得守株待兔。
//
//盲知在死后不久会进行一种有机的蜕变，将它的尸体化作泡沫与凝胶。在这时，要谨慎地记录它的位置，并且切勿贸然打断。
//4686b010629c08b6.jpg
//盲知的生物残骸
//M.E.G. 档案
//
//盲知的尸体将最终凝固并变为奶白色，当它完全变硬且能被顺滑地从当前位置取下时，你便可以把它储存在一个干燥的容器当中。请小心，盲知是很脆的。
//切记： 不要（任何情况下都一样）试图清洗盲知的任何一部分，这会导致一个不可逆转与停止的负面进程开始启动。（遇水的盲知残骸必须被丢弃。）你可以用干燥的刷子掸去盲知标本上的疏松异物。
