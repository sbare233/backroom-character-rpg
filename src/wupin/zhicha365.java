//知茶是什么？
//知茶并非一种万应灵丹，它更倾向于一种膳食补剂或“维生素饮料”，由研碎的死去盲知溶解在沸水化开制作而成。这种饮料辅以杏仁水将效用更佳。
//
//尽管该效应依然属于传闻性质，但已有可靠的报告显示，饮用这种补剂将丰富饮用者的身体、精神，甚至“超自然”力。目前已确认饮用一次饮品便能暂时性加强这些能力，而对其定期进行摄入则能够累计永久性的增幅。
