//研钵和研杵
//用来将死去的盲知磨成粉末
//M.E.G. 档案
//
//最后一步是把盲知研磨成精细的粉末，这是通过小心地将盲知标本打碎成小片实现的（你可以在这时清除杂质与污垢）。随后你要用研钵和研杵进一步捣碎碎片，接着将粉末储存在干净且干燥的容器中即可。
//我个人知茶的配方
//以8盎司（或一杯）作为液体的标准分量
//
//f0a8fa6b2df0b5cb.jpg
//小匙（1/16茶匙）
//M.E.G. 档案
//
//每次饮用时切勿混合1/8茶匙或者说两“撮”以上的盲知粉末。一撮指的是食指和大拇指能捻起的量，也就是1/16茶匙。
//
//永远记得要将盲知粉用沸水溶化，这是为了杀菌。
