//描述
//消食莴苣是一种仅由B.C.A.（后室厨师联盟）研发并栽培的异常作物，外观类似于前厅菊科莴苣属莴苣，但体积略小于后者。
//
//消食莴苣可食用，口味与普通莴苣无异，建议经过烹饪后再食用。值得注意的是，消食莴苣包含绝大部分人体必需的营养成分1，但似乎完全不会为食用者提供热量——由于B.C.A.俱乐部成员有意地避开这一话题，该现象的原因未知。M.E.G.推测，消食莴苣内部的新陈代谢过程不同于任何已知的生物，而为消食莴苣提供能量的物质则完全不被人体所吸收。
//
//- 收起
//M.E.G. “测验者”部门在E.P.B.的协助下，从消食莴苣叶片取得微量样品，这种结构类似多糖的物质从未在前厅的任何一种生物中被发现，并被命名为莴苣多糖。莴苣多糖在消食莴苣中充当与淀粉类似的角色——通过光合作用合成，并被用于储存能量以供消耗。不过，莴苣多糖因其结构特殊性，食用后难以被人体识别，不参与人类新陈代谢，不为人体提供能量。
//
//E.P.B.人员亦指出，他们从消食莴苣中首次分离得到一种植物蛋白，组成肽链的氨基酸与以往的所有记录皆不符合，据称，该植物蛋白不被所人体吸收。
//
//——Romando Bērzi
//
//
//消食莴苣会为食用者提供大量饱腹感，且效果明显强于一般蔬菜，通过X光透视观测，消食莴苣在接触胃中消化液后逐渐膨胀，且最大可以膨胀至原先体积的5～6倍，推测消食莴苣中含有与胃液强烈反应的物质，暂未确定这种极具针对性的设计是否为B.C.A.有意为之。
