//用途
//消食莴苣口感清新爽口，且较于普通莴苣似乎更易保持新鲜，普遍被B.C.A.俱乐部成员及其他后室中烹饪爱好者用于烹制菜肴。
//
//消食莴苣不提供任何可为人体提供热量的物质（如典型脂肪、糖类、蛋白质），营养成分充足且产生饱腹感，有促进人体消耗自身皮下脂肪的作用，可以作为减脂食品长期食用2，尤其受到有降低体脂率需求的人群与喜爱清淡饮食者的青睐。
//
//- 收起
//6月5日本档案发布于M.E.G.官网后，B.C.A.相关方面称，后期将以相同的方式，继续培养与消食莴苣性质相似的蔬菜品种。7月2日，有流浪者报告称部分B.C.A.俱乐部开始将冠以「消食甘蓝」名称的商品摆上货架，商品外观类似于前厅十字花科芸薹属甘蓝，且食用后同样不为人体提供热量。
//
//在后续的报告中，B.C.A.开始售卖诸如冰草、马铃薯等异常作物，这些商品均表现出上述特殊性质。经讨论，M.E.G.决定以发现时间为标准，将具有类似特性的所有物品标记为Object C-159的子物品。下为已记录在案的Object C-159品种（为保持简洁删减重复出现的“消食”部分）。
