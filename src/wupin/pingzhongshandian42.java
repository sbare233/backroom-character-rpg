//实验编号：5
//日期：2018年6月23日
//使用物品：Object 42，蓝色闪电变种
//目标：进一步测试Object 42的传导性。
//实验配置：瓶子被放在一个空房间的中间。将一块砖用蜡粘在天花板上，并静置直到蜡融化。
//结果：当蜡融化时，砖块掉下来将瓶子砸成碎片并释放出了闪电。由于没有任何东西可供传导，闪电反而击中了一面墙，留下了烧焦的痕迹并导致产生臭氧。
//结论：所以，当没有任何东西可供它传导时，它就是普通的闪电，不错。
