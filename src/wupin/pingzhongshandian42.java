//实验编号：1~3
//日期：2018年5月24日
//使用物品：Object 42，蓝色闪电变种
//目标：测试Object 42的传导性。
//实验配置：将闪电通过一个自动机械臂释放到房间内，房间内有一根连接到功率计的铜线。
//结果：每次实验中，闪电在被释放后都会立刻通向导线。当电流通过铜线时，铜线发热并短暂发出蓝光，但每次电流到达功率计时，功率计都会爆炸。
//结论：如果它的威力足以毁坏功率计，我确信我们正面对的是一个很严肃的问题。
//
//实验编号：4
//日期：2018年6月17日
