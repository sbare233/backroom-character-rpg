//描述：
//麦迪逊大道的人体模型是一具外观破旧的人体模型，它由塑料材质被制造，拥有着古铜色的皮肤和仿真类型的脸部风格。模型穿着一件破烂的西服，西服的表面覆盖着钙与硅质的金属垢，呈现出似乎是在水下浸泡许久的模样。
//
//当有流浪者目击到人体模型时会产生轻微的幻觉。看到它的流浪者们描述说，人体模型的身影会一直在他们的视野中挥之不去，这种影响会持续几天到若干年。流浪者们在这段时间里受到的精神影响被认为是负面的，不知道这种影响是出自该物品的异常性质，还是只是一种对幻觉的自然反应。
