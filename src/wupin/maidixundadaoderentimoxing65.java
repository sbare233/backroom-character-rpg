//20/12/19
//他离开了。我不敢相信我终于恢复正常了，说实话我昨天都不知道我的脑子里在想什么，我想我可能断片了，我逃到了毁灭之地，准备躺下休息一会，当时的一切都有些模糊不清，我觉得我当时好像看到了什么，长得和龙一样的生物正在朝我走来，但我却没有为它让路，我感觉这家伙应该要杀死我了。当我看到火焰从它的口腔溢出，正要将我吞没之时，我的心里忽地浮现出一种感觉，我清醒过来，走出了之前所困的阴霾，不过我觉得，我振作过来得时机有些太晚了。但转机出现了，人体模型突然出现，挡在了我与巨龙中间，它是，真的出现了，而不是之前围绕我的诡异幻觉，我能确定它是真的，因为它在那时以某种方式吸收了巨龙吐出的火焰，给予了我足够的时间逃跑。老实说，我真的不是很确定到底发生了什么，甚至不知道这一切是不是真的，但我的确感觉好多了。
