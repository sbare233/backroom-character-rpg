//使用方法
//1.提前打开电子终端和主体设施，预热系统。
//2.选择一名年龄介于25~45岁的成年男性进入压缩睡眠舱。
//3.将各类电缆和电极接入受试人员的大脑，将辅助软管注入配套液体。
//4. 辅助软管连接注射针头，插入受试人员的静脉，打开压缩睡眠舱装置的第一道安全保险。
//5.在电子终端输入层级具体参数、离开时间，打开压缩睡眠舱第二道安全保险。启动装置。
//6.在显示屏上密切监视受试人员目光所及范围的情景，若出现异常，启用紧急撤离程序，避免受试人员的脑死亡。
//7.到达离开时间或紧急撤离成功，受试人员意识回到当前层级，拔下软管，第二层安全保险关闭。
//8.受试人员离开舱体主体，结束探索进程，第一道安全保险关闭。
//9.关闭机器。
