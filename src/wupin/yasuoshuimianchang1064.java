//以使用压缩睡眠舱进入不宜反复进入的层级调查的方法调查，有效的避免了冒险队员的人力损耗，节省了探险队在各个层级间穿梭的时间，大大降低了探索层级带来的经济损耗。但其弊端也十分明显：需要层级的详细参数；只能通过意识进入，无法触碰和精细化检测物体；一次只能进入一人且启动一次所需的能量消耗巨大……总之，压缩睡眠舱使用的情景较小，对于层级的探索，将长期保持使用以人力进行实地勘探的方法为主。
