//面具种类：
//为了迎合流浪者的人格，墙壁面具有不同的种类。因此，以下的面具种类是根据佩戴者隐藏或掩盖他们真实性格的程度而分类的。
//
//全面：2
//哥伦比亚面具：吸引流浪者最多的面具种类，哥伦比亚面具会覆盖佩戴者一半的面部，看起来优雅多彩。该面具的佩戴者和佩戴前并无二异，但他们的行为将更加神秘。在击杀流浪者和实体时，他们会在悄无声息地绕到落单目标的盲区后将其杀害，并且将尸体藏起来。
//
//布塔面具：布塔面具没有遮盖嘴部的部分，外观呈矩形， 整体颜色大致相同且被漩涡花纹覆盖。 这种面具的佩戴者会变得咄咄逼人，并且不喜欢与他人争论。佩戴这种面具的人更倾向于使用钝器杀害目标。
//
//潘塔罗娜面具：这种面具的特点是有一个相当大的，类似于鸟喙的钩鼻，它会覆盖佩戴者的上半面部，并带有一对斜视的眼睛。这种面具的佩戴者的行为看起来十分可疑，他们会尽力的伪装成正常人的样子。潘塔罗娜面具的佩戴者的性格会有明显的变化，他们在和流浪者交谈时会不断地说出有关后室的事情，以此试图让流浪者转移对他们的注意力。当被问及有关个人隐私的问题时，他们会变得偏执并且试图回避谈话。如果他们发现自己被跟踪，他们便会以最快的速度找到并杀死跟踪者。因此，建议远离潘塔罗娜面具的佩戴者。
//
//注：根据M.E.G.的实验结果，全面的佩戴者都是自愿戴上面具的，而并未受到物理或精神上的强迫——尽管他们完全有能力拒绝这么做。
