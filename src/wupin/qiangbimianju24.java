//半面：
//沃尔托面具：沃尔特面具整体为白色，能够覆盖整个面部，上面常有金色的漩涡花纹。这种面具的佩戴者会变得狡诈多疑。但总体而言，他们的行为还是和正常人一样的。沃尔托面具的佩戴者和哥伦比亚面具的佩戴者有着一样的神秘行为，但在杀人后不会隐藏受害者的尸体。
//
//狐妖3面具：狐妖面具是一种有着各种颜色的狐狸面具。通常覆盖佩戴者一半的面部，这种面具的佩戴者会变得鬼鬼祟祟，甚至有点调皮，而且非常轻浮。但他们会对个人隐私闭口不谈。该面具的佩戴者会引诱他的受害者，并且使用一种极为快速的手法杀死他们。
//
//阿莱基诺面具：阿莱基诺（或哈勒奎）面具有着各种颜色，通常有着短鼻和弓眉，偶尔面具会有一个大的头饰或者衣领以搭配铃铛。带上这种面具的流浪者会奇怪地大笑起来，通常情况下不能解释他们的乐趣。阿莱基诺面具的佩戴者会做出鲁莽，不合时宜的决定，不断为自己的行为找借口。他们的杀人方法很奇特——他们会走到受害者面前，然后给他讲一个笑话，如果受害者的反应没有让佩戴者满意，受害者将会被残忍地杀死。
