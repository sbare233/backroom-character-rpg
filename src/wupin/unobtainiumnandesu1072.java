//特性
//难得素主要有A型和B型两种类型，A型拥有易燃、催化和增幅的活跃性质，B型具备抗燃、惰性化和减幅的性质。
//
//将难得素A型和B型混合可以产生极大的爆炸力后同时在爆炸范围内向爆炸中心产生吸引力，若其中含有其他易爆物质可以发生连续多次的爆炸。据实验，它的产生的能量总和是火盐的11.7倍左右。
//有些难得素还具有磁性，据测试，难得素其制品有些具有磁性以及导电性，其原因未知。
//
//用途
//已知难得素A型可以与火盐掺杂，制作集束燃烧炸药，用于开发和探索。难得素B型与蓝砂掺杂会使其低温不融化并且转变为深紫色晶质，作为集成电路板或压印器的材料。
//
//获取
//已知利用诺克立普贴会在多次切出或切入的区域会产生一定量的难得素矿化植物中萃取。或通过将难得素结晶埋入土壤从而影响到半径25米周围的植物使其变异为紫色后产生的少量疣状物质中萃取，但难得素结晶会被逐渐消耗。
//
//基础制品
//难得素源质
//这种形态的难得素是人为制作的，将难得素、难得素结晶与液态火盐混合高温烧制并冷却，凝聚而成的透明结晶块。结晶块大体为粉色，一块1千克的结晶可能只有1克的紫色结晶，其原因未知。
//
//已知粉色难得素源质通常用于精密部件以作为类似金刚石的镀层、以及防具镀层和武器镀层，而紫色难得素源质通常被视作更好的铀，相比于铀，其放射性不会影响人类和实体。
//
//紫色难得素源质具有极大的易燃性，似乎永远不会被燃烧所损坏，所以它也可以充当煤炭等物品。
//
//难得素合金
//这是难得素金属质、源质与任意材料的合金产物，其整体颜色呈现为紫色、粉色和其中任意材料的颜色，拥有类似大马士革钢的纹理。不同合金钢其中任意材料的成分会有不同的能力和作用。
//
//这种物品常加工为高级金属制品，已知若其中任意材料的成分为火盐和蓝砂并制成武器可以得到引燃的效果，能点起紫白色的闪耀的火焰，这种火焰蔓延要比普通火焰更快且难以熄灭，且受到此火焰影响的实体碎块可作为合金钢中的任意材料。而将实体碎块作为合金材料会产生具备其实体部分能力的特性。
//
//而难得素合金还有一种泛用性极其广泛且易制造的初级人工制品，通常为区别于难得素合金被称为多素后室合金。多素后室合金是在制造工艺上由于难得素金属质、源质作为极其少量或微量而得名。
