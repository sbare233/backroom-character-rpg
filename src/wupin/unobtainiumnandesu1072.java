//Object C-72 - “难得素”
//评分: +20+–x
//描述
//难得素是仅后室所有的物质，但通常直接作为材料。它们在后室以特殊方式被发掘并应用，且还拥有其他两种人工变体。它们以矿化植物的形式出现，暂未发现其他难得素自然附着物。
//
//难得素通常存在于一种矿化植物中，矿化植物通常为粗细不一的藤蔓与少量结有类似真空管外形的难得素结晶，大小在1~15cm之间。其藤蔓为闪亮的紫色，而结晶花朵通常散发着紫色的微光或雾气，散发的成分仍在研究中。值得一提的是，其藤蔓在隔绝空气的条件下热解后会形成紫色的闪耀且坚硬粗糙的粗制难得素金属质，若进一步提炼所得的产物即为难得素。其难得素结晶经采集后为保留难得素结晶的独有性质，需要将它浸泡在液态火盐和杏仁溶液中。
//
