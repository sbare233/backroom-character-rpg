//描述
//Object 6是一面十分巨大、看似正常的墙镜，被悬挂于一个绿色逃生出口标识之下。该物品存放在Level 0中一个平平无奇的房间内，但这个房间极其偏僻，很难找到。该物品未曾在其他层级中出现过，其唯一的移动方式就是随着Level 0内部线性空间的变化而移动。
//
//镜子本身为长方形，高97.1英尺（约246.6厘米）、宽61.9英尺（约157.2厘米）、厚0.24英尺（约0.61厘米）。其表面光滑无暇，只在靠近地面的一侧的中央位置存在两行刻字。
//
//刻字内容如下：
//
//现实
//
//哥林多前书 13：12
//
//这些文字的含义目前尚未破解。而它们与该物品特性之间的关系目前也没有定论。
//
//特性
//I.异常映射
//对于所有观察者而言，该物品都表现为一面普通的墙镜。然而，在暴露于该物品数小时后，其会逐步展现出异常的性质。
//
//大部分观察者表示，镜子里的影像会变成他们在前厅中最重要的人、事、物，并且正是那些他们会常常想念的。最常见的情况是观察者的家、生活的街道、学校与办公地点，且总会伴随着他们的爱人、家人与宠物一起出现。
//
//观察者通常会坚称他们在镜中看到的景象是十分宁静和谐的。镜中所有的人物、物件与地点都表现得非常正常，并且与观察者记忆中的别无二致。观察者们表示他们在镜中没有看到后室、或是与后室相关的特殊物品。有趣的是，一些观察者甚至声称他们在镜中看到了已故的亲属或是宠物，以及已经不复存在的场景。
//
//观察者们同样表示，他们无法以任何形式与镜子另一头互动。然而，他们无一例外地都在镜中看到了另一个自己的形象。观察者们在镜中的形象与他们本人的年龄、外貌接近，但会穿着与镜中场景相应的服饰。
//
//有小部分观察者表示他们在镜中看不到特殊的景象。对于这些人来说，该物品持续表现为一面正常的墙镜。调查表明这些人大多出生在后室，或是已经在后室中生活了相当长的时间。
//
//也有一些未经证实的谣言，称该物体会反映出截然不同的场景。有些人声称镜中的场景会发生巨大的变化，显示为后室某处、或是一个神秘、虚构的场景。对于这些传闻的调查研究已经提上了日程。
//
//人们对于镜中场景的反应各有不同。如预期一样，观察者会表达思念、怀旧的情绪。大部分人怆然泪下。有一些观察者会激动地试图打破镜子（迄今为止尚未有人成功做到这点），进入其中的场景。虽然记录在案的观察者反应通常都是他们由内而生的情感，但调查人员仍在研究该物品是否会给观察者的情绪施加额外的心理影响。
//
//有些观察者的确发现自己很难忘掉这面镜子。在离开该物品后，许多观察者表现出了不算严重的心理、精神或是情感的低落。暴露于该物品数小时后，观察者会感到思念与怀旧，但这被认为是人之常情。有些人也称自己感到冷静、轻松；他们表示，在再一次瞥见现实之后，他们感到了一种一切落幕的结束之情。
//
//所有试图对镜中现象拍摄或摄影的尝试都没有成功，从照片与视频中只能看到一面映射着空旷房间的墙镜。因此，研究人员们推测这种现象是一种由该物品引起的幻觉。
//
//II.物理性质
//经过严密的审查后，已确认该物品在展现异常前后都没有物理上的变化，也没有在其中发现未知的技术。然而，似乎任何剐蹭、破坏该物体的行为都是无效的，也无法将其从墙上取下。
//
//我们强烈不推荐1任何人试图破坏或剐蹭该物体，我们尚未完全发现它的性质，也许未来能够加以利用。同时，这样的行为也会为流浪者带来危险，因为该物体会对攻击做出防御反应（详情见附录的音频日志）。
//
//目前我们推测，发生在该物品上的一切特殊现象都是它自身造成的，而非它所在的房间。该物品所处的房间与Level 0中其他的房间相比，并没有什么区别。然而，这个房间呈现出欧几里得几何空间的性质，其尺寸与形状不会变化——这一点与马尼拉房间非常相似。
//
//由于其稳固的几何性质，显然，切入或切出该房间是不可能的。不幸的是，这使得我们难以在非欧几何性质的Level 0中定位Object 6。从镜子中直接切出似乎也是不可能的（更准确地说，人为地试图从中切出是不可能的。详情见附录的音频日志）。
//
//由于定位的困难，有关Objcet 6的研究进展十分缓慢。因此，M.E.G.迫切希望能够与知道镜子起源与其具体功能之人合作，尤其是任何被称作“哥林多”的个体。如果你曾从遇见过镜子的人那里听说过什么奇闻，我们也非常欢迎。
//
//发现
//在该物品被正式发现前，有关它的谣言就已在流浪者之间流传了数年。在推测其可能是一个潜在的后室出口后，M.E.G.迅速提高了重视程度。M.E.G.踪迹制图者军团的“穴居人”小队于2021年3月24日成功找到了该物品，正式确认了其存在。
