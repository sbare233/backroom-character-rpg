//以上问题已有相当多的病例，尤其是进入60岁以上的探险者有更高概率发生。一旦发生将极有可能导致严重的后遗症，即使明知道切除跃层滚油会对宿主身体造成一定伤害，如果进入此状态也必须进行切除。
