//效应
//体内生长有跃层滚油的探索者会称为宿主，宿主有远比普通探索者更强的切入切出适应性，在速切玩家等几个经常切入切出的组织中表现的尤为明显。宿主们往往可以更快掌握一些更高精尖的切入技巧，比如反切或是连切。目前速切玩家记录中有相当一部分的保持者正是跃层滚油的宿主们。
