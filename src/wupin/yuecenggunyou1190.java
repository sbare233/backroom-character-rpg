//处理
//切除跃层滚油会对宿主造成极其严重且不可逆的病理性精神损伤，包括极强烈的幻痛、被害妄想症、孤僻症和广场恐惧症等，成因尚不明确且目前除药物抑制外缺少有效的治疗手段。
