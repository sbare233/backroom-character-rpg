//灰色
//
//灰色是杏仁水最常见的版本——它大多被打包在灰色、金属或透明的瓶罐中，流浪者将这种杏仁水的味道描述为带有杏仁味的微甜。在所有种类的杏仁水中，该变体拥有最高的营养价值，它比其他种类的杏仁水能提供更长时间的饱腹感，但这种版本的杏仁水除了简单的满足饥饿外对人体没有其他的特殊效果。据悉，使用灰色杏仁水还能够短暂驯服像死亡飞蛾与杰瑞这样的实体。
// 
//
//绿色
//
//这种杏仁水大多被装在磨砂质感的绿色瓶罐中，它拥有所有变体中最甜的味道，并且拥有最高的咖啡因含量——大多数人把它的味道描述为一种能量饮料，许多流浪者把这种杏仁水当做清晨的第一杯咖啡饮用。此外，并不推荐过量饮用该版本的杏仁水，长期对其滥用会对人体产生许多副作用，如失眠、嗜睡、疲劳、对心肺功能的损害，以及类似成瘾的依赖性等等。
// 
//
//红色
//
//该变体以其十分苦涩的味道而闻名——因此，除非生存所需，否则大多流浪者在一般情况下并不会饮用这种版本的杏仁水，流浪者们将它的味道比喻成“在刷牙后喝下的橙汁味”。这种变体同样因其饮用后对人体产生的有益影响著称，饮用这种饮料将提供类似抗生素和止咳糖浆的效果——这取决于饮用者需被治疗的疾病。它的普通效果包括延缓类似小感冒或头痛的影响、将血压提高/降低到正常水平，以及加速疑难杂症的恢复速度——它也是目前已知唯一一种治疗疫疾的方式。作为治疗的副作用，人们会在喝下这种杏仁水后感到十分疲惫，因为它里面的物质会将身体的细胞资源重新分配，用以加强身体的整体健康和提升免疫能力。这种变体应该谨慎使用，过量服用会造成感染、肺炎，甚至癌症，不应该在8小时内饮用超过一瓶红色杏仁水。
// 
//
//蓝色
//
//这种杏仁水一般被装在蓝色的瓶罐，且其没有任何特殊的回味。因为它对人有益的效果与那和白开水别无二致的味道，许多流浪者都将它作为自己最喜欢的口味。饮用这种杏仁水后，饮用者体内的褪黑素水平将进行上升，使其产生嗜睡的效果，这种效用在患有失眠症的人群间广受欢迎。此外，这种变体还可以有效抵御外部因素对人类心理健康产生的影响，它还能抑制早期的悲尸循环，甚至在治疗及时的情况下还能够将其直接逆转。它同样能帮助患有焦虑症、抑郁症和多动症的流浪者管理他们的心理疾病。对这种杏仁水的过量使用会造成永久性成瘾、视力受损和肌无力。
