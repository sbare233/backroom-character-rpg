//杏仁水几乎是后室中物品交易最受欢迎的物件之一，鉴于在许多层级建立起标准化的货币体系是不可能的，许多流浪者选择用以物易物的方式进行交易，这让该物品同样被赋予了一定的经济价值。杏仁水是诸如Level 5.1与交易保险库此类地点物品交易中最常使用的货币之一；该物品作用类似于一个代表一的标准货币单位，例如1美元、1欧元或1英镑，许多相关组织与社区出售的物品价值都会以杏仁水的数量计以衡量。杏仁水已经成为后室贸易最普遍的标准货币，因此我们建议，流浪者在Level 11或Level 6.1这样有大量人员定居的层级行动时，要随身携带一定数量的杏仁水。
