//特性：
//杏仁水拥有的特性取决于它瓶罐的颜色。最初，我们推测其性质来源于装盛它们的瓶罐，或这些液体本身的性质更倾向于是某种药水。但随着更进一步的研究，人们发现事实远非如此：我们发现杏仁水的这些性质是通过溶解在液体中的小剂量天然物被赋予的，然而，目前依然无法确切了解这些溶剂被掺入水体的方式与原因，以及它们为何无法通过天然方式进行萃取。尽管如此，杏仁水在所有正常情况下都完全可以饮用，下表列出的是对杏仁水不同种类效用的档案。
