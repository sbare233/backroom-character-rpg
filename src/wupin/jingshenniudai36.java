//颜色	情绪	描述
//红色	愤怒	Caedmal的研究表明，帽子上出现红色代表着刺剑水母正处于愤怒的情绪，这对它们来说实属是一种很罕见的心情，鉴于该实体以其欢乐的性格闻名。在记录观测到一只刺剑水母用触手蜇伤它的同伴时，被蜇伤的那只个体变得愤怒恼火，它的帽子变成了红色，并在Caedmal介入阻止冲突前叱骂了刺伤它的同伴。
//粉色	快乐	粉色被档案记录为代表快乐的情绪，精神纽带在有刺剑水母在附近的情况下总会显露出粉色。Caedmal叙述说，每当他前往刺剑水母们那里为它们检查状况时，它们精神纽带上的粉色总会变得异常鲜艳。他还记录道，在帽子处于粉色时，刺剑水母的声音会变得开心而欢快跳脱。
//蓝色	悲伤	蓝色同样也是一种罕见、不常显露在精神纽带上的颜色，Caedmal记录了一场他与刺剑水母的互动：一只刺剑水母在不了解该层级实质性质的情况下要求前往Level Fun玩耍，在Caedmal拒绝了水母的请求后，它的帽子变作了品蓝色，显露出了极度的悲伤，坚持想要加入这场所谓的派对，并在最终悻悻游开去往了天鹅座档案馆。
//黄色	恐惧	这种颜色稍微比红色与蓝色常见一些，但也不算是频繁出现。Caedmal记叙说：在Leo带领一名流浪者游览Level 222时，一只刺剑水母刚好漂浮路过，不小心刺伤了他们。这引起了流浪者对该生物的愤怒，水母开始表现得畏畏缩缩，十分害怕，在Caedmal安抚了流浪者的情绪后，他把这件轶事记录进了他的书中。
//紫色	激动	紫色是又一种高频出现的颜色，且通常是水母在不露怯的情况下帽子显现的颜色。Caedmal记录，每当他用饼干奖励水母们的时候，精神纽带便会变为紫色，水母在帽子为紫色时会表现得有些晕乎乎的，这更证明了紫色代表着水母们兴奋的心情。
//绿色	恶心	绿色远没有蓝色和红色罕见，但也不算是个时常显露的颜色，这种颜色被发现时，Caedmal正在给刺剑水母们讲述一个在Level 3发现死尸的故事，这个故事让所有水母惊得叫出声来，它们大多数的帽子开始闪烁出绿色的光芒，Caedmal把这种情绪归类为了恶心。
//至此是所有记录在案的颜色，但据推测还有更多代表情绪的颜色待被发掘。
//
//此外，精神纽带还拥有着给予刺剑水母通过心灵感应进行交流的能力，野生的刺剑水母与普通的水母类似，不会说话，然而，一旦帽子被戴到它们的头上，它们便即刻能够通过心灵感应给任何附近的流浪者们发送信息借以交流。据推测是帽子上的宝石赋予了它们心灵感应的能力，但由于宝石很难从精神纽带中被分离出来，且在取下后便无法复原——这点在目前还不得而知。
//
//罕见性
//如前所述，精神纽带没有固定的出现地点，尽管该物品没有层级秘钥般稀有，但它们也不是一件易于找到的物品。值得注意的是，它们最常于Level 3被发现，据推测是因为该层级广负盛名的包括稀有物品的物资丰富性，如果你十分迫切地想要寻找一顶精神纽带，推荐在非无尽面积的安全层级内进行寻找，有限空间为寻找提供的便利性，外加上没有外部因素阻挠，这两点将最大程度地保障你能够成功寻得目标。
//
