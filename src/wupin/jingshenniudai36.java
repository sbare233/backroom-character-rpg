//属性：
//精神纽带拥有一项神奇的属性，它们能够改变颜色。在未使用的状态下，它们是灰暗且无色的，长时间的待机甚至能让它们周遭的区域也变成灰色。精神纽带在佩戴到野生的刺剑水母头上后便会被激活，帽子会以类似心情项链的效果发挥作用，随该实体的心情改变颜色。如下是失落一族一位名叫Caedmal Tun的成员提供的，对该实体性质广泛研究的调查结果。
//
//许多个月来，我一直在这片险境中如履薄冰，生命岌岌可危、那些我目及的玷污邪兽与我置身其中、如同噩梦般的恶劣环境让我夜夜难眠。不过，尽管这一切都糟糕至此，这片无尽的黑暗里边倒也仍有些宽慰我心的永耀光芒存在，你们称之为“刺剑水母”的可爱生物们给予了我一种久未体会到的愉快心情。尽管你们以这个名字称呼它们，但它们在很久之前便已被先人所发掘，并以他们自己的系统命名着这些生物，他们大部分把这一生物称作“Narcomeba”与“Cartavela”。我第一次与这些迷人的生物相遇发生在几年以前，我在探索阿拉伯沙漠时注意到了一件奇特的状况：一只水母正躺在一块石头上，因为脱水濒临死亡。我立即把我最后仅剩的水源给予了这个生物，并带上它走了很远的路，直到我们抵达了避难所。我在这片区域中心发现了一顶嵌着宝石的草帽，水母见到它后，开始尝试挣脱我的手去戴上那顶帽子，我帮助它完成了这一行为。水母在戴上帽子后突然获得了沟通的能力，并且协助我找到了通往安全地的路。自这趟旅途后它便一直陪在我身边，至今，我已经找到了有关这些动物的许多实例，并开始研究它们的外观、习性以及其他宽泛的领域方向。我希望我的研究对你们组织能有所帮助，也许有一天，我们将最终解明这些无辜造物所遭受的悲剧，并让它们与自己应得的宝物重新团聚。
//
