//精神纽带是一些能在石英象限内找到的小帽子，后者为蓝色通道一片神秘的区域，也是Entity 600的栖息地。这些帽子十分迷你，大概只有普通人的手掌大小，它们的帽身中心总镶嵌着一颗被赋予了强大力量的宝石，这是帽子力量的来源，失去宝石的帽子是没有特殊效果的。这些宝石尺寸秀珍，内里闪烁着银河系色彩的星云，它们采用了类似分形艺术的设计，放大后其形状的细化变换如同移动的龙卷风(1)。这些宝石不该与它们镶嵌的帽子分离，因为一旦分离后，它们便无法再被重新组合。如果宝石与帽子分离，这两件物品都会变得无用，宝石会变作一块黑色的石头。
//
//精神纽带一般外形为各色种类的帽子，样式从高帽、贝雷帽到棒球帽皆有存在。它上面镶嵌的宝石本身的触感与重量同尺寸相同的普通宝石无异，稍显沉重，但整体的质量相对平衡。宝石与帽子似乎是坚不可摧的，无法借助外力对其破坏，但当宝石与帽子分离后，它们便会失去能力，变得易于摧毁。此外，试图将宝石从精神纽带中分离是十分困难的，需要借助高强度的重型机械加以实行。这种宝石的单独性质未知，因为它们总是与帽子成对出现。
//
//这些帽子出现的位置十分随机，它们能在每一个层级被找到，从Level 3到地铁，甚至是黯洃銫都有它们的身影。这些帽子是如何流落至此现已不得而知，但可以推测其原因在某种程度上与蓝色通道有关，鉴于上述层级是刺剑水母的栖息地。M.E.G.对这种帽子的本质作用产生过一些道德上的考量，许多高层认为它们实际上是某种精神控制装置，且在当时几乎禁止了该物品在诸如Level 11的宜居层级被使用。随着对该实体的进一步研究，我们发现精神纽带并不会对该实体的精神状态产生影响，它仅仅充当着近似于心情项链(2)的作用，它能够表达该实体的情绪且让它们拥有通过心灵感应与人交流的能力。许多人把该实体的声音描述为一种可爱的，像婴儿般的嗓音，不过具体的描述却各不相同。
//
