//除去与Entity 600明显的联系之外，该物品还在失落一族被回收的文件与文物上被发现有存在痕迹，右图呈现的文物是一件古老的陶器，据推测，它表面绘制的图画便是与精神纽带分离的刺剑水母的模样，也有推测说这是它们族群中孕母的古画。值得注意的是，这幅画中宝石被镶在了水母的额头，而非帽子上，不清楚这是作画上的失误还是出于艺术的自由表达，亦或者，这是该神秘物品起源的一个线索。此外还有一些古怪的信件与手记，上面描述着深不可测的海底生物们与它们神奇的力量，许多文件在细节上把这些生物描绘作天使或是神祇，失落一族一些存活的成员们可以背诵这些描绘他们水神伟业的史诗神话，但这些传说与刺剑水母的联系——如果两者确有关联——却已随时间消却。该组织的很多成员都十分乐于帮助M.E.G.拼凑推理这些零散的信息。
