//半透明的东西在那个层级比比皆是，可是这样的枇杷果我还是第一次见到。它圆又大，颜色鲜艳，边缘却很模糊，就像一团发亮的云霞。
//我试着吃它，但是就像吃一个铁球，完全咬不动。可是它的母体真的是很漂亮很漂亮的一棵树。所以我把它埋在土里，希望能再长出一棵。
//就在那个时候，果子突然开始褪色。
//我起初被吓了一跳，然后惊异地看见一棵枇杷树幼苗破土而出。它长得非常快，每根枝条每片叶子都不断的抖动、伸展，在半空中盘虬起来。
//（叹息）
//那真是一棵很美的树。
//我试着伸手去触摸，但手穿过了它。原来这只是一段影像。当我了解这一点的时候，一位女子忽然从树后转出。
//她穿着绯色的古装，眉目忧郁，慈爱。她就那样抚摸着那颗枇杷树，上下打量。在她的眼角仿佛有泪，因为她在轻轻呼唤一个人的名字。她的手很白，很柔软，像那棵枇杷树一样透明。
//我一直看着她，感觉骨头里灌进了一些酸软的东西，就像酸酸的枇杷一样。
//我从来没有见过我的母亲。我的父亲在我很小的时候因为意外切出和我分开了。然后我就到了这个未知层级，东西都是半透明的，人也是。这些人养大了我，后来我知道，他们是异人的一支。但我一直没有关于我母亲的任何消息。
//可是当时眼前那个女人，她真的好漂亮，好温柔，我甚至觉得她看到了我，而且在冲我笑。
//但是我摸不到她，就像摸不到那棵树一样。影像渐渐消失了。就在那时候，我闻到一阵寒凉的芬芳，它一下子渗入身体，我不禁打了一个寒颤。说来也怪，我因为患支气管炎从小咳个没完，可自从闻到那种气味，就再也没有咳嗽过了。
