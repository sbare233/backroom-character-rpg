//特性及用途：
//当枇杷果被埋在土壤里，它将会由半透明态完全褪色。此时土壤上方将释放一段立体影像。
//
//影像结束后，周围会弥漫起枇杷膏2的气味，持续一刻钟左右。所有闻到这种香味的肺部疾病患者都将立刻痊愈。M.E.G.公共卫生委员会正在评估利用这一特性进行大规模治疗的可能性。
