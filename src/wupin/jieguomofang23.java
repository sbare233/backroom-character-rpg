//结果魔方表现为一种魔方，且在不同层级被发现时，总是被打乱的。打乱方式均是不同的，而且总是极难还原。结果魔方看起来都被大量使用过并且一般很陈旧。这些魔方可以在室内层级或少数室外层级中找到。结果魔方会散发出类似不新鲜的比萨饼或儿童生日派对的气味。该气味无法通过清洗去除。
//
//效果
