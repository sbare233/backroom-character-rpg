//Wi-Fi是Wireless Fidelity（无线保真）的缩写，是一种允许电子设备连接到互联网的无线网络技术。目前我们还不知道后室中的Wi-Fi的来源，也不知道谁在操作网络，因为所有试图追踪任何路由器或提供商的物理位置的尝试都被证明是失败的。某些层级的Wi-Fi似乎更强，这些层级中的大多数层级的窗户都在外部贴有Wi-Fi标志。你离这些窗户越近，Wi-Fi连接就越强。通常情况下，Wi-Fi网络只能用于访问后室的内部网络，但最近的发现报告称，它们可以与后室外的网络通信。目前就这一问题还在分析当中。
//
//Wi-Fi接入点的名称都有些奇怪，而且这些名称都与它们的所在的层级有关。在Level 0，最著名的Wi-Fi接入点被命名为“K38Suh9FH98mBKR”，其中BKR是Backrooms的缩写。所有Wi-Fi网络通常都没有密码，而且都是随机断开的。层级本身越不稳定，Wi-Fi连接就越有可能随机断开。
//
//目前，后室在它的网络上拥有多个平台。下面列出了两个最值得注意的。
//
//M.E.G.数据库
//M.E.G.数据库被广泛认为是最可靠的数据库，它用于访问围绕后室的层级、实体和其他任何异常情况的数据。该数据库可以通过任何可以连接Wi-Fi和浏览器的媒介访问，并且每当有新数据出现时都会频繁更新。在整个M.E.G.数据库中，有许多条目被拒绝访问，因为这些条目的数据尚未完整。
//
