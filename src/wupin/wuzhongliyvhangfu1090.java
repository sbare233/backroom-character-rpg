//描述
//无重力宇航服的外观为纯白色，外部材质与前厅普通宇航服相同，但其中不存在通风服、水冷服(液冷服)、气密限制层和隔热层。无重力宇航服的外部保护壳以及内衣舒适层之间填充着多个紫色的散发霓虹光的团状物，其为无重力宇航服的核心部件。这种光团不会受到任何重力的影响，当其被从无重力宇航服中取出时便会开始漂浮，但漂浮过程中会受到空气阻力的影响。
//
//无重力宇航服的外壳使其内部的霓虹光球与宇航服形成了一个整体，穿戴宇航服的人可自由控制宇航服本体与其自身获得与光团同样的特性。
