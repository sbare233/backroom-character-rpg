//%E5%AE%87%E8%88%AA%E5%91%981
//穿着无重力宇航服跳舞的后室娱乐员工
//
//1.无重力宇航服的左手手臂位置存在一个10厘米X7厘米的操作板，在重力正常的环境下穿戴无重力宇航服时可通过调整其上方的转动开关来调整自身所受重力大小。无重力宇航服同样可在太空环境中充当宇航服使用，此时其不存在任何特殊作用。
//2.启动无重力宇航服前请先检查是否穿戴完整以及其是否存在未被完全密封的部位，如果在不完全密封的情况下启动无重力宇航服，穿戴者将被宇航服内外两种不同的重力撕扯并死亡。
//3.无重力宇航服的手臂操作盘上的开关旁有一个保险旋钮，在旋钮被打开前无法扭动转盘与启动宇航服。
//4.转盘共有5个档位，分别为“0”、“1”、“2”、“3”、“4”。开关初始状态时指向“2”，这是正常情况下宇航服与其穿戴者所受的重力，将开关扭到“1”档会使自身所受重力下降至原本的1/2，调到“0”时则完全不受任何重力影响。将开关调至“3”档位时，宇航服及其穿戴者将会受到原先4倍的重力，此时穿戴者通常会被压得举步维艰，如在此基础上将开关调至“4”，穿戴者会受到相当于自身8倍的重力并因此内脏破裂而亡。
