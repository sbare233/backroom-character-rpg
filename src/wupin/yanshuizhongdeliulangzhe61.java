//Object 61 - 盐水中的流浪者
//物品概述
//Objects 61.1 到 61.12 是一组帆船。它们可以独自在陆地或者水面层级上自行移动，不需要船员，也不需要船长。
//
//描述
//Object 61 或称为布瑞森的船只。是以已故的B.N.T.G.的食品部负责人阿加莎-布瑞森命名的一系列物品。符合下列标准的都可以被归入此物品：
//
//1. 这些物品一般可以被统称为“帆船”。每个物品的品牌和型号不同，但是都可以与前厅中的物品样式对应上。
//
//2. 当这个物品进入任意一个层级时，其任何表面都不会有动物、植物、微生物或者真菌的痕迹。
//
//3. 对于上述物品，在它们所出现的环境或者层级中，没有关于上述物品的预先记录或者描述。它们也会改变周围的环境来“解释”它们为什么在水外，尤其是在陆地地区。
//
//4. 这种物品有着轻微或明显的磨损。
//
//5. 这种物品不需要其他人的协助就可以进入或者离开任意一个层级，只要它们想这么做。
//
//任何符合上述标准的物品，尤其是符合上述标准1和标准5的没有被记录的物品，应当立刻拍照记录下来，以便更好的对 Object 61 进行分类或者调查。但是非常不鼓励对任意的 Object 61 进行深入探索，因为进入任何已知的或者疑似为 Object 61 的实例都是十分不安全的。
//
//Object 61 的实例是按照其发现的顺序用数字标注。到目前为止，已经记录下了12个实例。Object 61.1 是在1990年9月，由Booreson首次记录下来的。每次发现新的实例都会比上一次发现晚2到51个月。
//
//值得特别注意的是 Object 61 的实例在层级之间移动的路径。除了几个例外，通常一个实例在两个层级之间的路径是已知的，而人类也可以通过物理或者非物理手段来追踪，虽然这样十分困难。1虽然直接使用 Object 61 来进行层级间的移动对人类来说几乎是致命的，但是通过对它们在后室中的行动的记录，可以收集到更多关于在层级之间移动的信息。
//
//Object 61 的“消毒”过程
//在 Objects 61.1 到 61.12 停留于某个层级时，其上经常会覆盖有一些生命力较强的东西（像是细菌、藻类、苔藓等。）然而，对上面的生物物种进行抽样年龄测定发现，在层级之间的移动过程中，该物体上存在的所有生物都被清除或者被消毒掉了。
//假设这也适用于人类。出于各种原因，对这种假设的任何测试都是高度不可取的。
//
//Object 61 的实例在到达某一层级后，可能会在原地停留几个月甚至几年。同样的，它们也有可能在类似的时间里消失。Object 61 的实例一旦离开该层级，就没有可靠的方式去追踪，所以很难预测它们的去向。一般来说，我们可以安全的假设它们在没有记录的层级或者未知层级中的某个区域通过未知的出口重新出现。
//
