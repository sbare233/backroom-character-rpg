//Object 61.9
//日志：2014/1， Level 130
//Object 61.9 被发现于 Level 130 中一个大型拱顶游泳馆内，其安装有多个天窗，而该结构被认为是该物品的另一种表现形式。值得注意的是，Object 61.9 的船体上有几道很长的裂缝。
//
//该物品所在的被染成了不同寻常的茶色，同时池边还有一些茶色的污渍。通过进一步的测试表明，池水和污渍中都含有一定数量的体液，但是不属于任何已知的生命形式。
//
//日志：2019/7， Level 499
//Object 61.9 在 Level 499 被发现。这次它将这个层级的下层地下墓穴围成了一个被淹没的洞穴。而之前出现过的裂缝也消失了。
//
//随后许多报告提供给了M.E.G.，同时表达了对许多“神爱之繁生”成员被困在大迷宫表达的担忧，据估计大约是 Object 61.9 的干扰才导致这样的结果。在2019年7月30日，“导火索”小队的Rigel 和 Arcturus已经被派往Level 499 进行紧急处理。
//
//日志：2019/8， Level 499
//Object 61.9 和之前一起出现的洞穴在五周后一夜之间消失了。随后该地区又恢复了它原本的迷宫状态，同时被困人员也被解救了出来，没有人员死亡。
//
//在最初被困的30人中，有28人已无生命危险，还有2人正在紧急护理中。大多数人都表现出了感官剥夺和饥饿的暂时症状。可以将他们得以活下来的原因归功于他们在封闭的地下墓穴可以直接接触到泉水和植被。
//
