//主动攻击系统：
//散射爆破器：
//散射爆破器由发射器与蓝砂晶榴弹构成，榴弹内部嵌入了一枚火盐晶体。当弹药被击发时，由于蓝砂的耐高温性质，蓝砂晶外壳并不会被破坏，但内部的火盐晶体会因高温爆燃，将蓝砂晶外壳破坏并一齐散射出去。由于蓝砂的危害性质与火盐晶体的爆炸反应，这一操作通常会对敌对实体及建筑物造成大量伤害。
