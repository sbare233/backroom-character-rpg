//扼杀者意大利饭
// 
//
//材料：
//1磅扼杀者肉，生熟皆可
//1夸脱扼杀者高汤
//1汤匙植物油
//3汤匙无盐黄油，分放
//1颗中号青葱，切碎（约1/2杯）
//1又1/2杯香米
//1/2杯白葡萄酒
//1/4杯磨碎的帕尔玛干酪，一些额外的干酪用于刨花
//盐，调味
//1: 若需要烹饪扼杀者肉，最建议的方式便是水煮。加一些香料，如蒜、生姜和一些葱来添加风味，若生吃请跳过这步。
//
//2: 除扼杀者肉外准备好每一种材料，放到一旁，将扼杀者肉切成小块，备用。
//
//3: 在平底锅中，用中火加热高汤，待高汤温度上升后调至小火，不要让高汤沸腾。
//
//4: 在另一个大锅中，用中火加热植物油和1汤匙黄油，将葱煸炒2-3分钟直到其变成半透明状。
//
//5: 加入香米，继续煸炒1-2分钟，不要停止翻炒，以避免大米变成棕色。此时，香米会散发出坚果的香味。并被植物油完全包裹，另外也可以加入一些杏仁水来调味。
//
//6: 将白葡萄酒慢慢倒入锅中，继续烹饪1分钟，不要停止翻炒，直到白葡萄酒全部吸收到香米中。
//
//7: 用勺子将高汤倒入锅中，不断翻炒，直到香米将高汤吸收，每次加入的量不要超过1勺。这一步非常重要，千万不要停止翻炒，不然米饭会烧焦。
//
//8: 等到米饭几乎烧干后，再加入一勺高汤，重复这个步骤，直到高汤全部倒完。其过程大约需要20-30分钟，可以在过程中品尝，当香米有嚼劲后即可。
//
//9: 当所有高汤倒入后，加入剩余的黄油和帕尔玛干酪，按自己的口味用盐调味。
//
//10: 拌入鸡肉，盛在小碗中，配上额外的帕尔玛干酪即可享用这道美食！
//
// 
//
//评价：
//
//
//意大利饭做起来相当不容易，但绝对值得一试！如果没有更好的方案，扼杀者肉便是鸡肉的美味替代品！烹饪意大利饭的过程将会十分枯燥乏味，而且原料（尤其是扼杀者肉）很难获取，但是当一切都完成后你绝不会后悔！快享受这道美味佳肴吧！
