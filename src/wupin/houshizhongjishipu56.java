//肉饼
//1磅格拉夫肉馅
//盐
//切达芝士
//美国芝士
//格外的配料，如西红柿、生菜、洋葱等
//1: 首先我们从做汉堡胚开始，先将豆奶加热至微温，将酵母倒进去，静置3分钟。
//
//2: 倒入3又1/2杯面粉，取出5汤匙，然后倒入来自麦田的5汤匙谷朊粉，将其混合在一起。
//
//3: 在大碗中倒入刚才的混合面粉、糖和盐，若你有搅拌器，可以开始慢速搅拌，直至所有材料混合，然后慢慢倒入植物油、酵母混合物、鸡蛋和蛋黄，继续搅拌直到其成团。
//
//4: 将面团揉成球状，放入一个抹满油的玻璃碗中，盖上盖子，在明亮温暖的地方醒1小时。
//
//5: 将面团压在洒满面粉的案板上，将面团分成6块，然后揉成圆形。
//
//6: 将面团放在托盘上，盖上盖子，再放30分钟，将烤箱预热至375℉。
//
//7: 打开盖子，在面团上刷上蛋液，放入烤箱烘焙15分钟。
//
//8: 从烤箱中取出，刷上黄油。
//
//9: 开始做酱汁，洋葱切丁备用，用中火加热大平底锅，直到油完全覆盖锅底。
//
//10: 将洋葱煸炒几分钟直到透明，加入盐，关火。
//
//11: 将所有材料，包括洋葱，搅拌到单独的碗中，直到其完全混合。
//
//12: 开始做肉饼，取1磅格拉夫肉馅，放入大碗中，分成8份。
//
//13: 用中火加热平底锅，不放油，将1份肉馅放到平底锅上，压成饼状。
//
//14: 调味，煎2分钟。
//
//15: 翻动肉饼，加入奶酪，再煎1分钟。
//
//16: 将肉饼从锅中取出，开始组装汉堡。
//
//17: 在下半部分汉堡胚中加1勺酱料和第一块肉饼。
//
//18: 第二块肉饼重复步骤13-16。
//
//19: 加入第二块肉饼，然后淋点酱汁。
//
//20: 添加任何你想添加的配料，并盖上汉堡胚顶部。
//
// 
//
//评价：
//
//
//鲜嫩、多汁、美味，这个汉堡绝对会是你每天必须的美味！许多食材，例如谷朊粉和格拉夫肉，都可以非常容易地在麦田中获取，而且格拉夫肉是牛肉的最佳替代品。此外，你也可以试试用小麦面包作为面包胚，或是加更多的肉，比如培根！你还在等什么？架上烤架，带着你的兄弟一起去做格拉夫堡吧！
