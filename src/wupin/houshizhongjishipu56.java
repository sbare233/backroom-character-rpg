//材料：
//汉堡胚
//2又1/2茶匙酵母
//1/2杯原味幸运豆奶
//1/2杯水（水更好，但杏仁水也可）
//3又1/2杯面粉
//5汤匙谷朊粉
//1又1/2茶匙盐
//1汤匙糖
//3汤匙植物油
