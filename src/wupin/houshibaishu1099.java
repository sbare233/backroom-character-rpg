//但是，碍于后室环境、WiFi强度以及多种不稳定的现实层面之制约，后室白鼠的运作极其困难、也根本无法承载多个程序。在经由讨论决定后，M.E.G.最终决定仅将后室白鼠用于一个用途：运行“白鼠”程序。后室白鼠遂因此得名。
//
