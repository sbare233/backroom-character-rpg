//后室白鼠的原机与改装材料等并非由M.E.G.持有与提供，而是直接取自被“龙血族”毁灭的Level C-[数据隐藏]废墟；此层级的原生环境原本拥有着现阶段M.E.G.尚且不及的发达科技水平。然而，该层级遗留了大量可供回收的机械设备，甚至还发现了一台尚存修复希望的巨型机。M.E.G.仰仗多位人员的学识与不懈努力后，终于在[机密日期]得以将其改造为如今的后室白鼠。
