//该产品早期投产提及过一种透明血液，从当时的测试数据来看透明血液有等同甚至超越黑色血液的供应力，但仅登录后短短数日便因不明原因迅速停产。有识血肉未对此事进行任何解释并拒绝公开接受过该类改造的人员名称，研究者在一部分截获的无限意识通信中发现该类型血液似乎跟有识血肉的超自然理论“灵魂虫”有很大关系。
