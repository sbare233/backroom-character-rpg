//标准血红蛋白提供中肯的流动速度和输氧能力，没有什么特点也没有什么弊端，因为这是无机物文明原本就有的血液所以对身体的适配度也是最高的，上述改造仍然会保留少量红色血液作为基本调配。
//
//浅黄
//该颜色的血液含有更多的无机盐及高含量激素，大大增强使用者的爆发力，配合白色血液使用时可以有效提高使用者的综合运动素养，但如果单独使用耐力会明显下降。同时身体中的环境能让大多数细菌类生物迅速死于脱水因此对于常见的寄生虫和病菌有较强的预防能力，甚至可以抵抗一部分特殊病菌。
//
//黄色的另一种可能：前厅记录中提到少数人生因为血液中缺少抗原天生存在金黄色血液，可以自由供给他人输血，但也可能造成一些身体不便。由于这种基因极其稀少，目前尚未记录到此基因，我们也正在向人类征集拥有这种基因的蓝本。
//
//白
//该颜色的血液中更高的血脂含量，赋予血液更强的粘稠性和养分供应能力，安装此类血液还会附加一系列脏器改造保证血脂仅存在于血液中溶解而不形成其他问题。这使得受改造者能更好的控制自己体型，同时由于血脂含量增加神经系统可以更方便的获取养分，神经生长速度会略微增加， 这会让神经有更强的传达能力，也能在一定程度上促进大脑发育。
//
//绿
//该颜色的血液中有更高的胆汁素含量，同样需要为内脏覆盖少量保护器官，以保证身体正常运转，相比起正常颜色的血液，这一颜色的血液明显粘稠的多止血速度更快，但输氧能力略有下降需要配有更强输氧能力的血液共同调配。
//警告：该血液对未经过相关改造的人员有一定毒性，可能会造成器官衰竭等副作用，若是经常涉及战斗的人员使用请在身上进行相关标准。
//
//- 关闭
//是的，学者兄弟们确实尝试过使用叶绿素而非胆汁素来完成绿色效果，但是实践证明那一丁点穿透皮肤射进血液的光量吸收以后再转化成能量，对于整个躯体的能量需求就是九牛一毛，反而会增加一系列技术上的问题所以最终这一计划被废止了
//
//蓝
//该血液中的铁元素被替换成了铜元素，在常温环境下供氧能力比红色血液要弱，但是对低温高寒和缺氧的恶劣环境如level-c-271、level-c-272、level-c-273、level-c-274有明显更强的抵抗能力。由于更换内容较少对于身体的适配性也非常高，可以在身体中适当加入该类血液来提高环境抵抗能力。
//
//黑
//在与被称为龙血族的特殊实体组织进行大规模敌意接触后，有识血肉成功采取其中的一部分基因组，加上近期被发现的被称为特斯共生体的特殊类真菌生物高度自我活动的基因组共同形成的奇迹技术，相比起普通血液有匪夷所思的二氧化碳吸附能力：根据检测黑色血液可以运载数倍于红色血液的氧气，这也使得更粘稠的血液成为可能。
