//该类型的血液只是单纯的在血桨中加入不会被吸收，可以吸收光量并释放的荧光酶，不会给使用者带来任何额外的特殊效果，但是能够让皮肤下层和毛细血管表层表现出轻微的荧光。近期更替过的产品中这些微光可以以一定频率变化，在保证美观的同时能起到一定程度上驱散蚊虫的功效，对于居住在热带或是潮湿区域的探索者很有作用。
