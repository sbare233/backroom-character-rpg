//获取：
//博罗利亚虹石矿的获取并不困难。希望得到这种矿物的流浪者只需前往本文档“描述”部分所提及的几个典型层级，乃至于一切符合光照及地形条件要求的层级，仅花上几个小时稍加探索，就有较大的概率能寻找到他们想要的东西。至于如何制备不同状态的博罗利亚虹石，也可见于本文档“性质”部分，流浪者跟随其指引即可完成一系列的转化操作，故不再重复赘述。
//
//不同于前厅中的常见天然硫铁矿物，一般而言，博罗利亚虹石矿的纯度已经足以使其展现出几乎全部的特性，所以尝试提纯博罗利亚虹石矿是无甚价值的。不过，极高纯度22的博罗利亚虹石会呈现出奇异的、且往往被描述为“赏心悦目”的彩色，具有极高的观赏性，因而可尝试将其制作为机巧精致的摆件。23提纯的方法为将复态博罗利亚虹石粉碎后施加强热，投入耐高温的反应釜，加热直至1900K到2100K的温度区间24，等待杂质化合物气化析出，然后让博罗利亚虹石自然冷却凝固。这一过程成功的标志是在冷却过程中液态博罗利亚虹石开始显现出各种颜色，发出的光线颜色也随之改变。正因为这一过程中流动着的彩色液态虹石极富美感，高纯度的博罗利亚虹石又被称作博罗利亚流彩石。
//
//由于在博罗利亚虹石精炼过程的初筛阶段中重力选矿和磁力选矿无法发挥太大作用25，而电解提纯和化学提纯均不适用或无法实现如此高的纯度要求，这种原始的沸点分离法实际上是目前最高能效的精炼手段。当然，其耗能之高也是可以预见的，因此在条件恶劣的后室中，任何人都不被建议进行这种不具备实际意义的实验；目前M.E.G.正在寻找蒸馏法提纯博罗利亚虹石的高效替代方案。
//
//在某些情况下，流浪者同样可以通过将博罗利亚虹石与空珀以任意比例混合一同加热至823K以上来实现空珀的转化效应，以期获取更多的虹石。通过这种方式获得的博罗利亚虹石状态与作为拷贝模版的虹石物态将保持一致，然而转化而来的博罗利亚虹石纯度仍然与转化前的空珀相同。就目前C层群的交易市场行情而言，空珀的价值要远高于等重的博罗利亚虹石26，因此如非必要，一般情况下鲜少有流浪者会尝试进行此反应。
//
//用途：
