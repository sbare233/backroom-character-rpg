//描述：
//瑞撒毒气是一种极其危险的生化武器，无羁常在与其他团体的冲突中使用。该气体呈浅红色，与一氧化碳的流动性相当，可在特定介质中以近卷须状向周围相对热源的位置扩散。到目前为止，尚不了解瑞撒毒气的合成方式，但有不可靠传言称这种气体可由混合悲尸的淤泥和一种未知溶剂（据说是腰果水或苦痛汁液）制得。目前尚未在后室发现天然的瑞撒毒气矿藏，但其创造/收获应归因于U.E.C.，因为在已知团体中他们最擅长对其进行利用。
//
//瑞撒毒气的特性将在被吸入后表现出来。吸入其的受害者将立刻陷入悲尸循环，其进行速度显著快于接触悲尸体液，在吸入的50秒内就会发生完全不可逆的转化。已知在该转化期间受害者对毒气的反应包括肌肉组织的快速流失，皮肤组织的剥落（可深达真皮乳头层）及经由人体孔道的大量失血。该气体的所有使用记录都以人类为对象，故瑞撒毒气对实体的影响仍不明。
//
//截至本文档撰写时，仅有一位瑞撒毒气幸存者。下附对其的访谈记录，以供参考。
//
//附加信息：
//对幸存者3和M.E.G.生命线成员Mx. Harper Vivace的采访记录。
