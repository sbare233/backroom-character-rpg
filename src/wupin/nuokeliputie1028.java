//当其中一片诺克立普贴黏贴成功后，使用者半径2米内的其余诺克立普贴均会溶解，因此不建议携带过多的诺克立普贴或在其存储点周围使用。黏贴超过24小时，或使用超过两次后，同样会导致诺克立普贴溶解：它会缓缓地将自己折叠并慢慢融入墙壁，随后使用者正常切出的可能性将减小，目前已有部分速切玩家因滥用诺克立普贴丧失自然切出的能力。未成功溶解的诺克立普贴会有极低的概率转变为灰色。
