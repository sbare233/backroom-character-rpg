//黑星声称此物品的工作原理是通过干扰黏贴平面的量子不稳定性从而增加切出概率，但尚缺乏实验证明这种说法是否准确。根据B.F.P.F.基于量子测不准原理对于此物品在黏贴时的构成系统的度量张量函数分散性的测定结果证明了黑星的理论：
