//将人造诺克立普贴背后的防粘纸沿着虚线撕下，黏贴在较为规则的平面3上后即可使用。若防黏纸撕下后30秒内未进行黏贴，其黏性物质会凝固，从而导致诺克立普贴失效，并在一段时间后溶解成原液。尝试回收已黏贴的诺克立普贴4也会导致此类事件的发生。
