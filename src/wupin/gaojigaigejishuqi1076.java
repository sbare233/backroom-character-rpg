//描述
//高级盖革计数器为拥有黄色塑料外壳的一类电子器械，有着正常的盖革计数器的外观构造，其面盘上存在一个数码显示屏与两个位于侧面的橙色按钮。已证实高级盖革计数器的外壳具有极高的结构强度与极强的耐热耐腐蚀性，可以保证其在绝大多数情况下能够正常工作。
//
//用途
//高级盖革计数器拥有三个档位，每个档位都具有不同的功能。
//
//测量辐射
//高级盖革计数器的第一个档位功能为测量周遭环境的带电粒子辐射数值。在此档位下，该物品的功能与正常的盖革计数器没有区别。
//
//测量实体生成概率
//高级盖革计数器的第二个档位功能为测量附近环境的实体自然生成概率，测量结果显示为整数，其测量数值越大附近区域实体自然生成概率越小，包括不具备攻击性的实体的生成概率。
//
//以下为统合了一定数据之后得出的结果，注意：该表格内容不一定绝对正确，在一些特殊层级中数值可能出现偏差。
//
//实体情况	无实体或极少量实体	少量实体	中量实体	大量实体	多到难以计数
//数值结果	11800-6500	6500-3900	3900-1400	1400-170	170-1
