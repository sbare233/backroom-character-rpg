//
//测量自然切出概率
//高级盖革计数器的第三个档位功能为测量其头部所指区域的自然切出概率,其测量结果为与切出概率成正比的一串数值。当该物品指向已经贴在墙上的诺克立普贴时，其数值会显示为“FULL”。
