//
//当它们外出活动时，手中会握有一瓶加有加有某种由Level C-224内部产生的食用酒精的杏仁水，这时它们会表现得与前厅中喝醉的人类一样。但请不要攻击它们或是抢夺它们手中的杏仁水罐子，当宿醉者手中的杏仁水被抢走时，他们会无差别地对抢夺者发起攻击。经过检测，一个正常的宿醉者在被激怒时它们将会大幅度提升他们的力量，大概与三个成年男子的力量总和持平。
