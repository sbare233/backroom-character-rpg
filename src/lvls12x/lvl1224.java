//偶尔，你会在天亮时见到他们。这时它们将保持高度的善意，会将自己调配出来的“特制”杏仁水赠予你。这些杏仁水的味道是固定的，似乎是在Level C-224中的某个地方产出的，瓶子上的标签会或多或少地受到一些损坏，这并不影响引用。经流浪者口述，杏仁水的味道与“没有了气的汽水一样，但要更淡一些。而且质地似乎要更加浓稠，总的来讲它的度数不高，但与其他酒类相比会有令人上瘾的感觉。“
