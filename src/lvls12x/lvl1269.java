//流沙坑
//有的时候，该层级内会形成一些或大或小的沙坑，这些沙坑极其危险。基于这些沙坑的特性，我们将其称为“流沙坑”。
//
//当流浪者踏入流沙坑中时，其颜色会被扭曲并快速在其身体中央汇聚为黑白颜色的漩涡。在此过程中，流浪者的位置会以一种超乎常理的方式向流沙坑中心靠近。当其位置与流沙坑中心几乎重合时，流浪者身体上的漩涡将会连带其肉体飞入流沙坑内部。一般情况下，这会导致流浪者死无全尸。
//
//海市蜃楼
//与大部分沙漠一样，Level C-269中可能会出现海市蜃楼。不过与之不同的是，Level C-269中出现的景象是固定且真实的。在海市蜃楼出现时，流浪者正前方的天空会幻化出一座金碧辉煌的宏伟神殿，流浪者可以进去进行短暂的休息，在神殿中能够发现一些物资，例如杏仁水与火盐。任何事物在神殿中均会停止褪色，但离开后会加速褪色效果，该神殿最多存在12小时。
//
//实体
//Level C-269中不存在任何实体。有传言称此层级自身就是一个实体，不过这并未被证实。
//
//基地，前哨与社区
