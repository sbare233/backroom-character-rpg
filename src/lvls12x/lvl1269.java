//描述
//Level C-269是一片极其广阔的沙漠，沙面上有一些零散的骨骸，与一般沙漠不同的是，Level C-269的沙子颜色为灰色，而且沙子颜色会跟着对此层级的探索而逐步加深。探索的距离越远，沙子就越松软。这些柔软的沙子会使得流浪者疲惫并感到无助。该层级没有严格意义上的白天及黑夜，日月颜色永远保持灰色且难以辨认。
//
//Level C-269的天空中云层稀少且无任何颜色，不过这些云雾仍具有能够降雨的特性。这些降雨是该层级可饮用水的唯一来源。值得一提的是，降雨的时间几乎是固定的，每两次降雨的间隔大约在10天左右，每次降雨量大约在1.5毫米左右。
//
//绿洲
//在该层级中有可能会遇到“绿洲”，绿洲内长有大量植物且环境多变。绿洲内通常会出现小型湖泊。请务必注意一点，不论是湖泊中的水还是绿洲中植物均具有毒性，食用这些物品会产生持续1-5小时的腹痛，晕眩，呕吐等症状，杏仁水可以有效的缓解它们。在绿洲中有可能会出现1-3栋废弃的建筑，建筑中将存在杏仁水之类的生存物资。
//
//绿洲中的环境与事物均为灰黑色。
//
//失色现象
//在进入Level C-269后的大约48小时后会开始出现"失色"症状，携带的物品将会随时间氧化，身上的颜色也会慢慢失去。在失色的同时，流浪者体内的养分会被沙漠所吸收——用于加深自身沙子的颜色。当流浪者完全失去颜色时，其养分已被完全吸干，身体上的皮肉也被氧化，留下骨骸并化作沙漠的一部分。
//
//在失色的过程中，补充养分可缓解此类症状，但由于该症状为层级特性，故无法完全根除，只有离开该层级才能够摆脱。
//
