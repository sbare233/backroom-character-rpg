//该地周边的云雾现推测无任何异常性质，由自然界普通水分子组成的水汽，小水滴，和小冰晶等，能见度大部分时间都低于1500米。由于山体部位高于云层，且之上由于未知原因并没有云雾的组成，所以山体部位的年降水量几乎为零1。该层级的云雾通常是高层云2和高积云3。
