//出口
//於園區南邊的女廁第三個隔間中，先關上門隨後拉下沖水，之後打開廁所門將會切入Level C-82
//在園區北邊的過山車等候區，擲3次以內三顆骰子1皆是6的情況下2便能切入Level C-8。
//進入園區東邊的海盜船設施便會切入Level C-214
//進入園區西邊的鬼屋設施走到深處，打開工作人員專用通道的門會切入Level C-444。但鬼屋設施中常有大量的魔童及紅杉妖精出沒，還請流浪者格外小心。
