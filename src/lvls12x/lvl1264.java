//透过飞船前端控制室的玻璃可以看到外部的太空，飞船前方常有陨石飞来，可通过操控武器系统将其摧毁。但当该层级没有流浪者存在时，不会有陨石击中飞船，原因未知。控制室中有一台显示器，两个座椅，以及一些不知道怎样使用的按键。在控制室中，Wi-Fi信号非常强，任何电子设备均可连接。
