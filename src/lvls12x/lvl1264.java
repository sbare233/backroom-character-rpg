//储藏室中有几排货架，上面放有一些食物，如皇家口粮、杏仁水等。奇怪的是，在储藏室中，Wi-Fi信号微乎其微，摄像机会受到强烈的干扰，无法正常使用。储藏室中的杏仁水相当一部分是有毒的，无法饮用。有报告称在一些储藏室中，有些货架可以移动，移动后原处会出现一扇橙色的大门，推开即可进入其他封闭的舱室。
