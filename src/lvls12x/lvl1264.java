//Level C-264的主体为一艘大小约为1000平方米的太空飞船，飞船内部大部分设施已损坏，在M.E.G.探险队员的修复下，部分设施恢复正常。飞船内部的颜色大多为银色、灰色和深蓝，部分舱门无法打开，其余舱门可打开的舱室多为储藏室。
