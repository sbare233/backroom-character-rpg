//一些上述实体在Level C-273中生成。
//
//该层级极其寒冷，M.E.G.观测到该层级温度在-196º~-210ºC之间。因此该层级没有可供给呼吸的空气，建议前往Level C-273时携带呼吸设备以及可靠的极端防寒装备2。
//
//Level C-273存在周期约为16.3时的昼夜更替，但日光辐射所提供的热量远不足以温暖这一层级，仅能使少部分表面的液氮蒸发形成氮气雾。
//
//由于此层级的极端环境，M.E.G.对于此层级的探索以短时间目视或无人机观测为主。根据观测结果，暂时将Level C-273分为海洋、海岸及山脉三部分。
//
//海洋
//Level C-273的海洋如前文所述由液氮构成，似乎占据了Level C-273的绝大部分可观测区域，面积可能趋于无限。此海洋拥有与普通海洋类似的海床结构，最深处可能有约3000m3深。海洋中似乎没有任何其它物体。
//
//海岸
//Level C-273中的主要可探索区域，地表由致密的雪和冰屑组成，因此可以放心在其上方站立。此区域暂时不存在任何形式的建筑物或道路。暴风雪4有时会侵袭这一区域，造成视野受限。
//
//山脉
//Level C-273中距海岸约30km处似乎有一座山脉，M.E.G尚未能够对此区域进行探索。与Level 420不同，这一山脉似乎不存在类似于山洞或裂谷的结构，并终日被氮气雾环绕。此山脉平均海拔约为2000~3000m5，地势似乎较为陡峭，而山体似乎较为平整。
//
//实体
//由于此层级的极端环境，本层级不存在任何常规的有害实体，目前观测到的有且只有一种此层级的特有实体，其外观类似于结冰的肥皂泡，但似乎无法被击碎。该实体能够自发在Level C-273中生成，并通过某种未知的方式飞行。尝试将其带离Level C-273将导致实体死亡。出于可能的对于该层级生态的保护需要，M.E.G.建议不要将这些实体带离Level C-273。
//
//本层级除极端环境外没有任何已知危害，因此该层级被推测可作为一个液氮等低温物质的稳定来源6，但不建议流浪者在未经充分准备的情况下擅自进入。
//
//基地、前哨与社区
//M.E.G.“低温应对小组”
