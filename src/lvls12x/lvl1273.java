//
//描述
//Level C-273的外观类似于前厅中的海洋，但海洋中的水完全由液氮1所代替，沙滩则似乎由致密的雪和冰屑所代替。目前仅对Level C-273进行了小范围探索，推测该层级可能是无限的。
