//伪人
//伪人是 Level C-276 大陆状貌时社会的基本组成者，它们往往有着与人类同等的体型与行为举止，并呈现出常规的情感变化。其他情况下伪人也存在其他外表，如侏儒或体型较大的龙形生物。除非人类要求，否则它们对人类并不具有任何意义上的攻击性。
//
//但据守书人介绍，伪人不存在任何意识或记忆能力，在其内部存在判定机制以确定它们在不同情况下的不同行为与表情，研究负责人 Dr.Bright 倾向于它们是一类哲学丧尸1，这一说法目前被较为广泛的接纳。伪人将在每次梦境结束时中随大陆一同消失，并在梦境开始时随大陆一同出现，尚无法判定每次出现的伪人是否相同。
//
//这类实体相较于人类而言存在一明显特征，即在他们的右脸颊下方有一个五芒星印记，这一印记不显眼但可被轻易发现。这是在 Level C-276 中唯一一种辨认人类的方式。
//
//守书人
//守书人被描述为一个身着绿色斗篷、长满络腮胡的男性。他自称为 Level C-276 本体的化身，他对流浪者或做梦者都保持友好态度。该实体具有对本层级的一切调控能力，梦境结束时他表示乐于使做梦者在梦境冒险中感到快乐，并表现出悲伤，希望其「尽快再次来访」。他十分热衷于讲奇幻故事以及形形色色的在本层级发生的故事，他最经常讲的故事是一个刚进入梦境就找火山口跳入然后强制自己醒来的人。
//
//然而，强制闯入核心演绎地带或冒犯性的行为仍会引得守书人反感并导致拉入黑名单，这将使被拉入黑名单者无法再切入层级。
//
//百年梦境
//早期对于核心演绎地带并未有过多要求，在其中生活的三名人类往往可以接近做梦者，甚至与其建立一定的关系。随后守书人对此有了严格的规范。即「进入梦境生活的人类不得与做梦者存在任何交集」，这一规范的制定普遍被认为来自持续时间长达两百年的迄今为止最长梦境，一般被记作「百年梦境」。
//
//前厅回合制游戏爱好者 Bera 教授为此梦境的做梦者，守书人称其「有着那么活跃的想象力！」，因此百年梦境十分广阔与宏大，Bera 教授征服野兽、解救一名由当时生活在梦境中的人类扮演的公主 Claier，并与其成家立业，建立王国。尽管守书人一再提醒梦境的虚伪性，他仍拒绝醒来。
//
//「他在幻想世界里陷得如此之深，让我很快意识到他对现实世界而言更满意这里。我告诫他这都是幻觉，不过他不听我的。他说如果他被迫离开，他会立即结束自己的生命。我试着让他保持快乐尽量久……」守书人说道。
//
//在二百年后，极度不稳定的梦境走向崩塌，Bera 教授离开 Level C-276，Claier 被强制召回到 Level 1，在回到 Level 1 后她似乎处于失忆状态，时任项目负责人 Dr.Bright 向她解释了她在哪里以及她是谁，数分钟后，Claier 恢复记忆，并于当晚在洗手间使用一捆绳子将自己吊死。她的遗言被写在手机备忘录中：「致我亲爱的 Bera 国王，我永远回不去了。」
//
//Dr.Bright 递交的报告中描述了上述内容，并在结尾处写到：
