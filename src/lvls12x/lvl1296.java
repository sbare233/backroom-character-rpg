//房间
//防空洞内共有六种不同类型的房间，它们分别是武器室，餐厅，储藏间，医务室，娱乐室与休息室，除此之外，该层内同样也有一些没有名字且上锁的房间，这些房间的门无法被破坏，任何物理攻击都会导致流浪者被吸入且传送至未知的楼层。
//
//武器室：
//
//武器室内没有任何的光照来源。并会在其中随机刷新少量的枪支，弹药与军用短刀，但每当流浪者想要拿起枪支时，将会有一种不明的眩晕感。直至真正拿起时，流浪者的手会有一种莫名的强酸侵蚀感，与上文提到的灼烧感不同的是，这会立马使你的手被腐蚀以至其完全断裂。不过当你触碰弹药，军用短刀时则不会有任何影响。目前尚不清楚为何触碰枪支将会导致流浪者受到伤害。
//
//除此之外，武器室时常游荡着大量的钝人，它们展现出了惊人的再生能力与攻击能力，一般情况下它们不会轻易攻击流浪者。建议流浪者在非必要下不要试图激怒它们。
//
//储藏间：
//
//储藏间中储藏了大量的物资，包括但不限于杏仁水，手电筒，电池，压缩饼干，能量棒，过期的罐头等，但是长期呆在该房间会受到不同程度的精神影响，轻则出现人格分裂，幻听，重则出现幻觉，并在最终七窍出血而亡。值得注意的是，该症状只可通过杏仁减轻，不可消除，且这种风格症状会伴随流浪者就进入的次数而叠加。除非是在物资非常缺乏的情况下，擅自进入储藏间并不是一个明智的选择。
//
//餐厅：
//
//整个餐厅占地约为100平方米，其中后厨的门已被锁上，不可被强行进入。餐厅整体呈圆拱形，其内部灯光昏暗，十分的压抑。墙壁周围贴满了长度不一的管道，这些管道多由铁制成，并时刻在运作中，管道上面锈迹斑斑，并不断散发着高温，触碰它会导致流浪者的受到严重的灼伤。餐厅内杂乱的摆放着许多长条桌椅。流浪者可以在餐桌旁见到部分正在进食的无面灵，即使这些无面灵都受到了Level 11效应的影响。建议流浪者不要尝试掠夺他们的食物，无论它们看起来有多么可口。食用它们将会导致严重的腹泻。
//
//该区域内可以找到的物资有杏仁水与能量棒，但是极为的缺乏。
//
//休息室：
//
//宿舍该层级内较为安全的地方，内部空间大小无法被探测，似乎是无限的。墙壁不断的在往外渗水。宿舍里面摆放着数量未知的破旧三层床，这些床可供流浪者进行休息。有时个别的无面灵会在上面休息，请流浪者多加注意，遵从上文的提议，非必要的情况下就不要打扰他们。有时休息室内部会传出来源未知的女人与婴儿的哭声，有个别的无面灵会捂着双脸坐在床铺旁边，不建议流浪者接近它们。有个别的床上摆放着正在运作的老收音机，其播报的内容大多为战事时讯，这些时讯的内容大多为一战，二战等大型战争的战况，偶尔参杂着一些前厅里的时讯。目前尚未清楚收音机为何会播报这些内容。
//
//流浪者可以在柜子中找到手电筒，安眠药等物品也会有很小的几率找到残缺的纸与墨水将尽的笔。
