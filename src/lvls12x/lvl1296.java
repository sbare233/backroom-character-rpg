//描述
//Level C-296是一个无限循环的地下防空洞，并有部分通道尚未完工。该层级起初由一名无名流浪者于2021年2月27日所发现，但却在2021年3月6日才得到了完全探索。据M.E.G.考察，该防空洞名为“曙光”最早建造于一战时期，并经过了多次的改造。防空洞内气温保持在-2至39摄氏度之间。防空洞内部空间时大时小，据准确测量后，通道最宽为6米，最窄仅有50厘米。通道内唯一的光源来源为其顶上的探照灯，并会发出频率不明的嗡鸣声。这些通道可能会以任何角度弯折，弯曲，甚至垂直向上或向下，使得探索其内部变得极为困难。防空洞内部的地面异常泥泞，遍布废弃的防毒面具和各种食品包装袋；通道内的墙壁大多由可以水泥墙所组成，且会时不时溢出杏仁水，但据M.E.G.调查该杏仁水重金属超标且含有一定剂量的核辐射，建议流浪者不要饮用，空气中也有一种铁锈与腐尸混杂的味道。当流浪者在防空洞内部探索时，可以听到防空洞外炮火轰鸣的声音，当这种声音出现时，防空洞会突然震动，某些通道会轰然坍塌。有流浪者报告称在通道坍塌后听到了多种野兽咆哮的声音，但具体原因未知。
