//事件
//“崩坏”
//当流浪者行走在过道上时，有可能会遇到“崩坏”事件，崩坏的特征为无征兆的地面塌陷，墙壁挤压，突然遭受不明实体袭击等，此时流浪者将面临两个抉择，一个是进入到最近的房间躲避崩坏，二是切入正在崩坏中的地面，但只有极少数的流浪者在选择切入地面后生还了。除此之外当流浪者在走廊上停留太久时，其本身的听觉会变得异常灵敏，会听到更为强烈的炮火声与各种呻吟声，同时伴随的还有幻觉等，这种状况可以通过咀嚼从CookMaster那里得到的口香糖来减轻。
//
//“集合”
//有时防空洞内会发出刺耳的警报声，在警报声过后将会有大批的无面灵从各个房间内涌出，并来到餐厅内部集合。由于部分过道过于狭窄，会经常使得流浪者遭受从四面八方而涌来的无面灵踩踏而死。大部分的无面灵会在“集合”后瞬间消失，只有少部分的无面灵会重回它们所在的房间。
//
//“敌袭”
//“敌袭”事件表现为有不明的武装分子突然攻入防空洞内部，并不断的搜索并掠夺每个房间的物资，他们装备精良，但没有统一的着装，相反，他们看来像是来自不同时代的人。不仅如此，他们会无差别攻击任何看到的流浪者，无面灵与其他的实体，并且不会受到“崩坏”事件的影响。他们会在出现30分钟过后消失，据流浪者声称，他们好像凭空消失的一般，直接消失在了流浪者的眼前，并好像什么事都毫无发生般。“敌袭”事件通常发生在深夜，值得庆幸的是，他们不会进入宿舍里掠夺。
