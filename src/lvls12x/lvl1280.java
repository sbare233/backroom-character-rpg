//实体
//目前已知的存在于Level C-280的实体有五种：
//
//攀爬者：危险。出现在大理石墙体上，通常被描述为一种四肢畸形的人形生物，面部扭曲，关节朝外凸起，双目无珠，牙齿外翻，嘴角流着未知的绿色液体，皮肤呈死灰色，通常不会穿着衣服，目测该实体可能长期营养不良，少数攀爬者身体开始腐烂，伴随着令人窒息的恶臭。该实体移动速度较快，可以轻易追上流浪者，移动途中会发出指甲摩擦大理石的声音，但是其不会接触地板。攀爬者具有趋光性，流浪者遭遇攀爬者时，应立即关闭手电筒并靠近没有攀爬者出现的墙体，待摩擦声消失后可以继续前行。攀爬者皮肤感知度低，可以接触它而不被警觉，实体可以被杀死。
//捕食者：危险。通常被描述为肥胖的，令人恶心的，厌恶的，邪恶的，可怖的，压迫的，阴暗的，扭曲的实体。其头部戴着鸟类头骨，头骨嘴里咬着一颗明珠，可以照亮直径约十米的空间，身着黑色未知材质的羽绒大衣。在Level C-280遭遇该实体时，请绕道而行。实体有趋光性，会主动寻找除自己以外的光源，捕杀任何进入其光亮范围的生物，发觉猎物时，实体会发出骇人的尖叫从而吸引攀爬者。除流浪者外，捕食者为该层级唯一光源。
//倒吊人：危险。该实体通常伴随着大量另外实体出现。被描述为被绷带缠满的人型生物，像尸体一样倒吊在走廊中挡住流浪者的道路，有报告显示倒吊人会突然从不可观测的天花板上坠落下来挡住流浪者去路。同时也有报告称存在活动的倒吊人在墙体上留下“رفض الضوء في ضوء”后消失。实体是否有趋光性未知，不会攻击流浪者。
//跟踪者：安全。被流浪者描述为一坨灰绿色的扭曲蠕动遍布黄褐色粘稠液体的生物，可以勉强看出该实体有着两个似乎可以称之为腿的东西，身体上随机分布着无数孔洞，中间有着深红色或黑色的绒状物。实体移动时会发出类似于肥皂泡破裂的声音，且在地上流下一摊油滑的液体，发出令人厌恶的气味。实体在流浪者未察觉的情况下顾涌接近流浪者，在流浪者发现后实体会迅速离开，不久再悄无声息的回来。会发出人类一样的窃语——“الله نزل الظلام في الوقت المناسب ، مخلوقات الأرض ضد السماء للصلاة من أجل النور ”。
//转变者：危险。关于此实体的记述甚少，有流浪者报告其为一匍匐爬行的人形实体，也有报告称其拥有修长的手臂与佝偻的躯体，目前仍未确定流浪者报告的真实性。最近一次有关转变者的目击是2021年12月3日，接近Leve C-280社区Free-Gaq的区域，人员报告目击一个五边形的生物与捕食者缠斗，此过程中该实体成功杀死捕食者并将其吞噬，随后自身蹂躏成一团滚动离开。有流浪者注意到转变者身体带有与安全房一样的符号，由此推断安全符号不可抵御转变者。除此之外，2021年3月数个流浪者遭遇该实体，据幸存人员描述，转变者将尸体卷曲成一种难以置信的状态并将起吞下，过后不久会有类似于攀爬者的生物从其腹部爬出。转变者移动速度较慢，是否拥有趋光性未知，每次目击事件均发生在社区Free-Gaq附近。
