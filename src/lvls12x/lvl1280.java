//描述
//Level C-280为一片全黑区域。目前尚未探明其是否为封闭空间。所有经历过此层级的流浪者都报告Level C-280室温处在12℃~16℃之间。
//
//已知流浪者前往Level C-280时通常会出现在能见度极低的房间里。房间左侧靠近床头的橱柜中可以发现饮用水与食物，另一侧存放有手电筒，不过根据流浪者报告，手电筒发出的光只能照亮3米左右的前方。需要注意，Level C-280中的手电筒并不恒定，有流浪者报告在房间中发现没有电池的手电筒或根本没有手电筒。除此之外，房间衣柜中有着可以抵御寒冷的衣服，床上放有电脑，没有网络，显示时间是2022年2月29日，可用于资料存取。
//
//房间房门上有概率发现未知符号“نايت”，在手电筒的映照下会发出淡蓝色的微光。目前尚未破译文字信息，不过可以确定因为该符号致使层级中的实体不敢轻易靠近房间，因此房间中有符号的房间是绝对安全的。如果不幸穿越到Level C-280，流浪者可以将发现的符号抄下，以驱除该层级的实体，如果没有发现符号，那么不建议对Level C-280进行探索，在床上睡一觉，流浪者将前往其它Level。
//
//安全房通常位于Level C-280的西北角落，根据探测者的丈量，Level C-280的占地约三亿平方英里。其中道路错综复杂且伴随陷阱，失踪流浪者不计其数。
//
//离开安全房后，在没有光源的情况下，流浪者无法看清任何事物。实际上除安全房手电筒外，任何外来光源都将失去效果。房间外的装饰可以推断出似乎是处于某栋建筑内部，但向上无法观测到Level C-280的天花板。该层级墙体与地板均由大理石瓷砖构成，道路蜿蜒，两边分布有房间，可以在其中获得大量补给品或穿越到其它层级，不建议进入以木料为材质的房间，因为房门间隙中传来光明。
//
//有符号的房间一定安全。
