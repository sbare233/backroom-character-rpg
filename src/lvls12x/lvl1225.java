//描述
//Level C-225是一个无限延伸的现代化城镇，每个流浪者在进入时都会随机进入到层级内部的一栋大楼中，流浪者进入的房间多为未装修过的空房，偶尔可以看见杏仁水等物资。这些大楼层数约为二十层且有未知的电力供给，但也有极少数的大楼中断绝了电力流通。大楼内部有电梯，流浪者可以使用这些电梯在大楼中上下出入，同时一些电梯会出现通往负一层的按钮，它们无法被点亮。
//
//每栋大楼的一层都是商铺，目前发现商铺种类有：早餐店、彩票店、便利店、电脑店、复印店等，在这些商铺中，它们的内部除去应有的设施以外就只有一些宣传Level C-225内部风景和宜居程度的张贴画，这些张贴画做工简单潦草，是某些流浪者的作品。
//
//电脑店中的电脑通常会处于打开的状态，且页面会附带有一个进入“level.cn.225.com”网站的快捷方式，进入这里电脑中的浏览器的收藏夹也会看见这个网站。实际上，这个网站中并没有什么值得一提的内容，只是一个充斥着大量低质量的有关于Level C-225的图片的图床，但在无特定出口的其他层级中通过电子设备打开该网页会进入此处该层级。早餐店通常能发现一些没有被吃完的早餐，似乎有人刚刚在这里吃完早餐，这里总是会有一些人的气息。彩票店里会出现一些被随意置弃的文件，它们的内容大多与该层级相关联。尽管便利店是一间真正的便利店，但这里的一些食物早已过期很长一段时间，只有一些日用品能给流浪者带来帮助。
//
//这里，植物并不以具体的方式出现，只有当流浪者对回归前厅中的生活抱有希望或是对绿色植物极度渴求的话，那么绿植们将在最靠近流浪者的一栋大楼的墙壁上冒出，绿植的种类众多，但大多数是多种植物的某种特征的集合体。这些植物蔓延速度在开始时极快，而后将会变得缓慢然后固定在墙壁上，它们有能够镇定流浪者心神的功效，通常会散发独特的芳香。每当流浪者远离这些植物一段距离并且过去二十分钟后，这些植物将会消失。尤其值得一提的是，如果流浪者与这些植物相处时间超过五小时，那么植物由原先的绿色变为暗淡的青绿色，与腐烂的植物无异，而且当植物腐烂时，它们将会散发出一种气味刺鼻的气体。流浪者不当吸入这些气体，一旦吸入这些气体，那么他们将会出现反胃、头晕脑胀甚至出现缺氧的现象。
//
//如果流浪者一直向城市边缘行走的话，那么行走距离越远，城市的延伸速度越慢，但至今没有一个人超过城市延伸的速度，即便是跟上它也做不到。原因也很简单，引用某位流浪者的话说就是：“如果哪天这个层级的延伸速度比不上人类的话，那么这个层级恐怕会瞬间崩塌。当然我不排除那个‘超能力者’会死的可能…。”
//
//当流浪者在这个层级中滞留时间将要满足二十四小时时，他们的眼前将会浮现前厅生活中的回忆。虽然一些景象对于流浪者而言是不好的回忆，但它们确实或多或少的对流浪者的情绪方面的稳定起到了一些作用。
//
