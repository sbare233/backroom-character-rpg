//现在我将这个问题写在纸上，希望有能像M.E.G那样的组织能发现这篇手稿并加以验证，最后撰写进这个层级的记录里面。这也算是我为探索这个层级做出了那么一点的贡献了，我并不是什么冒险家、科学家，只是一个想为探索后室做出那么一点贡献的家伙。
