//手稿二
//事情还得从我刚进来时说起，因为这些大楼与我在前厅中所居住的地方很像，于是我怀念起了久违的前厅生活。就在这时，那些大楼的墙壁上忽然生长出了一些植物，它们速度极快，很快覆盖半块墙壁。实际上我并不爱欣赏植物，尤其是在后室这样的有着高度未知性的地方里，即使是再诱人的东西我也不会多看一眼，当然除了杏仁水和皇家口粮。
//
//但这次，这些植物们所焕发的生机与我之前所进入的层级内容格格不入，并非是某种特殊特征的突出体，它们之中的特征是众多植物的融合、展现，它们似乎本就不应该在这里存在。我发现我爱上了这些东西，它们给予我一种久违的感觉，一种从未见过的情感。既不是极度的绝望，也不是无谓的希望，而是切切实实的能够给我离开后室的感觉。
//
//不过也请注意，别跟这些植物待太久，当我在它们旁边睡下的时候，一种窒息感把我从刚刚获得珍宝的喜悦中拉了出来。再一看，那些绿植竟然全都表现出腐烂的姿态，这并不是一个好的象征。
