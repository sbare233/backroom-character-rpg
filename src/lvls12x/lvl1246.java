//在该种类层级里你需要破坏所有的镜子，注意碎片划伤你的手流出的血液也会引起饥渴者的注意。在你不受伤的情况下破坏所有镜子时，你会发现出口的墙上会多出一面镜子，切入他后你会在进入时的入口出来。
