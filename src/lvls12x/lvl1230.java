//实体
//由于Level C-230的特殊环境，该层级只存在“城池”一种智慧型实体，其余物种的生物学特征与前厅中的贝类及中小型鱼类完全相同。“城池”被开拓者协会的生物学家墨菲·罗斯特尔发现，且只存在于Level C-230与Level 7中。
//
//据研究，其个体具有低等智慧，且会进行有意识的活动。其个体的外形类似于前厅中的巨藻，体长从30m至1000m不等。且其周围覆盖着厚度约3cm的抗腐蚀胶状物质，目前尚未得知该物质的生成方式。
//
//与巨藻不同，成年的“城池”个体具有坚硬且极具穿透力的“根”。成体“城池”个体会不定期在基部分泌一种油状发光物质，据研究，这类物质会强烈地吸引流浪者并致幻那些被其所吸引的流浪者们。
//
//流浪者们可以采集“城池”个体的某些部分用于食用，其口感被开拓者协会描述为“裹满沙土的皮革。”非紧急时刻建议不要食用。
