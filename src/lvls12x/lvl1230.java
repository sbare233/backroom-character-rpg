//描述
//Level C-230是一片开阔的海洋，其深度约为1100m，面积约为30000万平方公里。 Level C-230内部有着极强的Wi-Fi信号，其内部由“城池”群构成，类似于前厅中的巨藻林，海面上不存在任何自然的支撑平台。
//
//通常的，以20小时为一周期，Level C-230的海面上会在随机地点漂来11艘长约3.2米，最宽处约1.2米的木船。木船上一般会放置有长度不等的粗麻绳，细线及大小不等的防雨布，物资提供来源目前未知。
//
//经调查，当流浪者切入Level C-230时，其味觉与嗅觉会奇迹般的消失，这种症状似乎会在离开该层级时消失。
