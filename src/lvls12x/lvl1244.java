//离开该层级的唯一方式是等待公交车的出现。Level C-244内有着一辆没有车牌、外壳斑驳、车速极其缓慢、随机出现的公交车。流浪者们只需正常挥手示意，即可使其停泊并上车。公交车将在漫长的行驶之后抵达一座商场并就此停车，直至流浪者离开。
