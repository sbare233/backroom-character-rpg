//Level C-214内部的交易不接受杏仁水或其他任何实物货物，只接受前厅流通的各种主要货币。在层级公路入口附近有一个专门的货物交易所，专供各类大型组织和流浪者将所携带的实物商品转换为前厅流通货币，包括美元、卢布、人民币、英镑、欧元、日元等主要货币。可进行兑换的货物种类繁杂众多，但每日可进行兑换的商品种类和最大额度都略有不同，且有最低货物数量限制，如果流浪者个人携带的商品数量小于最低阈值则不予兑换，另外层级每日兑换的货物数量也存在最高限度，但上限通常很高，一般不必担心达到交易上限。注意，杏仁水总在可转换商品之列，且价格与各种货币以发现层级第一天的固定汇率锚定，使杏仁水每日总能以不定数量和各种货币的固定价格交易，不受汇率变动影响，这意味着所有货币种类都能使用不受汇率影响的固定价格交易杏仁水，此种情况会导致何种后果目前未知。
//
//将实物商品转换为货币后，即可进入层级内随意的金融交易所进行交易。普通的流浪者可以根据自己的财产，看好的交易对象和金融服务种类进行有限投资，并从中收获利润或承担损失。大型组织的一般业务也将如此，如果更富有的流浪者进行较大投资或大型组织需要发起大额业务，则会有专职的无面灵高层管理者将他们请入高级，装修奢华的VIP商谈室，告知一些内部信息，洽谈业务。
