//描述
//Level C-266是由荒芜的平原和峡谷构成，层级中随机分散着枯掉的灌木，偶尔还会有像是风滚草一样的物体到处移动，层级存在正常的昼夜交替，层级气温早上通常处于16°C～23°C，记录在案的最高气温达到49°C，晚上会跌至-1°C~-4°C，天空中也有类似太阳的物体，但是天空中不存在云彩，由于层级中没有任何水源，所以层级中的空气异常干燥，并且地面干燥到开裂，将杏仁水倒在地上杏仁水会立刻渗进土里。
//
//ab andoned-1866567_960_720.jpg
//Level C-266的坠机
//
//坠机
//坠机是Level C-266存在一种奇特现象，在层级中有一架飞机，其会经历一次又一次的坠毁循环，该飞机的类型为标准的小型民航机，根据调查者的调查显示，其会在正午12:00坠毁到层级的随机区域，晚上12:00又会突然消失，并重新在层级的天上随机飞行，其飞行轨迹和飞行速度不是固定的，并且大约会固定在300米的高空中飞行，当其飞过头顶时能明显听到引擎轰鸣声，其坠毁时会有浓烟飘起，这也是定位坠机点的唯一办法。
//
//其内部有瓶装杏仁水、罐装龙肉等丰富的资源，但是由于其会在固定的时间点坠毁，所以大部分的资源都会因此毁坏，同时其坠毁时会有大量燃料流出，不知何原因燃料并不会燃烧。
