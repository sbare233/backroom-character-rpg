//状态II：在状态II中，“所爱之人”开始对流浪者进行进一步精神控制。流浪者将会看到有关于来到后室之前最爱的人的正向的幻象2 ，如果是男性流浪者，那么最大几率看到的与自己最爱的一个恋人相关的幻象回忆。若男性流浪者并无情史、或该流浪者为女性，则是与自己的亲生母亲、女性朋友等生命中最重要的一位女性相关的回忆。（出现形式为恋人的可能性依流浪者本身的性取向而定）。而在状态II中，该实体的白色连衣裙会闪出异常的光亮，有小概率使流浪者短暂致盲。
//level-c-805-p2
