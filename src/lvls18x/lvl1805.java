//实体
//“所爱之人”
//该实体在控制流浪者精神与心智的同时，会使流浪者产生“幻觉”。关于此实体，目前对其属性尚未明确，但已确认其不是无面灵等任何已知的某种实体。
//
//每一位从Level C-805中生还下来的流浪者，在被提问到关于此层级的回忆时，都会不约而同的提到一句：
//
//「我终于见到了我‘所爱之人’。」
