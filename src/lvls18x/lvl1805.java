//状态III：在状态III中，“所爱之人”会开始接近流浪者。而此时流浪者仍沉浸于状态II的美好幻想之中，但在靠近的过程中，流浪者会感受到心跳不停加速，且周围的气温缓步但可感知的升高。但值得一提的是，如果流浪者在进入状态III时成功的“击碎”幻象时，“所爱之人”会直接消失，并留下一支红玫瑰花，将其拾起后，会直接被传送至Level 18。
//状态IV：在状态IV中，流浪者眼前的幻象将会消失，取而代之的是与其距离约十米远的“所爱之人”。如果流浪者在这时有大幅度的位置移动，则会直接进入状态V。因此即使部分流浪者有可能在这时已经失去视觉功能，我们也非常不建议状态IV逃跑。
//状态V：在状态V下，该层级内会从暗处生成大量无面灵及少量悲尸。然后，“所爱之人”将以缓慢的速度继续靠近流浪者。在靠近的过程中，流浪者脑海中会听见一句话：“全部都告诉我吧。”
//随后，若想求生，需要流浪者大声喊出心中所有想对那位思念之人说的话，如果成功使她作出反应，则状态解除，详情见下文。流浪者会在“所爱之人”的怀抱中入睡。再次醒来时，会被传送至Level 18。
//
//反之，“所爱之人”则会主动与流浪者接触，将身体穿过流浪者。在这个过程中，流浪者会有强烈的心脏撕裂、炸裂感，最终导致死亡。
