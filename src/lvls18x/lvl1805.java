//体长约1.7m
//生理特征体现为一名青壮年女性，体态特征可能因流浪者心理状态而改变。据多数人目击，表现为身着过膝纯白色布制连衣裙，手捧一束白色的花球。有时连衣裙的白色会表现出异常的光亮，有小概率使流浪者短暂致盲，进入状态III，原因不明。
//似乎拥有控制流浪者心智、思想等的能力。
//可自由移动，但任何尝试接触此实体的行为都会失败，而是会穿过此实体，在穿过此实体的过程中，流浪者会有强烈的心脏刺痛的感觉，如有心脏病或类似疾病病史者，可能会因此而昏厥甚至直接死亡。
//此外，该层级在流浪者进入状态V时，层级暗处会生成大量无面灵及少量悲尸，他们在一般情况下会保持正常状态，但会在“所爱之人”表现出敌意时与其同时表现出敌意，原因未知。
