//Level C-800的环境类似于一家午夜时分的空旷商场，拥有明亮的照明条件和完整的装修。当前并没有流浪者触及到Level C-800的边界，因此无法测定其面积。相较于其他层级，处于这一层级时流浪者会更容易注意到环境的空旷与人类踪迹的匮乏。
//
//与Level 0相似，Level C-800具有非线性空间的性质，也会令进入该层级的流浪者与其同伴失散开来，无法在层级内汇合。然而与Level 0不同的是，该效果并不适用于未成年人类流浪者及其直系原生亲属，意味着养育了孩童的流浪者家庭进入该层级时不会与彼此分离而落单；不过，与该家庭无血缘关系的其他同伴将依然与其失散。
