//Level C-800内部的商铺繁多，但内部均没有任何可供流浪者携带的资源：餐厅和零食店内没有任何食品饮料，家具店中的各式家具无法拆卸和移动，衣装店中的衣物总是不符合流浪者的审美1。进入任何商铺的后房2都会来到Level 0。另外，出于某种未知效应，在本层级拍摄的照片当中涉及到商铺品牌的部分总是会变得无法识别。
//
//在以后室标准时间而言的前厅节日所在日期（如春节1月～2月、圣诞节12月24～25日）进入Level C-800是唯一能从该层级获取物资的方式。在流浪者进入Level C-800后，其面前的第一家商铺当中会出现一份3礼物，内容一般为其童年时期最喜爱的玩具或系列书籍，以及若干符合其喜好的零食。礼物上总是附有一份纸条，写有流浪者的名字及节日祝福语。与该现象相对的，整个层级的装潢在节日期间都会变为与节日相对应的风格。
//
//Level C-800内存在着若干个大型充气城堡和小型室内游乐场，在部分餐厅内也存在儿童游乐区域。这些区域中的游乐设备均可使用，而不会造成任何危险。14岁以下的人类儿童接近或处于这些区域时，会表达出在该这些区域游玩的意图。若其父母拒绝在这些区域逗留，则其情绪会陷入低落状态，或是产生愤怒情绪。额外的，任何流浪者处于这些游乐区域时，均会听到一种儿童嬉戏声。据称，这种声音“微弱而清晰，带有回声，似乎是从远方传来的”，当前未知该声音的来源与产生原因。
