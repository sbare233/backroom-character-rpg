//大多数商店的布局整齐有序。每个商店中都有一排排整齐的货架，货架的一端延伸到了很远的地方。在一些较大的商店里，偶尔可以在商店里发现货物和材料整齐地堆放在空地的托盘上。它们的高度取决于房间的整体大小。当产品从货架上取下时，商店会定期重新进货。目前尚不清楚这种现象是如何发生的。
//
//刚刚进入此层级时，这些商店看上去似乎干净整洁:地板擦洗得干干净净，货架摆放整齐，商店中弥漫着空调凉爽的冷气。然而，经过进一步的探索，流浪者会发现某些商店的条件比其他一些可以在这个层级内找到的商店更糟糕。一些区域的灯被发现被破坏了，许多冷冻食品通道的冰柜出现故障，无法正常工作。有些过道也可能完全混乱，他们的产品散布在地上，完全混乱。
//
//造成这些损害的原因目前还不清楚，但我们怀疑是居住在Level 480的实体所为。发现的破坏规模和类型的一致性也可能表明，破坏主要可能来自M.E.G.尚未遇到的一个未记录的实体。
//
//穿越此层级并不是一项简单的任务。该层级包含肮脏、漫长的店面走廊，它们通常紧密地挤在一起。这些商店可能比它们的入口要大得多。在进入一家商店时，流浪者们可能会发现，商店的内部空间可能与从外面看被相邻的商店占据的区域重叠。正因为如此，Level 480被认为是一片非欧几里德的线性空间，由于其性质，不可能有逻辑地安全地穿越Level 480。
