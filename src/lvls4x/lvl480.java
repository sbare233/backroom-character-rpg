//停电期：
//Level 480最危险的时段是所谓的“停电期”。在此期间，Level 480的所有灯全部关闭，所有窗户都以未知方式关闭。这使得关卡内的所有流浪者都被困在漆黑的黑暗中，考虑到上述关卡中的风险性，这是非常危险的。在此期间，重要的是要有人造光源来发现可能在附近的实体——最好是一次性的，例如荧光棒，以防笑魇攻击。流浪者们应尽可能地保持沉默，以向可能潜伏在附近的任何实体隐瞒他们的位置。值得庆幸的是，停电期不会持续很长时间，通常只持续大约五 (5) 到二十 (20) 分钟。
//
//由于从 Level 480 获取产品存在风险，因此它们在后室的其余层级具有很高的贸易价值。如果流浪者不顾上述风险而寻求获得 Level 480的强化产品，他们应该为他们肯定会遇到的危险做好充分的准备。
