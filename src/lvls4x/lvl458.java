//实体
//活体披萨
//Pizza_Margherita_stu_spivack.jpg
//一个在层级发现的活体披萨图片，无法得知摄影师随后的遭遇。
//
//这类实体类似于前厅发现的经典披萨，但存在几个关键性的区别。它们总以新鲜的、温热的披萨饼的形式存在，并散发着少量的热气。然而奶酪和一些配料会如同严重肌肉痉挛一样轻微地在上面跳动，如果有人试图触摸、靠近或拿起活体披萨，这些奶酪和配料就会跳到这个人身上，并且像吸盘一样吸住这个人，通常是吸在脸上和手上。这一次攻击会惊动附近的所有活体披萨，这些活体披萨就会同样吸附，如果附近存在足够多的活体披萨，这个人可能会被埋在大堆大堆的其他披萨里。
//
//该实体会将它们的袭击目标转化为披萨原料，并创造新的活体披萨。这个过程通常需要数个小时，过程中活体披萨处于最脆弱的状态。一旦袭击目标被完全转化，旧的活体披萨与新出现的都会分散到原来的位置，返回时活体披萨们会像鼻涕虫一样蠕动。如果长时间内没有袭击目标供攻击，该实体将分裂寻找目标。三个小时内，一个「饥饿的」的活体披萨将通过细胞有丝分裂为两个相同个体。一旦其完成分裂，两个新个体将分散寻找新的居住区域。
//
//丘酪生物
//若流浪者或其他实体试图杀死或控制活体披萨，活体披萨这类实体可能成为在该层级稳定的食物来源。然而如果活体披萨被杀死或者吃掉，所吃掉的部分是知名的，它们的组成物质是在被食用时对活体组织有传染性。一部分活体披萨会与流浪者的胃酸发生反应嵌入胃内壁中，披萨奶酪的拉丝会以蠕动的方式进入胃腺并刺入当中的毛细血管。一旦成功，拉丝会释放出未知蛋白质与流浪者的 DNA 结合，至此，流浪者将被开始转换为丘酪生物。
//
//大约一个月后，吃过活体披萨的流浪者会被彻底转换为丘酪生物8，它是面团、融奶酪、番茄酱和各种配料组成的无定形物质，一个单独丘酪生物的配料取决于孕育它的活体披萨配料。由于未知的原因，如果丘酪生物质量被消耗，它也不会引起污染。与活体披萨不同，丘酪生物常伏击猎人，它是非常活跃的实体，它追逐猎物并在追逐时呈现人形，然而丘酪生物并不对所有实体都存在攻击性，它们会长时间处于低活跃状态，在这段期间，丘酪生物没有攻击性，你甚至可以触摸它。
//
//与活体披萨一样，丘酪生物会与猎物融合，但他们的目的不同。活体披萨的融合是为了繁殖，而丘酪生物则是为了增加质量，随着质量的增加，丘酪生物变得更大更重。然而丘酪生物的变化存在一个上限，通常是一吨9左右。因此，重量达到上限的丘酪生物将会用质量覆盖 Level 458 的大部分地区，一旦这个界限被达到，丘酪生物会开始分裂为无数个形成不久就会消失的活体披萨。
//
//类变形虫
//虽然活体披萨分裂过程通常是正常进行的，但在某些情况下这个过程仍然会因为自身缺陷而被外部力量打断，在一个正处于分裂过程中的活体披萨上呕吐是打断或破坏这一过程的最快方式。当这个过程以该方式被打断时，面包皮上会出现气泡，配料的位置会被打乱。
//
//这些异常个体会极其痛苦，但它们会仍继续常规的生活，这一痛苦感受会在它们食用到流浪者之后被治愈。然而如果长期不进食到一定的时间，这种痛苦会加倍之于活体披萨，这会导致它无法正常分裂。前期这会导致两个独立个体粘连在一起，两个独立个体试图继续分裂，但这只会导致更多的个体粘连在一起，于是一个新的实体因此诞生。
//
//虽然类变形虫10只能活一个月，但它们的生长速度极快，可以覆盖 Level 458 的广大区域。这些实体会无差别攻击附近一切东西，将大量实体同化。它们同样会积累热量使其沸腾甚至燃烧，你只需要最大的类变形虫就可能被点燃。这种热量增加与其寿命相关，垂死的类变形虫个体只需要在五英尺范围内就可以点燃一张纸。
//
//此外，类变形虫不会显现出任何衰老迹象，它们的死亡是突然性的，特点是其生长和运动几乎瞬间停止。死去的个体从天花板或者墙壁上剥落下来，形成一大堆。由于其巨大的体积和超高的热量积累，死亡的类变形虫会在随机区域自燃，这些火一般在尸堆中心产生。实体会在六个月后燃烧直至腐烂消失。
//
//巨无霸披萨
//关于巨无霸披萨只有极少的信息11，但据说它遍布整个 Level 458 的墙壁、天花板和地板。巨无霸披萨的活动标志包括地面的震动、层级部分地区意外地膨胀，墙壁、天花板和地板地裂缝被一层层薄薄的黄色液体覆盖。
//
//如果一个人站在上面提到的裂缝上足够长的时间，一条由沸腾的奶酪、番茄酱、植物与动物肌肉组织组成的触手会从洞里伸出进行攻击。该触手滴落的黄色沸腾液体与围绕裂缝的液体相同，如果成功袭击，被袭击目标会被拉入裂缝中。被拉入裂缝中的流浪者通常不会被再次发现，少数的被发现的幸存者留下了严重烧伤，发现时身上沾满与裂缝相同的液体，如果要从身上洗掉需要不断努力擦洗数个小时，之后液体会慢慢凝固，与水接触后可以被剥落。
