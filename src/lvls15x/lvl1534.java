//曲廊
//曲廊是一处整体晦暗，狭窄曲折的走廊。因其过于曲折迂回且有极多岔路和死路，从而得名曲廊。曲廊平均宽度约为1.3m2，高约为3m。或许是由于此处特殊效应的影响，曲廊与其他两部分甚至其他非线性空间层级相比更容易使人失去方向。
//
//曲廊的地面同样由水泥铺就且较为光滑。墙面受潮严重，经常会出现鼓起的墙皮和裸露的水泥墙面。曲廊的顶上不时会有荧光灯提供照明，使大部分曲廊保持微亮的状态。
