//曲廊的拐角处常会出现电闸和水阀，这些闸门控制着一定区域内的供电和供水。为了防止因供电或供水故障导致出现一些不必要的麻烦，M.E.G.不建议触碰电闸和水阀的任何一个按钮和阀门。
//
//大部分曲廊两侧间隔排列着许多铁门。大部分铁门是锁死的，而一部分铁门上有编号。如果一扇铁门上有编号且其未上锁，那么进入该铁门会带你来到编号所对应的C层群中的层级3。如果该铁门上没有编号且未上锁，那么进入该铁门会带你来到杂物间。
