//描述
//Level C-511是一片被前厅中国常见的水泥乡村公路分割的土地，层级内部似乎永远处于秋季下午，但无法观测到太阳。这条公路将层级分割为两个部分，一侧是种植有多种前厅旱作农作物、正处于收获期或接近收获期的田野，另一侧是广阔的草原和流过草原的河流。田野和草原通过已干涸的沟渠相通，沟渠上方是各种材质的小拱桥，连接着各段乡村公路。尚未发现公路的尽头，该层级可能在公路方向上无限延伸。
//
//尽管Level C-511的环境适宜流浪者长期生存乃至建立可持续的聚居地，但由于以下异常效应，很少有流浪者能够不在初次进入时死亡。正如这张被上传至数据库的照片所示，进入Level C-511的流浪者总是会到达照片中的地点，站在乡村公路上；此时，道路的尽头会出现照片中的小镇。看到远方城镇的流浪者会感受到“从内心深处喷涌而出的怀念和安心”，并被它吸引——根据对所有进入过该层级的流浪者的背景调查，和前厅联系越紧密、越着迷于回到前厅的流浪者，受到来自小镇的吸引越强。其中，无论他们的实际文化背景和生活经历如何，有过长期前厅生活经历的流浪者都会在第一时间把远方的城镇当成自己的故乡。注视它的时间越长，和前厅有过的联系越深，来自城镇的精神影响就越强。当这种精神影响强到一定程度时，受影响的流浪者会不由自主地迈步向前走去，试图到达那个小镇。
//
//从公路以外看，正在“前进”的流浪者此时在公路上原地踏步而不自知，直到死于饥渴或劳累。尽管小镇的影响仅持续很短的一段时间，但开始“前进”的流浪者无法停下，哪怕打断自己的腿也会被层级内部的力量强迫“迈步”，这会引发强烈的痛苦和恐惧。有人声称自己看到死去流浪者的尸体在公路上原地“前进”，暂无影像资料佐证。同时，无论前人在公路上留下多少痕迹，都会在下一个人进入Level C-511时从其眼中消失，但很多时候仍可闻到腐烂气味。目前，所有极度渴望回到前厅的流浪者在进入该层级并看到城镇后无一幸免。对于他们来说，这是一条永远走不到尽头的回家路——尽管那些人只是被困在Level C-511中，在这条有着故乡幻象的断头路上走向自己的死亡。值得注意的是，在该层级公路以外区域无法观测到这座城镇，原因不明。因此，Level C-511被文档的撰写者标记为宜居诱饵，即在一定条件下宜居但存在致命因素的层级。
//
//由于尚未确认准确的入口，对Level C-511的勘探受人手和设备条件限制，当前该层级的准确面积仍未知。层级内部散落着大量的活动板房，内部可能有农具、家具、少许陈旧的电器或干燥的粮食，但不会在被取走后刷新。所有活动板房内部均有良好的水电供应和稳定的Wi-Fi信号，这些Wi-Fi信号源在板房外无法连接。
//
//本层级不存在任何敌对实体，河中的鱼、其他水生生物以及草原上的植物和真菌均为前厅物种，均无毒且可食用。Level C-511内唯一一种实体是生活在草原上的无足鸟，它们在铁皮房中筑巢，但从未试图跨越公路。该层级存在另一种无害的异常效应：被收割过农作物的土地会自行长出品种完全相同的农作物，但这些植物不会遵守通常的生长规律，而是每半个月“刷新”成它们应有的生长进度，直至完全成熟。
//
//基地、前哨与社区
