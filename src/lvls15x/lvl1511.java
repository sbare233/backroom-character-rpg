//团结村
//幸存者在公路附近的田野侧自发形成的小型社区，围绕一座活动板房建设自己的房屋，以收割田野中的农作物为生。根据对这些人的背景调查和采访，团结村的流浪者大多出生在后室，少数来自前厅的流浪者则是在幼年时误入了后室，且几乎没有对前厅的记忆。他们试图提醒过进入者不要抬头看远方的城镇，但所有尝试均以失败告终。
