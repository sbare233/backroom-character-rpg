//
//该层级中的时间流速及昼夜更替与前厅中夏季保持高度一致，温度恒定于25℃，进入该层级中的任何计时设备都会与该层级时间保持统一。当清晨5：30时，天空逐渐亮起，但该层级中并无传统意义上的太阳，光线仅是亮度增加，并未有位置上的变化。当时间为8：30时，之前的光线会完全消失，于距离该层级可抵达位置较远的云状物质中会升起一轮"月亮"，同时，该层级中的天空中会出现相比前厅中更加明亮的"星星"。
//
//该层级的玻璃平台上有小部分玻璃为浅紫色或深蓝色。当流浪者位于这些玻璃方块上方时会听见来源不明的乐声，其中浅紫色玻璃为轻音乐，且多为流浪者曾经听过的。深蓝色玻璃为交响乐，多为巴洛克风格的大调曲式。当流浪者听到任意一种音乐后，都会感到无比的宁静，专注力会大幅度的提升。同时，患有精神疾病的流浪者听过该层级中的音乐后，症状出现了不同程度的减轻。
//
//该层级每间隔一段时间会出现"流星雨"，通常会在夜间发生，持续1-2个小时观测到流星雨的流浪者会感受到心灵上的慰藉，同时会思念家乡，亲人。观测过流星雨的流浪者会感到"释怀"的情绪，通常这会让流浪者放下之前的仇恨，同时也会放弃主动伤害其他流浪者的想法。值得注意的是，如果患有精神疾病的流浪者在观测流星雨的同时站在彩色玻璃上，这名流浪者的精神疾病会被完全的治愈，目前造成这种现象的原因未知。
//
//该层级正中央的建筑是可以进入的，建筑外观呈现为西方中世纪时期的装修风格。从外部观测此建筑占地仅有1250平方米左右，但内部的似乎为另一个空间，面积达到了18350平方米1左右，同时，该建筑内部的面积会随着流浪者进入的数量增加或减少。
