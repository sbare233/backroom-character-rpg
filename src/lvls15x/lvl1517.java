//大厅
//流浪者进入建筑内部后通常会切入进该建筑的大厅部分，大厅内部包含一个吧台，一个休息处和多扇通往其它区域的门。吧台前有一名可以交流的无面灵，该实体会通过在纸上写字来回答流浪者的问题。大厅内的门可以通向餐区，剧院和住宿区。
//
//餐区
//餐区的外观类似于中世纪的宴会厅，但其面积要大得多，在房间正中间有一张贯穿整个餐区的长条形桌子，桌面上放有餐具，每天的7:00,12:00,6:00时，通往餐区的门才可以成功进入，同时桌子上会出现数目庞大的食物。经观测，餐区内的食物数量和房间面积取决于下一次餐区开放前层级内的人数。餐区结束开放的时间为开放后一个小时，如果流浪者在结束时仍未离开该区域的话，流浪者会直接被传送到建筑外的随机一个彩色玻璃块上。
//
//住宿区
//该区域的结构类似于酒店，已经在此居住过的流浪者进入住宿区时会直接切入其所住房间的楼层，首次进入则是随机切入。每个房间均为单人间，其中生活设施较为齐全，同时每个房间内均有一个书柜，书籍多为娱乐报刊或童话绘本。
//
//住宿区与剧院连通，在每日7:00-7:30之间可以切入流浪者所在楼层尽头的红色双开门进入剧院，每日7:30时会有无面灵于剧院台上演出，演出时长通常为一小时或1.5个小时，这种音乐与踩踏彩色玻璃时听到的音乐相同，均具有缓解精神压力的功效。
