//层级中多数房间内配有收音机与电脑。方形金属老式收音机由1号电池供电，可接收纯音乐电台及播报前厅全球性事件的新闻。电脑则多为白色款老式台式机，其性能远超其外观型号，甚至与一些前厅新款家用机相当。一款名为《穿越后室》的Rougelike单机冒险游戏被预装在台式机上面。有传言称其中内容对后室中现实事件具有预见性，但因其所谓的“对应”多为过度解读而被判定为阴谋论。
//房间内大多配备齐全，但大多数物品都于四角处被做了标记。标记相溶于载体表面，当物品位于四角标记内时其颜色为亮黄色，而物品被拿走时其将渐变为深蓝。若凌晨零点时选定住所内仍有标记为深蓝，则可能会影响流浪者次日物资的获取3。
