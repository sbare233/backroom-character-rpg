//每日倚靠遥远的国家大事、游戏与短视频来刻意消磨 “无事” 的时光，渐渐习惯一人的生活，静态的独处。直至遇到一个契机、或永远如此下去，沉溺于麻木的生活中，慢慢侵泡、腐烂。
