//Level C-500是一片无边际的居住区。楼房由混凝土、螺纹钢与砖石等砌筑而成，风格均呈现为白漆外墙的多层住宅样式。楼房通常会占据所处区域约70%的占地面积，成井字排列并不断重复直至远方。不同楼房的老化程度不一，以区域渐变的方式呈现。但由于渐变幅度并不明显，这使得流浪者往往很难立刻察觉。
//目前，流浪者遇到绝大部分的楼房并无老化迹象；小部分其电线和水管会出现老化现象，楼房内部也将弥漫有不同程度的陈旧、腐朽气味；极少数楼房则展现为严重老化，甚至垮塌为废墟的状况。有复数报告称，少数废墟下正在一砖一瓦地“生长”出新的楼房。按目前的“生长”速度估算，其完成所有“生长”进程预计将耗时30年以上。
//
//楼房之间铺设了草皮绿化带与沥青混凝土铺面道路。绿化带常年自发湿润且从未见其枯萎，当流浪者靠近时会感到其四周空气异常潮湿，踩在上面会有浓烈的绿叶挥发物味道散出1。道路整体呈网状结构随楼房延伸至整片层级，其两侧的红色塑料材质中国结路灯会在夜晚PM5:30时亮起，并于PM10:00时熄灭。
//偶尔楼房间的空地会出现若干无法启动且不显活性的车兽（疑似）。因其从未表现出活性状态，以至于“其是否真为车兽”一事M.E.G. 部门内部至今未能得出一致结论。
//
//楼房内走廊高约2.5米，地面铺米色方形瓷砖，墙角下深色踢脚线，两侧为亮白漆墙面，一侧排列有白色塑钢横拉方窗，另一侧为若干褐色防盗门。居住区的楼房是可居住的。当流浪者在门前产生想要入住的想法时，铜制钥匙便会坠落2在其口袋中或附近。
//入住房间后，当流浪者路过其他房门时则会受到“此处有人居住”的心理暗示。由于这是一种作用于人类底层认知的暗示影响，流浪者很难立刻发现，但其可通过推理与思考被察觉。被影响者会更倾向于相信此处是有人居住的，或同学或同事，在日常中经常见面且彼此熟识。当被要求讲述关于对方的相关内容时，其通常被描述为“虽然都很面熟，但却又格外的陌生”。但事实上那里根本就没有人，谁也不在那里。
//
//ZQgPqg.jpg
