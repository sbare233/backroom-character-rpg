//物资会在Level C-500无边际的空间中随机散布，其中杏仁水与罐装龙肉居多。它们通常不会被四角标记，但与Level 1中不同的是，层级中的物资并不会随机出现（或消失）。其被收集后区域内便不会再出现新的物资，除非新的楼房开始“生长”。少量物资会随建筑进度逐渐出现，直到达到一个未知的数目后停止，目前物资出现的原理未知。
//除了搜刮外，每隔一定楼房间隔便可找到小块空地，此处摆放着若干“铁皮柜”。其为青绿色铁质薄板组装而成，内部有塑料管道与地下相连，其正面右侧有方形玻璃按钮，需每天早晚各按一次5才能使其持续二十四小时的正常运转。当流浪者于AM8：00至PM5：30的时间段内保持清醒时，最近的“铁皮柜”便会于次日凌晨在柜内产出少量物资。据研究推测，其原理是从层级各处收集物资，并通过管道运送至柜内的。
//物资主要以食物和饮用水为主，医疗或其他品类的资源则严重缺失。其可提供流浪者一天的消耗外还会有约1/4天份量的剩余，但若未能在时间段内保持清醒或住所内四角标记于凌晨时仍为深蓝，则次日柜内产出的物资会相应减少直至消失。
//柜门上时常会出现复杂度低、繁琐且无趣的谜题6。解开谜题的次日柜子内物资的摆放会变得更加规整，否则其布置会凌乱异常。
//有时“铁皮柜”内会额外多出一些电子产品等远高于每日产出物资价值的物品，其可被拿取并正常使用，但这会导致之后相应天数的物资缺失。据报告称，少数区域每隔半年会有疑似无面灵的飘忽的人影于凌晨三五成群来窃走物资，并会导致持续三天的物资缺失。
