//文化发现
//对于保留的文化遗迹进行进一步探索后探索者们对这里原本的文化风俗进行了还原，目前能确定这个城市最早的修建者应该来自云贵高原一带，其文化特点中明确展示了前厅中国高山区域少数民族聚落的文化风俗习惯。针对其文书的进一步解读显示这些人名确实来自云贵高原地区，推测是当地少数民族后代。
//该文明有大量石碑详细完整的记录他们一族的历史，但由于生存环境突变其文字经历了剧烈的变动，似乎从最初的象形文字转化成为了方便雕刻而使用的短平直符号，但原本象形文字遗留的大致结构以及较为混乱的叙事结构组，最终导致其语言在结构上极其混乱只能尽可能推测其含义。
