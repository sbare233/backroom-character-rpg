//地面主体由被流水侵蚀和沉积作用用的凹凸不平的石灰岩构成，水流溶蚀导致该层级地势落差极大，加上缺少光照条件以及地面湿滑等恶劣环境的因素导致未探索区域频繁发生失足摔落事件。
//
//地面和洞穴顶可摸到大量生长中钟乳石，滴落的水会汇聚成小溪流汇入部分区域的地下水潭。水源和空气的来源目前尚未被探明，但化学分析确定他们可以正常的被人体摄入而不产生危害。
//
//尽管整片区域的水汽含量大致相当，但是以切入点3为界限越向下的区域越容易出现大规模积水。向上的区域则较为干燥，部分地面会生长有少量真菌类和近似苔藓类的生物。值得一提的是尽管下方较深却并未展现出因上方压力而质变或是坍塌的情况4。
//
//人文环境
//该层级存在大量年代久远的古建筑构成一座具有完整功能的城市，绝大多数基础设施都保存完好，包括道路、居民区、农田等，城市的主体结构有较为粗大的钟乳石雕琢形成，相比起普通的石灰岩有更高的材料强度，关于城市修建者的详细内容以及进一步的考古发现请移步“文化发现”段落。
//
//在原有城市的基础上后来的居住者们逐渐构建起一套相对完善的城市体系，依靠数百条锁链贯穿整个城市，进入此层级进行交易或是生存的流浪者都需要尽可能摸索着锁链前进以防止发生危险。
//
//生物5
//除了人类外该层级还存在着大量生物构成完整的特殊生态体系：这些生物中绝大多数的外观都和前厅中生物类似6，但感光器官都被退化，取而代之的是灵敏得多的听觉和嗅觉。
//
//由于地穴生态系统能量较少而且地形差异巨大，毒液一类捕猎并不现实，因此绝大多数生物都进化成体型小无毒但有尖利爪或牙的生物。尽管单只很难造成威胁，但是如果集群攻击或是在某些较为危险地形下突然受到攻击还是很容易引发意外，建议提前做好预防措施7。
//
//该层级的真菌起到近似普通生态链中生产者的地位，带出样本化验确定其可以缓慢侵蚀岩石并吸收岩石融化在水中产生的少量的能量，也记录到用水流代替身体内循环机制的类植物和少量近似于奶茶珍珠的微生物能够在常温下直接分解水以获得能量，但获得的能量比奶茶珍珠要高的多，推测这是奶茶珍珠的基因模板来源。
//
//除人类以外有大量有识血肉也来到这个层级，尽管无法确定他们的具体人数但有大量流浪者的报告，在探索过程中触摸到类似有识血肉有机物建筑的肉质，值得一提的是，有识血肉建造的连接巢中有可以通向其母层级的血肉腔道，从其中进入者将不会返回原有层级，这似乎是该层级唯一被记录的实体种类，似乎不会生成其他的实体。
