//停车区
//停车区大致可分为两个部分，我们将其命名为川域和雾域。
//
//川域
//川域的整体外观除了在部分标志线内有未知红色物质铺洒于地面，其他与正常的室外停车场别无二致。
//
//根据某位流浪者的陈述，当他踩在这类红色物质上时，感到了焦虑，恐慌，甚至害怕的心理，视野也越发模糊，且似乎看到了大量实体向他靠拢。但当他远离那片区域时，实体随即消失，身体状况也逐渐恢复。原因未明。
//
//在此区域内，将会有微弱或中等的视觉变化间断作用于流浪者。
//
//至于休整的地方，流浪者只需找到一盏白色现代路灯，并处于路灯的光源下，这样遭到实体的攻击的概率会大幅降低，并且会逐渐消除身上的疲劳感。
//
//据观察，这些路灯一般均处于常亮状态。但偶尔路灯会以由暗到亮的方式出现明暗变化，此时，出现实体的概率会与灯光的亮度成反比。值得一提的是，当即将发生此事件时，流浪者周围会出现未知来源的嗡嗡声。
//
//若发现路灯频繁闪烁，则说明视觉变化将可能作用于流浪者。
//
//这里有着不适宜人类居住以及长期探索的危险实体，除去经常出现在各种层级的死亡飞蛾，窃皮者，还会出现非常危险的抛锚客，突刺者。唯一值得庆幸的是，这里是警告风筝的栖息地之一。
//
//让大量流浪者慕名而来的则是这里的物资，其不仅丰富且种类不一，含量与Level 3相当。目前已被证实存在的物品有杏仁水，油腻棉花糖，皇家口粮，层级密钥，瓶中闪电，后室TCG卡牌，倒转怀表，幸运豆奶。
//
//雾域
//雾域的整体外观与川域十分类似，也为一个正常的露天停车场。且无实体，无物资，无视觉变化效果。可以作为流浪者短暂休整的地方。
//
//进入此区域后，所有的摄像设备都会失效，因此实际的场景只能靠流浪者的描述来得知。
//
//虽然此区域并无视觉变化作用于流浪者，但在大部分区域，都会弥漫着一种霾类物质，其中夹杂着部分星云，浓烈时将会影响流浪者的可视度。
//
//须得警惕的是，尽管在该区域的流浪者在生理层面上体现出了一种“恒常性”1，即新陈代谢保持一种稳固恒定的状态，不再需要同外界进行物质交换，细胞也同步地停止生长同衰老的变化。
//
//然而，一旦流浪者离开该区域，原定被遏制的生理变化会毫无保留地爆发于流浪者的躯体上，系列的饥饿、困倦、酸痛会骤然间席卷而来，在该区域停留的时间愈久，则该恶质变化爆发得愈发猛烈。
//
//因此，流浪者若需长期驻留于此，请做好以上心理准备，或者永远生活在这里，加入他们。
//
//天空直梯
//流浪者若需去往其他部分2，则要找到天空直梯。其外观为一间标准的现代电梯，且无法发现源头所在。
//
//直梯的位置一般处于一片空旷，且无路灯之地。在流浪者靠近它之时，位于其上方的感应灯便会开启，以便于流浪者发现。
//
//搭乘天空直梯去往下一部分时，此过程通常需要十分钟左右的时间。但多数流浪者在电梯中会表现出时间感知错乱的特征，这会导致原本十分钟的路程，流浪者却经历了十五分钟甚至更长。因此，为避免此现象对流浪者的负面影响，我们推荐流浪者在搭乘前准备计时类物品。
