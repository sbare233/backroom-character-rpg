//公路
//公路的外观为一条长度未知，宽度五米的道路，道路材质一般为沥青或水泥，在此区域中，将会有中等程度的视觉变化作用于流浪者。
//
//由于视觉变化对流浪者的持续影响，人们会错认为在该区域实体活动并不活跃。但事实上，包括萤火路灯，车兽，突刺者，暗影等系列实体，往往潜伏在难以被流浪者发觉的角落展开袭击。
//
//在此区域中，路灯通常显得可有可无。然而值得一提的是，其排列方式一般是凌乱的，且大小，款式，照亮程度均不一。但不规则的摆放正是萤火路灯想要的结果，它似乎基于伪装的意图，将路灯摆放的较为杂乱。所以当流浪者经过时，应当尽量远离任何路灯。
//
//公路中，车辆来往为普遍现象，但此区域并没有正常的车辆。所以多数流浪者在访谈时所描述的“大量车辆行驶于此”有两种可能：
