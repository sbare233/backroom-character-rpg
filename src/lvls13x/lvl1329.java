//描述
//Level C-329有两个主要区域，一个区域为能进入此停车区的公路。另一个则为停车区。
//
//在此层级最常见的现象则是视觉变化对流浪者的影响，对于视觉变化，通常是指在特定的区域内对流浪者及其物品所产生的一种视觉效果，此效果对事物及其性质等并无影响。但流浪者及其物品受到此效果的影响后其视角内的事物皆呈扭曲，锯齿化，波浪等效果展现出来，以及有概率使流浪者出现幻象。效果的强弱则主要由流浪者所处的环境因素影响。
//
//lights-2773056_960_720.jpg
