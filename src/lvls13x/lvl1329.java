//大部分的栅栏在关闭时，流浪者可以使用暴力的方法来进入，也可以通过清除栅栏周围的突刺者来让栅栏开启。若流浪者强行进入，除非有极快的速度，否则将会在触碰到的一瞬间产生高压电，直至你死亡。
