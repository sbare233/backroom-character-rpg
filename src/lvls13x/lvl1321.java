//警戒区
//警戒区内灯光较为昏暗，是Level C-321中最危险的部分，你可以通过深入一些地上画有警戒线的走廊进入。
//
//警戒区中存在着多种危险实体，如窃皮者，钝人，具有强烈攻击欲望的无面灵等，它们不知为何无法离开警戒区。这些实体大部分都有着一定的异常效应，如曾有流浪者报告称在被一只无面灵追逐时，后者突然发出机械声，然后迅速缩小变成了一台照相机。该层级内异常效应运作的原理及规律尚不得知。
//
//警戒区的管辖权现在属于红星建筑队的“金网”部门，他们会帮助你离开这一区域。
//
