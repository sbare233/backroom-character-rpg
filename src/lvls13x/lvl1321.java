//“金网”是1993年时从红星建筑队中分裂出的一个子组织，该组织有着独立于红星建筑队的运行系统及决策权，但仍在名义上隶属于红星建筑队。
//
//金网的前身是红星建筑队于1990年成立的金网调查队，该调查队由以胡竞为首的十五名成员组成。金网在1991年调查Level C-321时与红星的建筑部门起了冲突，后者认为该层级坏境较为稳定，消除警戒区内的异常后稍加改造便可成为宜居层级，但前者认为该层级中的异常实体有较大的研究价值，应保留。
//
//前任红星领导人李师同意胡竞带领金网在此层级进行为期两个月的短期研究，再根据他们的研究成果评判该层级的异常是否值得保留，但截止期限未到，李师便因病去世，关于Level C-321的改造计划也因此不了了之，相当于默许了金网在该层级独立运作。
//
//金网目前拥有三百名左右的成员，并使用红星建筑队的技术在该层级的警戒区建立了基地与前哨，负责记录和研究该层级内的异常实体。M.E.G.对金网的行为持赞成态度，会定期给他们提供物资，倘若流浪者误入警戒区，应第一时间找到金网成员并在他们的帮助下离开。
