//
//即便生活条件及进化使猫人更加完美，猫人的平均寿命依旧只到20年，半个月至一年就达到性成熟与其祖先家猫一致，也导致猫人的历史就人类角度而言并不漫长。就目前的资料表示，猫人从进化到现代发展时间跨度仅有一至两千年。
//
//重要事件
//在猫人的历史中最重要的一场矛盾，就是猫人与魈面兽的生存地盘之争，魈面兽原先与猫人是同属的物种，因地形气候与生活环境导致魈面兽，看上去就像一位佝偻的老人，因可怖的面容得其名，原先魈面兽生活得地形与气候较为阴暗潮湿，且光线极为不足，使得魈面兽维持且进化出完全适应黑暗环境的夜视能力，因条件极差使得生存物资也极为匮乏，为了生存使得魈面兽养成极为残暴且不择手段的性格。
//
//由于生活环境的限缩导致魈面兽不得不开始迁往现在猫人的居住地，彼时的猫人祖先也恰巧途经此地，届时的两个物种已进化成可直立行走且会使用工具的程度，于是他们就展开争夺生存环境的战争。但情况维持不久，由于长期适应黑暗环境，魈面兽的视力已严重退化，这导致战局的地位相差甚远，最终猫人祖先已压倒性的攻势占下目前的生存环境。
//
//在此之后幸存的魈面兽在往南方迁徒时在一个时间点集体消失，根据最后发现的位置是位于目前层级的入口处，怀疑魈面兽有可能迁往Level C-278或Level C-888，但目前依旧仍未有直接证据可以证实此观点。
//
//有关于此层级的大部分生物皆是食草生物，以及大部分物种都有猫科动物的外观，仍需再获取足够资料才可进行调查，但以目前获取进度来看已遥遥无期了。
//
