//Level C-358的大部分区域属于未开发的原始森林，其环境条件允许发展出多元且复杂的生态系统，在随后的探勘纪录也表明，此层级不仅有着不亚于亚马逊森林的生物多样性，只是栖息在此层级的实体，虽就习性、身体构造方面相差甚远，但外观上更像是猫与其他物种的结合。其中以''猫人''为此层级的智慧物种，猫人是一种直立行走、具有与人类一致的智慧程度、外型形似家猫进化而来的人亚族实体1。
//
//层级的核心地带是一座由木头为建筑材料建设出的小城镇，其城镇规模相当于人类社会的五线城市，文明水平在农业时代，但不存在货币交易的形式，居民供给主要以自给自足为主，其主要粮食为稻米搭配纯素饮食。就饮食方面''猫人''与常见的猫科动物大相径庭，其身体构造进化成更适应纯素饮食，但有时也会补充蛋白质。
//
//社会、饮食结构与猫人
//猫人的饮食结构与社会结构习习相关，在猫人的社会中不存在统治阶级，每位猫人皆是劳动阶级，所以建筑、饮食、文化、语言方面讲求实用为主，但因此层级不存在可用的高效能源故猫人的机械结构仍停留在人力运作，所以饮食方面猫人更注重保腹感、高热量等需供给长时间消耗能量的食物，但因长年进化使猫人不再以肉类为主食，于是猫人改良了传统稻米研发出一种，可以提供比其他谷物更高碳水的改良米。
//
