//小道
//小道由沥青或石板筑成，道路无法被破坏。切入此层级时会出现在小道的起点。起点后面被警戒线和木杆拦住，上面有一个写有“前方危险，请勿靠近”的字样，任何尝试靠近或破坏警戒线的行为均以失败告终。
//
//小道全长约5.4km，在到达终点时，你将会回到起点。
//
//陡坡
//道路右侧是一处无限延伸的陡坡，其斜度略大，爬上去会十分费力，陡坡上布满人工种植的花草和树木。在这里休息是个错误的选择，因为即使是白天，草丛中仍然藏有许多“多眼蠕虫”的幼体，它们会在白天活动，保护好你的眼睛。在夜晚，这里会出现许多危险的实体，此时行动无疑是十分愚蠢的。
//
//河流
//道路左侧是一条河流，河水是浑浊的，经检测，河水除含有大量泥沙杂质外没有任何异常。即使是在白天，河面依旧有很浓的白雾，因此无法确定对岸的情况；但在夜晚，会看见对岸有时隐时现的亮光，但此时请尽可能的远离河流，曾有多名流浪者声称水里有对他们充满敌意的实体。
//
//红枫叶林
//ccc
//废弃工厂的一部分。
//
//红枫叶林距离层级入口约2.2km。这里违和的栽种着许多的枫树，枫树的种类与前厅的相似，但树叶一直以来都是红色的，没有变绿的迹象。在这里会经常性地刮起大风。
//
//废弃工厂
//工厂位于临河小道上方、红枫叶林内，主要由红砖和铁板筑成，其内部十分空旷且黑暗，但相当于其他地方，这里是一个很好的避难所，设施内会随机刷新物资，如杏仁水，罐装龙肉。夜晚，这里不会自然出现实体，但在河边闲逛的实体很有可能经过这里。
//
//雾镇
//雾镇位于河流对岸，两岸相隔约12m。雾镇的建筑类型被形容为“老旧的小区”，其房屋已经破败不堪。但即使如此，仍有少量无面灵居住在这，这些无面灵多为老年无面灵，友善并且无危害。夜晚在小道上看见的亮光已被证实是从雾镇发出的。
//
//曾有多名流浪者游到对岸来到了雾镇，并声称自己在这里看见了与自己亲人十分相似的无面灵，其多数为看见者已故或很长时间未相见的长辈。出现这种现象的流浪者都有一个共同点：他们的长辈都在或曾在类似的小区生活。
//
//雾镇是此层级最安全的地方，这里只会出现无面灵并没有其他实体，但这里十分不稳定，会更容易切出。
//
//实体
