//echi
//由流浪者绘制的鳄齿。
//
//实体名称：鳄齿
//
//描述：鳄齿的长有与鳄鱼相似的鳞甲，和6支细长的腿且牙齿密而尖，通体深黑且带有红色光泽，皮肤光滑，背部有大量尖刺。有三张大嘴，分布于头、胸腔、尾部。头部窄而长，脸的两侧各有两只眼睛，头上的嘴向两侧延伸，看似在"微笑"。它们会用侧面对着猎物并一直跟随，随后迅速的冲向猎物将其吃掉。仅在夜晚的斜坡上出现。
//
//防范措施：鳄齿头部的嘴很大，但是控制开合的组织却异常的少，所以鳄齿的头部仅仅只用于威慑，那个部位十分脆弱，成年人可以轻松地将其上头骨掰下。但此举并不能将鳄齿杀死，它们会迅速逃跑，但这也给你提供了足够的逃跑时间。但请注意，失去上头骨的鳄齿也失去了眼睛，它们会消耗一定时间在脖子处生长嗅觉器官。
//
//duoyanruchong
//由流浪者绘制的多眼蠕虫。
//
//实体名称：多眼蠕虫
//
//描述：多眼蠕虫是一种身体为米色的巨大的蠕虫生物，长有一张似犬的嘴，皮肤会分泌出一种毒素，接触到毒素的部位会出现红肿、瘙痒等生理反应，持续1~2周不等。当毒素不小心误食或进入伤口时，最初你会止不住的呕吐、腹痛，约3小时过后你的身体会开始陆续长出许多水泡，水泡会越长越大，最大直径可达3cm，一段时间后水泡会自行破裂且钻出许多多眼蠕虫的幼虫。
//多眼蠕虫的幼虫会钻进并吃掉一切生物的眼睛，每吃下一颗眼睛，其体型会增大，并且在其背部会长出一只眼睛，当眼睛破裂时，则会有大量的幼虫泵出。幼虫会一直存在，长度大于40cm的个体仅在夜晚出现。
//
//防范措施：多眼蠕虫的速度不快，但还是应当保持距离，避免沾到毒素，更不要随意的去往斜坡上的草地。若是不幸误食毒素或使其进入伤口，请不惜一切代价弄破水泡或者把长有水泡部位浸泡在杏仁水里，以防实体大量繁衍。
