//地表中含有河流水系，水系以东北角斜跨地表。水系色彩为亮绿色，属性清爽、无害。春原的房屋色彩由亮绿色向暗绿色渐变，房屋内部渐变为所上述色调度呈现顺序相反，其色彩渐变未有影响材质，材质类似为CD灯而密度确是CD灯的5倍。
