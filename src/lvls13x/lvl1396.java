//暖原
//暖原的草坪呈亮黄色且尖部为青橙色，地表草丛色彩为黄色，其中拥有深黄色的花朵，散发出比春原境内花朵，更浓郁的气味其镇定效果，强于春原内的花朵。暖原的河流水系以放射状分布，且水系色彩为土黄色，液体属性类似温水、无害。
