//实体
//本层存在“便车”、无面灵、猎犬、悲尸、爆酸者、“捕兽夹”、潜行鲨和“聚合”，部分实体在此层级会出现与往常截然不同的行为。
//
//无面灵
//在此层级，无面灵表现出与Level 11截然不同的行为，它们大部分会对流浪者表现出极为强烈的敌意。通常他们会在森林里生活，有时能够在隧道内部发现他们，而仅有少部分表现平和的无面灵，被发现出没于草甸区域中。由于他们的生活环境，他们的衣衫相比较其他层级的无面灵要更为残破不堪。值得注意的是，幼年无面灵比其他层级的正常无面灵的行动更为迅猛，也更有在森林中袭击流浪者的欲望。除了草甸区域零散的无面灵团体外，似乎森林地区的无面灵形成了一定规模的“族群”，但是具体情况不明。不幸者联盟与M.E.G.探员都有试图与其交涉的尝试，但都以失败告终。
//
//猎犬
//猎犬在此层级更倾向于在森林中活动，它们很少离开森林，除非是为追赶猎物或躲避敌害。它们在森林中数量繁多，是其中最大的威胁，根据推测可能是因为森林内的环境非常适宜猎犬生存。通常成群结队狩猎，很少存在单只猎犬的情况，时常攻击流浪者和无面灵，它们似乎有很强的领土意识，对于侵犯领土的任何生物表现出强烈的敌意，如果森林的树上出现了爪印，说明这里是一群猎犬的领地边缘，建议不惜一切代价远离。
//
//
//“便车”
//“便车”是一种驾驶车辆的实体，他们的外貌无异于正常人类，身高为1.5-2米，他们是完全无害的，驾驶着一辆汽车，沿公路驾驶。遇到流浪者时会询问他们需不需要坐车。根据调查，它们的语言与人类的语言不同，但流浪者却能够理解。如果流浪者同意，那么他们就会搭载流浪者前进，在旅途中流浪者们随时可以选择下车，如果流浪者搭车超过一小时，那么他们就会睡着，醒来后会被送到任何一个安全的层级，身边会出现Level C-363车辆上出现的任何一种物资。如果流浪者不同意，“便车”会给予他们黄花或球状紫花。研究显示，流浪者若在来到Level C-363后没有在约莫72小时内没有乘坐过“便车”的车，那么他们将再也见不到“便车”直到下一次来到Level C-363，目前仍未发现让其下车的方法。当他们受到攻击时，会加速逃走，并不会反击。任何试图拍摄“便车”穿越层级过程的尝试都是徒劳的，在“便车”穿越前摄像设备会突然进入损坏状态，这种状态会持续到流浪者第二次主动触碰相机时，因此尚未得知“便车”是如何穿越层级的。
//
//
//潜行鲨
//潜行鲨的外貌如同前厅的双髻鲨，通常3米长，速度最快可加速到14mph，群居生活。已知它们可以在陆地中“游泳”，它们平时没有敌意，但在狩猎或者被激怒时将会追杀流浪者。通常它们会将流浪者拖入地底并杀掉，其咬合力似乎比前厅的任何一种已知的鲨鱼更强。它们的巢穴在编号BR-4的隧道中，它们无法离开地面，因此通常它们上山的速度相较于下山更加缓慢。通常它们会在森林的中心区域狩猎。
//尚未知道潜行鲨是如何在地面中游泳的，最流行的推测为“如同牧蛇一样切入地面”。其独特的狩猎方式可能也是依靠切入来进行的。目前发现了潜行鲨幼体和鲨鱼卵，依此推断其生殖方式与前厅的鱼类别无两样。
//
//
//“捕兽夹”
//“捕兽夹”是一种奇特的实体，通常生活于草甸区域，它们的外貌如同人们使用的捕兽夹，但浑身成乳白色。它们大部分身体由骨骼构成，骨骼间有关节，骨骼间有微小但及其有力的肌肉。它们用于夹住猎物的部分被称为口器，口器两旁连接的齿上可以分泌用于麻醉的毒液，同时也可以分泌一种类似消化酶的物质，测试发现它们可以在30分钟内将一只猎犬尸体彻底消化。口器中间有一块圆形的感应器官，当感受到压力时会立刻把口器关闭，很难打开，它们没有眼，只能依靠感应器官来感受周围的环境。它们的口器下方连接着几条细小的足，可以令它们以缓慢的速度运动。建议不惜一切代价避开草甸上突然出现的扁圆形白色物品。
//
//
//“聚合”
//“聚合”是一种危险的实体，一只“聚合”由多个个体构成，这些个体是一些亮银色的甲壳虫，值得注意的是，一只“聚合”个体无法生存，因此一群“聚合”个体所构成的集体被认为是一个实体。“聚合”具有较高的智慧，可以与人谈话，但它很少这样做，具有较强的敌意。“聚合”通常居住于BR-4隧道的500-?m处，目前在此层级仅发现了一只“聚合”。“聚合”可以根据意愿变化成不同的形态，也可以模仿一些实体的样貌和行为，用于蒙蔽敌人。一只“聚合”通常的形态为一只巨大的、有翅的蜘蛛，有毒素，可以麻醉猎物。尚未得知“聚合”是如何吃掉猎物的，最流行的猜测是“聚合”个体会将食物分而食之。
