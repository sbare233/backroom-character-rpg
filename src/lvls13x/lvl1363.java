//Level C-363是一片宏大的山区，时间保持在下午4点左右，Level C-363的山上可能会出现森林，也可能会生成出现草甸，由于地形的突然转变现象，极少数情况下两种区域会发生“重叠”。
//Level C-363的森林与前厅的森林别无两样，植物属于阔叶植物，少数情况下可以发现可用于饱腹的蕨类植物，味较苦，除此以外未发现任何副作用。森林内部存在严重的实体侵害现象，实体出没范畴一般可以划分为一个面积约10-20平方千米的圆，在其中会有大量实体出没，而相对的，森林外部范围内并不会出现实体。
//值得一提的是，森林中存在一些通常为绿色、蓝色或者黑色的特殊果实，这些果实具有一些特殊效果，一定程度呈现出类似于杏仁水等后室特殊物品的特点。绿色的果实尚未成熟，不可食用，蓝色的果实已经成熟，可以药用来缓解一些疾病，是此层级流浪者和实体的主要食物来源之一。黑色果实成熟过度，食用时会产生反胃的感觉。不幸者联盟的成员时常在这里活动。1
//在Level C-363山体上的草甸上则是有着各式的花朵，其中一些能够食用，而一些有害，这是另一种此层级流浪者与实体的主要食物来源，通常爆酸者会在这里生活，因此建议避开任何有腐蚀迹象的地面。以下是已知的四种花朵的功效：
//
//sweetflower
//一朵可食用的白色花。
//
//黄色花蕊白色花瓣的植株可以食用，能够饱腹，会有一种清凉甜蜜的口感，可以用于缓解红色花瓣植株所带来的副作用。
//主动触碰蓝色的花你将来到蓝色通道，目前被认为是一种自动切出，蓝色花朵数量极其稀少。
//生长有紫色花瓣的植株的营养组织内含有少量毒素，通过烘烤、烹煮等方式可以破坏液泡，释放内部毒液。生食时会中毒，症状包括腹痛、产生轻微幻觉、唾液分泌增加。大约12小时后症状才会逐渐减轻并消失，这些毒素并不是致命的，但会阻碍人体行动，建议非必要时避免生食紫色花朵。
//生长有红色花瓣的植株其总体上是无害的，然而，在根茎部位往往会长有一些锐刺，这些刺能够轻易地刺破人类或其他实体的表皮注射毒液，进而使得被注者遭遇一系列恶劣的反应，包括但不限于——引力减轻的幻觉（被描述为“灵魂出窍”）、剧烈的头痛、伤口极其瘙痒。，而以上作用都可以通过服用上文的白花进行一定程度的缓和，作用持续时间大约17小时。务必注意任何红色花朵。
//剩余花朵的功效未知
//在山区间横穿着一条蜿蜒曲折的公路，道路上偶尔会出现老式车辆，而这些车辆都是可以正常启动的。车上一般会存在有一些物资，如皇家口粮、杏仁水、火盐等。然而，应当注意的是，Level C-363的山体和公路似乎会随着探索时间的增加而不断变化，使得Level C-363无法用地图绘制的方法勘测地形，同时因此引发了多起坠落事故，甚至有些流浪者因此失踪。并不建议流浪者在没有必要的情况下以过快速度在公路上驾驶车辆。Level C-363的Wi-Fi很弱，有时甚至会出现断网的情况。
//Level C-363的山区有时能够发现连接公路的隧道，有时隧道后没有山体或山体上没有隧道），这被认为是山体地形转变所导致的。Level C-363的每个隧道口有可能出现可使用的汽车，这些老式车辆可能会在隧道口堆积在一起。所有隧道都标有编号，而可以按照编号分为四种类型，不同的隧道存在不同的内部情况，并且隧道的长度与山体长度并不相匹配。
//
//BR-1
//这个隧道的主要材质为钢筋混凝土，长约500米，灯照系统正常，内部几乎不存在实体，目前仅有两份实体发现报告，皆未被证实。目前认为隧道BR-1是整个层级中最安全的区域，是Level C-363的主要交易基地，不幸者联盟时常在这里活动。在隧道外无发电机轰鸣，即使BR-1是相较于四种隧道中灯光系统最正常的。
//
//BR-2
//这个隧道同BR-1一样由钢筋混凝土构成，长约600米，灯光系统有时出现闪烁现象。与BR-1不同，这里出现了更多敌对实体，包括无面灵、悲尸和爆酸者，在隧道外有轰鸣声，听起来是隧道内发电机工作时发出的声音。
//
//BR-3
//这个隧道的主要材质为大理石，长约800米，灯光系统的闪烁频率相较于BR-2的灯光更快。内部出现了更多的实体，包括无面灵、猎犬、悲尸、爆酸者和潜行鲨，这个隧道似乎是潜行鲨的巢穴，在这里发现了大量鲨鱼卵和潜行鲨幼体。在隧道外可以听见相较于BR-2更大的轰鸣声。
//
//BR-4
//这个隧道的主要材质为一种未知的吸光材质，这使得任何照射装备的照射范围会被缩小到方圆3米，隧道的照明系统完全失效。在内部时常存在M.E.G.人员设置的标尺，目前尚未得知这种隧道的长度，0-500m内不存在任何实体，此外杏仁水可以在这里被找到，少数时候可以发现皇家口粮。流浪者报告称隧道内存在以下实体：窃皮者、车兽、笑魇和清道夫，但报告人员都声称这些实体不是敌对的，即使他们攻击了这些实体，但它们仅仅只是“灰飞烟灭”了，推测可能是隧道的精神影响所产生的幻觉。500m以后只存在一个实体，它时常在这一段游荡，它被称为“聚合”，具有较强的敌意2，这一段隧道中不存在任何物资，标尺时常出现磨损现象，在深入过程中常常可以发现一些人类骨骼，但是根据唯一一根被带回来的骨骼的DNA检测显示，这些骨骼所含DNA不与任何一位已知的流浪者相匹配。在这里不存在任何精神影响，因此这里并不会出现实体幻象，你所看到的都是真实的。
//
