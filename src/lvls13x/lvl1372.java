//描述
//Level C-372是一座地势起伏不平的山脉，地表生长着茂密的植被。在多次长途跋涉后，我们仍然没有找到这里的边界，因此普遍认为该层级是无限大的。
//
//cuzco-valley-peru-landscape-225146.jpg
//Level C-372的丘陵地带
//
//总体上来看，Level C-372如同后室对安第斯山脉中部地区的复制品。这里气候温暖干燥，有旱季和雨季之分。雨季来临之时，地表植物会迅速生长，直至高度增长将近一倍；到了旱季，它们又会恢复原先的高度。该层级拥有完整的生态系统，很多生活在前厅的动物也同样在此处繁衍生息，最为常见的是无峰驼和大型猫科动物，它们应该也是错误地切入了后室并来到这里。
//
//Level C-372有着多种无威胁的实体。你可以发现虫群在土壤中不断地穿梭着，使泥土蓬松而肥沃，由于数量众多，它们具备作为食物来源的潜力。有流浪者报告称观察到了一种性情温顺的尸鼠，为了适应这里的气候，它们褪去了部分毛发并改为杂食。
//
//Level C-372的中心是一座被称为“新库斯科”的古代城市，为团体“太阳之子”所建造、占领（详见下文）。
