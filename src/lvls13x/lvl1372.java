//自称是五百年前在试图逃离“恶魔”的途中意外切入后室的印加人族群，他们建造了位于层级中心的新库斯科城，文明发展水平与前厅已经衰亡的印加文明相似。
//截至2022年，这个族群已经发展到了约1300人的规模，不同阶级的公民各司其职，构成了一个完整的社会体系。令人诧异的是，这个社会中没有“君主”这一角色的存在，不得而知印加人在五百年的光阴里是怎样保持团结的。
//太阳之子的成员平日里会种植作物、圈养家畜、开采矿石，也有小部分成员从事服务业、制造业和商业的工作。与他们互动被认为是有益的。
