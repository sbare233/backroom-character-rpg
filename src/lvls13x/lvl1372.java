//将物品浸入灰矿油中，确认其薄膜已经紧紧地附着在物体表面后取出。处理完毕的工具耐磨性极好，寿命长，在流浪者中广受好评。如果在物体中直接掺入灰矿油，其硬度、韧性、耐热性、耐腐蚀性都会得到大幅提升，这种材料可以用于制造各式工具、武器和机械设备，拥有广泛的应用前景。SM372-01是B.N.T.G.的A级开采对象之一。
