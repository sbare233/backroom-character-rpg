//在该层级中还有一种特殊的带有不同颜色标志的房间，这些房间随机的出现在走廊两侧，每一个不同的房间拥有不同的性质和特性。以下是M.E.G.对于每个房间的探索总结的报告。
//
//- 隐藏报告
//红色标志房间：
//红色标志房间是特殊房间中面积最大的一种，是占地约200平方米的方形房间，房间内相当的空旷。墙壁都是灰色的毛坯墙。墙壁顶部的四面有发光的荧光灯，照亮了整个房间。
//
//房间的正中间有一张木桌，木桌上放着一块发霉的松饼。流浪者可以将发霉的地方去掉来食用，味道与前厅的松饼一致，但无特殊效果，仅能用来充饥，且流浪者食用之后，松饼不会再重新刷新，但可以在新的红色标志房间内找到。
//
//蓝色标志房间：
//蓝色标志房间占地约120平方米左右，由两个区域组成，整个房间的墙壁和天花板上贴着崭新的浅蓝色墙纸，地面上铺着蓝色的地毯，房间内没有发现灯，但整个房间被未知来源的光芒照亮。
//
//在较大的房间里也有一张木桌，木桌上通常有1-3瓶杏仁水，偶尔还会出现皇家口粮等物资。与红色房间相同，流浪者在食用补给品之后，补给品不会刷新。较小的房间里有一张床，床只有床板，流浪者可以躺在上面休息，床不会对流浪者造成任何影响。
//
//白色标志房间：
//白色标志房间是该层级较危险的一种房间。占地约100平方米，房间构造呈圆形，房间的墙壁由乳白色的瓷砖砌成。流浪者在打开白色房间的门时只会看到一片黑色，且站在门外试图用照明设备照亮房间的行为均不会有任何的效果。当流浪者进入之后，房间会被未知的光源缓缓照亮，约10分钟后达到最亮。
//
//房间内会出现各种大小的木箱子，木箱子会在流浪者视野之外的地方不定时的刷新。流浪者可以在木箱里开出各种物品，比如一把无用的生锈钥匙、一张看不出来意义的涂鸦画、一包气球等。除此之外有流浪者在木箱里开出来似乎是被困在木箱里的肢团和悲尸两种实体。有时这些实体甚至会直接破箱而出，对流浪者造成威胁。M.E.G.提醒流浪者们应远离白色房间，以防遭到实体攻击。
//
//粉色标志房间：
//粉色标志房间是该层级中极少可以遇到的房间。根据进入过房间流浪者描述，这个房间是一个很小的六边形房间，在入口的对面有一个圆形的洞，通过洞可以到达其他的层级。除此之外房间没有其他多余的特征。
