//走廊区域看上去相当的破败，大部分墙面和天花板的墙纸都已经脱落，露出灰白色的墙面。地面上铺设的地毯充满了灰尘和从顶部断裂的管道中流出的水。这些管道中的水是由少部分杏仁水和大量饮用水混合组成，可以安全饮用，目前暂不清楚水的来源。天花板上的灯有少部分破损无法使用，但大部分的灯虽然灯罩被破坏或脱离了天花板被电线拖拽住，但依然可以发光。这些灯照亮了层级的大部分区域。
