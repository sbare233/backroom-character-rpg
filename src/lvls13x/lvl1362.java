//描述：
//Level C-362表现为一面积为20平方米，高3米的白色水泥房间，房间的西面，北面和东面分别有着三扇门，门上分别用黑色油性笔潦草的写有A，B，C，的字样。每扇门后各有一条长度未知的走廊，走廊的地面上均随意的摆放着大量的植物盆景，植物的种类几乎包含了所有已知的前厅植物，并且已知这些盆景的数量仍在不断增加，它们会在没有人观测时凭空出现在走廊的随机位置。
//
//A走廊
//A走廊的盆景增加速度较为缓慢，平均每天会多出一两盆。不知为何，通往A走廊的门于2022年5月22日突然关闭，无法用任何方式开启且无法破坏，所有在此之前仍逗留在A走廊的人员已被标记为MIA。
//
//B走廊
//B走廊的盆景增加速度最为缓慢，平均五六天会多出一盆，在B走廊逗留过久会令人产生一种怀旧的感觉。B走廊生活着两个浑身散发着绿光的无面灵，它们会定期在B走廊中巡视，然后将一些盆景拿起，带入B走廊的深处，不知去向，总之那些被带走的盆景都再也没有出现过，不建议攻击这两名无面灵。
//
//C走廊
//C走廊的盆景增加速度极快，平均一天会多出十几盆，但C走廊的盆景有一种特殊效应，当盆景被人触碰过后，它一定会在24小时内以自爆的方式自毁，M.E.G.已在C走廊处设立前哨防止C走廊的盆景被采集。
//
//基地、前哨与社区：
//M.E.G. Level C-362前哨
//M.E.G.在初始房间处设立了前哨，用以帮助流浪者离开该层级以及防止C走廊的盆景流出被用作武器。
//该前哨支持流浪者探索B走廊的深处，会提供杏仁水等物资。
