//目前M.E.G.已发现该层级与Level C-415一样，不遵循任何时空的标准模型，而是以非线性方式弯曲和扭曲。因此，各人很可能会发现自己离他们进入的地点不远。迷失在 Level C-398的人也经常观察到似乎违反已知物理定律的不合逻辑或其他异常特征。具体表现为有个别石块悬浮在空中，流浪者靠近这种石块时也会随之漂浮，且无法恢复原样，直至被洞顶异常尖锐的鹅管与石笋刺穿而死亡。
//
//同时，该层级中的空间大小也在不断的变化，其地貌也会随之改变，曾有记录说有一群流浪者在探索洞穴时被变化中的石壁挤压而死，目前M.E.G.已发现该层级唯一不会变化的地方为一处洞穴瀑布，现以将其作为基地使用，这里是洞穴内唯一的水源，但水中钙镁元素超标，只能在蒸馏后饮用。虽然上文提到该层级是一个石灰岩洞穴，但越往深处探索会发现该层级由不同类型的岩石组成，其占据主要部分的则是浅灰砂岩。有人曾尝试想用炸药炸开墙壁来逃出该层，但这么做只造成了洞穴更为严重的坍塌，任何想要炸开墙壁的行为都是不明智且被严厉禁止的。
//
//在Level C-398中没有任何的光照来源，M.E.G.曾试图在墙壁上装上洞穴探照灯以便于其他流浪者进行探索，但在灯安装后不过1天时间就会受到未知力量而破坏。
//
//流浪者在该层探索中会发现有许多的分洞穴，但里面大多通往的的是死路，只有少部分有极为狭窄的狗洞可令流浪者通行，这些狗洞宽度一般不超过1米，流浪者在里面爬行时洞中还会渗水出来并导致流浪者窒息，有流浪者曾报告说他们在爬行过程中发现远处有微弱的光亮，但都因狗洞的长度未知导致体力耗尽而未能达到光亮处。幽闭恐惧症的现象时常出现在正在狗洞中的人身上，除非流浪者的心理素质过硬，否则盲目进入狗洞的行为都是不明智的。
//
//实体
//目前该层中只发现了蝙蝠这一实体，暂未有任何目击其他实体的报告。它们大量的栖息在洞穴顶部，但这些蝙蝠似乎比在前厅中的更具有攻击力，且很容易被流浪者的灯光所激怒，一旦它们被激怒后，会立即从四面八方抓挠流浪者的裸露的皮肤与头发，所有被抓伤的流浪者都会在30分钟后毒性发作而死，表现为嘴唇全紫并肤色发青，他们的尸体皮肤表面也会分泌出毒素，一旦有其他有流浪者接触这些毒素，它们同样也会在30分钟后死去，服用杏仁水也无法治疗这一情况。
