//Level C-398整体表现为一处石灰岩洞穴，其内部道路崎岖，且空气浑浊，少有通风处，气温也极低，任何不携带保暖措施进入该层级的行为都会使流浪者发生低温现象并死去。所有电子仪器也在这一层中全部失效。当流浪者进入该层级后，会发现后方的路被落石堵死，据M.E.G.探测，发现该洞穴大致为喀斯特地貌，且其因地面溶蚀面积过而大造成渗穴现象，导致大量落石在洞穴内部坍塌，目前M.E.G.已收到超过10例流浪者因落石掉落而死亡的报告，所以请流浪者在勘察该层时做好充足的准备。在洞穴内部还发现少量的石英，铁，铝的氧化物与人为开采的痕迹，有人提出该洞穴前身应为私人矿洞，但M.E.G.还未进行证实。
//
