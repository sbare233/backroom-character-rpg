//描述：
//Level C-300是一个存在于人类1的思想上的层级，且只会在梦境中进入该层级。进入过Level C-300流浪者对于此层级的描述各不相同，甚至可以说是大相径庭，但无一例外，他们声称身处此处会令人感到平静和舒缓，亦或是一种似曾相识的感受，且绝大部分受访者在描述时称“这个层级带给我一种怀旧感”或是“它令我想起了自己童年那些模糊不清的回忆”。由于流浪者自身情绪的波动，他们“眼中”的环境总是会遭到不同程度的扭曲，扭曲程度与情绪的波动强度成正比。
//
//虽然存在于梦境中，但Level C-300是一种明显有别于梦境的存在，表现为流浪者的自主思考能力、对身体的操控力和对周遭环境的感知力，除此之外，当流浪者离开Level C-300，也就是从梦境中苏醒后，仍能在足够长的时间里保持对梦境的记忆，这足以让人记下在此层级中的感受。
//
//“卧室”
//如同前文所说，Level C-300的外观因人而异，但在几乎所有流浪者的叙述中，他们在进入层级后，都在第一时间发现自己身处一间卧室的床褥上，并同刚刚睡醒一般困倦，因此，卧室通常被视为Level C-300的起始点。
//
//在报告中，卧室的空间相当狭小，最小者仅能容下一张床，最大者也不过约25平方米。流浪者在卧室中通常会感受到一种独特的温暖与舒适，也有人称，他们“躺在床上、紧紧地拥抱着棉被时，明明处于一个陌生的环境中，却产生了强烈的安全感”。这些感受被认为是自发产生的情感，而非层级强行赐予的。由于强烈情绪的产生，卧室的场景通常存在着中等程度的扭曲。
//
//直接走出卧室的前门，将被送至“回忆”区域。
//
//“信物”
//在卧室最显眼的位置，流浪者将会发现一件其幼年时期曾持有并已经丢失的事物，即“信物”。所有案例中，流浪者们皆称这是他们“儿时最珍贵的东西”，且无一例外在某种强烈情绪的驱动下选择携带它们离开卧室。据称，在持有信物时，流浪者的负面情绪会逐渐消散，直至恢复至常态。
//
