//“回忆”
//“回忆”表现为进入者儿时记忆最为深刻地方，如前厅中的学校、公园或游乐场。目前并没有观察到回忆的边界的案例，故一般认为此区域是无限大的。在此，流浪者会产生怀旧感，但这种情绪并不足以导致画面的扭曲，因此，回忆中的场景对于流浪者来说显得更为清晰。据称，即使无法观察到任何其他人类的存在，流浪者仍能听到数人以自己的母语交谈的声音，内容多数为寒暄或谈论日常生活。
