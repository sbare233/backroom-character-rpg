//基地、前哨与社区
//由于Level C-392面积巨大，较为危险，难以到达，Wi-Fi强度低，因此此层级只有一个组织。
//
//蹭吃蹭喝蹭网392组织
//这个组织隶属于M.E.G.，常驻人员有四人。基地驻扎在灯光房的一间40平方米的小房间里，他们一直保持和老者的交谈，保持老者与他们的好感度，并白嫖使用老者提供的充足食物和资源。灯光房的Wi-Fi信号很强，生存条件良好。他们能帮助进来的流浪者，给予他们物资，且劝告他们要保持对老者的友好交流，保护流浪者的安全。并且能够帮助流浪者离开Level C-392（见下文入口与出口部分）
