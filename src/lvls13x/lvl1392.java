//描述
//Level C-392表现为一片破败的由中式老房屋和石制街道构成的老城区，类似于前厅中的古城或是老住宅区，此层级中房屋与街道有着规律的排列组合，但每个房屋的外表结构和内部装横都是完全随机的：有如内部全空的房屋，有的房屋内存有大量物资和家具。甚至有实心的房屋。Level C-392没有昼夜更替，永远滞留在夜晚，但东部的天空相对比较亮一些。这里的温度保持在27℃～33℃，街道在这里呈网状分布，有宽有窄。
//
//在Level C-392中，暴露在强光下是非常危险的，即使只是一瞬间，你也将会被大量实体锁定，尤其是钝人和死亡飞蛾，即使您精神状态十分正常，你也会被钝人袭击。这里的灯光虽稀少但总是凭空的出现在或是凭空的消失。这导致您很有可能没有预兆的被强光照到。不过您依然可以安全使用约150流明以下的弱光，比如电子设备屏幕，火把，小灯泡等
//
//在Level C-392的街道中，充斥着类似于蟋蟀类昆虫的叫声，据来过这的流浪者称，这种声音会使人心情烦躁，易怒，长期生活在这种声音下会导致体力不支甚至发疯。这是非常危险的，因此可以选择待在室内或者使用耳塞，摄入大量杏仁水也可以缓解这种影响。
//
//区域
//根据建筑的不同形式和排列，大致可将这里分为4类区域
//
//R1MIr5Q36H9Nbgf.jpg
//一座寺庙…
//
//旧街
//所有拥有宽型街道的区域的统称，是这一层级中最常见的区域。低矮的老旧的中式店铺挂着破烂的锦旗分布在街道两旁，在这一区域能够看到货架七零八落的百货店、假人满地堆的服装店、腐臭弥漫的小吃店等等。颇似曾经辉煌的商业街被废弃了一样。这一类区域中灯光更多，更危险；但相对的，物资也更丰富，不过能够安全食用的只有罐头和杏仁水。
//
//旧巷
//这是由一条条较为狭窄的石子路构成的区域，这些石子路漫长而遥远，但是走在街道上你会发现这段距离很短。巷道两侧建有一排排两层高的中式老住宅。这里几乎没有灯光，只有部分楼房的窗户里透着微弱的光，但并不代表这片区域很安全。
//
//灯光房
//如果您运气好的话，在旧巷区域探索时，您可能会发现一间叫灯光房的敞开着大门的建筑，那是实体“老者”所在的房子（见下文实体部分）。这里是Level C-392中最安全的地方，这种建筑内部是独立的，整个层级里的所有灯光房永远是同一个。
//
//施工区域
//被一道道施工铁皮墙围住的区域，别进去！那里面全是碎砖钢筋和淤泥的废墟，你会陷进去的，小心散落的电线，它们依然危险。
//
//ZmJ4dCc3oRLaP9B.jpg
