//远光魇
//出现在旧巷区域的一种敌对实体。没有人见过它的真正外表，它会发出一阵尖锐的咆哮随后突然现身在流浪者的身后一段距离，并且像一个远光灯一样向着流浪者射出一束约2000流明的强光，强度能够照亮整条巷子。这时它会快速地向着流浪者移动，你这时要赶紧掉头以你最快的速度奔跑，跑出旧巷区域便能逃脱。
