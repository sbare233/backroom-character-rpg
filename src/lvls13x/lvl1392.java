//老者
//居住在灯光房里的一种中立实体，拥有较为高大的人形身体和不停扭曲的诡异男性面貌。本体只能在灯光房里移动，但是其意识可以游荡在整个层级，甚至可以对流浪者说耳语。他可以流畅地说汉语，乐意与流浪者交谈和交易，且声称只要能和他聊天便可给予住处和食物。一般情况下保持友好，但是如果试图攻击他或是挑逗他便会变得敌对，发起攻击时可以整个的将他所厌恶的人吞下，尽管你跑出了灯光房也难以逃脱。
