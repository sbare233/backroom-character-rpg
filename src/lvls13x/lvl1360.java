//不时会有其它层级的实体进入，当前发现曾进入过Level C-360的实体包括但不仅限于： 笑魇、肢团、猎犬 、窃皮者、悲尸、无意识的无面灵以及死亡鼠。这些实体大多数都是以无目的性地游荡或静止不动的状态进入这一层级，也发现过极少数个例是以追逐某物的姿态出现，并且会在察觉流浪者存在并对其表现出敌意的瞬间垂直穿透地面，随后完全消失不见。当前无法确认这些实体的去向，这一现象可能部分解释了这一层级内没有其他层级实体的缘故。
