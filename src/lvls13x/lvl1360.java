//在每个流浪者的主观视角中，身体和其他物品在视觉上重合时可以通过流浪者的身体看到其背后的物品，肤色与物品固有色发生复合，导致总体上颜色偏暗。
//在每个流浪者的主观视角中，身体与其它流浪者的身体在视觉上重合会导致交集部分完全消失，边缘总是光滑的。
//这些影响会在离开Level C-360后逐渐消失，但仍然会在衣物边缘处出现不规则的灰白格子图形，图形会随着身体的移动而改变，以及身上穿着的暖色衣物会亮度增强，此效果可以等待24小时后消失或使用杏仁水擦除。在Level C-360内使用杏仁水也可短暂消除影响，但通常在3分钟后重新显现。
