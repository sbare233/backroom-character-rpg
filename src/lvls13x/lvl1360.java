//块面：
//块面是一种看似二维平面但仍然属于三维空间中实际存在的实体，尽管难以发现和测量它的厚度，但依旧可以确认由它构成了每一个正多面体的结构。块面通常是以坠落的姿态从高空落下，落在地上后也完全静止不动。期间会有正多面体主动改变轨迹接近块面，当一个正多面体来到块面附近后，将会将其涵盖在内部，随后继续进行运动；而块面则会完全消失。
//
//由于数量过于罕有，对块面的了解正等待进一步观察和发现。
