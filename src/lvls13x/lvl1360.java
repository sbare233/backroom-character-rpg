//虚线框：
//在极少数情况下，在没有流浪者和建筑的空地上会出现一个虚线框，这些实体多数是由不断闪烁的白色虚线构成的矩形或圆形的虚线框。实体出现后会在原地滞留3小时到7天不等，然后消失在原地。
