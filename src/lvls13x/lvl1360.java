//线条：
//根据最早一批进入这一层级的流浪者描述，在最开始探索时，曾在地面上发现过一些黑色或笔直或弯曲的线条在相交运动，但在察觉到流浪者的到来后迅速消失在了视野范围内，在这之后再也没有见过其它线条实体的存在。
