//描述
//Level C-360是一处无穷尽的平直空间，可以触碰到的东面、南面和地面完全平直，不存在任何弯曲。这一层级的每个方向均为灰白相间的格子图案，无法在墙壁和地面上进行标记或留下痕迹。
//
//目前仅发现在东南方向存在一个由东面和南面汇聚成的角落，若沿着其他方向前进则没有一个明确的边界和尽头。在这一层级的任何位置所看见的环境都完全一致，未发现任何可识别的建筑物。
//
//无论朝着任何方向前进，在主观视角中周围背景的灰白格子图案会跟随着自己一起移动，接近或者远离墙壁并不存在透视差异；这种情况不会影响到其它流浪者的视角。
//
//在Level C-360内没有任何可见光源和投影，放置物品不会在墙壁和地面上产生阴影和反射，但流浪者和物品自身的投影情况依然存在。根据当地居住者的描述，光源和阴影会在离开物品的边缘处被“整齐地切开”，没有任何办法对其造成影响。
//
//这一层级似乎没有明显的昼夜更替，但所有进入其中的流浪者都具有一致的生物钟，会在几乎同一时间一起产生困意、饥饿，也会几乎同一时间醒来；但被带入Level C-360的动物并不会受到影响，并且每一只动物的生物钟都不尽相同，以至于总会导致睡眠过长或缺乏睡眠。
//
//vacancy1
//Level C-360东南角
//
//这一层级内没有任何空气，也不会表现出任何气象模式，但身处其中的流浪者并不需要进行呼吸也可以存活和正常活动。被放置在这一层级内的建筑物不会发生风化和自然损毁；食物不会腐烂变质，但会随着存放时间而逐渐失去它的味道；植物和动物会一直维持在进入这一层级时的状态，流浪者的身体状态也会受此影响，毛发不会生长且几乎不会罹患传染病，但到达特定年龄段后依然会出现大多数的老年病症状。
//
//进入Level C-360的流浪者通常表现出不一致但有规律的变化，这种变化不会造成可观察到的生理和心理影响，仅仅只是在视觉上表现出异样。目前发现的变化包括但不限于：
//
//暴露在衣物外的皮肤皱纹、伤疤、色素沉着和痣全部消失，但在衣领、衣袖和裤脚处会有很小程度的变色（通常是50%中性灰）。
