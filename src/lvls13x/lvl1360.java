//在这期间将任何物体放入虚线框所在的范围内会导致该物品的消失，在Level C-360早期探索阶段时，有未注意到这些实体出现的流浪者走入影响范围内，导致经常有流浪者消失。在这之后很长一段时间里，虚线框都作为居住者排放生活垃圾的地方使用。
