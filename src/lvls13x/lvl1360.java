//正多面体：
//在Level C-360内不定期会有一些正多面体从墙壁的一端穿过，这些实体内部是空的，并以匀速沿着直线向着一个方向前进，直到消失在视野中；即便使用无人机或汽车进行追赶也无法跟上这些物体的速度，无法确认正多面体的来源和去向。存在极少数的正多面体会一直悬浮在空中或在地面上静止不动。
//
//正多面体被记录有4种类型，即正六面体、正八面体、正十二面体和正二十面体。偶尔有机会正多面体会低空运动，有时会直接穿过流浪者的身体、物品和建筑物而不受阻挡，事后流浪者声称被实体穿过会感受到一种难以描述但异常美好的感觉，例如被蓝色正六面体穿过头部时的感受会被描述为“我闻到了蓝莓的香气，感觉自己的身体像一朵翠雀花一样盛开”，且每次的感受都不一样。
