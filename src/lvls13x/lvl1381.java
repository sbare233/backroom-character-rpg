//来处
//
//岁秋七月乙末，初至汾州，尝过故人吴君之居所，见其门环绯紫，盖视之。但十息，忽入幻市，其烛无焰，色呈雄黄，其内空而无人，余行数刻，终见一缺，入内而感坠。遂至此。
//去处
//
//游舟反虢阳岛，待日出洑，月坠湖面，二者于壑而交，色光渺渺，方见盘中存物，乃为一六爻成卦。然！爻，皎也，日月相阖，方有动变之姿。鼎周生蓍草，拾其五十，陈于前，行蓍筮之法，心念去矣，诚然而掷。使六次，如三卦雷同，六卦为阳，方使出。然余暂留此处，未尝去也，麦格族亦无告所至何地也。
