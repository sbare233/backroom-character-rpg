//入口
//尚不明确，不过似乎切入该层的人，多数会抱有想回到前厅的信念，但并不坚定和具体。
//
//出口
//当至少每个类型的房间都走过10次，并对行走在这些这些房间中的动作和对房间本身产生倦怠之情后，则会来到一间大部分日常物品被“后室物品”替换的房间。其中北面墙壁上的镜子没有对房间的倒影，只有流浪者的身影，其余则是一片空白。
//
//镜子会有微弱的精神暗示，吸引流浪者靠近。当流浪者选择靠近镜子而非远离时，则会下意识地将头抵在镜面上，并随着精神暗示的引导回想自己在后室中的生活。当流浪者回想了很长一段时间时，会发现墙壁有所不稳。此时一用力，就能将镜子墙推倒到后方。
//
//镜子墙被推倒后，伴随着玻璃破碎声，整个房间的其余墙壁都立即消失不见，仅剩下一片潮湿的黑暗。此时唯一的光源便是碎在黑暗之中的镜子碎片。捡起碎片后，可以看到碎片之中映着某个层级的景象。此时，流浪者应当对着镜像中的路，倒退着走动。若是不避开镜像中所指示的障碍物，甚至直接穿行的话，会令镜像变红，越是偏移红色越是深，直至一片猩红看不清任何东西。
//
//在顺着镜像里所折射出的路，在黑暗中行走了好一会后，可以在镜像中看到镜像里出现了一扇门（尽管对应的层级的相应位置并不一定有这扇门）。在调整碎片的角度，使这扇门被完整地包含在碎片镜面之中时，碎片会不再显示那个层级的景象，而是映照着流浪者，以及其身后突然出现的大门。流浪者不能在不借助碎片的情况下观察到那扇门。
//
//流浪者在手持碎片倒退行走到那扇门时，会一脚踏空，然后坠落到 Level 0 或 Level C-1 之中，切回最开始的地方。
