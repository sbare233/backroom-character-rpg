//
//进入该层级后，向右看去就能看到居酒屋，在大部分情况下都是有客状态。一般流浪者在看到居酒屋后就会立刻了解这里的规则，原理暂且不明。
//
//在入座后，大厨会走到面前并询问流浪者想要吃什么，这个时候只需对大厨说出一个食物的名字即可。得到食物的名字后，大厨就会去到内侧以无法被任何方式观察的方式将流浪者要求的食物制作，这通常与流浪者提出的食物的复杂程度有关，但通常不超过五分钟。这些被制作出来的食物一般情况下是被认为可以安全食用的，但其被端上来的食物的外貌与味道似乎与流浪者本身的认知有关，有一组同行的流浪者报告称，他们的一名同伴点了红烧狮子头这一道菜，结果大厨真的端上了一个被烧熟的狮子的头部，对那名点菜的流浪者询问后得知，他根本不知道狮子头是什么只是想尝一尝而已。M.E.G.在这里对其他流浪者建议，不要尝试去向大厨提出你们自己根本没有见过的食物，以免发生潜在的问题。
//
//当食用了这些食物后，流浪者只需将自己认为符合这一餐价值的物品放在桌子上便可离开，目前没有流浪者提供过没有“付款”的记录，因为他们称一旦出现这个想法，他们似乎就会变得十分的沮丧。
