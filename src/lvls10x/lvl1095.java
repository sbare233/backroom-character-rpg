//在这个层级遇到实体是很正常的，但不必对其感到惊慌，从理论上在这个层级出现的任何实体都不会对其他流浪者产生任何兴趣，它们也都会遵循规则再次进行点菜。但不要尝试对任何实体进行侵略性行为，这样做通常会另大厨恼怒并发生严重的后果，这通常会让流浪者被扔到一个危险的层级。
