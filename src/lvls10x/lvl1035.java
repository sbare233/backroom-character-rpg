//“工作人员”是一种尚未在除Level C-35之外发现过的类人实体，数量未知。它们身穿不同颜色的制服，肌肉力量很强，体态大致与人类相同，但面部除了大量的眼睛之外没有其他可见的特征和器官。“工作人员”会徘徊在本层级中，高效地进行从灌溉草木到清扫地面落叶等一系列养护工作；每个个体拥有属于自己的工作划区，正常情况下不会离开本区域。在持续工作8个小时后，实体会穿墙进入就近的员工通道（一扇没有锁孔的黑色铁门），并于8小时后离开通道。“工作人员”对流浪者一般会选择无视，任何交流的尝试都以失败告终；但在行动被故意阻挠或发现流浪者行为恶劣时，它们会当即停止行动，用全部的眼睛盯住流浪者，这一凝视将持续三十秒2，若流浪者继续当前的行为，“工作人员”会将其撕开，而后清理周围的残骸和污物。本层级没有其他实体。
