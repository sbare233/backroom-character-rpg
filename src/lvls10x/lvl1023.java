//描述
//Level C-23 是由不同风格的工厂、办公区与无人商店拼接而成的规律非线性层级，占地面积未被计算，但已探索到该层级的边界。Level C-23 中的房间以密铺形式紧密连接，且由短走廊或员工门相互连接。高速Wi-Fi在Level C-23内部全面覆盖，且无论装饰风格，每个区域都会提供充满全区域的、平均照度约为350lx的日光灯照射。
//
//工厂房间通常由现代化的自动化作业流水线组成，不同房间内的流水线产出产品不尽相同，但已发现的流水线产品包括了绝大多数常见于后室的物品与食物。原材料将自作业机械中凭空生成并在机械内部中制作成需要人工作业的半成品，堆砌于需要人工分拣与处理的区域。当流浪者参与作业时，流水线将自动处理人工环节完成后的半成品并产出成品。任何半成品皆不能食用或离开该房间，且破坏机械将使房间中所有未完成的半成品消失。
//
//办公房间形似典型的中下层职员办公区，大量半隔断的格子工位挤占了该类房间的绝大多数区域。工位桌上通常摆有一台旧式计算机设备、一台黑白喷墨打印机与大量空白A4打印纸。部分工位与走廊间摆有各种观赏绿色植物与装满纯净水的饮水机。当办公房间存在流浪者时，流浪者的面部与身体数据将被采集，当流浪者表露出困倦或睡眠欲望时，办公房间将自动降低温度以试图打消流浪者的睡意。
