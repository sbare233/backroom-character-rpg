//无人商店与前两者不同，具有统一的现代极简风格便利店装潢。商店中摆满了空空如也的货架，一个无人收银台与正在运作的收银机。流浪者可以在收银机的人脸识别模块中刷入自己的面部数据，此时收银台将结合上次刷脸之后流浪者在流水线中的工作时长、流浪者“提供”的隐私数据量及收集的大数据喜好，为流浪者提供“最适合此人”的物品。提供的物品通常是流浪者曾在本层级中提到的物品，如火锅、螺蛳粉、杏仁水、乃至于极为稀有的皇家口粮，但有时收银台也会提供难以名状的结果，记录在案的结果有“异装癖服装”、“新鲜的雄性犬科动物粪便”与“新鲜的人类■■器官”等。
