//实体：
//“月亮”
//岛屿天空中的“月亮”被认为具有生命。当人员凝视月光时，他将逐渐陷入一种极端消极的情绪状态。有调查人员声称自己在月亮当中看到了一张可怖的人脸。不仅如此，当长时间暴露于月光下时，流浪者的皮肤上还会出现不同程度的腐蚀痕迹，且流浪者的理智会加速流失。这一现象无法用已知理论解释。这类伤痕通常会在涂敷杏仁水、或是上文提及的黄色蘑菇分泌液后有所好转。此外，部分曾经陷入幻觉的流浪者声称听到了“月亮”的低声嘶吼。下面是一些由他们写下的片段。
