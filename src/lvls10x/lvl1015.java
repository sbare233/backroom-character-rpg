//孤岛的面积约为五千平方米。岛屿地貌较为平整，且中央有一片湖泊，其中的成分已证实为杏仁水。湖泊周围生长着一种外观十分独特的荧光蘑菇。此类蘑菇大多散发黄色荧光、少数呈绿光。黄色荧光菇已被证实可安全食用，但绿色蘑菇分泌出的汁液疑似具有致幻效果。部分调查人员在食用后者后丧失了部分记忆且陷入十分狂热的情绪状态。食用黄色蘑菇可缓解这一症状，但无法弥补已经造成的记忆缺损。关于这一分泌液的后续研究还在进行之中。
//
//孤岛上的荒地普遍覆盖着暗紫色的草地，其上分布着一些低矮灌木，生长有深紫色的莓果（外观与蓝莓类似）。这一莓果常被用于制造紫色染料，且极难褪色。
//
//孤岛南处的一小块附属岛屿上有一残骸，外观上推测其为某种飞行交通工具。残骸上隐约可辨认出“S T ◼️ ◼️ F L ◼️ R E”的字迹。曾有流浪者在残骸的周边目击到脚印，但并没有任何图像资料能佐证这一点。
