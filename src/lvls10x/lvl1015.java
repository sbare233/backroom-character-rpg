//船夫
//岛屿东边的海洋中存在着一种叫作“船夫”的奇特实体。它们形似海豚，却通体透明且散发着荧光。此类实体绝对友好，一部分甚至可以与人类进行简单交流。
//
//当流浪者对其表示出希望前往对岸的意愿后，“船夫”将以四五个聚为一群从而提供一种来源未知的向上力场。流浪者步入其中后，船夫会轻微调整它们之前的距离，直到力场大小与流浪者重力相一致，随即开始向对岸移动。
//
//当处在船夫的力场中时，流浪者不会受到逐尸鸟的锁定，也不会因“月光”而受伤。如果流浪者在此时向海中吐痰或投掷垃圾，船夫可能会显得气愤，并将流浪者掀翻至海中，随即散去。因此应尽量避免此类在前厅中被认定为是“不文明”的行为。如无异常情况，船夫最终会将流浪者送至Level C-15.1。
