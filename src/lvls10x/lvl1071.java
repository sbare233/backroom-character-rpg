//本层级为红星建筑队的主要基地，建筑队领导人李丹带领他们的队伍，在本层级修建了许多生存设施和交通道路，以保障在此层级定居或进行交易的商人能顺利的与其达成合作。同时，这里也是许多“革命家”展露自己的野心的圣地，关于后室意志的学术讨论便在此地诞生。
