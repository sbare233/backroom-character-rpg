//描述
//Level C-71是一处绵延数万里的多雾森林，虽然雾气常年在森林中环绕，却仍然能有正常的阳光从林间落下。目前暂不清楚为何在不允许雾气出现的条件下仍然会有雾气的产生。正午的阳光驱散了部分雾气，使得能见度达到1000米左右，然而在夜晚，雾气将会更将浓郁，森林中的能见度不足5米。本层级330天为一年，一年中没有明显的四季，而是分为雨季和旱季，雨季的气温10~22℃，旱季的气温18~35℃。
//
//目前森林发现的乔木品种
//(1)黄桷树：高大落叶乔木，叶片较大，树势强健。
//(2)水杉：落叶乔木，枝疏叶细，树干通直高大，树形壮丽。
//(3)香樟：常绿乔木，树冠近球形。树皮灰褐色，纵裂，小枝无毛。
//(4)赤松：常绿乔木，树皮桔红色，树干上部树皮红褐色；枝平展形成伞状树冠。
//
//尽管如此，流浪者仍不需担心会遇到有害的实体。根据大量观测结果和巡逻报告，无足鸟鸟群和野生狸花猫是这片森林的唯二两种实体，它们经常会在白天与夜晚相互出现。若流浪者在森林中看到了发光的眼睛，则其为狸花猫的瞳孔散出的光线。
