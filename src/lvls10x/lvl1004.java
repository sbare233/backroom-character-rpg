//于是我落到了巨大的阶梯上，这些阶梯绝非给普通人使用，而是给传说中的独眼巨人使用的。不知道为什么，我没有受到重力带来的伤害，这地方绝对有某些更神秘的力量存在。
//我抬起头，这座神殿跟雅典的帕特农神庙如出一辙，连柱式都很相似，唯独不同的是，这里是没有经过战乱和历史的帕特农神庙，一切都镶着金边，那些女人雕像手里捧着的雪松木也都完好无处，没有腐烂，眼睛里嵌着的金子闪闪发光。
//我艰难地爬上台阶，每爬一步都要费很大的力气，因为它们的高度落差极大，我必须用双手扒住台阶的金边，然后奋力将我自己拽上去，总体如同在做引体向上，这种在前厅部队里才会用到的训练手段现在也落到了我身上。
//
//等我到了地方，一切又变得如此的小，甚至是不成比例的，比我刚才看到的一切都大得多，仿佛在我爬升的过程中，这些东西变大了数百倍一般。
//
//有一扇巨型大门在我的眼前浮现，这扇大门上布满了浮雕，描绘的都是诸如赫拉科勒斯之类的众神英雄事迹，在大门的顶部，有一座宙斯坐在王座上的雕像，手里捧着一束闪电，似乎要向我掷来。
//然后，不知怎的，我想推门走入这座宫殿，这座神殿吸引着我加入他们，我踩着地上洁白的大理石，向那扇大门走去。我贴近它，倾听里面的声音，是一阵笑声，男女的声音都混在一起，还有震耳欲聋的跺脚声，以及酒杯相互碰撞的声音。
