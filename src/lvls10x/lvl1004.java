//描述：
//Level C-4为一处面积为300平方米的红色岩土地，土地的三边被金属围栏包裹，这些金属围栏具有极强的弹性和十足的吸热能力。由于围栏外部滚烫的炽热火焰，这些围栏可以轻易将你烫伤，并且让你在距离他们太近时感到干渴不堪。因此，应当携带足量的水资源补给以保持你的水分。
//
//该层级的中心处有一块圆形巨石，重量未知，起初，任何探险者都可以轻易将其推动，然而，随着推动路程的增加，你所感觉的重量会逐渐增加。这种重量不是无限增加的，而且因人而异，重量总会在到达你能支撑的临界点时停止增长。
//
//该层级的一边没有被金属围栏包裹，而是向外延伸，逐渐升高，形成一座红色的高山。经过探险者总署的勘探，这座山有3000米之高。该山的表面崎岖不堪，布满岩石，有一条土路从山脚处延伸至山顶，这也是你离开这里的主要路径。
//
//通往山顶的路是痛苦的，在你前进时，会有被锋利的金属刀片切割脚底的感觉。目前没有办法可以消减这种痛苦，原因在于，如果你试图穿鞋登山，就会被从天而降的巨大石锥刺穿身躯——至少有数十个插着骸骨的石锥仍然屹立在道路上。
//
//如果爬到山顶，通过山顶向另一侧俯瞰，就会看到下方燃烧的火焰，以及更下方一座貌似大理石材质的宫殿。这座宫殿据传是真实存在的，而非幻想。活跃在几百年前的后室传奇作家Colbin·伊斯坦布尔曾经撰写过一本手册，描绘宫殿中的景色。据他描述，他和一位名叫“西西弗斯”的实体交涉，而这位实体将他从山顶向下抛掷，速度“快到火焰根本无法追上”，他因此来到了那座宫殿。以下是他笔记的摘录：
