//
//游船餐厅上可供选择的菜品有：河豚刺身，发酵鲨鱼肉，鲱鱼罐头和炸鱼薯条(有50%概率“缺货”)。不要尝试选择其他的菜品，因为它们总是处于缺货状态。一旦你选择了一项缺货的菜品，该举动便会激怒“侍者”。它们会把你带到一层的甲板边上并将你推到海里去。
//
//河豚刺身会有99.3%的概率含有河豚毒素，食用它将导致你在接下来的数十分钟内痛苦地死去，除非你有“幸运豆奶”，食用后迅速饮下约5瓶任意口味的幸运豆奶即可解毒。杏仁水也可用来解毒，但是其用量将达到50瓶左右，因此不建议使用此方式。
//
//一旦你点了发酵鲨鱼肉或鲱鱼罐头，你将在嗅到臭味的2分钟内失去意识，这时饮用杏仁水可以减缓失去意识的时间。一旦食用了发酵鲨鱼肉或鲱鱼罐头，你将立即失去意识。
