//实体
//这里偶尔会遇到一些无面灵就餐，但是他们并不会表现出敌意。
//
//班纳老板
//
//餐厅的老板是一位名为班纳的中年无面灵。他会热情地邀请你免费品尝菜品。在游船将要沉没时他会抢先坐上船上唯一的救生艇逃离。倘若你抢到了救生艇，你也可以慷慨地带着他一起逃离。他会给你指路。如果你足够幸运地摆脱了鱼群并到达他所指的目的地，你将有概率回到你来时的层级——但这样做同样会使你轻易丧命。
//
//侍者
//
//无面灵侍者穿着整洁的西装，行走在游船餐厅内，它们通常保持中立。如果你激怒了它们，它们会把你扔到海里去。
//
//河豚
//
//与现实世界通常生活在暖水海洋的河豚不同，它们可以在刺骨的冰水中游动自如。它们不会主动攻击你，但触碰它们将使你受到河豚毒素的毒害并引来它们的攻击。此时立即游开并饮用幸运豆奶可以缓解毒害。但鉴于它们分布极高的密度，在海中时建议坐上救生艇。
//
//刺鲀
//
//“刺鲀”无毒，但是它们更具攻击性，会主动对你发起攻击。它们的尖刺可以对你造成开放性伤口——流出的血液会招来大白鲨和虎鲸。而它们同样可以用尖刺扎破救生艇。
//
//大白鲨和虎鲸
//
//你的气味和血液会吸引它们。它们锋利的牙齿将把你撕成碎片。你将成为它们的美餐。
//
//这是一个统称，因为很少有人能看清它们真实的样子并活着逃出本层级。人们猜想它们可能与前厅中的大白鲨和虎鲸等大型食肉类海生生物类似。
