//第一层是甲板和厨房，也有少量的客座。你可以站在这里欣赏周围冻洋的海景，但是请小心被海风吹到海里去。你也可以解下一艘救生艇从这里进入冻洋。
//
//第二层为封闭空间设计，是主要的客座区。
