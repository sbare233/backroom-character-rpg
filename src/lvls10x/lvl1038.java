//Level C-38被描述为一个室内综合购物广场，其外部样貌迄今为止没有任何可供参考的可靠文字或多媒体资料，因为Level C-38没有能够通往外界的门和窗，曾经有探险者试图凿穿墙壁，但是被证明无效。
//
//Level C-38是一个长方体封闭建筑，一到四层地面均铺有米色大理石地砖，在地面边缘处饰以青绿色大理石地砖，每层的天花板由正方形浅灰色pvc板构成，并在一些特定位置挂有紫色导引牌。
