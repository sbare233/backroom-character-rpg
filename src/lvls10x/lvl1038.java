//餐饮店只可进行扫码点餐，点餐成功后菜品会自动出现在餐桌上，用餐完毕餐具会自动消失，餐厅后厨门整体无法打开与破坏，无法观察到内部情况，据报告称后厨内二十四小时不间断发出较大的风声，但与抽油烟机运行的声音不匹配。
