//
//Level C-38的一层是综合区，其两侧的ab座围绕中庭两端分布，中庭的空间上下贯穿一到四层，中央地板置有一部长四米宽二点七米，高四米的数字电子钟，其计时方式与普通钟表无异，无法被破坏。
//
//一些衣帽店和餐厅分布在ab座，衣帽店的商品被形容和正常的服装无差异，其商标牌均为空白，被取走后会以未知方式进行补充。
