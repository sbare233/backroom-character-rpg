//描述
//Level C-56是一条无限向前延伸的类似于前厅中学校的走廊。走廊的两边会随机出现房间和窗户，这种分布似乎是不固定的，有时两边的房间和窗户会均匀分布，有时则会只有房间或只有窗户，从窗户向外看去只会看到云层。
//
//Level C-56的房间形态各异，但最常见的，还是类似于前厅中普通教室的布局，有着讲桌、黑板以及约三十把桌椅的房间。但这种房间的门无法被打开，目前可以被尝试的所有破坏房间墙或门的行为都以失败告终。
//
//在Level C-56中，除普通教室以外，还能找到类似于前厅中学校的办公室、宿舍、音乐教室和体育教室等房间，这些房间通常为可以进入的，并且当中可能有着杏仁水、火盐甚至皇家口粮等任何可以在后室中被找到的物资。
//
//目前并没有任何在Level C-56中发现实体的记录。
//
//基地，前哨站和社区
//虽然Level C-56的补给来源和环境都相当稳定，但不知为何，这里没有任何已知的基地，前哨站和社区。
