//环境与实体
//Level C-29是一个面积无限且高度在6~7米间的平坦空间，表面分布有大量的人形实体以及少量的非人形实体，这些实体始终处于朝向相同的单向移动的状态。
//
//Level C-29的具体装潢因人而异，流浪者普遍报告其切入的Level C-29的地板主要由花色或白色瓷砖、灰色石砖、黄色盲人道等组成，该层级的照明主要来自天花板上的白色荧光灯。故此Level C-29被描述为“很像哪个地铁站或者地下通道”。
//
//Level C-29的人形实体团中的每个实体已确认为个体实体1，目前“行人”数量极大且不可计数。“行人”外部特征与人类极为相似2，外观年龄在25～45岁不等。可观察到的全部“行人”具有与人类相同的面部特征，但大都面无表情、眼神涣散。大部分“行人”衣着工作西装，但也存在少部分“行人”穿着其他服饰，包括但不限于蓝白相间的无标识校服、工装、警服、便服等。
//
//值得一提的是，自Level C-29被正式收录以来，极少量流浪者报告在该层级中发现身着M.E.G.工作服的“行人”出现在“人群”中。
//
//据报告，大部分“行人”在移动时使用电子产品，包括查阅手机、佩戴耳机听音乐，拨打电话等。但由于手机屏幕亮度过高而无法观测到其显示的内容。3
//
//“人群”在无意外时，以恒定的5.4km/h的速度向固定方向做匀速直线运动。“行人”以高密度站位，每个“行人”之间的空间极小。若流浪者在“人群”停下，将被后方的“行人”撞倒，若流浪者无法及时起身，将持续遭到“人群”的踩踏，直至死亡。对此，流浪者在Level C-29中意外倒地后，应及时采取有效的防护措施。待遇到较大的“行人”间的间隔时，起身再次进入“人群”。
//
//所有“行人”均无敌意，不会主动或被动攻击流浪者。若主动攻击“行人”，将引发“人群”恐慌4，并提高其移动速度，使流浪者遭遇踩踏事件的概率大大增加。任何武器对“行人”都不会造成实质伤害。“人群”会保持高速运动，直至流浪者到达终点。
//
//任何尝试与“行人”的沟通均以失败告终。
//
//Level C-29内存在少量其他已知的非人形陆生实体，包括无面灵、肢团、猎犬、窃皮者等，均不具有任何攻击性。经推断，这些实体均非该层级原生实体，具体来历不明。除外表不同外，这些实体的其他特性与行为均与“行人”相同，永远漫步于“人群”中。
//
//性质
//流浪者切入Level C-29时将处于站立状态，可自由移动空间仅有0.6～0.7m²，流浪者切入该层级后仅有3秒的反应时间。当流浪者意识到自己已进入Level C-29时，应立即开始随“人群”流动方向移动，以防遭遇踩踏事件。流浪者跟随“人群”行进完全程后即可离开该层级，即到达终点。
//
//流浪者在Level C-29中的具体需要移动的路程因人而异，如果流浪者主观认为“人群很长”或“到终点要花很久”，则其需要与“人群”保持移动的路程将大幅延长，到达终点所需时间同样会随之增长。目前与“人群”的可移动路程理论上无上限。
//
//任何尝试超越、突破、干扰、阻止“行人”的行为均被证明是无意义的。
//
//所有离开Level C-29的流浪者均报告在随“人群”移动时，会听到脚步声、无法识别的交谈声、手机彩铃声、无法识别的音乐声等，尽管经调查所有“行人”及其电子产品均未发声。
//
//空间上，进入Level C-29的流浪者均无法相遇，即使两名及以上的流浪者几乎同时切入该层级。但有流浪者报告称其可与“似乎离得很远的”声音进行简单交流，该种说法目前无法得到证实。
//
//根据报告和实验，流浪者切入Level C-29后将受以下四个阶段的精神影响5：
