//第一阶段：30分钟时，逐渐出现厌倦感和劳累感等。
//第二阶段：3小时后，感受到无力和迷茫等负面情绪，并称“想要停下来休息”。
//第三阶段：5小时后，产生自我怀疑、自我否定、自我放弃等等类似“认为自己很失败”的想法。
//第四阶段：10小时后，出现无法识别的自言自语，数分钟后只能识别出流浪者的呼吸声和背景“人群”的脚步声及交谈声。尽管流浪者与外部的通信保持通畅，但所有报告均表示不管如何呼唤都无法再次得到流浪者的回复。值得注意的是，所有音频记录中均未出现“重物碰触地面的声音”等可推测流浪者倒地的声音，故可以排除流浪者遭遇踩踏事件的可能性。6
//及时摄入杏仁水可有效缓解上述影响，以上精神影响均会在流浪者离开该层级后消失。
//
//无论如何，我们都建议切入该层级的流浪者若无特殊情况应尽快离开该层级，不要长时间逗留。
