//Level 718是后室的第719层。据Entity 440提供的信息，人类早在公元前539年巴比伦陷落之后就发现了这一层级，但在1984年3月22日又被我们重新发现。
//
//描述
//Level 718是一片看似无边无际的晴朗蓝天，其上漂浮着零星而遥远的白云。
//
//地面由某种能够完美反射出天空的液体材料组成，这是一层薄薄的清水状液体。这种液体无法从地面上清除，并且其具有一定厚度，任何方法都无法将其刺穿。这一层级的生物能够在这种液体上行走，因为它的表面张力似乎足以支撑任何重量。躺在这种液体上的感觉非常柔软，就跟躺在床上一样，只是更结实。但是，如果你站在这种液体上面，感觉又像是站在某种木头之上。
//
//Level 718的太阳永远不会落下，任何进入Level 718的人都会感觉到一种“活力恢复”和“精神振奋”。在Level 718，人们不需要睡眠，但在如果想在该层级休息或睡觉也很容易。
//
//Level 718的温度始终保持在21.1°C或70.0°F。在Level 718似乎总能闻到甜食的香味。其具体的味道因人而异，许多人都报告说闻到了“新鲜烤肉桂卷”的味道。
//
//该层级唯一的实体是实体440，许多进入Level 718的人将其称为“The Historian”。这个实体看起来像一个巨大的、约9英尺高的人形生物，其全身都散发着光芒，发光强度为1000流明，这就表明其不可能被拍摄或用肉眼观察。任何试图减少或过滤这种生物发出的光的尝试都是徒劳的。同样，摄像头在Level 718也会停止工作，只有离开该层级才能恢复正常。
//
//该层级上唯一的其他生物是沉默僧侣们，他们是一个非敌对的半宗教狂热团体，由自愿加入沉默僧侣的人组成，加入他们的方法为向他们提出请求或向实体440提出形而上学问题。他们会在整个Level 718内漫游，但他们大多数人倾向于呆在实体440附近。他们不允许与其他任何人交谈，他们似乎与实体440存在着某种心灵感应交流。除了实体440，他们也并未表现出与其他生物进行交流的意愿。
//
//任何未加入沉默僧侣的人不得在Level 718停留超过24小时，如果超过时间限制，就会收到立即离开的警告，然后会在7分钟后立即被传送至Level 717, Level 4, Level 0,或是Level 719。在超过该24小时的时间限制并被传送出该层级后，试图通过从Level 4的窗户外坠落再次进入Level 718的人将被传送到Level 719，试图通过Level 717的白雾进入Level 718的人将出现在Level 719。
//
//基地，前哨和社区
//沉默僧侣的修道院
//-隐藏
//后室内许多到达这一层级的旅行者都曾考虑过成为沉默僧侣的一份子。因为他们似乎从不需要食物或水，也不需要睡眠或任何形式的休息。众所周知，他们偶尔会玩不同的棋盘游戏、读书、画画或参加其他休闲活动。
//
//请注意，任何对沉默僧侣的攻击都是徒劳的，因为他们身上的伤在约18秒内就会被治愈和消除。这包括任何身体上的伤害，甚至是死亡。任何试图在该层级内伤害他人的人将会立即被驱逐出该层级，效果类似于超过该层级的24小时时限。
//
//这是Level 718唯一稳定的“前哨”。
