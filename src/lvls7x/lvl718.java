//入口和出口
//入口
//通往Level 718的唯一入口是在Level 717穿过浓密的白雾，或者有小概率从Level 4的窗户外掉落进入。据实体440所说，也可以通过从美索不达米亚的不同地方切出进入Level 718。
//
//出口
//有三个已知的出口。第一种方法是进入距离实体440约一公里的浓雾当中，该出口通向Level 719。你也可以往回走，穿过进入该层级的白雾，回到Level 717。最后一个已知的出口是要求实体440将你发送到一个生存难度为0或宜居的层级。离开的人可以指定传送的层级，也可以被传送至一个随机的生存难度为0或宜居的后室层级。有时人们会被传送至后室的宜居子层级。一个常见的随机目的地是位于Sub-Level 9.1的深红森林。
