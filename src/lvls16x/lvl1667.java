//M.T.S.G.（或MTSG）
//物质转化合成印枪，又称MTSG5，是Level C-667内一种独有的特殊加工用具，在任何其他层级内MTSG皆会失效。MTSG外观为一把外观呈流线型的白色“焊枪”，但MTSG的功能远远不止于此，MTSG拥有一块镶嵌在尾部的液晶显示屏和侧方离子流体推进器。除了能进行常规的焊接熔毁操作，MTSG还能在较为坚实的表面上依照“蓝图”对材料进行加工（如粉碎，熔化，拼接等），制成各类”蓝图“所示的物品。值得注意的一点是，只要是“蓝图”上所显示的物品，MTSG都能无视材料的各类物理性质或后室内存在的现象进行相应的操作。MTSG在进行自律加工时不需要人工操纵，依靠侧方离子流体推进器进行位置的变换。目前所有尝试对MTSG进行逆向工程的尝试均告失败，原因为MTSG内部的精密结构在拆卸过程中极易遭到损毁，导致MTSG报废。尽管有着内部的精密结构，在常规情况下MTSG仍表现得较为坚实可靠。
