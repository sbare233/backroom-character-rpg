//
//重水海，顾名思义，海水含有大量无法饮用的，存在氘原子、氚原子的重水，而这些重水即为上述非托卡马克核聚变电站的所有原料来源。在重水海上除有机玻璃球外无法寻得任何其余大到从远处以肉眼即可观察到的大型构造，但仍可时而发现一些漂浮在海上的诸如小型皮筏、帆船、邮轮小块残骸等各类其他构造。重水海的蒸发量很低，却足够夹杂天然水形成降雨。层级内所含有的放射性危害皆来自于此，但由于后室所具有的异常性质，层级内的辐射危害已降低到只能引发初入层级时的恶心，头疼等感觉的水平。目前尚未收到有流浪者因为放射性异常死亡的报告。
