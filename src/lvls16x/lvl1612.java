//该层级行星的卫星相较于行星而言体积更小，但同样具有完善的大气与生命循环系统，能够支持生命生活。卫星上普遍覆盖雪山，在部分雪山顶峰会存在一小块草地，尽管植物被证明是无法在山峰上存活的。夜晚期间层级卫星温度不低于零下三摄氏度，最高不超过三摄氏度，温度不随高度而变化，雪山的冰雪虽然在较高温度下仍不会消融，取样调查中将雪样本放入调查一起或带出层级后雪快速消融为常规的水。
