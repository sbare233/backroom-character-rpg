//无面灵是Level C-114中唯一存在的实体，但其被该层级扭曲成了三个种类，并可以和流浪者正常交流。以下为无面灵的三个种类：
//
//photo-1516841273335-e39b37888115?ixlib=rb-1.2.1&ixid=MnwxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8&auto=format&fit=crop&w=2294&q=80
//一张该层级中医生与护士的照片。
//
//医生
//医生通常为身着白色大褂，戴着听诊器和蓝色医生帽的友好无面灵。其会尝试对病人进行诊断和治疗，它们在一般情况下会快速地诊断出病人的病因，并制定出一套科学可行的治疗方式，治疗方式包括且不限于：
//
//药物疗法（药物通常为治愈水晶和杏仁水，以及层级内特有的无标签药品）
//手术疗法（成功率最高的疗法）
//物理疗法（但目前没有成功的案例）
//在医生进行手术时，其白色大褂将会变为浅绿色。
//
//当医生治疗的病人死去后，其心情将变得十分失落，故建议流浪者在碰到失落的医生时对其进行安慰。
//
//护士
//护士通常为身着蓝色护士服的友好无面灵，它们通常会对病人进行日常的照顾和看护。有时，也会换上浅绿色护士服辅助医生进行手术。
//
//病人
//病人为该层级中唯一的不稳定因素，通常为身着蓝白交间的病服的无面灵，病人在该层级中出现时，通常会带着不同的伤势，同时将会表现出不同的行为，有时，它们将会十分配合医生和护士的治疗；有时，其会对医生、护士以及其他病人展现出攻击性。
//
//当病人表现出攻击性后，医生和护士会对其进行控制，并将其送至9至10层。若流浪者帮助了医生和护士制服病人，其将会得到一些物资作为谢礼，物资类型有：
//
//杏仁水
//火盐
//寂静汁液1
//治愈水晶
//被治愈后的病人将会在Level C-114的1层中游荡。
//
//进入了Level C-114的流浪者将会统一被视作病人，尽管大部分流浪者的身体并无任何问题。
//
//护士会先将病人转移到2层进行诊断，在医生对病人进行诊断后，护士会根据病人的伤势，将其转移到不同的楼层。
//
//在Level C-114中，有着不同作用的房间，这些房间的分类为下：
//
//photo-1538108149393-fbbd81895907?ixlib=rb-1.2.1&ixid=MnwxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8&auto=format&fit=crop&w=2428&q=80
//一间位于该层级5层的病房
