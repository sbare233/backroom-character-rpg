//病房
//病房为病人所居住的地方，在病人的治疗进行结束后，便会被送至这里。
//
//一间病房内有八张病床，每张病床对应着一名病人。
//
//在病床边的床头柜中，可能会出现一些物资，比如杏仁水、治愈水晶和一些没有标签的药物，可以通过礼貌地询问护士或医生来知道药物的具体作用，但该层级中所有没有标签的药物都将会在切出该层级时消失。
//
//手术室
//photo-1551076805-e1869033e561?ixlib=rb-1.2.1&ixid=MnwxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8&auto=format&fit=crop&w=2232&q=80
//一张未进行手术时手术室的照片
//
//手术室为进行手术的房间，该房间在未进行手术时可以进入，但进入前需要进行消毒，并穿戴上医生的白大褂和蓝色医生帽。在进行手术时，手术室不允许任何人进入，但流浪者可以选择协助医生进行手术。在手术完成后，将会获得和制服病人相同的物资作为谢礼。
//
//Level C-114有着10个楼层，可以通过电梯在楼层间进行转移，其分别的作用为下：
//
//1层：为接受病人的楼层，同时也用做仓库。若病人的身体没有任何问题，或被治疗至正常后，将会被送往该层。
//2层：为诊断病人的楼层，同时也是16层中，唯一拥有手术室的一层。
//3至4层：该层的病人被诊断为轻微伤，通常，该层的病人的伤口会在进行简单的消毒及缝合后送至1层。
//5至6层：该层的病人被诊断为轻伤，通常，该层的病人会被外敷治愈药水后被送至1层。
//7至8层：该层的病人被诊断为重伤，通常，该层的病人会先送至2层进行手术后，再送至该层进行观察。
//9至10层：该层的病人通常有着心理问题，由于医生没有治疗心理疾病的能力，故只能将其控制住后，送往该层。在病人的心理问题减轻后，将会被转移至其他楼层进行治疗。
//基地，前哨站和社区
//“医者们”
//该团体由7名医者组成，他们热衷于向该层中的医生学习医疗知识，并且通过制服病人和协助医生手术而积累了大量杏仁水和治愈水晶，他们十分友善，且愿意与他人交易。
//
//入口和出口
//入口
//在后室C层群中进入任何一间医院都有几率进入该层级。
//
//出口
//在被医生确认痊愈后，可以向护士提出离开，护士会引导流浪者到医院的大门，走出大门后，便会回到原先的层级。
