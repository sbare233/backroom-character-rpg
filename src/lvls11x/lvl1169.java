//当地原住民相信所有猎犬均系同一个实体，并且根据民俗传说将其称呼为毛娼妓「けじょうろう」，传言中它因怨念而生，总在夜里会诱惑并杀死男性，故此也时常与当地的失踪事件联系在一起。尽管存在多次目击记录，但到目前为止仍未有任何一起被证实由猎犬所为的伤害和失踪事件。
