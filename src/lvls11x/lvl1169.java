//距今最久远的记录则描述了一种进入鸟居后的光景：进入鸟居后将是连绵不绝的鸟居整齐排序在一起，并且通向一座山的高处；借此得以找到进入高天原「たかまがはら」的真正入口，并且能够得到可以使人长寿和返老还童的变若水。
