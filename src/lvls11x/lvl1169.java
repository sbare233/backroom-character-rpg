//逢魔时「おうまがとき」
//逢魔时通常指当地时间在黄昏（17点-19点）或黎明（3点-5点）时出现的现象，通常呈现出遍布整个天空的霄色和乌云，颜色的饱和度逐渐增强，并伴随着强风和轻微地震。据说在此时，会有未知的存在敲响每一个居民的门扉。不得在此时为敲门声开门。
//
//当逢魔时出现时，这一层级内的所有实体均会变得急躁且充满敌意，并且开始在人流密集处汇集，然后漫无目的地游荡。大部分原住民会一直躲在家里，裹着厚厚的棉被；仅有一小部分未及时回家的原住民会像其他实体一样在街上游荡，他们总是倾向于聚集在参道上，围绕在玉垣和鸟居旁边。值得注意的是，此时原住民的瞳孔会变成霄色。
//
//逢魔时总是呈现出类人的表情，通常是大片乌云内的凹陷或天空背景下的少数乌云构成，因上方两处呈拱形和下方为倒拱形而被描述为在看着地上的人微笑，故此当地原住民都将逢魔时视为一个具有思想和因怨恨而生的妖怪。
