//描述
//　　
//Level C-195似乎是前厅的中国南方某城市的复刻版，流浪者评价其复刻精度可以用“一模一样”形容；该层级内部的资源、自然环境和布局也与前厅相同，且拥有8小时为一周期的昼夜循环。但该层级内部包含大量敌对实体，使得长期开采该层级资源并用于其他层级的计划变得困难起来。
//　　
//Level C-195有一种奇怪的专属特性：所有敌对实体似乎都是该层级的“市民”，且都对彼此保持中立或友善态度，并遵守夜伏昼出的生活规律。在层级各处都能看到身着各式城市人类服装的无面灵；悲尸扮演着建筑工、环卫工人等前厅劳动者的形象，有的也以商店售卖员、餐厅服务员等形象出现；猎犬有时会以宠物狗的姿态在无面灵身旁出现，就像在Level 11中可能会出现的情况一样；死亡飞蛾通常会出现在植物旁边，就像前厅的昆虫给花授粉一样，无论雄雌。笑魇、窗户等其他实体也会出现，但其行为似乎不受该层级特性的影响。
//
//在没有注意到流浪者时，实体们都会按照上述的行为模式行动。但当一个实体注意到流浪者时，该实体将立刻开始对流浪者展开不择手段的攻击，且会在将流浪者杀死后恢复原来的行为模式。雄性死亡飞蛾同样受该特性影响并会攻击流浪者。
//
//该层级的部分区域有红荨麻正在生长，请务必不要靠近这些区域以避免感染。值得一提的是，该层级的实体在由红荨麻感染上气球病后，即使已经到达病症的第二、三阶段，仍然不会对其它实体产生敌意，但其它实体会尽可能地远离他——前提是这些实体没有发现人类。
//　　
//由于其环境与前厅城市相仿，Level C-195也有与前厅城市相似的商业区和居民区等区域，这些区域内有大量的受Level C-195的特性影响的实体居住和工作。商业区中的大部分品牌无法识别，但也出现了一些前厅中的品牌。每天9：001时，大量的实体会从居民区涌出进入商业区“工作”。从9：00到17：00的8个小时中，会有不是工作人员的未知来源的实体来到商业区“消费”。这些实体使用的货币种类与样式，以及它们之间是如何交流的无从得知。到了17：00后，商业区的实体会回到居民区休息，直到第二天的9：00。夜晚期间，室外区域仅有笑魇活动，是流浪者从商业区搜集资源的好时机。如果流浪者在此时造成破坏，到了凌晨4：00时被破坏的地方会自行复原。
//
