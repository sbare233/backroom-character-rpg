//Level C-195的公共设施等似乎均由实体维持运转，也能在公路上看到公共汽车、私家车和特种车，所有的车种的品牌及车牌号均无法识别。有些实体似乎会乘坐或驾驶这些车辆前往商业区的“工作”地点。曾有流浪者在白天冒险外出探索时目击到在一个十字路口发生了一起两辆私家车相撞的事件，并造成了一系列的交通堵塞。该流浪者报告说，自己“至少看到了几十几辆由悲尸和无面灵开着的车子堵在了红绿灯口”。在事故发生后约20分钟，一辆警车开到现场，从上面下来2个身穿警服的无面灵。此后，该流浪者选择在未被大量敌对实体发现时脱离现场，未能记录更多情况。这项目击记录表明，Level C-195中存在由实体组成的执法部门。
//　　
//Level C-195内也存在类似“学校”的地区，这些地方经常会出现未成年无面灵，且多半有成年或老年无面灵陪同。所有学校的门牌上均没有任何文字或信息。在6：00～7：15，学校的门口会有来自居民区的大量未成年无面灵涌入，这个时间段被称作“上学”；相对的，在17：00这些无面灵会从相同的大门走出并回到居民区，被称作“放学”。尚不知道未成年无面灵在学校内是否会开展“学习”，因为所有试图进入学校的举措都失败了。与商业区的情况不同的是，学校附近的区域在“非上下学”的时间段时没有任何实体，包括笑魇。
//　　
//在Level C-195的各处可以检测到较为稳定的Wi-Fi，但目前被成功破译密码的Wi-Fi信号源仅有16处。通过该层级的Wi-Fi可以访问前厅的部分网站，包括但不限于[数据丢失]等，但不能访问前厅国家的政府网站或前厅学校的官方网站等。该层级的实体并不会因为流浪者连接Wi-Fi或访问网页而得知流浪者位置，因此在各类网站上进行操作是安全的。
