//描述
//Level C-117是一座面积约为9000平方公里的现代城市，拥有复杂的立体交通系统、覆盖整个层级范围的网络信号和约400万人口。该层级拥有体量较大且完善的武装力量和政府管理体系，并有3个高等学校，但没有民用客运飞机站与列车站，取而代之的是当地的近地面飞行器。
//
//architecture-1868667_640.jpg
//近地面飞行管制期间流浪者社区上空拍摄的Level C-117
//
//
//Level C-117内社会的科技水平显著高于2022年的前厅社会，例如全面民用化的全息影像、个人低空飞行器，军事科技方面则能够生产能量立场护盾并进行小规模商用化，多数武器仍属于化学动能武器，但火药性能有所提升。
//值得注意的是，该层级内部并没有任何类似农场的自然食品作物繁育基地，只观察到存在一个食品加工厂。目前该层级的食物来源未知。
//
//当地政府对流浪者的态度较为温和，在城市边缘开辟了一块区域供进入Level C-117的流浪者生活，但不提供食物与任何社会救济福利。在该层级生活的流浪者被要求遵守当地法律，并且当地政府拒绝M.E.G.与其他团体进行正式交涉，这意味着如果有流浪者在该层级违反了当地法律，则无法受到M.E.G.组织的保护。因此，在进入该层级之前，我们建议先阅读该层级政府出版的《民法典》。我们不推荐流浪者在该层级产生任何冲突，当地警察普遍配备武器，并且对流浪者态度并不友好。
//
//注意事项：
//该层级政府所颁布的法律与前厅社会有所不同，需要各位注意
//
//不要将杏仁水带入Level C-117， 任何甜品或味觉感受为甜味的可食用物体在该层级均属于违禁物品，被发现将遭到被送往食品加工厂服役的处罚。Level C-117内部有免费提供的纯净水。
//我们建议进入Level C-117的流浪者尽快找到一份工作，定期的清理房间和社区和帮助特殊人群也被视为一种“工作”并能得到酬劳。
//如果有人向你提供食物，尤其是甜品，无论是否免费都不要接受。如果条件允许，可以前往最近的警局以“提供甜食罪”向其举报，作为报酬，你会得到那份食物，但不建议立刻食用。
//不要试图在商店内购买糖、非全麦面粉、饮料以及水果。
//每天早上出现在房间门口的面包可以选择不吃或者丢掉，但别让其他人拿走并食用，否则会受到处罚。
//保持身材，不要发胖。如果你的身体在进入Level C-117就有肥胖现象，则不建议进入该层级。
//如果你进入了当地政府，特别是在健康生活委员会内工作后，不要向同在Level C-117的流浪者或当地居民讲述你的工作内容或是你在政府机构内了解到的信息。如果你实在感觉难受，建议离开Level C-117。
//不要靠近位于城市边缘的食品生产工厂，最好也不要打听相关信息。
//你和你的终端设备会被监视，但这是正常的，并且不影响生活。但建议不要就当地政府的行为发表意见，否则你在离开Level C-117时可能会遇到一定困难。
//不要犯罪，Level C-117内部没有监狱或看守所存在。
//在这里活下去不难，但对大多数人来说可能会有些不适应。
//通常来说，Level C-117被流浪者称为“难得赛博朋克风的层级”，在这里生活并不需要时刻警惕实体威胁，但相对的你可能会需要找回在前厅生活时所锻炼出的现代社会交际能力。M.E.G.支持并建议“出生在后室的一代”进入Level C-117生活一段时间去学习知识和增进社交能力。
//
//令人庆幸的是，Level C-117内部的原住民之间没有被发现能够离开该层级。
