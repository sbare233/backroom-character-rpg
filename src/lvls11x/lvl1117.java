//Sweet Croissants&Pizzas甜品店对M.E.G.组织持友好中立态度，他们为进入Level C-117的流浪者提供住宿和工作机会，并借此向M.E.G.换取一些物资——作为非正式性交易，M.E.G.向Level C-117政府输出人口，而后者则为M.E.G.提供部分武装与科学技术，层级内部的教育机构也对外开放。但甜品店以维护健康生活为由拒绝M.E.G.特工与武装人员进入Level C-117。
//尽管M.E.G.与甜品店处于非正式合作关系，但由于以上原因，流浪者在该层级的生存状况实际由甜品店负责。
