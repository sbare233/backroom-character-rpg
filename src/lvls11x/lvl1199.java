//描述：
//Level C-199的主要部分是一个老旧的住宅，其内部布局类似于中国乡下的建筑。住宅共有三层，均有水电供应。一楼由餐厅、一个厨房、楼梯间和庭院组成。该层级的时间恒定在下午三点至五点左右，且没有任何实体。该层级具有类似于Level 12的特性，所有有关于该层级的照片都会看起来像加上了一层滤镜，使其变得令人感到怀念起来。
//
//进入Level C-199后，流浪者根据其经历和性格会对环境产生不同的感觉。大多数流浪者会对此层级感到一股强大的既视感与熟悉感，即使他们从来没有来到过这个层级。这些流浪者中的一部分会对此地产生迷恋，但依然会同意离开，并在离开后产生一种依依不舍的情绪。这种情绪只会持续约一个星期左右，之后便会消失。少部分流浪者会对该层级感到厌烦，并立刻离开该层级。
//
//同时进入Level C-199的人数似乎是有限制的，一般为三个。如果尝试进入该层级的流浪者中其中两个人是其他人的父母，则该层最多可以同时进入五人。流浪者在此驻留的时间也是有限的，在此驻留的时间越长，流浪者就越是有一种想要离开的情绪，即使他们并不讨厌留在这个地方。该层级在有流浪者驻留时任何人都无法进入该层级。
//
//一楼的地面为石质，上面布有少许小裂痕，似乎是因为冷热骤变导致的，不过有报告称注视裂痕会产生想要触摸裂痕的感觉，注视的越久这种感觉就更强烈。触摸裂痕即可消除这种感觉，且不会带来除此以外的任何后果。餐厅中央有一张餐桌和数只椅子。椅子的数量似乎会根据进入该层的流浪者数量变化，且总是会比流浪者总数多出两只，目前尚不知道为什么会产生这种情况。
//
//厨房中有油烟机、煤气灶等设备与砧板、菜刀等物品，也会生成米、蔬菜、肉类等食物，以及小米辣、醋、酱油等各种调味料。煤气灶旁有一扇窗户，会发出白色的阳光，但无法透过玻璃看到任何东西。食物生成的数量同样会根据进入该层级的流浪者的人数变化，且种类会符合每个人的喜好。将从厨房中已有或生成的物品带出该层级是不可能的，因为这种尝试会使人产生无法忍受的巨大的罪恶感，并因此立刻将原先试图带走的物品放回原处。使用完毕物品后没有将其放回原处不会产生后果，且下一次进入该层级时物品会回到原处，就像没有使用过一样。如果上一次使用砧板和菜刀后没有清洗，则下一次进入该层级时用砧板和菜刀处理的食物会带有上一次使用时处理的食物的味道，这种效果产生后不会持续至第三次进入该层级。
//
//楼梯间的光线较为黯淡，但仍可看清楼梯本身。除此之外，楼梯间底部还有一扇门，穿过它可以到达Level 189，且到达后仍可由此返回Level C-199。由于Level 189为一系列卫生间组成的层级，因此推断这扇门原本通向的是住宅的卫生间，但目前尚不知道这扇门为什么会通向Level 189。如果在Level C-199逗留的流浪者全部由此离开，则门会在所有人走出后关上并无法打开，且不能强行破开。反之，若离开的流浪者并不是逗留该层级的所有人员，则门将可以打开。1由楼梯间可以来到二楼。
