//二楼有两个房间和一个阳台，其中通往阳台的门无法打开，强行破开的尝试均以失败告终。两个房间中，一个布置有一张双人床、一个衣柜以及两个床头柜，另一个布置有两张单人床和一个柜子。衣柜、床头柜与柜子通常是空的，且流浪者放在里面的物品会在所有流浪者离开该层后以未知方式回到流浪者身上。物品回到流浪者身边的位置不定，从流浪者的背包到衣服口袋均有可能。2所有床均盖有一条被子和两个枕头，据报告称躺在上面“会有一种极度舒适的感觉”，但目前关于床垫使用的材料没有任何已知信息。
//
//在二楼的楼梯口的天花板有一个通往三楼的入口。由于[数据丢失]，目前无法进入三楼，且不建议尝试进入三楼。
//
//穿过一楼的大门可以到达庭院。照射到庭院的阳光使这个区域热到几乎无法停留，因此无法对这个区域进行更多研究。庭院有一口井，可以打捞到井水，但由于庭院的温度，目前没有办法一次打捞大量井水。目前打捞水量的最多记录为一升。井水具有杏仁水的功效，其他方面基本与前厅的纯净水相同。不知为何，井中的井水似乎可以自行补充，且不会因为高温而蒸发或沸腾，而在打捞后这种特性将会消失。庭院的墙壁似乎与住宅一楼的地面材质相同，但由于温度无法对其进行检测。庭院还有一个与墙壁等高的铁栅栏门，透过门可以看到外面种植有玉米的农田，但不能看到别的景物。庭院也是Level C-199为数不多能够看到外部景色的地方。由于庭院的温度，尝试打开铁栅栏门会立刻导致烫伤，因此不建议也无法探索外部环境。不知为何，庭院的高温并不会影响到住宅内部。
