//描述：
//Level C-112是一个类似盘山小路的地方，一条由被人工简略修整过的岩石所铺成的道路而围绕一座山而组成。岩石道路一边靠向山体，而另一边则是悬崖，并且山体上生长着很多黄山松（PinustaiwanensisHayata），且整体环境的湿度较高，气温也处于15℃-18℃之间。
//
//进入该层级时，所处的位置通常都为山脚下，此时流浪者向后转去会发现没有道路，从道路的尽头向下看去将看到与天空同色的虚空，似乎整个层级并没有连接到“地面”而是以一种未知的方式浮在空中。此层级的海拔高度似乎很高，从悬崖向外望去会发现云层通常都在此层级所能踏足区域的下方，层级内的气压很稳定，没有出现高原反应的报告。从下向上望去，此层级大概40-50米高并且上方的空间会越来越小，但实际上根据大量流浪者的报告称，此层级的高度绝对不止40-50米高，并且向上无论走了多少层，围绕山体实际走一圈的时间都没有变少。向远方望去，可以看到更低的地方有一些其他的山峰，但由于湿度问题导致经常起雾，远处山的山峰的细节无法确认辨别。此层级山脚Wi-Fi信号偏弱，但越接近山顶Wi-Fi越高。
//
//每绕半圈左右，流浪者便会发现整座山有一个向内的凹槽，岩石道路也会沿着凹槽向内延伸，并在内壁通过一个悬崖方向完全与外界裸露的山洞。山顶有一个湖并且顺着靠山体凹槽一侧的沟顺流而下，最终形成瀑布流向虚空。湖水全部都是杏仁水，但对疾病的救治效果十分微弱，通常需要加热后才会显现出来，其味道接近纯净水。
//
//岩石道路宽3米左右并且每上升20个台阶就会出现一个平台，随后继续向上的台阶的方向将会向山体靠拢，从而形成一个环形向上的道路。每个平台都会在靠向悬崖的一侧延伸出一座中式亭或者阁，这些亭阁造型不一，亭与通常的亭子无异，但内部较为干燥并且实际空间会比从外观测到的要大一些；阁与通常的小型阁楼也没有太大区别，内部同样干燥与宽阔。这些亭阁据统计大约有上千个。
//
//山顶是此层级的最顶端，这是是一个约500平方米的平坦区域，除了杏仁水池塘外，这里的土质养分充足，适合种植。山顶有一座整个层级占地最大装修最精致的阁楼，进入阁楼向下走可以前往区域书阁。
//
//此层级内用有一种名为“第C-112效应”的异常效应存在，在任何加入了某个团体组织1的流浪者有在此层级想要建立一个据点或者与其他人为了团体组织争夺利益的想法时，他们会经历剧烈的头痛，任何药物都无法缓解。如果流浪者强忍头痛继续打算施行想法，头痛将会变得更加剧烈，直到流浪者放弃这个想法为止。
//
//实体
//层级内出现了多种实体与动物，除去山顶池塘及靠山体溪流内的小型鱼类外，无足鸟与待救狸奴曾被多次目击。
//
//“仙人”
//一名身着中国古代风格服饰的白发老人，其踪迹难以判断。经常出现在流浪者附近给予自己的观点，大部分流浪者似乎都很喜欢接受它的论点并很少与之发生争执。
//
//基地、前哨与社区
//因“第C-112效应”的存在，目前此层级内只存在松散的流浪者社区，这些流浪者想要做出决定时一般通过投票决定。
//
//入口与出口
