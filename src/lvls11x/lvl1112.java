//当流浪者进入该区域后，便可以看到一个放置于楼梯对侧的一扇关闭的木门，木门旁有一个书桌，而书桌上则有一个竹筒。该竹筒刚好可以放入一卷竹简，而如果放置的竹简是记载了那些类似C层群环境的竹简，那么木门将可以被打开。流浪者进入后便可以来到竹简所描述的层级，但这个传送是单向的，流浪者无法直接回到书阁内。
