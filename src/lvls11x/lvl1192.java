//自从此处变为森林，其他层级的生灵也来到了此处。我们的圣地只允许和平的生灵来到这片土地，截止至本篇文档撰写之时，也仅有无足鸟与狸花猫这两种生灵居住于此。有时，一些来自家乡的动物也会在此处突然出现，我们认为，它们就是这片圣地赐予我们的“礼物”。这里也有一些奇异的原生生灵居住在此，我们对它们知之甚少，建议你不要打扰它们。
