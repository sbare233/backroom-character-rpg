//实体
//Level 906中有一特殊实体，她看上去对整个图书馆具有绝对的控制权。这一实体从外观上看是一名皮肤苍白，拥有金色长发和淡蓝色眼睛的矮个子人类女性。她通常的穿着为一件精致午茶袍，不过也被见到身穿其他的各种服饰，例如晚会礼服以及管家服和女佣服，并称自己为"Blanche"。她十分欢迎来访者的到来，并会用茶招待任何访问她图书馆的人。如果来访者表现出不礼貌的行为，他们首先会被警告，而后因他们的不逊举止而被Blanche大声斥责。如果他们拒绝改变自己的行为，他们会被以合乎礼仪的方式训诫。怀有敌意的来访者会被无形力量钳制住。如果这一来访者继续表现出敌对行为，他们会被强制送出图书馆并回到原先的层级。
//
//此外，微光向导经常被发现在这一层当中漂浮。根据报告，它们会自动消失、闪烁，并以看上去没有规律的模式移动，比较其他层中观察到的这类实体的行为，这些行为是非典型的。
