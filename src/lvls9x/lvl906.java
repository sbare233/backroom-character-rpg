//茶室由图书馆中央的一处小型厨房区域组成。其内有着可工作的烤箱和熔炉，几台储藏柜，里面储存了各种类型的茶，以及一只装满了瓷制杯碟以及银制餐具和大量烹饪书籍的壁橱。这里是Blanche为客人准备茶与饼干的地方，有时还会用来准备正餐。她的茶会往往会持续一到两个小时。在这期间Blanche会尝试与她的客人聊天，询问从他们的生活到兴趣至专业领域的各种话题。
// 
//
//客房：
//Level 906具有未知数目的空卧室，供Blanche的客人们使用。Blanche能够自由改变和操纵这些房间，来更好地收容客人，满足他们的需求。如是，这种可变化的性质使得对这些房间进行固定描述不太可能。受伤的客人会被带到其中一间客房，而Blanche会帮他们处理伤口。这些房间中的一间似乎被她的男管家兼门徒Tom Von Haderach永久占用了。
// 
//
//超视
//有传言称，长时间待在Level 906可能会导致一种被称为“超视”的现象。虽然没有目击者陈述、事实报告或任何其他形式的证据，但该传言已经普遍流传开来，难以忽视。 据说这种现象可能对神经系统造成严重损害，导致记忆力、自我认知、感知能力的丧失，甚至在某些情况下致人死亡。
