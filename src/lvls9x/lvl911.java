//已然进入雾中的那些流浪者早已杳无音讯。若他们真的“生还”，请务必无视他们，他们再也不是人了。这些非人生物是无害的，他们仅仅是再没有生活之渴望的，已然迷失的孤魂野鬼。不必为他们感到扼腕痛惜，他们不会感知到任何感情。
