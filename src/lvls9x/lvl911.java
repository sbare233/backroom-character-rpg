//若卧房的门莫名开启，应在门再次关上之前保持不动，并迅速检查何人失踪。
//忽略所有敲门声。敲门的可不仅仅是些在开派对的善茬。
//无论何人何物在召唤你，在6 AM前绝不能走出卧房。
