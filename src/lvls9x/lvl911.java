//若另一位流浪者出现在浴室内，你仍可以使用浴室的其余部分。
//若浴室内空无一人，请在进入后随手关门。
//若你在淋浴时发觉有其他个体进入了浴室，请无视他们。无论他们是人类与否，请务必在他们离开后再走出浴室。
//— 夜间规则 [12 AM – 6 AM]
//尖叫声是助你生存的帮手。若尖叫声停止了，流浪者们必须顶替尖叫声直至其重新响起。
