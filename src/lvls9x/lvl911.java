//
//跳离这趟班车可不是明智之举，任何尝试离开班车的尝试都是徒劳之举：每个出口都无法使用；车窗坚不可摧，车门紧闭锁死，车身上连一丝剐蹭痕迹都无法留下，直到每个人都到了终点站。 但你真的想跑出去吗？在车里难道不是更安全么……
