//地下室的门处于常闭状态。
//如地下室门敞开，周遭气温会于你注视空气之时骤降，请马上跑向起居室并稍等几分钟。在气温仍是正常之时，立马关紧地下室门。
//有时起居室内的电视会莫名自启，无论电视放送内容为何，请马上关闭电视。其放送的某些影片总会令观影者汗毛倒竖。
//酒店内有许多不同的楼梯，如其中有些楼梯似曾相识，切勿走上这些楼梯。
//酒店内总是会出现2条狗和1只猫。2条狗结伴而行，而猫会从远处跟随它们。
//如猫忽然直立在原地，说明最近有某位流浪者已经身处黄泉。而狗则会去分食其尸体。
//若狗未被公猫尾随，立刻前去寻找他。他有时确会离奇失踪。
//— 日间规则 [12 PM – 12 AM]
//各位流浪者须将其随身物品置于卧房之衣橱内。任何不置于其中的物品最后会消失不见。
//就寝时间为9 PM至12 AM。如你未能在午夜时分归宿，那么卧房之门将不会再为你敞开。
//若你感到饥肠辘辘，厨房内则会出现已为每位客人准备好的正餐，就餐时间为7 PM至11 PM。切勿在其余时段闯入厨房。
//切勿让狗在此时段闯入卧房。相反，应让猫进入卧房之中，在此时段他能为流浪者带来好运。
