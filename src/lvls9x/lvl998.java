//Level 998是一片空旷、平坦的平面，由状似云层的物质构成。这些云被压印在地表，就如同3D纹理或是全息投影一般。随着流浪者位置与视线的变化，云会发生相应的移动，并给人以立体的错觉；但实质上，该层级的地面为坚硬平坦的固体表面，带有羊毛般的纹理。这些地面上的纹理会给人一种发自内心的感觉，仿佛其正是记忆中虚幻而熟悉的云。
