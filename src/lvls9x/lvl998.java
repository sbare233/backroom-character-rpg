//虽然Level 998内的云层地表是最稳定的区域，但在离开房间大约30千米后，层级性质便会出现变化。流浪者踏上的云层将开始变得灰暗，同时，天空也将与地面一样发生色调转换。伴随着颜色的变化，凉爽的微风将会一同出现。这一现象导致此时的温度将会从室温3降至大约11°C，即52°F。
//
//此时，将会有少量的各类“普通”实体出现。根据目前深入Level 998的探索者上传的信息，笑魇、钝人、被Entity 17感染的人形及非人形生物，以及木灵，都会于此现身。然而这些实体并不会如同在其他层级时一样正常行动，而是会漫无目的地徘徊、张望。随着气温下降、灰度上升，Level 998内的实体将越发温和，准确地说，它们的举止将越发与其正常行为模式相悖。
//
//大多数在该层级出现的实体除了移动之外，几乎什么也不会做。在很多时候，它们甚至会忽视眼前出现的流浪者，只是单一地向外移动，背离着该层级的中心。
//
//然而，值得注意的是，Level 998内的实体并非是严格“安全”或是“温顺”的，对于任何人而言，都不应用类似无害的词汇形容它们。在Level 998内激怒实体、过于靠近实体，甚至只是单纯不走运，都有可能会导致实体被激发出正常行为模式，开始攻击流浪者。
