//虽然这些二维平面云层构成了Level 998的主体，但流浪者们抵达该层级时，并不会直接出现在云层之上。Level 998不像大部分层级一样具有多个入口，无论从哪个层级进入该层级，似乎都会抵达同一位置。所有Level 998的入口都将通向一栋写字楼楼梯间的最底层。
//
//在走过八段台阶之后，流浪者将抵达一个小而空1的房间。最近，这个房间里出现了一堆彩绘鹅卵石，大致上可以用于计数自愿进入Level 998的人数。目前仍然不知道Level 998云层的入口处堆积了多少鹅卵石，但根据最新上传的照片，已经有约38颗石子被粗略地堆成了一堆。
