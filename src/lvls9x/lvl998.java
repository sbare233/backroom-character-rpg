//虽然没有任何图片可以证实或是否定，但是据称，Level 998的黑色区域中实体数量将会大幅上涨。与先前的灰云中层地带类似，黑云区域内的实体也不会遵循它们原有的行为模式行动，只会像大部分来到这一区域的流浪者一样，向着背离中心的方向漫游。然而由于此时实体出现的概率较大，流浪者依旧有可能会受到它们的攻击，尤其是那些兽性较强的实体，例如笑魇、猎犬、悲尸、爬菌、瘟疫地精与牧蛇6。
