//描述：
//这个层级呈现为一座由浅灰色混凝土筑成的废弃建筑物。因为建筑已经很旧而且开始变质，墙面上经常出现棕色的污斑。向窗外望去，不能看见天空，仅能看见大楼。所有窗外的大楼都是相同的。这些楼高达无穷，有无限的地下楼层、亚楼层、字母编号楼层（A、B）等等。
//
//不是以纯数字编号的楼层都有奇异的性质。例如，B1层极似Level 1，B2层极似Level 2，由此类推。A编号的楼层全部整体翻转，C编号的楼层上下颠倒。由此类推。奇异的是，每个楼层都没有发现任何实体，而且每个楼层的面积是有限的。所以，探索这里能够有效地替代探索危险层级，规避风险。
//
//在电梯的按钮中楼层通常很难找到，因为按钮是无序排列的。当你确实找到目标后，电梯可能会用一个小时才能到达目标。在第一层的主电梯中，重要的楼层都被标上蓝色字，奇异性质楼层被标上橙色，与后室层级相似的楼层都被标上红色。
//
//层级被认为含有无限的房间构型和组合，但因为这里天然的布局，这不可证明。每部电梯拥有不同的楼层按钮，而且每个楼层有4部电梯。它们通常布局在楼层的每侧。在曾经探索过层级的7个人中，有4个是M.E.G.成员。有三人在层级中停留了整年以上，剩下的仅仅停留了少于一个月。有一人在这里停留了三年以上，并将发现全部提供给M.E.G.。但现在此人已经失踪。以七人进入层级的数据计，达到此层级的概率仅有0.0091%。
//
//有一个实体经常在B编号楼层中出现。它有能力构建后室层级，而且正在建筑所有层级的副本。其他楼层只是失败实验品而已。它在B编号楼层中被发现，因为它一直在改进所有层级的复制品。现在我们对它所知甚少，主要因为它喜爱独行，且没有被真正看到过。
