//地面层： 这层楼看起来像塞浦路斯的普通房子，完全由混凝土制成。这层楼有一个厨房，一个客厅，一间浴室。客厅里有一条向上的楼梯。此层有 杏仁水。这是被探索最多的楼层。这个楼层的地板上有一个洞，能通向地下楼层。
