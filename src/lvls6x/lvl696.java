//1层：这个楼层与任何房屋里的楼上相似，除了它完全由混凝土制成。有一个卫生间、一个卧室和一个阳台。这层能够找到一个撬棍和稀少的杏仁水。有一个与地面层相连的楼梯，所以不需要坐电梯到达这里。
//
//1.5层： 此楼层类似于DJ室。于此可以找到一个不可食用的蛋糕。此楼层极难到达，因为绝大多数电梯不包含1.5按钮。所以可行的入口是乘电梯去2楼，但在正确的时间离开电梯。这很困难，因为电梯在移动时不会自动开门，强制开门也很难。奇怪的是，1.5层确实有电梯，但当你到达选定的楼层时，它会消失。
