//地下楼层
//穿过地板，可能到达地下楼层。除此之外，没有方法去往地下层级。一旦到达地下层级，就可以看见上下的楼梯。地下楼层没有电梯，所以深入地下层级就需要使用楼梯。奇异的性质会每层递进，例如-1层可能会发生“卡顿”。如果你想要撞掉一个物品，掉落可能需要2秒钟，而且你的手也有可能穿过这个物体。从物体掉地到听到砰声可能也需要两秒钟。发现过的最深地下楼层是-3190层。这里的所有东西都是故障似的，而且难以穿越这些故障找到出口。
