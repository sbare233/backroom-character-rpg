//若流浪者尝试穿过该层级的森林，他们将返回到他们出发的地方。目前的理论是该层级公寓以外的区域是不稳定的，导致流浪者的行走路线在不经意间发生弯曲，走向反方向。这不会立即对流浪者造成伤害，但会迫使流浪者穿过大门进入该层级的公寓。
