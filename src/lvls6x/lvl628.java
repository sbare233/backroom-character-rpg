//入口
//汪洋最稳定的入口是Level 7内有时能被找到的一个有着浅紫色墙纸，入口处有着紫花沙的房间。当你进入这个房间后你会现身于亚特兰蒂斯内。当你离开这个房间时你则会出现在汪洋的海床上。向上游动会让你相较正常情况更快地出现在汪洋海面。从这里朝任一方向游动，你最终会到达荒漠，在特殊情况下你也可能会到达小岛。
//
//另一个进入方式是可在Level 4内使用，有时你能在一个浅紫色容器内找到一瓶杏仁水。饮用这瓶杏仁水会让你感觉神清气爽。在继续行进时你会看到未知来源的水体，在被注视时这些水体会消失。而你若是与他人一同行动，或呆在原地，那么你仍会停留在Level 4内。但若你能跟随这些幻象般的水体，你最终会来到亚特兰蒂斯。而以此方法多次切入此处会使你出现在汪洋海床之上。如果你朝水面游动，则会有木制航船把你从水下捞起。木制航船最终会将你带至水面，然后将你送往荒漠。
//
//出口
//若你在非漩涡处朝下游动，你会发现在水体中有着大量的气泡空间。进入汪洋内的这些气泡空间后你可以保持一段时间的呼吸，使你能在Level 7内苏醒。
//
//小岛
