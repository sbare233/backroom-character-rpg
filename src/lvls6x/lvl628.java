//荒漠是此层级内最容易进入的板块。最常见的进入方式是脱水。无论何时流浪者进入了脱水状态，他们都会瞥见荒漠一角。如果他们就这么一直注视那一角直至昏厥，他们会以一种脱水感释然的状态在荒漠中再次苏醒。这种进入方式会使其出现在荒漠中心附近。唯一的另一个入口是从汪洋里游上来，或者驾船上岸。这样你会进入荒漠边缘。
//
//出口
//荒漠中有着不少别具一格的出口，每个出口都较难寻找。在荒漠中心附近，你会找到一个被废弃的小棚屋。进入这个棚屋后你会发现这里有售许多与荒漠有关的商品，比如说紫被陆参的外皮。因为棚屋内没有任何店员，你可以随意拿取这些商品，而这个商铺会在无人时进行自动补货。由于商铺内没有其他对流浪者有用的商品，无论你所欲如何，拿走便是。离开商铺后你会出现在枢纽内。在此之后除非你能回到荒漠，不然你就再也无法回到这个商铺。
//
//第二个出口大有不同。在汪洋与荒漠交界地带，流浪者有时能找到紫花流沙堆。如果流浪者踏入了流沙堆并完全没入其中他会昏厥过去，之后在Level 628其他地方苏醒。而紫花流沙堆是相当罕见的，这一离开方式只能在特定情境下使用。
//
//最后一种离开方式在整个荒漠中都有机会使用。如上所述，只要被极光蛇咬伤，你便可以在Level 18内醒来。
