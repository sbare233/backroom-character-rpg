//赛尔扎尖塔
//赛尔扎尖塔可以在通道内无意中被遇见，赛尔扎尖塔因此也被视为相对稀有。赛尔扎尖塔可以是各种形状，最为常见的样式是塔形，与传统的尖塔造型类似。赛尔扎尖塔共有三层，每层都有着极大差别。
//
//一层的空间较为空旷，作为赛尔扎尖塔的迎客门厅。一层内有着许多独特的书籍，没有一本是已能理解的语言书写。例外则是一本总是被藏匿的风尘漫漫的旧书。这本书的内容也大多难以理解，目前只有在第12页的一个单词可以辨认，是为“赛尔扎”。
//二层比一层更小，但也值得提及。此层至其边缘充满了形形色色的不同液体，你将不得不游过整个子层。唯一的例外是其入口，它通过一个泡泡状虹吸器来回收这些液体。不同颜色的液体对攀爬有着不同的效应，在入口处可以找到一个漂浮着的筛网，这个筛网在你每次进入时都会变换位置，而筛网自身拥有着三种存象。其中粉色液体总是有害的，有较高的概率能将你杀死。青色（如果有出现）液体是可吸入的，可以缓慢地愈合你的创口。最后的是灰色液体总能使你陷入昏睡，你会在入睡一小时后再次在入口处的虹吸器旁苏醒。在游过此子层时可以看见不少无规律漂浮在水上的尸骸。
//三层是所有子层中最为神秘的，它似乎是处于一种能令悔至心灵的独特状态。这个子层会让你身陷悔恨自责之态中。这些悔恨具象化后可以是你过去曾遇见的一个人，一个物品等，若是一个实体出现在你面前，那么极光蛇将会潜入此层，它会立刻对你展开攻击。
//因为其出现的稀有性，此层仍存在不少未解之。目前将其视为大概率是Level 628的一个出口。
//
//629阶梯
//不论何时何地（除非你在赛尔扎尖塔内），你有较小概率能走进一个楼梯。这个楼梯似乎是通向荒漠的，然而它实际上能直接通向Level 629。这一楼梯通常会将你带至Level 629的任一其他入口附近，因此请务必小心。这也被视为Level 628整层内的一个主要出口，因为它在流浪者间是一个相当普遍的选择，能在沙地壁垒之内任一地点被找到。
