//绿洲
//绿洲则是你于荒漠中跋涉时在极少情况下能找到的又一个区域。绿洲有着能将内部几乎所有实体打散的独特能力，除了偶尔可见的紫被陆参，这里有时还能见到在绿洲中行进登陆的紫被海参，它来到此地以期寻得庇护。绿洲名副其实，占有着荒漠中难得一见的淡水水体。现今仍不明是否仅有一片绿洲，或许荒漠内会有更多其他的绿洲。但证据表明存在两片绿洲的概率较低，尽管我们最终不可能证明会有更多，还是仅有一个。
//
//绿洲周边有着非常独特的景观。在一望无际的紫花沙海中你会发现这令人振奋的渺渺沧海之一粟，但这一块水体完完全全由淡水构成。据传杏仁水偶尔也会散布在绿洲之中，但必须是在岛屿无法抵达之时，但我们也不可能证明这一传言，因此这仅仅被定性为谣传。在绿洲周围已观测到多种灌木和其他植被，它们似乎能从绿洲中的水体汲取养分。目前观察到这些植物生长只需要摄入水分，若阻止植物摄取水分植物会先因营养匮乏在脱水前死亡。这一现象的具体原因很大程度上还是未知数。在淡水旁可以看见一棵长相酷似松树的树体，它的位置时常发生变换，但通过其显眼的褐红色仍可以轻易将其辨认出来。
//
//绿洲自身也有着许多明显特征，代表了其奇异性质。在绿洲的尽头边上你可以透过灌木丛看见一口井。这口井似乎以未知因素能吸引流浪者靠近。此性质仅在你只意识到井本体和位置时显现。当你真正靠近这口井时，井会将你拽入其体内，你将会长时间无法逃出，在掉落足够长时间后，你最终会因未知力量重新出现在地面上，这一效应同样有待解释，也难以解释，这主要也是因为各位亲历者的描述晦涩难懂，不便理解。
//
//沙地壁垒
//在沙海中行走时你偶尔也能遇到沙地壁垒，虽然它们在沙丘中更常见。沙地壁垒是一个由各色子单元构成的巨型洞穴系统。在沙地壁垒中，空间隔断物大多数是沙石，这些沙石同样是浅紫色的，但色调偏深。沙地壁垒的各个子单元结构大相径庭，这些组分将会在以下文段展示：
//
//通道
//通道是沙地壁垒中最蔚为巨大的结构，是沙地壁垒中的主要组分。通道实质上是一系列大型洞穴，各个通道的形状和大小也是千差万别。据报道曾有人在通道内发现了极光蛇，但实际上通道内游荡的实体大多数目稀少，皆为散兵游勇，不足为惧。而这一现象的对应解释是通道内用于在荒漠中生存的必需资源，对人类来说就像是杏仁水和食物之类。
//
//通道的各个个例都有着天壤之别。但通道最常见的外观还都是一条狭窄的廊道，廊道周围不存在任何可见生物，而通道中的子区域内可以发现窝穴。有时你也能遇见巨大的敞开式洞穴，洞穴内仅是充满了石笋2。通道内最为罕见的部分当属廊簇，廊簇是存在于狭窄廊道中的小型开缝。在廊簇之中，杏仁水是常见的，也可以在廊簇中找到回到沙海的路径，这些出口路径大多表现为在洞穴穹顶上的开口。
//
//碉堡
//碉堡是沙地壁垒内一种罕见的结构，最常分布于通道之中。碉堡相比该区域内其他结构相对较小，尤其是与通道本身相比较。碉堡内唯一能见到的实体是紫被陆参，这些紫被陆参的实用价值也大大提高。但是同时紫被陆参也进化出了分泌强酸液的能力，味同沙石。鉴于这些因素，强烈不推荐食用碉堡内的紫被陆参。
//
//碉堡本身可以被视为小型的沙石堆积体，容易与通道内的廊簇混淆。因此，拥有紫被陆参是唯一能将其与廊簇区分开的特征。在碉堡内可以发现许多岩架，极少情况下这些岩架上会出现杏仁水，而相对更常见的是腰果水。由此我们极度不建议在此单元内饮用任何液体。另一个值得注意的特征是岩桌, 即地面上的凹坑。但也应知道在此单元内不存在椅子，那些被先前流浪者遗落在此的椅子除外。
//
//离开碉堡时，你一般会回到通道内。极少数情况下你也能离开碉堡，此时你会缓缓切入至汪洋之内，更确切地说你会进入深海。深海之内对流浪者构成威胁的事物极少，为了避免被这些极少数威胁伤害，建议在离开碉堡前做好准备。这一切入过程相对于标准的切入过程更为流畅。切入过程发生的转变感觉就像是在缓慢地变换位置，感觉自己正渐渐扎入深海之中。
