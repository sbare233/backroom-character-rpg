//入口
//小岛是最难进入的一个板块，目前仅仅已知三种经证实的进入方式。在有着楼梯的层级向下行走，在入口处围绕着一群茜茜，如果你与其打赌的赌注是看见真正的美景，则赢下赌局后他会向你展示一个新的楼梯。这个楼梯会在一段时间内扭曲，在楼梯上行走时最终会有一个台阶崩落，你会不可避免地下坠一段额定时间，最后在小岛上软着陆。
//
//另一个已知的离开方式要通过杀死巨水蛇实现，若你在极端情况下“杀死”了巨水蛇，它的尸体会重新蠕动并将你带至小岛，在成功到达后它又会洄游至深海。
//
//出口
//小岛有着极多的出口，每个出口都可以轻易遇到。在任意点位，你都可以从小岛走入汪洋。请牢记一旦你走出浅滩你就再也无法原路返回小岛，而更常见的情况则是你会在一片荒漠旁的浅滩上再次出现。若如此你应乘坐首席航船离开小岛，入睡后你会再次在Level 777中醒来。最终的离开手段是让自己进入睡眠状态，此方法有几率将你传送至 枢纽。
