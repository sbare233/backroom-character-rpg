//沙海
//沙海是荒漠中最为常见的地貌，占荒漠面积的绝大多数。沙海中存在大量实体，包括蜃楼虚影和蝇凫，以及其他数目庞大的未记录实体物种。沙海被记录为相比于其他部分的地貌更加缺乏植被与动物群系，但这并不意味着沙海中不存在植物个体。沙海中有着数目最多的巨人柱及其他许许多多种类的其他仙人掌，皆为亮紫色。无论何时你是经由荒漠切入此层级的，沙海都将会是你最常进入的区域。
//
//沙海之中有着各式各样的建筑和棚屋，然而它们都是相当难以被找到的。这很大程度上是因为荒漠板块内自身的随机性,，这与其随机变化的环境和表征相符。例外则是当你能知道沙海中一个确切的建筑位置，并前去寻找它，你会发现你比之前有更大概率碰到它。许多基地都是在沙海内广阔的空间中建立的。
//
//在沙海中脱水的进程会变得相当迅速，而且脱水发生率远高于此板块内所有其他区域。沙海中的水资源弥足珍贵。但是即便如此仍可以因未知因素在沙海各隅发现瓶装杏仁水。据推测这些杏仁水应该仅仅是被流浪者落下的，尽管他们这样做的意图和动机不明。沙海是荒漠中已知最为稳定的一个部分。
//
//沙丘
//沙丘被认为是荒漠中最为危险的区域。在荒漠中游荡时偶尔能遇到沙丘。沙丘内也存在着大量实体，几乎所有的沙丘内实体都是危险的，包括大紫壳虫。在此区域的沙暴中还可以看见被卷入风眼的紫被陆参。建议流浪者避免进入此区域。
//
//沙丘是荒漠中寥寥无几的存在风动现象的区域之一，这也是能将它与沙海区别开的重要特征。除了沙丘能将大量的紫花沙聚集在一块，沙海和沙丘的区别微乎其微。沙丘内存在着其独有的植被，但这些植被的存在性尚未被证实。据传沙丘内的植被极易被制成成瘾性药品，因此有许多流浪者会主动走向沙丘进行探索。
//
//目前M.E.G.已在沙丘周边尽可能多的地点各设立一个前哨站。因此沙丘是目前唯一一个在沙海中可以肉眼察觉确切大小的区域，大约有50平方千米。但这个数据实际上也是存疑的，因为在沙丘中行动时它的大小似乎会膨胀不少，而每当一个M.E.G.基地淡出视野后，沙丘的大小便远远大于由外部观察到的尺寸。沙丘的确切面积也无法被测得，因为在沙丘中行动存在较大危险，荒漠本身的性质也会干扰对其大小的测定。
//
//沙丘内部有两种出口，这些出口都是相当少见的。在漫无目的游荡于沙丘之中时你有概率突然出现在沙丘之外，在沙海之中。目前能进行以此方式离开的原理仍然不明，一旦你离开了沙丘，你便再也无法返回，而是出现在沙海内的随机一处地点，无法返回的情况会持续到你能再次定位到沙丘。另一类出口是穿过沙地壁垒，这些壁垒有时可以从远处看到。如果你成功进入了壁垒，你会因不再能看到沙丘而同样无法原路返回。
