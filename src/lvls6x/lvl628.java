//楼梯自身不是由沙石构成的，也不是由松散的紫花沙构筑的，这一奇妙效应一直让流浪者不得其解：为何这些散沙能让流浪者在上面行走，同时走过区域的沙子又会以令人困惑的方式掉落。这一效应在你一看见629阶梯时便会发生。而这里的沙子也是层级内唯一不是紫花沙的特例，相反拥有一般沙子的颜色。
