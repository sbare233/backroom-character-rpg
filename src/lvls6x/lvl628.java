//荒漠
//荒漠是延伸极广的一块陆地，几乎完全由上文所述的紫花沙覆盖。覆盖近乎整个荒漠的紫花沙似乎能影响在此板块的所有植被和动物群系。荒漠中的植被种类繁多，有时还能发现一些无法在传统沙漠中发现的植被和动物群系。这可以归因于层级内独有的紫花沙的奇特性质。荒漠内最常见的物种是巨人柱1，它们都有着亮紫色的外观。
//
//荒漠内实体丰度极高，每种实体都是层级所独有的。由于荒漠内的实体种类数目过于庞大，目前还是有许多未被登记的实体。荒漠中实体共有的属性是他们的着色，大多是些蓝色，深紫色，浅紫色。荒漠中实体的生物学特征千差万别，而某种程度上它们都可以算是哺乳动物。
