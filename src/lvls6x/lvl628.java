//窝穴
//窝穴是你于通道中漫溯时能见到的一类小凹凼的总称。这些窝穴极易被忽视，因为它们能完美地潜藏于环境中。窝穴内常盛有一些小东西，比如说几簇干草和一些水资源补给品。窝穴内最大的特征还是其中所含的极光蛇。一般每个窝穴内会藏有1-3条极光蛇，正因如此，这些窝穴也很可能通向Level 628的出口，也是离开Level 628最为简单的出口。
