//层级特有事物
//“无痛死亡”
//Level 60 中的死亡事件多是由“无痛死亡”造成的，这是 Level 60 中独有的奇特现象。当流浪者专注于海风时，该现象就会发生。此处的海风会导致流浪者们感到放松、镇静，在层级内的时间越长，这种感受就会越强烈。这种现象只有在流浪者们感到焦虑、担忧、害怕、绝望时才会起效。虽然这种效应持续时间极长，无法自行消退，但只要遵循“指南”中所写内容，流浪者可以凭自己或在他人帮助下及时脱离这种效应。这种效应的影响会随时间不断增强，建议流浪者们随时牢记“指南”内容。
//
//虽然 Alphmoan Dextra 起初并未发现这种现象，但是有不少人在来到该层级后报告了这一效应。至今已有 10 人因为这种现象而死去7。Alphmoan Dextra 已向英勇者提议，如果看出有人被这种效应影响，要尽快解救他们，他还向 M.E.G. 基地保证他们一定会营救、帮助受害者们，即使这样会导致受伤甚至死亡。
//
//指南
//如果你在该层级中感到乐观，请立即回想其中潜藏的危险。
//如果你感觉海风有所变化，请不要留意，继续前进。
//不要陷入沉思、冥想。
//如果你看到其他流浪者表现得很积极，无须犹豫，请立即帮他脱困。
//帮人脱困的方式有多种，但最常用的是打耳光。
//“海”
//M.E.G. 同英勇者一同测定了“海”的成分和特性。结果显示，Level 60 中的“海”和前厅中的海洋成分一致。不同的是，“海”中的水会不断变化，有时会表现出“门”的特性，有时会完全退去，只留下赤裸的海岸8。经测量，在状态正常时，水的深度为4英尺（约1.22米）。然而，由于实体存在，英勇者无法继续测量其他区域的精确水深。
//
//“海”的特性异常，不建议流浪者们涉足水中或于水褪去时在海岸附近行动9。目前，已确认至少七人曾因被笑魇追赶而不得已进入水中躲藏。据相关报告所称，其中两人最终进入了 Level 7，一人侥幸回到了下层区域。其余四人被发现时已在水中死亡。这四人的死因当时未被查明，因为死者的身体上没有任何伤口、划痕。但后来英勇者在调查“海”时又发现了它的新特性，这一特性或为四人的死因。
