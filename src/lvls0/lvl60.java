//Level 60 是分段式的层级，外形似为一条海湾旁的混凝土步道。步道看起来像刚完工不久，且被“精心维护”。虽然层级中有路灯可提供照明，但这些路灯多数似乎已经损坏或被关闭。建议在步道上行走的流浪者们保持警惕，因为此处有很多敌对实体。Level 60 中没有昼夜循环，大部分区域都处于黑暗中。流浪者们应尽力避免进入 Level 60 的上层区域，以免遭遇敌对实体（多为笑魇）。该层级由 Alphmoan Dextra 在2020年9月发现，此人是“英勇者”的一员。他在 Level 85 中溺死后进入了该层级。此人精通武功，这一技能在他初探该层级时派上了用场。M.E.G. 在收到他的报告后，又派出9名各有专长的人前去陪同。
//
//尽管该层级内环境黑暗，它的异常特性却会让其中的流浪者忽视附近潜伏着的大量笑魇，感到“放松”、“平静”，在流浪者们开始关注起海湾吹来的微风时，这种现象就很容易发生。有人认为这种现象是该层级的一个陷阱，专门用来让人放松警惕，陷入被实体攻击的危险中。这种效应被命名为“无痛死亡”。
//
//在英勇者的帮助下，M.E.G. 调查、确认了该层级的出生点1。结果显示，只要在任意含有海洋的层级中“溺死”，就会出现在该层级的“海”中。
