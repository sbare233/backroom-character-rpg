//在餐厅某个座位坐下可能会“召唤”一些实体，这些实体似乎是流浪者熟悉的人，然而他们的脸不可见，看起来反而更像相机一样“失焦”。任何尝试站起来走向实体的举动都将会使餐厅内的灯闪烁，灯光恢复正常时实体消失。
