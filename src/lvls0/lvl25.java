//Level 25像是一幢遗弃许久，被洗劫一空的电子游戏街机建筑。它于17/07/12被初步发现。
//
//描述
//Level 25主要由大型的，有回音的房间组成，这些房间充满了各式游戏机。它们的地面，以及各式的窗台和橱柜都积有一层薄灰。这些橱柜大多都钉在通常是栗色的高大墙壁上。
//
//在Level 25的游戏室间，流浪者很容易就会发现一些较为小型，不甚引人注目的走廊。这些次要区域通常空空如也，但据称其中有一些箱子，以及久置甚至冻住的旧式锅炉。锅炉中有着后室中常见的资源补给，如杏仁水或一些普通的零食。
//
//正如几乎所有被记录在案的层级，Level 25的全貌亟待探索。Level 25的尺寸大致为从南到北8.2英里长，从东至西9.2英里宽。2
//
//游戏街机
//Level 25的大部分区域都被电子游戏机占置，装饰。这些街机看起来是来自20世纪80年代前中期的。它们中的相当一部分惨遭大肆洗劫，或被以各种方式砸坏。这层的屋子里常常堆满了这些破烂的游戏街机。
//
//平均在每大概1500台街机里才有一台未被损毁的。这些幸存的机器被改装后，可以将正在游玩的玩家送向后室中指定的层级。只需向街机中投入一枚25美分硬币并集中精力，玩家就可以被传送至后室中的另外一层。这种交通方式也并非单向，因为流浪者总会在穿越处身后约100步的地方找到另一台游戏机。
//
//然而，由于大部分街机已经年久失修，它们的性能已经十分不乐观。通向Level 10的街机由于进水已经彻底失去效用。
//
//这些游戏机所通向的层级无法从外表或者游戏名称上得知。然而，M.E.G.以及一些其它人士在机器的周围或表面进行了标注。
