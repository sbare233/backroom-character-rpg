//
//那是2008年的12月份，临近圣诞。那天家里只有我……一个人。我记不得太多了，但是我记得整个房子都塌了，不知道这是否有什么含义。截至我写下这些时，房子里仍然有该死的节日装饰品。好吧，然后我在被冻昏后醒来……谁知道过了多久。我醒来后看到我的房子和往常一样。尽管由于发生的事故，地板上有些杂物和玻璃碎片。我环顾四周，发现窗外的世界依然在。虽然我很快就意识到情况并不简单。我试着打开我家的前门，但它……只是把我带回来了。恐惧攫住了我。我不知道发生了什么，也不愿知道。
//
//这一切发生的太快了。我猛地倚在墙角处，背靠着墙坐了下来。我只是坐在那里震惊和思考。我现在该做什么？我开始观察周围，探索房子本身。家里全部物件看起来都挺正常，整个卧室、浴室、厨房、我的一些设施。全都保持原状，甚至包括地下室。
