//
//现在我只能在这里。好像没有出路，似乎我的余生都会困在这里。你知道我只是……想念人类。我想念我的家人。想念我的生活。因为我的高收入工作，我的孩子也由妻子照顾。我只是想知道我为什么被带到这里。这一切都事出有因……对吗？
