//
//不过，这种补货没有特定规律，它总是完全随机的。有次它几周都没有补货，我不知道我是怎么存活那么长时间并且感觉如常的。一直到后两周时，我突然感到非常饿，好像之前的饥饿感聚集到那时才冒出来折磨我。也直到那时我才去查看地下室是否补货，确实如此，但大多数包裹都被撕开并损坏了。然后我观察到一些动静，于是慢慢地走向那个小食物堆，看看里面是什么。结果发现是一只小老鼠钻进了食物堆里，正享用它所见的一切。它应该在一切发生前就住在这儿了。
//
//我小心翼翼地把老鼠困在一个罐子里，把它安置在楼上一个曾经用来养仓鼠的小笼子，它成了我的小朋友。我叫他Louie，每天都喂它，但老实说这无济于事，因为我仍然……孤身一人。
//
//之后……几年时间过去了，在那段时间里，我会接待一些访客，现在也是。然而它们不是人。他们更高，更苗条，没有脸。不过，它们似乎完全无害。只是漫无目的地在我家里闲逛。有时坐在桌边或沙发上，要么只是看着窗外。有时，当我看电视时，它们会坐在我旁边。令我惊讶的是电视还能正常工作，但看之前我还得获取新的电线，所以没啥可看的。
