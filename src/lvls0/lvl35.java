//该层级被一个应远离的黑暗深渊所围绕。这里是一处巨大的停车库，有典型的暖光灯，以及一些相对微弱的绿色和蓝色灯光。这个停车楼共有六层，包括一座屋顶。令人奇怪的是，该层级并非无限的，其边界沿着该层四周的正常停车场风格的墙壁。少数情况下可以发现一辆车。进入这些车辆会产生不同的影响。
//
//该层级的出口或者说离开的途径很普遍，但是，应当不惜一切代价远离它们。当接近其中一个出口时，整个停车楼会停满车辆。如果这种情况发生了，最恰当的做法是要么直接逃入出口，要么爬上其中一辆，并尽可能快的穿过其车厢到达楼梯口以去往下一层级。在任何车辆的前方和后方经过会导致它快速启动。它会撞击并碾压直到人员死亡。
//
//进入不同车辆的影响：
//
//黑色2015年款福特Fusion： 无影响。
