//描述：
//Level 44像是一个荒凉且空无一物的零售商店，遍布着勉强工作的灯光。这些灯被认为是由电力驱动的，因为它们不会产生明显的荧光的嗡嗡声；然而，它们被报告称会发出轻微的电流声。
//
//通常，在暗处附近观察所发现的窗户被认为是安全的，没有报告指出在靠近它们的时候会受到影响。所有位于Level 44的活动窗口都显示着前厅的动态影像，不过，我们不确定它是否与现实有所联系。所有通过这个窗户进行语言或是非语言的交流都未曾成功过。
//
//tWJ38kYC4wmTrGu.jpg
//一个空房间的照片。
