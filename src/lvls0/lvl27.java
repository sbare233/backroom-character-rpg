//
//我管他。反正我拿上装备，通过跟其他人一样的方式进入这个层级。值得庆幸的是，M.E.G.提前向Level 11的居民发了通告，确保没人在我工作的时候来打扰。我得赞美他们的体贴，这样我在攀爬岩壁的时候，没有裸体男人盯着我看。
//
//我可不是什么攀岩家——但很高兴我是在这里将我的训练付诸实践。毕竟，从这儿摔下来可以掉进水里，我最多带着几处擦伤或瘀伤活蹦乱跳。更幸运的是，即使发生这种情况，这种泉水也会帮我痊愈。
//
//所以我在这儿，一寸一寸地往上爬，前脚越过后脚。尽管岩壁上的开口是圆润而非锯齿状的，但最终我可以抓住开口的边缘把自己抬上去。说实话，我越挣扎，越意识到自己是多么感谢这里没人围观我的这般窘态，要是我的同事看到，我觉得他们不会让我好过的。但这些都不是重点。当我爬过隧道时，顺便把面具戴在了头上，后背也撞到了天花板。逆流而上的我突然后悔自己的人生选择。温暖的泉水溅得到处都是，即使有面具阻隔我也不得不闭上眼睛。哎。这使我想起了在某个高中浴室里打旋儿的那段非常非常美好的时光。
//
//隧道一直在延伸……我完全失去了对时间和距离的概念。无法告诉你我在热气腾腾的水中爬了多远。我所知道的是，在好似永恒的一段漫长努力后，隧道终于变宽了，让我得以在进入一个巨型洞穴时直起腰来。它的墙壁离我那么遥远，以至于完全笼罩在黑暗之中。我无法准确测量它的大小——记得最清楚的是上方天花板的洞口。透过那里可以看到Level 27的深靛蓝色天空。有一堆高于水面的礁石通向外面，我迈着摇摇欲坠的步伐继续向上。
//
//当我上到外面时，我所看到的一切完全击碎了之前所做的任何心理准备。
//
//深蓝色的天空下是一片荒芜的平原，只有黑色的岩石。没有树木，没有实体，没有生命。一切都平整的像张纸。每座山谷都被夷为平地；每座山峰都被压碎。每个文明都被抹杀，小到蚂蚁大到山岭的一切都消亡了。除了我脚下的黑色岩石之外，什么都没有，它像是一层悚人的黑色油漆覆盖着整个地表。风暴石。这石头是风暴石。
