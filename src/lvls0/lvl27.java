//Level 27，也被称为“岩体泉”，是一个相对平静且小型的层级，不存在任何实体。该层似乎是有界的 无界的但唯一可进入的部分是一座大约200平方英尺（18.58平方米）的洞穴温泉，其中包含该层的所有入口和出口 包含该层的所有入口和除一个出口外的所有出口。这座温泉的水可以饮用且富含矿物质。尝过Level 27的泉水的流浪者称自己感觉头脑清醒，精力充沛。泉水的平均温度约为90°F (32.2°C)，与泡澡时所用热水的温度相似。两个微型瀑布分别从洞壁的开口不断地将新的泉水注入其中。泉水池是通过洞穴温泉角落处的一条小渠排水的，这条排水渠很小，以人类的体型不易爬过。请不要试图探索它。 不知道这些泉水从何而来又去向哪里，但似乎源源不断，是无穷尽的。
//
//Level 27在Level 11等层级的居民中非常受欢迎，因为它易于进入且拥有稳定的出入口。如果你打算到Level 27参观，请尊重你可能遇到的在泉水中沐浴的任何其他流浪者。如果有超过三十名其他流浪者在泡澡，不建议此时进入其中。
//
//事实证明，在泉水中沐浴一小时以上能对流浪者产生积极影响。据流浪者持续报告称，他们感到比之前更轻松、更平静、更积极。这使得来自Level 11的流浪者在工作中提供了更高的生产力水平。广为人知的是，这些水有助于缓解轻度的皮肤疾病和肌肉伤痛，如肌肉酸痛、瘀伤、皮疹和小伤口。但是不建议泡澡超过两个小时，因为长时间接触热水可能会导致头晕和恶心。考虑到该层在Level 11的居民中非常流行并被大众喜爱，因此也建议在沐浴后尽快离开，以便为其他流浪者腾出空间。
//
//洞壁的材质已确定为石灰岩。尽管这里泉水温暖，空气潮湿，但在 Level 27的任何地方都不存在任何植被，甚至不存在任何种类的其他生物。目前不知道原因。 我极其不幸地找到了原因。
