//描述
//Level 7是一片广阔到不可思议的海洋，似乎在所有方向上都无限延伸。该层级内部缺乏固定光源，仅存在普遍的自然光提供昏暗的照明。因该层级极端危险，又需要做好大量准备才能在其中成功导向，因此难以对该层级开展细致的勘探。我们对Level 7的所知如下：
//
//Level 7虽仍有大部分区域未被探索，但其理论上由两个房间组成：入口房间与承载着海洋的“房间”，后者的的混凝土天花板高悬于水面之上。入口房间似乎是Level 7内最易于抵达的切入点，只需沿着Level 6内的楼梯井走下即可到达。入口房间是整个层级内最宜居的区域，对于那些敢于探索深海的流浪者而言，其也是建立基地的第一选择。
//
//入口房间内家具齐全，一架书橱倚靠在左侧墙壁，其中摆放着几本来历不明的书籍；除此之外，还有一张小号的咖啡桌、一把椅子，以及一盏荧光吊灯。铺有地毯的地面上覆盖着一层浅水，深度与水洼相近。从Level 6的楼梯进入该房间后，正对面的就是通向海洋的门廊。
//
//任何试图穿越Level 7的流浪者都应注意，入口房间内的重力方向与该层级的其他区域并不相同。入口房间似乎侧着建立在Level 7的天花板上，打开门后，将会从上往下直接面向海面1。任何事物一旦太过靠近门廊，其受到的重力方向便会即刻强制性地调整为海洋区域的方向，有许多不幸的流浪者就曾经失误掉出门廊，落入“下方”的海中。
//
//Level 7的主体部分即为入口房间之外的海洋。入口房间的门固定在混凝土天花板的某一处，大约距水面4.5米的位置。尽管入口周围的水域几乎是完全空旷的，但有一些在水面上探索了足够远的流浪者发现了不知名岩石构成的岛屿，同样十分荒凉。
//
//大部分Level 7的海域都尚未得到勘探，但是目前的探索表明，该层级内几乎完全不存在生命。
//
//Level 7内的“区域”依据光照水平划分，具体如下：
//
//日光带
//
//正如其名，日光带是Level 7的海洋中亮度最高的部分，仅比水面稍微昏暗一些。对于日光带并无太多可以描述的，其为整片海洋中最萧条的区域。
//
//暮色带
//
//暮色带位于日光带之下，距离水面约一千米，亮度与温度比起日光带皆有显著下降。该区域内漂浮着零散的骨片与生锈的废铁，据报告还有人在该区域内发现过完整的骸骨。这些骸骨多为类人形，但却与人类存在明显差异。其下颚巨大，拥有长而锋利的牙齿，腿部的末端呈现出类似鳍肢的结构。该区域内还发现过一些巨大的怪异鱼类骸骨，并可观察到其受到撕咬、抓挠的痕迹。据称从该区域开始，水压便已变得极大，但Level 7内似乎没有任何区域的水压大到能够压碎人类的骨骼。然而，这一压强依旧是极度危险的，因此建议所有试图更加深入的流浪者尽快远离。
//
//午夜带
//
//午夜带，据推测位于暮色带之下约三千米处，表现出彻底的黑暗。在该区域中可以找到更多完整的骸骨，几乎全部都为上文提到的类人生物。有报告称在午夜带内发现了巨大到不可思议的鱼类骨架，其结构被发现者们描述为“难以理解的”。
//
//深渊带
//
//水深超过七千米的区域被认为过于危险，难以探索；迄今为止在深渊带内最长的一次探索也仅仅持续了不到两分钟。深渊带内存在着堆积成山的岩石与焦油，并且在深处有一个未知的源头持续产生稳定的气泡。该区域内有着大量上文提及的类人生物骨骸，其中的大部分都受到了强大水压一定程度的侵蚀。对于这一深度的海域目前所知甚少，仅有一些传言称，在某个海底山上存在有一个岩洞入口，能够通往Level 8。
//
//目前仍不清楚Level 7的确切深度，但是B.A.S.的成员坚称其深不见底。在午夜带和深渊带可以找到一些人造纤维的碎屑，表明海底上或许同样铺有地毯。
//
//实体
//关于实体，据报告，Level 7内仅存在着一个实体。七层之物独自徘徊在冰冷的海水之中，似乎杀死了海洋中所有其他的生物。
//
//许多年以来，多数人都确信Level 7内仅存在一个实体，也就是骇人听闻的七层之物。但在2019年4月18日，Level 7内出现了另一个居住其中的实体，其被称为小小。这两个实体都非常凶猛，且有着强大的体能，导致海域内再也无法居住其他生物。它们也构成了该层级内最主要的威胁。这两个实体有着不相上下的速度、力量与感知能力，流浪者务必要用尽所有方法远离它们。
//
//目前，小小已经将自己的活动范围限制在日光带与暮色带内，而七层之物则居住于午夜带与深渊带的浅层部分。虽然不清楚这一情况是如何形成的，但是这两者似乎已经达成了一致，不会在自己划定的领域之外狩猎。
//
//探索与生存指南
//由于Level 7内不合理的高危环境，我们撰写了这一简略的生存指南，供那些尝试穿越该层级的流浪者参考：
//
//入口房间应被作为基地看待，而探索海洋的时长应当被尽可能缩减。务必要从入口房间的门廊上向下悬挂一根绳索、或是一架梯子，因为这是唯一重返安全之地的方法。
//
//尽管Level 7的海洋温度极低，但只要不在其中滞留过长时间，这一温度就不足以致命。此外，海洋之上的空气有着未知的特殊性质，吸入该气体后可大幅延长人类的闭气时间，通常情况下能达到30分钟或更久。在这两个条件之下，原本看似不可能的潜水任务实际上无须潜水装备就能完成。
//
//最需要警惕的是该层级内的两个实体。无论如何也要避免与它们直接对抗，且只有在身处入口房间或岩石岛上、确保自己安全之时，才应该尝试与小小交流。尽管与这两个实体搏斗都只会招致死亡，但是由于七层之物长期生活在黑暗之中，导致其对于光线极其敏感；而小小那难以置信的强大听觉，则使其会受到巨大噪音的干扰。在知晓这两点之后，就有可能压制住这两个实体，在水下探索足够长的时间而保证自己绝对较为安全。
//
