//入口和出口
//入口
//你可以从Level 9 进入，方法是找到一个草场，躺下大约5分钟，然后你就会被传送到本层级。你也可以在Level 11 通过找到一座公园使用本方法。在Level 140不小心被树枝绊倒也可以把你送到本层级。你也可以和一座瞭望塔互动，它会随机出现在Level 37 或Level 62。考虑到Level 79的麦田可能是一个单向入口，据传也有可能从Level 10通过步行数百英里进入本层级。
//
//出口
//通过步行进入塔楼后的森林，你可以卡出地面，你将在Level 37醒来，而不是建筑物一楼。也可以在草地边缘找到一张长椅，它可能会被一棵树或一个灌木丛挡住。在长椅上坐上10分钟会把你送到Level 11。有人说通过看月亮有可能被传送到 Level 24，但不建议尝试，因为这个楼层尚待确认。如果你成功地从Level 10进入了本层级，你也可以尝试走进麦田，这可能会把你传送回去。


