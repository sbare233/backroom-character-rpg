//夜晚	当前
//无论Level 46处于哪种状态，沙尘暴都可能会出现，它覆盖了整个环境。沙尘暴就像前厅当中的沙尘暴一样危险。此外，一种高度敌对的实体，沙灵，会在沙尘暴中产生。
//
//沙灵有着强烈的敌意，它会攻击视野当中的一切活物，唯一的弱点是任意种类的液体；如果你的手边没有任何的水之类的东西，强烈建议你躲起来。在沙尘暴期间，十分推荐驻扎在绿洲附近。
//
//绿洲是很难遇到的。绿洲不受三种状态的影响，并且是寻找天然杏仁水的第二的好地方，仅次于基地“沙漠玫瑰”。
