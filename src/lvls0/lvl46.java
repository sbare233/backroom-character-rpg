//远古一族：
//远古一族是一个专用名词，指的是现在可能已经灭绝的文明，它曾经居住在Level 46，被认为是Level 46中各种异常废墟的起源。它们灭绝的原因还不清楚，但理论认为，所谓的“大灾变”是导致远古一族灭绝和他们的建筑被摧毁的原因。
//
//还有一种理论认为，远古一族可能与失落一族有关，因为这两种人使用的语言有着一些相似之处；失落一族的成员可能曾经是远古一族的平民，但这一观点尚未得到证实。
//
//可以肯定的是，远古一族拥有足够先进的科学水平来绘制星象图和预测太阳周期，这比M. E. G.所创建的太阳周期要精确和广泛得多。这一信息是从“圣殿古卷”中获得的，而古卷是在层级中最大的废墟之一里面发现的，而目前这个废墟已被The M.E.G.用作行动基地。
