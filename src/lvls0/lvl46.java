//白天： 当太阳在天空中时，此时温度可以高达70摄氏度，这对于人类来说是致命的。抵御如此高温的方法就是把废墟当作遮荫处，这种地方似乎有着特殊的性质。
//夜晚： 这里的夜晚同样致命，此时的温度会下降到-30摄氏度，如果缺少御寒装备，人就会患上失温症。和白天一样，这时的废墟同样可以作为一个安全的地点，因为它们还有着保温的特质。
//黎明： 黎明是探索Level 46的最佳时间。在这个状态下，温度会在30到40摄氏度上下，尽管此时光线昏暗，但是这个环境对于探索来说已是绰绰有余。
//与人们的想法相反，Level 46并不遵循24小时的昼夜循环模式，而是在这三种状态之间有着一个周期，它们在一段时间内的变化似乎是随机的。
//
//下表是登记过的状态：
//
//状态	持续时间
//白天	六个多月
//夜晚	一年
