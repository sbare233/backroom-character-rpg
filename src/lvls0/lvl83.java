//黑桃A号的组成总体上与洛杉矶级潜艇相似，但有一处不同：黑桃A在船体两侧有数个可用于观察的窗口。现实世界中没有已知的与黑桃A号样式一致的船只，因此M.E.G.认为她源于“后室”本身。人们通过这些窗口，记录了数种太平洋的常见鱼类，如太平洋鲑鱼、褴鱼和多线鱼。
