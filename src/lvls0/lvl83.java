//大观察家是Level 83中已知体型最大的实体，约是普通大乌贼的两倍。经常可以观察到它在黑桃A号外的附近水源游动，其动作非常有力，身体产生剧烈流动的水流，导致潜艇吱吱作响，强烈晃动。大观察家有种行为倾向，即在潜艇表面摩擦它锯齿状的触肢。当大观察家这样做时，建议人员保持镇定，抓住稳固的物体。没有表明大观察家吃肉的证据，因此可认为该实体是植食性动物。它似乎以零散地敲击船体为乐，是有节奏性的敲击，好像打鼓一样。据称，它有时也会移动潜艇，但从未严重干扰其核反应堆或内部的人员。
//
//大观察家似乎很乐于观察Level 83的居民，它将自己20英寸长的眼睛贴在潜艇窗口上。尽管其行为是消极、无害的，但大观察家的移动速度非常惊人，它的大眼睛突然出现在窗口前，往往可以吓到几个流浪者。尽管可能会引起流浪者的不安情绪，但这种情况发生时，最好还是像平常一样，保持冷静。大观察家只是在观察而已。
