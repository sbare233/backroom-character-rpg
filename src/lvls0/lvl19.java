//Level 19的箱子经常包含着可使用的食物，以及种类繁多的已有记录的物品。当一件物品被移出容器时，它有可能会开始快速腐烂3。然而，这种衰败的效果不仅仅会影响无生命的物体。一些特定的人群会感到恶心、偏执、头昏目眩，并且会出现一段时间的幻觉，甚至是昏迷的症状。目前尚不清楚这种现象是否会导致物理上的衰败。
//
//在Level 19的一些地板下面，可能会发出一种温暖的，如火焰一般的光芒，它的亮光可以穿透地板。这种光芒有一种奇异的，诱人的影响，这些影响会通过向人们灌输一种舒适平和的感觉来吸引流浪者。通常，它会直接提取并改变人们的记忆，使得它与记忆直接相连。
//
//橙色的光芒描述的是Level 19的地板下发出的微弱的，温暖的辉光。目前它的源头，以及它改变人们的记忆的原理目前都是未知的。目前的理论高度认为它就是衰败效应的源头，或者是它加强了衰败效应的强度。
