//描述：
//Level 65是一个常年雾气缭绕的森林，里面有着多种落叶树，其中主要的种类从未在前厅中见过。这个层级里的所有树就像是正在经历着秋天的颜色变化，它们的叶子都是血红色的。这些血红色的叶子有时会从树上落下，七零八落地飘落到地上。
//
//浓密而又潮湿的雾气在整个层级中弥漫；以至于从来没有人在这里看到过天空，但是人们怀疑雾的上方有着某种光源，因为整个层级都处于一种持续的、昏暗的黄昏状态。这个层级除了时不时的冷风以外没有其他的天气，而这些风让这个层级的温度维持在了一个比较冷的水平。
//
//在这个层级中，有些树似乎是食肉的，它们依靠着在这里失踪或是死去的流浪者维持生命，这些人的尸体会在死亡的数小时后被地面所吸收。除了树木，这里还有一些动物群落，它们主要由超大的虫子组成，最著名的巨型蜈蚣可以长到6英尺（2米）长。
//
//值得注意的动植物：
//血苹果树：
//2AQeYZFi5SmL7O3.jpg
//一丛血苹果树。
