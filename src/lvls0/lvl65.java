//在层级的深处，流浪者可以遇到一个被称为“树桩”的东西。这个树桩是黑色的，它就像是从一棵大树上整齐地切下来的一样，但由于它有着异常的影响，这让人很难去仔细地观察它。当流浪者靠近树桩时，他们将开始体验到异常的感觉，这一影响也会延伸到该地区拍摄的照片之中，这说明了它实际上可能不是一种幻觉。这些异常的景象包括：天空呈现出明亮的红色，其他所有的物体都被缩小成一片漆黑的轮廓，物体的曲线和角度会发生变化，成为一个尖锐的点。此时，流浪者在报告中说，他们发现自己越来越难以清楚地思考，开始感到时间过得比实际要慢，而且会感到自己与自己的身体分离。在流浪者触摸树桩之后，触摸部位的皮肤会受到严重的烧伤。这种被烧伤的皮肤是无法愈合的。
