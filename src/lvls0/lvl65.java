//在食用血苹果后不久，流浪者就会感到不适并开始吐血。他们会不可逆地转变成这个层级的蜈蚣之一。此时，他们会开始狂暴地行动，可以确定，他们已经失去了所有的人性。目前还不清楚这个层级中的所有蜈蚣是否都是吃了血苹果的流浪者，还是他们能够自己繁殖。
