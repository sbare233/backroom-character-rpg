//这些血苹果树会均匀地排列，每两棵树之间会有15英尺（5米）的距离。它们会像前厅中的苹果树一样结果，但是这些苹果会比普通的苹果大上1.5到2倍，而且外表也是血红色的，这也是它的名字的来源。当这些苹果被穿了个洞的时候，它们就会流出粘稠的液体，这些液体在视觉和味觉上都像是一种动物的血液。
