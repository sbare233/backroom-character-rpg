//光穴最奇怪的一点是，在其森林深处，可以发现许多被遗弃、毁坏的建筑。这些建筑几乎都是杂草丛生，被苔藓和真菌覆盖。它们与不少著名的前厅建筑有着惊人的相似之处，但又有明显的不同。目前已发现一座金字塔形的亚历山大灯塔、一座球形的大金字塔、一座方形的大角斗场和一座三角形的帕特农神庙。
//
//在这些遗迹内有大量文物，与前厅多个时期的历史文物有着相似之处。最常见的文物是陶器，但也发现了一些受损的绘画和布料。这些文物都有一个共同点，它们的质量明显比前厅的同类文物差。在遗迹的墙壁上可以发现文字，通常以褪色、匆忙记下的信息形式出现。这些文字总是以拉丁文书写。在本层级没有发现与公元1000年后造物相似的建筑。
