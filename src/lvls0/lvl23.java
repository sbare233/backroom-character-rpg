//深绿巨人
//深绿巨人是从小行星核心长出的巨大树木。它们比前室中最高的树还高，最高的深绿巨人高约2英里（3.2km），直径约60英尺（18.3m）。 深绿巨人负责支持小行星的内部生态系统，既将水蒸气分配给其他树木，又为其他动植物提供栖息地。这些树木的年龄未知，因为没有砍伐它们的打算。这些树木对层级的稳定性至关重要，因此不得干涉。
