//掩没哨兵
//当进入Level 23 时，强烈建议不要说出“这只是时间问题。”这句话。这样做会唤醒掩没哨兵。这些实体被认为困在木头与树皮墙壁中，除了尖叫什么也做不了。一旦说出这句话，哨兵们就会开始尖叫。根据听力范围内哨兵的数量，其产生的声音可大到足以刺破耳膜。一些流浪者报告称，这些尖叫声听起来就像生锈的电锯切割木材。
