//在该层级的地表之下，存在一些大型开放洞穴，在这里树木可以生根发叶。这些洞穴由一种被称为园丁之歉的特有实体照亮。园丁之歉的行为与萤火虫十分类似，具有生物发光性，呈现出各种不同的颜色。它们的名字来源于其不停发出的奇怪声音，听起来像在低语“对不起…”
