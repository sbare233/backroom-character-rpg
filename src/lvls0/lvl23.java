//水之心
//核心仍是该层级中最未被探索的部分。据报告，它是空心的。一面球形天顶包围着核心，由从中心长出的深绿巨人的树干与树枝支撑。一些树木向下生长到这个空间，一些则从这里生长出来。在本层级中，该区域最为茂盛，生长着独一无二的奇特植物与真菌。有几种奇异的野生动物居住在巨人的巨大树枝上，这些树枝延伸到户外。
//
//在这颗行星的最中心，是它的心脏——水之心。水之心完全被巨人纠缠的根茎所包围，是一个巨大的水库，滋养整个层级。这些水被巨人吸收，并通过它们的叶子排放到上面的开放洞穴中。随后，天顶上的树根吸收凝结的水蒸气，并将其输送进光穴。更多的水蒸气被释放到光穴中，既被生长在地表的树根吸收，也被向下生长回核心的树根吸收。利用园丁之歉的生物发光，以及自然的永久的阳光，这些树木获得了足够的生存能量。
