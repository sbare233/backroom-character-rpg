//描述
//Level 23，也被称为石化花园，是一个巨大的、小行星级规模的超级有机体，主要由一些相互缠结的树木物种组成，树木类似于前厅中的那些。尽管其引力与地球极为相似，但据M.E.G.估测，Level 23 的表面积与矮行星谷神星大致相同。由于该层级的地表拥有空气腔及蜿蜒的隧道，类似于一个大型洞穴系统，因此探索整个层级是可行的。大多数实体生活在这些区域的地表之下。该层级存在异狐、猎犬、肢团、笑魇、亡鼠、雌雄死亡飞蛾以及科拉比图鸟。还有一些未知的本土实体生活在光穴之中，类似于前厅中的某些物种，目前正在对其进行研究。尽管非常罕见，但该层级还能发现牧民。
//
//Level 23 的表面是一片极其茂密的森林，几乎没有光线穿过。该层级没有昼夜循环。从地表的树冠穹顶望去，能够看见天空中有一颗恒星，但其方位从未改变，即使横穿行星的地表。M.E.G.已确定，整颗小行星正永久地沐浴在来自各个方向的阳光中。
//
//组成Level 23 的树木丰富多样。它们的前厅对应物来自各类不同的生物群系。其中最为常见的树种包括北美黄杉、白杨、桃花心木、红杉、雪松、柏树和白橡树，尽管许多树种在前厅为外来物种、濒危甚至已经灭绝。此外，还发现几种无法识别为任何前厅已知树种的树木。
