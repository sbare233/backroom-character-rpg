//Level 39的边界区域。
//
//边界区域 是指位于Level 39边界地带的一片区域，距离它的中心区域约150公里。那里的区域相对平坦，树木稀疏，通常没有叶子。边界区域的温度要低很多，平均温度一直都在12°C (54°F)左右，那里的天空总是阴沉沉的，十分昏暗且阴云密布。
//
//层级的这一部分非常不稳定且危险：在边境区域，飓风经常刮倒流浪者，龙卷风和雷暴也像这样存在于此。正因如此，很少有流浪者愿意冒险，试图穿越边境的树林，因为仅仅是身处其中就足够危险了。据推测，那片树林的后面可能是丘陵地带，那里兴许没有此处危险。M.E.G.的特工们正试图组织一支探险队，进一步探索考察这片区域。
