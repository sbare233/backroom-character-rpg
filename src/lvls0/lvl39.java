//Level 39 是后室的第40层。它于1998年1月14日被一个匿名的流浪者发现。
//
//描述
//Level 39由许多蜿蜒的泥泞小道组成，它们的两边覆盖着广袤的橡树林。这一层级是有限的，呈圆形，跨度约300公里（190英里）。Level 39内不存在昼夜循环，被固定在一个永恒的黄昏时段。尽管在Level 39不断变化的时段中天体正常出现，但天体并没有真正存在于Level 39层级本身内。本层级的温度恒约 20°C~23°C (68°F ~73°F)。
//
//Level 39 的路径随机的扭折弯曲，有时会形成无效闭合的线圈和曲圆。层级的这一特征使得流浪者们很容易丢失方向感。Level 39内的路径通常有2米（6.6英尺）宽。
//
//Level 39内的橡树被证明是不可移动且不可摧毁的。经过进一步的实验考量发现，这些橡树并非活体，它们的根系逐渐生长嵌入到这一层级的土壤中，因而它们其实是这一层级不可分割的一部分。
//
//该层级的地表覆盖着一层落叶、干橡树枝、苔藓，还有一些罕见的砾石。有时可以在浅层的草上找到体积很小的可食用菌。这一层级有一些池塘，但它们的跨度通常不超过5米（16.4英尺），深度通常不超过半米（1.5英尺）。Level 39是一个地势起伏相对较大的层级，山地的坡度在5°~ 70°内波动，这使得层级的某些部分在没有适宜装备的情况下很难探索。
//
//去过Level 39的流浪者们表示，该层级让他们感受到一种很不自然的平静。他们中的许多人还表示，该层级给他们带来了一种极为强烈且令人舒适的怀旧感，即便他们在进入这一层级之前从未见过或体会过这一层级或与这一层级的类似的区域。同Level 14一样，Level 39的环境似乎可以催眠流浪者，让他们在层级内停留很长时间。在大多数情况下，这将导致流浪者在Level 39内逗留至会影响其健康的时间，令许多人死于饥饿或脱水。流浪者们还发现，在这一层级的中心区域，这种影响会更强，而在边界区域，这种影响便会趋近于零。幸运的是，只需要饮用一小口杏仁水便可以消除这种效果。
//
//鉴于这一层级的催眠效果是极易消除的，水和食物也是很容易获取的，而且在这一层级中不存在其它任何实体，因而Level 39具有成为长久定居点的潜力。
//
//边界区域
