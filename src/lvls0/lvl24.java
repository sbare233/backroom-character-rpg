//描述：
//Level 24表现为一塑料月球模型，附有多个其他塑料星球。中心存在一LED太阳，据进入该层级的人员的身材，其尺寸完全符合。月球是其中唯一可以抵达的星球，估计其表面尺寸有1.46 千万平方英里1。奇怪的是，这些星球之间均无相互间的引力作用，但越过该月球时仍受常规引力（例如你无法进行 月面跳跃
//moon jump
// ）。这些星球均以金属丝相互连结，且在缓慢转动。
//
//鉴于该巨型太阳系模型的尺寸，周边环境难以观测，但其似乎为一巨大的、昏暗的维多利亚时代书房。未知是进入层级时流浪者被缩小了，还是层级本身就较常规人类而言尤为庞大。尽管本不该如此，可从其他有日月变迁或具备月球的层级观测到Level 24；观察者描绘了与此月球完全一致的灰色和蓝色阴影。
//
//基地、前哨和社区
//B.N.T.G.塑料矿
//B.N.T.G. Plastic Mine
// 
//鉴于月球是一塑料制固态球体，它被B.N.T.G.用作几乎无限可用的塑料材料来源。约有20名矿工每两周来到这里一次，进行地下挖掘并将以巨大容器装好的塑料拖回他们在Level 1的基地。这些塑料容器用于制造简单武器或用于维修物件。然而，这种行为极其危险，因一层100 英尺2厚的油漆覆盖了整个月球表面，其中含有大量的铅，通过其挖掘塑料时会释放有毒烟雾，为此需要防毒面具。
//
//"博伏得"
//"Beauford"
// 
//极少情况下，一男子将进入Level 24所在书房并坐在一把椅子上读书。他被B.N.T.G的矿工昵称为“博伏得”，着燕尾服，秃顶，有少量白发。未知他是某种无面灵还是因面容模糊而难以辨认。一般认为他是无害的，但他有时会走到星球模型旁摆弄或以毛巾清洁之。这对Level 24的流浪者而言极其危险，因为他们可能会被压扁或死于“地震”。
//
