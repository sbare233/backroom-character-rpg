//Level C-462是后室C室群的第462层。其外观类似于一个前厅中混凝土搅拌机的内部。
//
//描述
//准确而言，Level C-462的内部如同一个巨大的卧轴式强制混凝土搅拌机。“卧轴式”是指按照前厅中的重力方向，层级的中轴线为水平；“强制”则是描述搅拌机的工作方式1。有时搅拌机不工作，流浪者可在其中自由探索；工作时分为装料、搅拌、卸料三个阶段，流浪者应尽快离开正在装料的该层级。
//
//层级边界为搅拌桶，内部中心为一个巨大的搅拌轴，从搅拌轴分出数个搅拌臂，搅拌臂末端有搅拌叶片。
//因为层级的特性，层级内部几乎没有任何光源。搅拌桶开口开启时会有部分光射入，但由于内部机械结构的遮挡，层级内部依然昏暗。如果要探索此层级，请携带可靠的光源。
