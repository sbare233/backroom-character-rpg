//该地区是M.E.G.组织最先发现的特殊地区，位于主要街道东南方向不到2米处的位置，在外观上是中国式的牌坊建筑，并且已经腐烂很久的样子，值得注意的是通往鬼门关地区的小路上偶尔会出现蠕灵、八层之蛛这种昆虫类实体，虽然这些实体不会拥有主动攻击流浪者的意识，但若流浪者刺激这类实体，那么其他实体就会随之一起攻击流浪者。
//
//流浪者在去往鬼门关地区的小路上会时常听到不明男人与女人的嘀咕声以及婴儿的啼哭声，有报告证明这种声音会唤醒本不该属于流浪者的记忆，且流浪者会认为这段记忆是合理的。当然也有其他特殊情况，一些流浪者会无法忍受这些声音对自己的精神折磨，而试图通过结束自己生命来解除折磨，因此建议使用杏仁水与少量的治愈水晶，这可以暂时压制这些未知声音，这两个物品可在该层级的一些基地、前哨与社区中获得。
//
//城隍庙
//该地区是距离鬼门关不远处的一座老式祠堂，其内部有一尊较为破烂的城隍爷雕像，有一顶长度为2米的香炉，且用以供奉祖先或神明的香火仍然不断燃烧，其烧出来的气味可以使流浪者在精神紧张下有所缓和，尽管有很多人员报告称并未看到有其他人员为香炉续火，但仍然在不断燃烧。祠堂内部的两侧摆放着各种古代书籍，然而书籍中的内容大部分都是空白的，只有少数的书籍的文字以未知文字书写，因此翻译的工作极其困难。
//
//也有很多物品和实体存在于其祠堂内部，这些物品和实体在发现该层级之前就已经被记录起来，尽管这些实体对外来人员没有任何敌意，但还是建议流浪者们不要靠近或与他们有任何交谈行为，我们尚未明确该行为是否会对这些实体产生敌意想法。
//
//忘川河
//该地区是位于城隍庙附近的一条较宽的小河，小河看上去十分浑浊，但从河面上能清楚看到反射在其表面上的夜空与月亮。当有来自前厅的生活用品掉落在这条河中，并且流浪者尝试将其捡起时，会发现无法捡起该类物品，有观察报告称，当流浪者捡起时，该类物品就好像变为反射在小河的河面上一样，而且当流浪者长时间注视这条河时，精神上会十分恍惚，并且逐渐朝着小河中心走去。
