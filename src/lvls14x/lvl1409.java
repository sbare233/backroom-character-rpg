//
//虽然用于勘察与监控该层级地形的无人机在路过大部分建筑时，摄像画面会受到微弱干扰，但大部分的地区都是可以正常进行勘察与监控。然而当派出满电状态下的无人机后，并完成一次勘察工作后进行回收时，发现无人机都已处于低电量状态，因此，在特殊情况下，会有几架无人机因耗电已久而摔落在地，推测该层级的一些地区的特殊性质会对无人机的电量产生耗电现象。
