//描述
//Level C-400是一个面积有限的层级，它由两个部分组成，分别为一个类似于教堂的室内空间，和一条长度未知的走廊。鉴于该层级没有高价值资源但有颇为危险的精神影响，我们建议流浪者避免进入该层级，试图进入该层级者可自行进行M.E.G.的精神健康水平测试和“绝对衰亡阈限”测试，未达标者不建议进入该层级。
//
//教堂
//该区域是Level C-400中唯一可供多人同时进入的区域。正如其名所言，其表现为一个面积约800㎡、层高约50m的大型哥特式教堂。教堂外观老旧，内有长凳、布道台等摆设，但均因为时代久远和疏于维护而显得肮脏破败，各种壁画和装饰也被严重磨损，无法确认具体内容。教堂内唯一的光源是教堂外部的白色虚空，因此教堂整体环境昏暗，进入这一区域的流浪者常常把这里的气氛形容为“压抑且阴暗的”，“仿佛正在举行某人的葬礼”，并因此感到压抑和不安1。鉴于该区域容易让流浪者情绪低落，而较低的精神健康状况将使流浪者更容易受到长廊的吸引，我们建议流浪者尽量避免在该层级停留过久，并在停留过程中备好足量的杏仁水。
//
//教堂中随机散落着一些大小不等的十字架雕塑，但值得注意的是，这些雕塑的主题并非前厅中教堂内常见的“耶稣受难像”，而是描绘了一位老人被捆绑在十字架上的图景。这些老人的性别、服饰、体型、姿态各不相同，唯一的共同点是他们都半闭着眼睛、表情安详。有传言称这些雕塑的原型为于此层级失踪的流浪者，被长廊燃尽的受害者将被制成雕塑供人悼念。凝视这些雕塑超过半分钟将使流浪者陷入强烈的悲伤中，严重者会因此进入悲尸循环。虽然这一影响可通过服用杏仁水进行缓解，但M.E.G.依然建议流浪者远离这些雕塑。
//
