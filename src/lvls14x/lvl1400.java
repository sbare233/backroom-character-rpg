//Stage 1：这只是一段燃烧的回忆
//这只是一段燃烧的回忆乃受害者在长廊中所经历的第一个阶段，包含了整个流程中的前两个小时。在个体走入长廊的一瞬间，长廊中总是会响起一段由前厅音乐人The Caretaker创作的歌曲《it's just a burning memory》，该阶段也因此得名。在这一阶段，长廊将如上文所述般变化为受害者回忆中的模样，其燃烧的对象多为受害者较近期的、较浅层的回忆，而吸引效应所带来的依恋感与怀旧感也将随着流浪者向前行走而不断增强，这将使受害者离开长廊变得更加困难。此时受害者的回忆将以其印象最深刻、使用最多的物品的形态出现，如打火机、手枪、玩具熊等。这一阶段回忆燃烧的速度是最快的，这些物品将以每分钟5到10个的速度刷出，在回忆燃烧得最激烈的时候，受害者常常可以看到整个走廊都被昏黄的火焰包裹，记忆的灰烬四处飘扬，场面蔚为壮观。
//
//在【stage 1】初期，长廊的外表总是显得稳定、精致且逼真，归还的流浪者们将其描述为“与我曾经走过的那些层级几乎一模一样，除了是空的以外”。随着受害者的不断前行，长廊的结构也将变得更加错乱，其具体表现为模糊不清且缺乏细节的表面，异常增生或缺失的结构，突兀尴尬的摆设，图画和文字变得无法理解等。这一变化十分缓慢和难以察觉，且吸引效应也会使受害者倾向于忽略这一变化。即使是那些精神状况良好、感知觉较为敏锐的受害者也常常需要“6分钟现象”的协助才能发觉环境的扭曲，而更多的受害者则会继续无知无觉地走下去，沉浸在越来越强的怀旧感和安定感中，走向自己的终焉。
