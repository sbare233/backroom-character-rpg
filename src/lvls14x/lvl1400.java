//长廊
//长廊是一条长度未知、结构可变的走廊，其具体结构和内部设施会随着进入其中的个体而变化，若站在打开的门外观察该区域，则会看见一片不停翻涌的灰色迷雾，其中不断闪过各种随机的画面片段。值得注意的是，这一区域似乎只对有自我概念的个体敞开大门2，且每个进入的个体都不会在长廊中看见任何除自己以外的活物。推测该区域可能拥有多个副本，其会将进入的个体平均分配到这些副本中。
//
//长廊的具体结构会随着进入其中的个体而发生变化。其外表与受害者正在燃烧的记忆相对应，例如，当长廊正在吞噬个体关于学校的记忆时，其将表现出学校走廊的样貌。其长度则与个体的行进速度有关：无论个体的行进速度是多少，个体总是需要六小时左右才能走完全程。受害者能且仅能通过走出木门的方式离开长廊，所有翻越长廊两侧的“墙壁”、破坏“墙壁”、拿走“墙壁”上的摆设（例如货架上的货物、墙上的挂画等）的尝试无一例外均失败了。长廊的所有结构，包括摆设在内，均展现出了违背常理的坚固和稳定，人类已知的手段几乎无法影响它们。
//
//长廊的尽头为一个放着棺材的灰色房间，棺材后悬挂着个体的黑白遗照。走完全程的个体将自行躺进棺材里，然后棺盖合上，个体随之消失无踪。在个体消失后，教堂内将会响起一段模糊不清的哀乐，随后布道台上出现一盘录影带。录影带中存储着一段视频，内容为个体走完全程的影像记录。至今为止，M.E.G.已于布道台上发现了数十卷录影带，本文档中所有关于长廊的描述皆来源于这些影像。
//
//作为该层级最危险的区域，长廊拥有两个广为人的异常特性：
//
//吸引
//长廊的第一个异常特性被称为吸引，即它会周期性地对层级内所有流浪者进行影响，令其产生进入长廊的冲动。通常，这种冲动并不强烈，但若流浪者精神状况不佳3，则效应对其的影响会忽然增强到几乎无法违抗的程度，具体表现为个体的情绪突然变得非常激动，不顾一切地想要进入长廊，对阻拦者则会使用包括但不限于辱骂、欺骗、暴力行为、威胁自残/自杀等手段进行对抗。
//
//这一效应在个体进入长廊后将会持续存在，且会随着个体的前行而不断增强，其具体表现则改变为引起个体强烈的安宁感和怀旧感，让个体产生“仿佛回到了最熟悉的地方”的感觉。此时的个体将不顾一切地向长廊尽头行进，因为其会认为“长廊的尽头就是家，我日思夜想的家”。由于不断增强的吸引效应和不断被磨削的自我，个体在长廊中越是前进，其生还的可能性便越是渺茫。目前为止，尚未有任何进入【stage 2】的个体归还的报告。
//
//燃烧
//长廊的第二个，也是最臭名昭著的异常特性被称为燃烧，即随着个体在长廊中前行，个体的一切都将被缓缓燃烧殆尽。从个体对自身经历的记忆，到个体所掌握的各项知识和能力，再到个体的人格、感情、自我概念、逻辑推理、抽象思维等基础心理功能。当心智被全部燃尽后，只剩下一具空壳的个体将跌入长廊尽头的墓穴，就此消失无踪。即使个体成功逃离长廊，由该效应造成的失忆和大脑功能受损也是无法逆转的，因此我们建议流浪者无论如何也不要进入长廊。
//
//该效应的具体表现为：当个体在长廊中行进时，长廊两旁将刷新出各种物品，这些物件的形态和种类不定，但它们都代表着个体的某段记忆。当个体靠近某件物品时，该物品将立即被点燃，并在约五秒后化为灰烬，与此同时，个体对应的记忆也将不复存在。在旅途后半段，个体的自传性记忆几乎已全部被燃烧殆尽，此时物品所代表的事物为个体的各项认知技能与心理功能，点燃这些物品将造成无法治愈的精神残疾。物品燃烧时产生的火焰温度很低，并不会将人烧伤，但相对的，也没有任何手段可以阻止燃烧。
//
//值得注意的是，个体的失忆似乎遵循【由近及远】、【自浅至深】的顺序，即个体总是会先遗忘最近的记忆，再遗忘更久远的记忆；先遗忘记得不牢、无足轻重的记忆，再遗忘更深层、更重要的记忆，对脑功能的损害则被放在了最后。尚不知这一顺序有何意义。
