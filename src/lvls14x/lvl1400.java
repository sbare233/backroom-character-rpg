//“6分钟现象”
//经过了对归还者的采访以及对录像的分析，M.E.G.发现进入长廊的受害者总是会在旅途的第6分钟左右感到明显的违和，这一过程通常被描述为“猛地打了个寒颤”，“突然发现这一切都非常不自然”。此时，那些精神状况较好的受害者将被该现象惊醒，在一段时间的迷茫后选择原路返回，而精神状况欠佳的受害者则将在一瞬间的呆愣后继续自己的旅途。受害者此时的选择几乎决定了自己能否或者走出长廊。
//
//经过调查和分析，研究者们认为有许多因素促进了“6分钟现象”的产生。当安息之旅进行到第六分钟时，吸引效应还未达到能有效影响受害者神志的强度，燃烧效应亦无法对受害者的认知能力造成重大伤害。但此时长廊的缓慢扭曲已经进展到可被受害者察觉到的地步，且受害者也会慢慢开始发觉自身的记忆出现了不自然的缺损。在这些因素的共同作用下，受害者将会进入惊恐状态，并短暂地从吸引效应中摆脱出来，进而迅速发现自身所处环境的“不自然”之处。
//
//在系统研究了所有这些导致“6分钟效应”的因素后，研究者们提出了绝对衰亡阈限理论。绝对衰亡阈限是一种心理量，其被定义为“刚刚可以引起个体对自身‘衰亡’的察觉的最小刺激量”，而所谓“衰亡”则被定义为“个体的记忆、意识状态、认知功能出现非自然的模糊/缺损，或缺损的进展不自然地迅速”。该心理量的值是决定个体能否从长廊中归来的重要因素：绝对衰亡阈限较低，即较容易察觉到自己正在“衰亡”的个体将更早遇到所谓“6分钟现象”，从而从长廊中幸存，而绝对衰亡阈限较高，即不太容易察觉自己正在“衰亡”的个体则很有可能成为该层级的牺牲品。除此之外，该心理量还可以有效预测个体对其他“自我意识磨削”类异常精神影响的觉察能力，最典型的如Level 404中神性实体的影响、Level 200的怀旧陷阱等。目前，研究者们已推出绝对衰亡阈限的测量方法，该方法拥有较高的信度和效度，流浪者可放心使用。
//
