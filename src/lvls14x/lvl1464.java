//该层级被发现的历史可以追溯至1984年，年轻的层级探索者白道言自Level C-27的竹林处切出，并偶然来到了这一层级。白道言将这一发现，及其对于该层级的第一次探索公布于当时最大的后室信息分享论坛“backroomback.brs（后室归还论坛）”之中，并在社群的讨论区中得到了激烈的反响。许多人前往Level C-27，试图通过复现白道言的行动前往这一理想乡般的宜居层级，但没有人能够再度复现当时的情景。白道言先生于1987年在探索新层群的行动中中不幸牺牲，享年31岁。为纪念他对Level C-464发现的重要贡献，他的遗骸被葬在竹林泉旁，与他最爱的景致长眠于一处。
//
//1996年9月，归属于红星建筑队的层级探索者郑和平通过穿过Level C-58的河流进入本层级。通过三角定位确认本层级就是白道言于12年前偶然进入的宜居层级之后，红星建筑队组织了一批攻坚人才，对已知的两处层级入口进行了长达数月的不断尝试。红星建筑队从未向外透露层级入口发现的时间与人力细节，当大量关于该层级的信息于1997年5月向外公布之时，红星建筑队已完成了本层级殖民地、即“红星建筑队生产合作社”的建设。
//
//XBXabd.jpg
//水稻纹枯病(Sheath blight of rice)，又名水稻云斑病、水稻云纹病，俗称花杆、花脚瘟，是一种由立枯丝核菌(Rhizoctonia solani)引起的大范围作物疾病。该真菌可寄生于水稻、麦类、高粱、玉米等前厅与后室的120余种高产粮食作物，影响谷物灌浆，引起植物枯萎倒伏，无法正常抽穗，降低结实率，减少粒重。病害通常导致作物减产10-30%，严重时可达50%以上。
//
//2001年7月，因层级间运输中防疫失误而导致的作物疾病传播，水稻纹枯病在Level C-464及数个红星建筑队的作物中大量传播，导致该组织作物田产大量减产、土地受立枯丝核菌感染，并进一步导致了席卷2001~2004年的大规模暴乱事件，即“难集年”事件的发生。因红星建筑队的教条性集体化、强制粮食采购、快速工业化及农业盲目跃进式发展所带来的黑天鹅事件所带来的极端影响，难集年事件的发生导致了大量边缘殖民地成员与“思想落后”地区的农民因饥荒而大量死亡，致使部分殖民地的分裂与独立。而据已有信息推测，建筑队对此的回应则是将之归结为“富农”与“思想落后分子”的蓄谋破坏，加以更为严苛的军事政治镇压及对外的情报封锁。这次事件被分裂后的诸势力将此事件一次有预谋的大屠杀，而红星建筑队并未对此做出任何有效回应。
//
//据信，于2001年10月第一次大规模减产后，层级中部分不满于红星建筑队人民公社化运动的公社成员与其他边缘殖民地及未知势力“笮家阀”取得联系，于本层级创立新后室价值派自由社会、即新值派。新值派由红星建筑队殖民地中众多派别的持异见人士组成，其获得一致的政治主张仅限于推翻红星建筑队势力对殖民地的影响、实行直接民主制与实行固定田赋。
//
//2002年7月，新值派发动了一次成功的武装叛乱，镇压了位于树铭半岛的生产合作社办公厅，屠杀了所有红星建筑队的干部与先进人员并接管了这一层级的政权。自此之后，该层级与后室的联系基本断绝，已无法得知层级内的情况。但于不可靠信息表示，新值派内部的动乱从未停止，对于权力的争夺、对人民的可持续性竭泽而渔导致聚落的政治局面动荡且混乱，并直接致使了2014年后该层级自然环境的急剧恶化与人员大量死亡。
