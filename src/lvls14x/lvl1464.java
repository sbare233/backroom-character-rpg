//Level C-464为一座坐落于约6x6x6km³、封闭且无限循环的正方体空间中的宜居浮岛，常驻人口约60~90人。空岛长7.2km、面积约31.6km²，全岛全时段皆近似于热带海洋性气候，常时平均温度21℃，气候稳定、降水均匀。层级中重力加速度g为9.65m/s²，方向为垂直于空间水平边缘向下。空岛的宜居陆地由两部分组成——东北部1由山峦、喀斯特地貌与冲积平原组成的“树铭半岛”，以及西南部的稀树草原与平原地带“世泽半岛”。两半岛间由起自树铭半岛步青峰的山泉“竹林河”与岛上最大的淡水湖“琉灰湖”相区隔、由3.2km宽的地峡相互连接。该岛多山多湖、气候适宜、且不存在具有威胁的后室实体，具备极大的宜居潜质。
//
//该层级昼夜循环约为18小时，由12小时的强光照氛围与6小时的弱光照氛围组成。Level C-464的光线源自于无形的空间边缘，在自浮岛边缘眺望对岸时可清晰辨认构成正方体的发光带放出的柔和光芒。光照强度遵循极值为0.5lx与50000lx的正弦函数规律变换，于第6小时抵达顶峰、第13小时达到最低值。该区域水体无潮汐或类似引力影响，这使得该层级的湖水相较前厅更为平静而缺少涨落。
//
//Level C-464存有多种有价值的自然资源，包括大量的淡水、各类热带亚热带植被、及似三叠系砂岩矿床产出的各类矿物。该层级不存在本土鱼类，但于红星建筑队物种引进与培养后，岛上各河流与湖泊皆已具备满足渔业捕捞要求的种群数量。
