//
//20██年，关于Level C-410的档案被内鬼泄露，U.E.C.的一支小队在得知此事后私自闯入了Level C-410并进入了“超人梦”，使用枪械射杀了██名流浪者的梦中形态，所有在梦中被射杀的流浪者均陷入脑死亡状态。Level C-410的负责人理查德在与该小队的队长谈判后选择私了，事件以U.E.C.提供15kg火盐告终。
