//大堂正对大门的一侧有一条走廊，目前探索的长度已超过了6千米。在走廊的两侧有着紧闭的门，门上通常贴有写有“XX梦”字样的标签。目前仅发现了两扇可打开的门，分别为“超人梦”和“下坠梦”。以及使用在Level C-321发现的一柄沾有血污的层级密钥打开了一扇名为“逃亡梦”的门。
