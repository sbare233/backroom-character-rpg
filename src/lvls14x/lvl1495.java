//哭地很大声，很自由。这是只有我一个人的梦境层级。随着我将一口气哭完，我的身子弯下去，我突然意识到下面是什么：星空。
//
//下为星空，取代了那地面。建筑物都浮在星空之中。星空灿烂，如潮，如漩，如发光的被照亮的尘。每个星都慢慢地向心地旋转着，如漩涡一般，而那旋转的中心漩底似乎有一颗空缺的星。
