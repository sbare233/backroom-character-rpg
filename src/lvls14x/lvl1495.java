//至少能完成未完的愿望。至少能对人世再背叛一次。
//
//这是一个人的世界。而我并不孤独，有那些星漩中的星相伴。但是又似乎只有归入其行列，才能……
//
//即使星空是陷阱也在所不惜。因为我曾经——年少的疯狂过，我曾经——不是曾经了。现在。我现在只想再次，所谓的，结束生命。
//
//于是我抱着它向下跳。跳了下去。
//
//失重的飘忽与紧张，但只是生理现象。
