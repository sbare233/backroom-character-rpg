//Level C-476是一个占地约为七百万平方公里的近现代风格住宅区街道。该层级内存在有大量的同一风格的建筑物并在其中夹杂有为数不多的建筑废墟。层级内的几乎所有区域都存在有从20伦琴至2600伦琴不等的核辐射，并在一些区域内存在有微量的中子辐射。该层级的天空被乌云完全覆盖，虽然有光线从云层之上照射下来，但是并无法确定天空光源的具体位置。初步测量发现天空中的云层中存在大量带有放射性的灰尘。本层级的地表覆盖有大量积雪，偶尔存在有一些未被积雪覆盖的地区。本层级内的天气始终保持在阴天至暴雪之间，且除了一些特定区域，其它常规区域发生持续性降雪的几率高达百分之九十八以上。
//
//该层级内的积雪温度一般会维持在-110℃左右，误差不超过±5℃。地表上的积雪不会随着降雪而加厚，但是积雪上因外物活动产生的痕迹会随降雪而被覆盖。地表的街道上存在有一些没有车牌的汽车，这些车的内部部件上也没有任何产品编号或厂家信息，油箱里也没有汽油。曾有人尝试过向油箱内加入汽油以此来启动汽车，但是均因不明原因无法使汽车正常运转。该层级内零星分布着一些被雪掩埋的生锈绿皮信箱，有的信箱内会存在有一种白色的旧信封。信封中的内容仅有一行疑似用血液写成的巨大的“HELP ME”字样。在该信封被丢弃之后，信封会在四秒钟内转化为大量的黑色飞灰，这种飞灰拥有高达7500伦琴的辐射。
