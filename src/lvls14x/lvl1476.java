//晴天娃娃:
//一种无攻击性的类似于玩偶的实体。它们通常出现在该层级的建筑物内部，一般是会挂在一些横杆状物件上。当晴天娃娃存在于一个建筑物内2~4个小时之后，它会瞬移到该层级的另一个建筑物的内部。当一个有WiFi的建筑物内存在晴天娃娃时，这个建筑物的WiFi信号质量将得到大幅度的提升。当晴天娃娃被人所触摸时，它会立刻瞬移到其它的建筑物内部。
