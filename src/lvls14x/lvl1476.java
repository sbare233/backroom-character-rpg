//“生者”:
//一种类人型实体。五官模糊扭曲，只能看清大概轮廓，全身漆黑，穿着散发腐肉味的烂布状沾满黑污泥的衣服，其身体躯干干瘪无比，瘦骨嶙峋。拥有极高的攻击倾向，并表现出了正确使用工具甚至是枪械的能力，据被攻击过的流浪者称，这种实体拥有使用谋略计谋的能力。不过到目前为止，尚未有成功与“生者”发生语言交流的事例发生。“生者”极度孱弱，在常规正面作战时，一个正常人即可对抗四个未携带武器的“生者”。不过值得注意的是，“生者”一般会结群行动并随身携带金属刀具，一个“生者”集群内通常有5~12个“生者”。
//
//无论在什么情况下，“生者”都不会尝试去前往地表。如果它们的目标从下水道井口跑到了地表，它们只会围在井口附近等它们的目标重新跳下来。“生者”已被证实拥有一种除视听嗅以外远程发现人类的能力，并且这种能力会随气温的下降而提升。值得一提的是，你可以通过往你自己身上涂抹“生者”衣服上的黑泥来让“生者”无法辨认出你是人类。不过如果你在身上都是黑泥的情况下主动攻击“生者”的话，你周围所有的“生者”都会对你表现出远高于正常情况的敌意。“生者”在杀死人类之后喜欢将尸体带走，这些被带走的尸体极有可能是成为了“生者”在大型房间内聚餐所使用的食物。
//
