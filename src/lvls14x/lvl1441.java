//
//自从Level C-441被发现后，由于其环境气候都更为接近前厅，而且层级的稳定程度很高，很少发生各种不确定的灾害，所以为数不少的流浪者开始携带自己的财物和资源迁入此层级，层级的自然环境也完全满足定居生产条件，杏仁水存在于一些稍小面积的独立水域，例如泉水，湖泊，溪流和较小的河流。其他的较大的河流，湖泊等普通水资源也非常丰富，同样拥有丰富的地下水。土壤可以种植近乎所有温带作物，例如粮食作物，包括谷物、蔬菜、水果、薯、豆，经济作物，包括纤维，油、糖、饮料作物，药物、染料、香料作物等，和饲料、绿肥作物，如苜蓿、紫云英、三叶草等。同时也有相对繁荣的动物活动，除一些例如双尾狐和母鸡的后室特有物种外，更多是前厅物种，比如可供狩猎的鹿、羚羊、野猪、兔子、野牛等，也有为数不少的，似乎是逃散的家养牲畜，包括奶牛，肉牛，绵羊，山羊，马，火鸡，猪，鸡，鸭，鹅等，这些家养牲畜很容易驯服，很少出现能够造成安全威胁的野兽。
