//各个大型组织占有、划分了自己的势力范围和领土，招募、接纳海量的流浪者，将其纳入自身保护和提供公共服务，其中一些被招募进入组织作为正式成员，但绝大多数作为劳动力和公民，接受大型组织保护和享受公共服务的流浪者被要求遵守组织制定的法律，接受命令并向组织指定部门缴纳税款。但同时也有大量的流浪者不愿接受任何组织命令，而选择自由不受约束的定居和流浪生活。但由于各种不确定因素导致各大型组织的边界模糊动态而不固定，甚至许多组织的边界并不重合，中间存在相当的荒芜地带或大量自由流浪者社区。随着各方面的迅速发展，人员的生活也渐渐稳定，但由于层级的空白地域已经渐渐减少和各组织边界的模糊，加上其中有相当的流浪者自由社区，各组织时有因争夺土地，技术，资源和人口而爆发争端和冲突，此外并不是所有的流浪者都追求稳定的生活，一些流浪者会同大型组织的叛乱分子占据险要地形，不时掠夺周边居民的财产，再加上层级间出没的实体，使得层级并非完全的平静与安全。
