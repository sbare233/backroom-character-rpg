//
//剧院正门位于观众席正后方，是一扇白色双开门。流浪者可以通过此门随意进出Level-C-490与剧院。Level-C-490被毁灭后，正门被贴上“正在维修”的标牌，且无法以任何手段打开。正门左侧有“零食自助”窗口，该窗口放置有一台可无限取用的饮水机及一台无限取用的爆米花机。经测定，饮水机内的液体为杏仁水，爆米花机产出的爆米花永远保持在26℃。“零食自助”窗口旁是洗手间和员工休息室。正门右侧有一扇标记着“员工通道”的黑色单开门，任何意图进入员工通道的人都会被保安阻止。“员工通道”的正对面是由两名保安把守的剧团长办公室。
//
//观众席正前方是舞台，演出未开始时有幕布遮挡。舞台后方为“候场区”，只有演员身份的流浪者可以进入。舞台左右各有一扇标有“安全通道”的白色单开门，上有绿色荧光的“EXIT”字样；通过这扇门就可以回到你切入Level C-438之前所在的层级。
