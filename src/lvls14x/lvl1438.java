//剧院由名为“阖家欢剧团”的艺术团体所控制，正常情况下内部无任何敌对实体。然而，一旦流浪者的行为触犯“剧院守则”，保安会从暗处走出并将其拖入员工通道围殴致死。
//
//剧院通常每天会表演5场剧目，其剧本随机选取自前厅的一些剧目，包括但不限于古典戏剧、戏曲、小说、动漫、游戏等。演出时间会根据剧目调整，一些用时较长的剧目可能会使当天演出的剧目少于5场。
//
//流浪者每次切入该层级时，他们都会随机出现在大门旁的两个通道之一。通道的尽头是一扇只可从内部打开的门，门口有一位工作人员迎接该流浪者。工作人员将会递给流浪者剧团守则以及一张“演员”卡和“观众”卡。撕碎任何一张卡片会导致另一张消失并获得对应身份，身份的效果如下：
//
//观众：可以自由行动，但无法进入“候场区”。一天之内观看3场以上剧目可能会转变为保安、大众评审团或群演。
//
//演员：将会被传送至“候场区”并随机获得一本将要演出的剧目的剧本。剧本内容将指导流浪者如何行动和表演。他们可以进入“候场区”，但是他们在表演完成之前无法通过正门和安全通道出入该层级。在表演完成后他们的身份将会被转换为“观众”。任何表演前或表演中剧透一经发现会被认为触犯“剧院守则”。
//
//若是将两张卡片一起撕碎，流浪者将会切出到原先所在的层级。
