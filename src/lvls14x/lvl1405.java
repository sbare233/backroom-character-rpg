//Level C-405是后室C层群的第405层。
//
//或者，你也可以称呼这里“暑梦”。我们商量之后都觉得这个名字不错。
//
//描述
//zVPbxpMSWgj4ucn.jpg
//这是“浅界记忆”中一个已确认稳固的据点，也是我们成员的生活场所。
//
//Level C-405是一个典型的“变化”层级，表现出夏日的气候特点，且不存在昼夜交替。该层级内没有固定的空间结构，只有大致轮廓与框架，而其内填充的建筑则是不确定的。
//
//Level C-405内发生变化的具体规则与观察者密切相关。当一片空间中存在人类观察者时，该空间内的一切事物都将维持稳定；而在观察者离开后，此处区域的空间将陷入非稳定状态，直到下一次观察者进入才会重新稳定。届时，此处区域的空间将恢复原样，或是坍缩为新的景观。
//
//据测试，大部分物品都无法维持空间的稳定。即便它们是从外界带入的，也会在观察者离开时随空间一同陷入不稳定状态，并在下次坍缩时重组或消失。在此值得注意的是，由于某种未知的原因，Object C-2 - “好梦罐”似乎与其他物品不同，可在一定程度上维持周围空间的稳定。
//
//或许我可以说得更简单一些：Level C-405是一场梦境，一场盛夏之梦。
//
//也许你会觉得这么说显得“很玄乎”，但你只要亲身经历过几次就知道了。在“中度睡眠”以后的区域，一切都在变化：只需要两次转身，你就能抵达一个崭新的地点。在这里迷失不是难事，就像你总会在梦里迷路。
//
//在闲暇的时候，我常常会想——这究竟是谁的梦呢？
//
//在多数时候，Level C-405都可以大体上分为四个区域：“浅界记忆”、“中度睡眠”、“深层梦境”与“渊域夜魇”。这四个区域没有明确的界限划分，但是会给人造成完全不同的体验。虽然没有具体的指示，但是流浪者在区域间出入时会感到明显的“错乱感”。
//
//不同区域之间，存在着数个固定的“交界点”。这些特殊的位置不会随层级内空间结构的变化而转移或消失，可以成为稳定穿越层级的锚点。但值得注意的是，在不同“交界点”之间的路途不一定能维持稳定。
//
//在Level C-405的部分区域中，会出现餐馆、售货机等设施；在其中可以获得足够的补给。但这些设施同样会随着空间变动而消失，因而难以找到稳定的补给点。
//
//以下为四个区域的具体介绍：
//
//“浅界记忆”
//Q5dCmoZlP4Fc1jp.jpg
//一个停车位。偶尔你能看到几辆车，但它们都无法启动；更多时候，只能看到几个轮胎。
//
//“浅界记忆”是Level C-405内最稳定的部分。该区域的外在表现通常较为固定：一片由多个老式居民楼构成的街道小区。同时，其内的时间也稳定在正午时分。
//
//“浅界记忆”中几种固定的景观分别为：大理石喷泉、停车区、自行车棚，以及居民楼。这些景观出现的位置与数目并非是不变的，但其“出现”本身却已被确认为是底层逻辑。居民楼较之其他景观往往更加稳定，空间变化效应仅出现在房间布局、楼层数、楼层结构与外观等方面，而大体坐落位置则改变较少。
//
//“浅界记忆”内会随机出现少量好梦罐。在被使用之前，它们通常能维持半径十余米的稳定空间。
//
//作为Level C-405内空间结构最稳固的部分，“浅界记忆”理所当然地成为了M.E.G.在该层级内的落脚点。
//
//那是在多久之前呢？我的时间观念一直不太好，来到这里后就更是这样了。
//
//两年了吧，我猜。
