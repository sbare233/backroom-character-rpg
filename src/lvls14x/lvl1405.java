//不得不说的是，这里确实有迷路的风险。我们只建议流浪者在手持指向树脂一类物品时深入Level C-405，否则最好找到一个像我们的成员一样老练的向导。没有适应这里的变化的话，很容易在快速更替的景象中迷失自我。
