//
//进入“深层梦境”后，流浪者将陷入一种“恍惚”的状态。这种状态下，流浪者依旧能够保证意识，但是会一定程度上变得缓慢。
//
//从“深层梦境”开始，Level C-405将出现昼夜之分。这一区域内时间的流速不是均匀的，但这仅限于主观上的时间，流浪者客观的年龄与心智并不受此影响。
//
//“深层梦境”内，空间变化开始变得如同家常便饭。很多时候，只需几次睁眼闭眼，就会发现身边的环境发生了天差地别的变化。同时，景观的“逻辑性”也会经常性地缺失，常常会出现空中楼阁、巨型天体、光影颠倒等不合理的现象。
