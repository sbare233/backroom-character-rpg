//由于“深层梦境”特殊的性质，不建议任何流浪者试图深入此处。虽然层级本身较为安全，但是偶尔出没的实体以及快速变换的环境都将成为流浪者的威胁。更加迟缓的思维也会进一步使流浪者陷入危险。
