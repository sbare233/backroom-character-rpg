//那之后我建立了“清醒梦”前哨站，最开始只有我一个人。收集好梦罐、刷刷论坛、睡觉，也可能去更深处看看风景。后来人逐渐多了起来，我们现在有八个人了。除了收集好梦罐之外，还需要引导那些误入的新人离开，还要和不知何时出现的“夏日回忆”流浪者打交道。
