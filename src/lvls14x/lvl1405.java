//“中度睡眠”
//“中度睡眠”比“浅界记忆”更加不稳定。在进入此区域时，会感到明显的昏睡感。流浪者将很快适应这种昏睡感，并对之习以为常。
//
//“中度睡眠”内的空间变化较之“浅界记忆”更加频繁，且景观的差异也更大。以下为一些记录在案的“中度睡眠”景观：
//
//中式园林，包括空阔的亭台、走道、花园、假山等等。
//一所高校，通常包含空无一人的教学楼、图书馆、实验室与住宿楼。所有书籍的文字都模糊不清，难以辨识。
