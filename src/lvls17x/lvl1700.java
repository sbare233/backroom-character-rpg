//从Level C-700的任意一座桥梁向下方观察，看到的都是一片无尽的黑暗，这一行为会给流浪者一种非常强烈的孤独感，使流浪者感到不安和恐惧。目前已在该层级发现并报告了不下20起流浪者观察虚空时坠落并失踪的事例，M.E.G.不建议以除科学研究之外的理由进行观察虚空的活动。
