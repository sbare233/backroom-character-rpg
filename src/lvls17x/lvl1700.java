//两面墙壁之中间隔10米至200米不等，墙壁间由一些风格各异的桥梁连接，桥梁与墙壁的连接处会出现一扇木门。Level C-700中的木门大部分都无法打开，即使借助斧子等工具也无法破坏木门，甚至连刮痕都不会产生，流浪者无法进入木门内的空间，也不知道木门内到底是什么，但M.E.G.的成员在多次对木门的研究中发现，木门内部会传来一种类似于低吼的声音，多数流浪者会理所当然地认为这是该层级幽闭环境带来的心理错觉，不过根据M.E.G.专项调查组的研究，这种声音是真实存在的。M.E.G.也无法给出声音来源的任何信息，目前仅推测为实体的叫声。
