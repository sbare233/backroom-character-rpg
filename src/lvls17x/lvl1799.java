//露娜
//露娜是本层级唯一存在的实体。外表与普通人类少女无异。在流浪者的描述中，露娜对他们非常友好，会邀请他们在自己的家里留宿休息一晚。通常来说，流浪者不会拒绝露娜的好意，我们当然也不建议流浪者对此做出拒绝。若流浪者接受留宿，他们会受到热情的招待。在流浪者准备睡觉之前，露娜会请流浪者陪她到庭院中观星，并与他们进行一些闲聊。露娜通常会让流浪者分享自己的旅行经历。这些行为对流浪者的身心都有明显的放松作用，流浪者可以在这里进行确实的休息。
//
//从目前在其他流浪者处得到的信息来看，露娜在与流浪者的对话中有时会间接地展现出她对后室的一些层级似乎存在一定了解，但流浪者无法和她进行与之有关的话题。在层级中一切关于我们所理解的「后室」的逻辑都会以某种方式被扭曲，流浪者自身并不会意识到这一点。
