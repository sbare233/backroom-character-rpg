//此外，寂静之地的地面虽然无法用任何手段破坏，但击打地面会传微弱的呻吟声，原因不明。目前看来，这里除了地面能够造成视觉混淆之外没有其他危险。在这里，流浪者不会有饥饿和口渴的感觉，此区域也没有任何实体出没。Wi-Fi信号时有时无。
