//当流浪者向任意一方向行走10km后，就能够看到寂静之地的边界。边界由成群的山峦构成，将整个寂静之地包围了起来。山上虽然同样没有实体，也无其它有生物生存的迹象，但却经常能够发现一些动物的骸骨1。这些黑色的骸骨大多极其锋利，稍不留神便会被其划伤。因此流浪者在翻越山岭时应当注意脚下，成功翻过山岭后便能来到本层的区域二。
