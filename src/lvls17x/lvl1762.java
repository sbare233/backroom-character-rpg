//区域三 空白地带
//空白地带为本层的第三个区域，地面同样是混凝土构成，但混凝土变得异常脆弱，只需要敲打几下就会脱落。这时破坏混凝土砖所得的产物也变得更加奇怪，它们的截面类似于人类或是其它生物的血肉组织，并且被破坏的地方有少量的暗红色液体流出2。
