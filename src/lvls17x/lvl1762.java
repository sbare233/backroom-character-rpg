//区域一 寂静之地
//寂静之地是Level C-762的第一区域，该区域色调极其黯淡，天空中始终被浓厚的乌云覆盖，没有昼夜更替现象。地面由极黑的未知物质构成，它们被白色的线条分割成块状，这使得该区域的地面如同画作一般。
