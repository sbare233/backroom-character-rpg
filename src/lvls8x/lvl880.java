//这间房间被镶嵌在两侧墙内的六扇大窗所微微照亮。即使窗户如此巨大，舱室内仍旧是极度的黑暗。但尽管在如此黑暗的环境中，在室内的生活却总是保持着积极与活力，歌者们不断忙碌着，为他们的日常生活与工作劳顿奔波。友情提醒，你应当在穿越这片地区时小心一些，在这里的人们很容易被杂物，或在某些情况下，被人绊倒。
