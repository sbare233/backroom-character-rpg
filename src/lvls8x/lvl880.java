//电梯： 最初是被定位于入口大厅一边侧壁的小型立方电梯，它的墙壁与门似乎由某种黄铜所铸成，由后侧的玻璃幕墙外边的海洋为其提供主要的照明。电梯的金属板上配有八枚按钮。
