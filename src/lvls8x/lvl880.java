//除了屏幕按钮带来的昏暗光照，空间里是令人难以置信的一片黑暗。更何况这座迷宫因为充斥着被绊倒的危险性而臭名昭著。一些椅子的轮毂散落四周，被控制的墙壁与那些无法响应的面板一样会随着你向前探索突兀地凸出。因为它迷宫式的结构，人们很容易在电器领域迷路，高度建议寻找一名海沟歌者护送你通过房间，因为他们比一般的流浪者更善于导航。
