//主舱： 整个潜水艇中第二大的房间。周围大约有一个足球场的面积，并且墙体建得相当之高，这间房间因其巨大的尺寸在上部层以至于整个潜水艇中占主导部分，仍不清楚这样巨大宽敞的房间对这艘潜艇最初的作用是什么，但自这房间被建造以来，便根据海沟歌者的需求被改造成了类似于“村庄”的场地。这座村庄由看起来十分脆弱的废弃木材所组建，并偶有舒适的帐篷夹杂在楼宇中间。
