//电气长廊： 通往电气领域的长廊；有些类似于星光层的星光长廊，却比上部楼的东西大厅略微短些。只有非常少的舷窗提供照明，这让这里几乎伸手不见五指。取代舷窗，挂在墙壁上的是无响应的操控面板，上面布满了闪烁的灯泡和按钮。
