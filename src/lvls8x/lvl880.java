//在Level 880各种各样的居民里，只有“被选中”的存在才能够进入星光室，选拔程序的具体细节是未知的，因为大部分海沟歌者对这一问题的回答都不相同。然而，我们能够得知的是，当流浪者进入房间时，他们会经历一场所谓的“仪式”，这将导致流浪者被转化为一名海沟歌者，以至于永远停留在Level 880中。仅有两名已知的流浪者在进入房间后没有被转化为海沟歌者，他们是被称为“蛋蛋领主”的流浪者Kirby·Shatner与隶属于M.E.G.的Cyrus·La'Bumski。
