//陈列室： 整艘礁石行者号尺寸最小的房间，客观而言也是最奇怪的房间之一。它的入口在电气领域的西南转角深处，偏僻得像是有人刻意将其隐藏。陈列室在整个层级中显得格格不入，并非礁石行者号标准的深蓝色钢质壁面，它拥有着深色木质地板与黄宏条纹相间的墙纸图案。整个房间仅仅由一盏悬挂在天花板的昏暗灯泡提供照明，这也是整个Level 880内唯一的机械光源。
//
//这个房间得名于它所陈列的八个海洋生物标本，每一只生物都不类属于任何在前厅或后室的已知海洋生物。与进入Level 906的方式类似，沿着这些鱼类的名字滑动你的手指将会把你传送至其他的层级，每个奖杯传送的层级都有所不同，具体见下方的出口部分。
