//星光室： 除了星光长廊外的唯一一间房间，并在目前仍处于高度未知的状态。由于海沟歌者对这间房间的严密保护，关于这间房间的情报微乎其微。因为只有成员与即将成为正式成员的海沟歌者允许入内，截至撰写此篇报告时，仍然没有一次对星光室的成功勘察。对这间房间唯一能够确定的信息就是它尺寸巨大，形状相对而言呈现圆形，并且在墙面上高耸着尺寸大到难以置信的窗户，海洋透过这些巨大的窗户完全照亮了整个房间。此外有流言声称，这一房间的地面是由水组成的层面，流浪者与海沟歌者可以像在其他固体表面上一样走动。
