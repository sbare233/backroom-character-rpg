//入口大厅：一间小面积的矩形房间，似乎被定位在这艘潜艇的止点上。它拥有一扇巨大的舷窗悬挂在它的背壁，它的一侧墙面配有电梯，并且墙上一扇通往主舱的门正对着那扇舷窗。这间房间的名字源于海沟歌者见大多流浪者都于此进入Level 880而为其起名。有些特工曾问过海沟歌者们，为何这间房间明明不是一座大厅，他们却仍为它取出“入口大厅”这样的名字，这一问题得到了大量不同的回答，从“我到这之前他就叫这个了”到“因为这么叫它很酷呀”。
