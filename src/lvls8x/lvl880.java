//冷库： 一间相当寒冷的房间，可以通过电气走廊电梯旁边的一扇门进入。冷库是一间小面积的矩形房间，它被这个层级的住户改造成了某种储藏室，主要用来储存海沟歌者们捕来的未腐烂的肉类与蔬菜，此处同样也存放着一些放在这里会显得很奇怪的物品，例如衣服。这个房间通过一对镶嵌于天花板，不断喷出冷空气的通风口常年保持在恒定32°F（0°C）的温度。
//
//冷库的一个角落内有一扇舱门，将其打开会通向一条冗长的金属壁洞口，它会将流浪者直接投射到外部的海洋。这个位置奇怪的舱门是进入海洋的唯一途径、冷库内唯一的光源，以及海沟歌者们唯一的水源来源4。值得注意的是，由于舱外没有梯子等借以返程的装置，这个舱门是有去无回的。这个舱门的出口点大致位于礁石行者号的最南端。
//
//为了从外部观察礁石行者号的全貌，人们曾七次借由这个舱门进入海洋进行勘探，但最终均以失败告终。
