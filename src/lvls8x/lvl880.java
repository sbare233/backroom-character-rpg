//星光长廊： 一条相当长的走廊，两侧的墙壁挂满舷窗，天花板的玻璃穹顶星罗棋布地排列着。这条长廊笔直地通往星光室，在大部分情况下，任意时间都至少有两名身强力壮的海沟歌者在此巡逻。尽管潜艇的墙与窗户没有显现出明显的泄漏迹象，这一楼层的过道仍然被海水充斥的水洼所覆盖。如果对你鞋子的防水性能没有自信，建议在这条长廊上要小心走动。
