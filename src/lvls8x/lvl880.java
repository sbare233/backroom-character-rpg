//东部与西部大厅： 一对长廊，可以分别从主舱东西两边墙上的门进入，两条长廊拥有同等的长度与宽度并同样排列着舷窗。因此带来的极佳视野，再加上没有任何歌者在长廊安家，让这两座大厅共同成为了观赏外部海洋景观的最佳景点。
