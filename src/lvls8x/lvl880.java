//礁石行者号
//Level 880唯一的宜居区域是一艘大得难以置信的潜艇的内部，它被该层群的主要住民命名为 礁石行者号1 。礁石行者号似乎主要由黄铜、紫铜、组成大窗的强化玻璃以及一种未知种类、像钢铁一样、拥有深蓝色彩的金属所组成。这种金属似乎是礁石行者号主要的组成成分，因此这艘潜艇大部分被呈现为深蓝色。
//
//礁石行者号疑似能够独自运行，这让一些人相信它拥有自我意识，再不济也是由某种外部力量所操控着。尽管没有发现任何已知的功能电源或操作装置，这艘潜艇仍然一直保持着活动，这种现象为这两种理论进一步提供了证据2。此外，由于礁石行者号缺少发电设施，潜艇几乎没有任何的机械光源，但借着海洋照进的微光，使得空间内并非处于完全的黑暗。十分昏暗的环境让潜水艇的大多部分处于一种类似“夜间”的模糊景象。如果你视力不佳，虽然并非强制性，但也极度不建议将手提光源（也就是手电筒）带到Level 880，这一层级的居民视觉对光源十分敏感，并且会对手电的照射感到恼怒或不满。由于前文中提到的居民所具有的这种性质，他们对光源照射会呈现出如下几种不同的反应——有些会礼貌地让你将手电筒拿到一边，有些会怒气冲冲地恐吓你，而有些居民似乎会将光源照射视作一种挑战，直直地与其对视。总而言之，最好不要冒险冒犯他们。
//
//礁石行者号与任何在前厅已知潜艇的构造都不相同，它由四个楼层组成，并由一座单独的电梯将它们相连。一些在楼层上的房间对整艘船并没有明确的作用，这让该层级的居民将它们改造为居住与储存物资的区域。礁石行者号最特别的一点在于，它疑似拥有腿部：朝某扇嵌进外部舱壁的舷窗向外看，有时会有机会捕捉到一个短暂的瞬间，呈现出推测为是14只细长机械质感的腿部在驱动整艘潜艇前进的景象，而非任何传统地、依托螺旋桨进行移动的方式。这些腿部引发了许多有关礁石行者号外部真实样貌的猜测，大多人相信它是一种巨大的等足类或其他品种的深海甲壳动物。
