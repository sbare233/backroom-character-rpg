//垃圾间： 一个相当大号的房间，如潮水一样源源不断涌现的湿垃圾代替了所有地板。这个房间极其黑暗且寸步难行——沿着堆积如山的垃圾前进时，你必须万分小心，稍有不慎便会被卷入垃圾之中，在最坏的情况下甚至有可能被其直接吞噬。加上这是礁石行者号中唯一拥有敌对实体的区域，这点使它无疑成为了整个层级中最危险的地方。根据推测，这层垃圾是深不见底的，因为至今仍未有人能触及到被它淹没的固体地板。此外，通过移除房间内的垃圾来清洁它也似乎是徒劳的，因为不论从房间移走多少的垃圾，垃圾组成的浪潮仍旧会保持在同一水平线上。这些垃圾多由废木头、废金属与破布料组成，但你偶尔也能在其中找到一些其他有趣的东西，例如：
