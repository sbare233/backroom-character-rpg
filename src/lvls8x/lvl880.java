//隐藏 880 - 上部楼
//上部楼
//礁石行者号的上部楼恰好坐落在星光层的下方。它实际上充当着这艘潜艇“核心枢纽”的作用，包含流浪者来到这里的起始点与海沟歌者主要的居住楼层。毫无疑问，上部楼是这一层级危险最少的部分3。
