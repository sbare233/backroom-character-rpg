//隐藏 下部楼- Lower
//下部楼
//下部楼是礁石行者号的最底层，同时也是整艘船上最诡异的地方之一。整个楼层的大部分区域是一间被称作“电气领域”的巨大迷宫式房间。潜艇的这一部分是迄今为止最黑暗的，这意味着你极易在这一分层迷路，在没有一名至少海沟歌者的帮助下穿越这片区域是十分不明智的行为。
