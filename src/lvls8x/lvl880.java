//隐藏 880 - 星光层
//星光层
//星光层是礁石行者号最高以及最小的楼层，仅仅由一道长廊与一间未知尺寸的异常房间所组成，星光层似乎对海沟歌者而言拥有严肃深刻的宗教重要性，这里对他们而言似乎是一种类似“圣地”的存在。在各个楼层中，它拥有着最多的舷窗和玻璃幕墙，因此，它同样是整艘潜艇中最明亮的楼层。
