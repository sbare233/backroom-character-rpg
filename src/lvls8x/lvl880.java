//隐藏 880 - 中部楼
//中部楼
//中部楼位于上部楼与下部楼中间，它是唯一被认知为具有危险性的楼层。它的危险性来源于它是整个礁石行者号上唯一拥有敌对实体的地方。更糟的是，这层楼的大多数空间被笼罩于黑暗之中、不断移动变化的地形所占据。建议在进入中层楼时时刻保持高度警惕。
