//当阿米卡犬不可避免地走向死亡时，无论它的死亡是外部因素还是自然衰老，它的伙伴都会陷入到长达数周的悲痛中。在实体身上这种表现相对迟钝，相比之下，人类会产生近乎歇斯底里的抑郁情绪，并连续数日拒绝基本的饮食摄入。在极端情况下，这能导致脱水和饥饿而死。那些和阿米卡犬亲近的人类更可能受到这种影响，哪怕他们离开Level 888也无法避免。想避开阿米卡犬十分容易，因为它们的移动速度非常慢，无法追上以中等速度慢跑的人类。
