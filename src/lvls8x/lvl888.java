//- 骸布潜行者
//shroudstalker.jpg
//一只打算逃离的骸布潜行者的照片
//
//水蛭状的生物，身体两侧长有多组手臂，沿着整个头部垂直的分界线左右对称。据目击者称，它们体长约3 m (9.8 英尺)。事实证明，由于它们身上闪烁的异常的条状光带，想要拍摄它们的身形非常困难，我们认为这是它们所具有的某种自我防御机制，受到威胁后启动，在其死亡后也会持续一定时间。
//
//尽管它们的地面速度可达10 m/s (36 迈)，但骸布潜行者不喜欢直接接触其他生物。它们实际上像清道夫，选择从游荡的蜱蚤那里偷东西，或者等负蚤织工丢弃其猎物，而猎物这时往往还活着。虽然以上是它们最喜欢的取食途径，但骸布潜行者也曾多次想要袭击熟睡的人类。袭击猎物时，它们会以巨大的力量将猎物束缚，并用附肢做支撑，将猎物拖到偏僻处，然后开始发出阵阵尖啸。这样的叫声能同时震慑其他捕食者和猎物，但这种生物之间没有任何语言可言。
//
//一旦猎物放弃抵抗，骸布潜行者就会对其造成严重伤害，通常是骨折或是用尾巴造成的钝器伤，然后它们会将猎物回复到相对健康和平静的状态。这种造成痛苦，又掩盖伤害的循环，正是骸骨潜行者的进食方式。它们极度痴迷于在外观上掩盖自己所造成的伤害，确保骨头断的干干净净，淤伤完全消失。猎物对疼痛的反应变得过于迟钝时，骸骨潜行者就会将之弃若敝屣。
