//浮肿、苍白的人形生物，身高达4.3 m (14英尺)，面部长有一束卷曲的触须，掩盖了面部特征。由于其退化的后肢，负蚤织工无法直立，并仅具备有限的活动能力。负蚤织工和蜱蚤形成了一种互利共生关系，它们允许蜱蚤在自己的背部钻出浅洞来筑巢，作为回报，蜱蚤有时也会冒险寻找猎物，并在猎物丧失抵抗能力后将其引向织工的巢穴。
//
//一旦受害者进入到合适的攻击范围，负蚤织工就会抓住受害者，将其牢牢地控制在胸前使之无法逃脱。然后，它将触须垂到受害者身上，用它的下颚套住其头部。此举会启动一个缓慢的取食过程，过程中，受害者的大部分记忆都被啮噬，负蚤织工以受害者的记忆，特别是年轻时的记忆维生。人们根据间接证据推测，认为那些关于压力和创伤的记忆对其更富营养。
//
//为了使食物来源尽可能长期存活，负蚤织工会将一根管子插入受害者的喉咙或肚脐，每20小时一次，泵入紫色的浆液。1接近负蚤织工的唯一的相对安全的时间点就是在它吸食受害者的时候，但它会迅速抛下意识丧失的受害者，而后指挥自己背部的蜱蚤去攻击新的猎物。
