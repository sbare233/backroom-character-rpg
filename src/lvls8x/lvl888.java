//粗壮的双足哺乳动物，有许多畸形的性状。它们外观上类似无毛的犬科动物，但没有肋骨以下的部位，也没有下颌。它们是森林中其他实体除人类外的主要食物来源。阿米卡犬主要以其他生物的副产物和实体维生，通过一种效率极其低下的消化系统和一个小膀胱吸收营养，然后将剩余的东西反刍。在如此生理构造和数不清的身体缺陷下，它们的正常生命周期很少超出10到20天这一区间。
//
//当阿米卡犬接触到一个体型相对较大的生命体时，它会即刻对其产生不可分割的依恋，它的余生都将为这一绑定的伙伴服务，无论它是人类还是实体。这种忠诚是完全不可动摇的，哪怕是故意伤害也无妨。如果期间有任何其他生物试图攻击它的伙伴，虽然没什么用，但阿米卡犬会试图上前保护。还有人发现，它们能够在受到提示时进行“嬉戏”这一行为。
