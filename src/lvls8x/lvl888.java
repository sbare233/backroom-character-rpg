//- 蜱蚤
//sipohonap.jpg
//营养不良的幼年期蜱蚤。
//
//体表半透明的昆虫，解刨发现其与跳蚤相似，通常能长到20 cm (7.8 英寸)长，最大跳跃距离可达约12 m (40 英尺)。它们腿上的倒勾能作为钩子辅助攀附各种表面。已知它们会将自己掩蔽在有厚厚粉尘的地方，并在跃出咬住猎物前埋头等待。由于它们口腔中分泌的麻痹毒素，其咬伤几乎不会产生痛感。
//
//蜱蚤咬伤人类时，只会吸取与其身体大小相称的少量血液，因为此举的主要目的是为了注入麻痹毒素，一旦毒素的浓度足够，受害者的精神状况会受到严重影响。引发的症状包括强烈的空虚感、意识模糊、对自我意义的怀疑，以及去找寻附近的“慰藉”，也就是其他实体的渴望。如果没有注射更多毒素，以上症状会在两小时后消失。蜱蚤喜欢聚集在热源附近，并且会避免对放出热量的生命体造成致命伤。
